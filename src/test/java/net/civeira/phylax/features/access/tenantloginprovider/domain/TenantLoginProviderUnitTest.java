class TenantLoginProviderUnitTest {

  /**
   * @autogenerated CachedGenerator
   */
  @Test
  @DisplayName("Test a entity reference contruction")
  void test_tenant_login_provider_builder() {
    TenantLoginProvider one = TenantLoginProvider.builder().uidValue(UidVO.from("one"))
        .tenantValue(TenantVO.from(TenantReference.of("one"))).nameValue(NameVO.from("one"))
        .sourceValue(SourceVO.from(TenantLoginProviderSourceOptions.GOOGLE))
        .disabledValue(DisabledVO.from(true)).directAccessValue(DirectAccessVO.from(true))
        .publicKeyValue(PublicKeyVO.from("one")).privateKeyValue(PrivateKeyVO.from("one"))
        .certificateValue(CertificateVO.from("one")).metadataValue(MetadataVO.from("file_1"))
        .usersEnabledByDefaultValue(UsersEnabledByDefaultVO.from(true))
        .versionValue(VersionVO.from(1)).build();
    TenantLoginProvider withOne = one.withVersionValue(VersionVO.from(1));
    Assertions.assertEquals(2, withOne.withNextVersion().getVersion().get());
    TenantLoginProvider deleted = one.delete();
    Assertions.assertEquals(1, deleted.getRecordedEvents().size(),
        "After delete, one event must be stored");
    Assertions.assertTrue(one.getRecordedEvents().isEmpty(),
        "No events can affect the original entity");
  }
}
