package net.civeira.phylax.features.access.apikeyclient.domain.gateway;

import java.time.OffsetDateTime;
import java.util.List;

import org.junit.jupiter.api.Assertions;
import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.api.Test;

import net.civeira.phylax.features.access.apikeyclient.domain.ApiKeyClient;
import net.civeira.phylax.features.access.apikeyclient.domain.valueobject.CodeVO;
import net.civeira.phylax.features.access.apikeyclient.domain.valueobject.EnabledVO;
import net.civeira.phylax.features.access.apikeyclient.domain.valueobject.KeyVO;
import net.civeira.phylax.features.access.apikeyclient.domain.valueobject.ScopesVO;
import net.civeira.phylax.features.access.apikeyclient.domain.valueobject.UidVO;
import net.civeira.phylax.features.access.apikeyclient.domain.valueobject.VersionVO;

class ApiKeyClientCachedUnitTest {

  /**
   * @autogenerated CachedGenerator
   */
  @Test
  @DisplayName("Test a entity reference contruction")
  void test_api_key_client_cached_builder() {
    ApiKeyClient one =
        ApiKeyClient.builder().uidValue(UidVO.from("one")).codeValue(CodeVO.from("one"))
            .keyValue(KeyVO.from("one")).enabledValue(EnabledVO.from(true))
            .scopesValue(ScopesVO.from("one")).versionValue(VersionVO.from(1)).build();
    OffsetDateTime now = OffsetDateTime.now();
    List<ApiKeyClient> empty = List.of();
    List<ApiKeyClient> some = List.of(one);
    ApiKeyClientCached emptyCursor = ApiKeyClientCached.builder().since(now).value(empty).build();
    Assertions.assertFalse(emptyCursor.first().isPresent(), "An empty list dont have first");
    ApiKeyClientCached someCursor = ApiKeyClientCached.builder().since(now).value(some).build();
    Assertions.assertEquals(one, someCursor.first().get(),
        "A list with value must retrieve firt value");
  }
}
