class TenantTermsOfUseCachedUnitTest {

  /**
   * @autogenerated CachedGenerator
   */
  @Test
  @DisplayName("Test a entity reference contruction")
  void test_tenant_terms_of_use_cached_builder() {
    TenantTermsOfUse one = TenantTermsOfUse.builder().uidValue(UidVO.from("one"))
        .tenantValue(TenantVO.from(TenantReference.of("one"))).textValue(TextVO.from("one"))
        .enabledValue(EnabledVO.from(true)).attachedValue(AttachedVO.from("file_1"))
        .activationDateValue(ActivationDateVO.from(LocalDateTime.of(1980, 8, 20, 0, 0)
            .atZone(ZoneId.of("Europe/Madrid")).toOffsetDateTime()))
        .versionValue(VersionVO.from(1)).build();
    OffsetDateTime now = OffsetDateTime.now();
    List<TenantTermsOfUse> empty = List.of();
    List<TenantTermsOfUse> some = List.of(one);
    TenantTermsOfUseCached emptyCursor =
        TenantTermsOfUseCached.builder().since(now).value(empty).build();
    Assertions.assertFalse(emptyCursor.first().isPresent(), "An empty list dont have first");
    TenantTermsOfUseCached someCursor =
        TenantTermsOfUseCached.builder().since(now).value(some).build();
    Assertions.assertEquals(one, someCursor.first().get(),
        "A list with value must retrieve firt value");
  }
}
