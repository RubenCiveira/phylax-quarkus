class TenantConfigCachedUnitTest {

  /**
   * @autogenerated CachedGenerator
   */
  @Test
  @DisplayName("Test a entity reference contruction")
  void test_tenant_config_cached_builder() {
    TenantConfig one = TenantConfig.builder().uidValue(UidVO.from("one"))
        .tenantValue(TenantVO.from(TenantReference.of("one")))
        .innerLabelValue(InnerLabelVO.from("one")).forceMfaValue(ForceMfaVO.from(true))
        .allowRegisterValue(AllowRegisterVO.from(true))
        .enableRegisterUsersValue(EnableRegisterUsersVO.from(true))
        .wellcomeEmailValue(WellcomeEmailVO.from("one"))
        .registerdEmailValue(RegisterdEmailVO.from("one"))
        .disabledUserEmailValue(DisabledUserEmailVO.from("one"))
        .enabledUserEmailValue(EnabledUserEmailVO.from("one"))
        .allowRecoverPassValue(AllowRecoverPassVO.from(true))
        .recoverPassEmailValue(RecoverPassEmailVO.from("one")).versionValue(VersionVO.from(1))
        .build();
    OffsetDateTime now = OffsetDateTime.now();
    List<TenantConfig> empty = List.of();
    List<TenantConfig> some = List.of(one);
    TenantConfigCached emptyCursor = TenantConfigCached.builder().since(now).value(empty).build();
    Assertions.assertFalse(emptyCursor.first().isPresent(), "An empty list dont have first");
    TenantConfigCached someCursor = TenantConfigCached.builder().since(now).value(some).build();
    Assertions.assertEquals(one, someCursor.first().get(),
        "A list with value must retrieve firt value");
  }
}
