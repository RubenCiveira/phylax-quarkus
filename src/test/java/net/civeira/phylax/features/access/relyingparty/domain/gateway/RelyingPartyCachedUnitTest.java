package net.civeira.phylax.features.access.relyingparty.domain.gateway;

import java.time.OffsetDateTime;
import java.util.List;

import org.junit.jupiter.api.Assertions;
import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.api.Test;

import net.civeira.phylax.features.access.relyingparty.domain.RelyingParty;
import net.civeira.phylax.features.access.relyingparty.domain.valueobject.ApiKeyVO;
import net.civeira.phylax.features.access.relyingparty.domain.valueobject.CodeVO;
import net.civeira.phylax.features.access.relyingparty.domain.valueobject.EnabledVO;
import net.civeira.phylax.features.access.relyingparty.domain.valueobject.UidVO;
import net.civeira.phylax.features.access.relyingparty.domain.valueobject.VersionVO;

class RelyingPartyCachedUnitTest {

  /**
   * @autogenerated CachedGenerator
   */
  @Test
  @DisplayName("Test a entity reference contruction")
  void test_relying_party_cached_builder() {
    RelyingParty one = RelyingParty.builder().uidValue(UidVO.from("one"))
        .codeValue(CodeVO.from("one")).apiKeyValue(ApiKeyVO.from("one"))
        .enabledValue(EnabledVO.from(true)).versionValue(VersionVO.from(1)).build();
    OffsetDateTime now = OffsetDateTime.now();
    List<RelyingParty> empty = List.of();
    List<RelyingParty> some = List.of(one);
    RelyingPartyCached emptyCursor = RelyingPartyCached.builder().since(now).value(empty).build();
    Assertions.assertFalse(emptyCursor.first().isPresent(), "An empty list dont have first");
    RelyingPartyCached someCursor = RelyingPartyCached.builder().since(now).value(some).build();
    Assertions.assertEquals(one, someCursor.first().get(),
        "A list with value must retrieve firt value");
  }
}
