class UserAcceptedTermnsOfUseCachedUnitTest {

  /**
   * @autogenerated CachedGenerator
   */
  @Test
  @DisplayName("Test a entity reference contruction")
  void test_user_accepted_termns_of_use_cached_builder() {
    UserAcceptedTermnsOfUse one = UserAcceptedTermnsOfUse.builder().uidValue(UidVO.from("one"))
        .userValue(UserVO.from(UserReference.of("one")))
        .conditionsValue(ConditionsVO.from(TenantTermsOfUseReference.of("one")))
        .acceptDateValue(AcceptDateVO.from(LocalDateTime.of(1980, 8, 20, 0, 0)
            .atZone(ZoneId.of("Europe/Madrid")).toOffsetDateTime()))
        .versionValue(VersionVO.from(1)).build();
    OffsetDateTime now = OffsetDateTime.now();
    List<UserAcceptedTermnsOfUse> empty = List.of();
    List<UserAcceptedTermnsOfUse> some = List.of(one);
    UserAcceptedTermnsOfUseCached emptyCursor =
        UserAcceptedTermnsOfUseCached.builder().since(now).value(empty).build();
    Assertions.assertFalse(emptyCursor.first().isPresent(), "An empty list dont have first");
    UserAcceptedTermnsOfUseCached someCursor =
        UserAcceptedTermnsOfUseCached.builder().since(now).value(some).build();
    Assertions.assertEquals(one, someCursor.first().get(),
        "A list with value must retrieve firt value");
  }
}
