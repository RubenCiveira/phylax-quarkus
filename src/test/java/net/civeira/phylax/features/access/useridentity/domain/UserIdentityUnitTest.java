package net.civeira.phylax.features.access.useridentity.domain;

import java.util.List;

import org.junit.jupiter.api.Assertions;
import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.api.Test;

import net.civeira.phylax.features.access.relyingparty.domain.RelyingPartyReference;
import net.civeira.phylax.features.access.role.domain.RoleReference;
import net.civeira.phylax.features.access.trustedclient.domain.TrustedClientReference;
import net.civeira.phylax.features.access.user.domain.UserReference;
import net.civeira.phylax.features.access.useridentity.domain.valueobject.RelyingPartyVO;
import net.civeira.phylax.features.access.useridentity.domain.valueobject.RolesVO;
import net.civeira.phylax.features.access.useridentity.domain.valueobject.TrustedClientVO;
import net.civeira.phylax.features.access.useridentity.domain.valueobject.UidVO;
import net.civeira.phylax.features.access.useridentity.domain.valueobject.UserVO;
import net.civeira.phylax.features.access.useridentity.domain.valueobject.VersionVO;

class UserIdentityUnitTest {

  /**
   * @autogenerated CachedGenerator
   */
  @Test
  @DisplayName("Test a entity reference contruction")
  void test_user_identity_builder() {
    UserIdentity one = UserIdentity.builder().uidValue(UidVO.from("one"))
        .userValue(UserVO.from(UserReference.of("one")))
        .relyingPartyValue(RelyingPartyVO.from(RelyingPartyReference.of("one")))
        .trustedClientValue(TrustedClientVO.from(TrustedClientReference.of("one")))
        .rolesValue(RolesVO.from(
            List.of(Roles.builder().uid("one").role(RoleReference.of("one")).version(1).build())))
        .versionValue(VersionVO.from(1)).build();
    UserIdentity withOne = one.withVersionValue(VersionVO.from(1));
    Assertions.assertEquals(2, withOne.withNextVersion().getVersion().get());
    UserIdentity deleted = one.delete();
    Assertions.assertEquals(1, deleted.getRecordedEvents().size(),
        "After delete, one event must be stored");
    Assertions.assertTrue(one.getRecordedEvents().isEmpty(),
        "No events can affect the original entity");
  }
}
