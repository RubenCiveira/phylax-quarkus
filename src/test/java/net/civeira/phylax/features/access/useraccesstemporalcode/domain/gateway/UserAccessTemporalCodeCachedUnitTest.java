import org.junit.jupiter.api.Assertions;
import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.api.Test;

import net.civeira.phylax.features.access.user.domain.UserReference;
import net.civeira.phylax.features.access.useraccesstemporalcode.domain.UserAccessTemporalCode;
import net.civeira.phylax.features.access.useraccesstemporalcode.domain.valueobject.FailedLoginAttemptsVO;
import net.civeira.phylax.features.access.useraccesstemporalcode.domain.valueobject.RecoveryCodeExpirationVO;
import net.civeira.phylax.features.access.useraccesstemporalcode.domain.valueobject.RecoveryCodeVO;
import net.civeira.phylax.features.access.useraccesstemporalcode.domain.valueobject.RegisterCodeExpirationVO;
import net.civeira.phylax.features.access.useraccesstemporalcode.domain.valueobject.RegisterCodeUrlVO;
import net.civeira.phylax.features.access.useraccesstemporalcode.domain.valueobject.RegisterCodeVO;
import net.civeira.phylax.features.access.useraccesstemporalcode.domain.valueobject.TempSecondFactorSeedExpirationVO;
import net.civeira.phylax.features.access.useraccesstemporalcode.domain.valueobject.TempSecondFactorSeedVO;
import net.civeira.phylax.features.access.useraccesstemporalcode.domain.valueobject.UidVO;
import net.civeira.phylax.features.access.useraccesstemporalcode.domain.valueobject.UserVO;
import net.civeira.phylax.features.access.useraccesstemporalcode.domain.valueobject.VersionVO;

class UserAccessTemporalCodeCachedUnitTest {

  /**
   * @autogenerated CachedGenerator
   */
  @Test
  @DisplayName("Test a entity reference contruction")
  void test_user_access_temporal_code_cached_builder() {
    UserAccessTemporalCode one = UserAccessTemporalCode.builder().uidValue(UidVO.from("one"))
        .userValue(UserVO.from(UserReference.of("one")))
        .tempSecondFactorSeedValue(TempSecondFactorSeedVO.fromCyphered("one"))
        .tempSecondFactorSeedExpirationValue(TempSecondFactorSeedExpirationVO.from(LocalDateTime
            .of(1980, 8, 20, 0, 0).atZone(ZoneId.of("Europe/Madrid")).toOffsetDateTime()))
        .failedLoginAttemptsValue(FailedLoginAttemptsVO.from(1))
        .registerCodeValue(RegisterCodeVO.from("one"))
        .registerCodeUrlValue(RegisterCodeUrlVO.from("one"))
        .registerCodeExpirationValue(RegisterCodeExpirationVO.from(LocalDateTime
            .of(1980, 8, 20, 0, 0).atZone(ZoneId.of("Europe/Madrid")).toOffsetDateTime()))
        .recoveryCodeValue(RecoveryCodeVO.from("one"))
        .recoveryCodeExpirationValue(RecoveryCodeExpirationVO.from(LocalDateTime
            .of(1980, 8, 20, 0, 0).atZone(ZoneId.of("Europe/Madrid")).toOffsetDateTime()))
        .versionValue(VersionVO.from(1)).build();
    OffsetDateTime now = OffsetDateTime.now();
    List<UserAccessTemporalCode> empty = List.of();
    List<UserAccessTemporalCode> some = List.of(one);
    UserAccessTemporalCodeCached emptyCursor =
        UserAccessTemporalCodeCached.builder().since(now).value(empty).build();
    Assertions.assertFalse(emptyCursor.first().isPresent(), "An empty list dont have first");
    UserAccessTemporalCodeCached someCursor =
        UserAccessTemporalCodeCached.builder().since(now).value(some).build();
    Assertions.assertEquals(one, someCursor.first().get(),
        "A list with value must retrieve firt value");
  }
}
