import net.civeira.phylax.features.access.tenantloginprovider.domain.valueobject.VersionVO;

class TenantLoginProviderCachedUnitTest {

  /**
   * @autogenerated CachedGenerator
   */
  @Test
  @DisplayName("Test a entity reference contruction")
  void test_tenant_login_provider_cached_builder() {
    TenantLoginProvider one = TenantLoginProvider.builder().uidValue(UidVO.from("one"))
        .tenantValue(TenantVO.from(TenantReference.of("one"))).nameValue(NameVO.from("one"))
        .sourceValue(SourceVO.from(TenantLoginProviderSourceOptions.GOOGLE))
        .disabledValue(DisabledVO.from(true)).directAccessValue(DirectAccessVO.from(true))
        .publicKeyValue(PublicKeyVO.from("one")).privateKeyValue(PrivateKeyVO.from("one"))
        .certificateValue(CertificateVO.from("one")).metadataValue(MetadataVO.from("file_1"))
        .usersEnabledByDefaultValue(UsersEnabledByDefaultVO.from(true))
        .versionValue(VersionVO.from(1)).build();
    OffsetDateTime now = OffsetDateTime.now();
    List<TenantLoginProvider> empty = List.of();
    List<TenantLoginProvider> some = List.of(one);
    TenantLoginProviderCached emptyCursor =
        TenantLoginProviderCached.builder().since(now).value(empty).build();
    Assertions.assertFalse(emptyCursor.first().isPresent(), "An empty list dont have first");
    TenantLoginProviderCached someCursor =
        TenantLoginProviderCached.builder().since(now).value(some).build();
    Assertions.assertEquals(one, someCursor.first().get(),
        "A list with value must retrieve firt value");
  }
}
