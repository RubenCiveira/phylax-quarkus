package net.civeira.phylax.features.access.tenant.domain;

import java.time.LocalDateTime;
import java.time.ZoneId;

import org.junit.jupiter.api.Assertions;
import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.api.Test;

import net.civeira.phylax.features.access.tenant.domain.valueobject.DomainVO;
import net.civeira.phylax.features.access.tenant.domain.valueobject.EnabledVO;
import net.civeira.phylax.features.access.tenant.domain.valueobject.MarkForDeleteTimeVO;
import net.civeira.phylax.features.access.tenant.domain.valueobject.MarkForDeleteVO;
import net.civeira.phylax.features.access.tenant.domain.valueobject.NameVO;
import net.civeira.phylax.features.access.tenant.domain.valueobject.RootVO;
import net.civeira.phylax.features.access.tenant.domain.valueobject.UidVO;
import net.civeira.phylax.features.access.tenant.domain.valueobject.VersionVO;

class TenantUnitTest {

  /**
   * @autogenerated CachedGenerator
   */
  @Test
  @DisplayName("Test a entity reference contruction")
  void test_tenant_builder() {
    Tenant one = Tenant.builder().uidValue(UidVO.from("one")).nameValue(NameVO.from("one"))
        .rootValue(RootVO.from(true)).domainValue(DomainVO.from("one"))
        .enabledValue(EnabledVO.from(true)).markForDeleteValue(MarkForDeleteVO.from(true))
        .markForDeleteTimeValue(MarkForDeleteTimeVO.from(LocalDateTime.of(1980, 8, 20, 0, 0)
            .atZone(ZoneId.of("Europe/Madrid")).toOffsetDateTime()))
        .versionValue(VersionVO.from(1)).build();
    Tenant withOne = one.withVersionValue(VersionVO.from(1));
    Assertions.assertEquals(2, withOne.withNextVersion().getVersion().get());
    Tenant deleted = one.delete();
    Assertions.assertEquals(1, deleted.getRecordedEvents().size(),
        "After delete, one event must be stored");
    Assertions.assertTrue(one.getRecordedEvents().isEmpty(),
        "No events can affect the original entity");
  }
}
