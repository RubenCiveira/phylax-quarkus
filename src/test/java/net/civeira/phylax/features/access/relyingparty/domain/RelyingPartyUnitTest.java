package net.civeira.phylax.features.access.relyingparty.domain;

import org.junit.jupiter.api.Assertions;
import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.api.Test;

import net.civeira.phylax.features.access.relyingparty.domain.valueobject.ApiKeyVO;
import net.civeira.phylax.features.access.relyingparty.domain.valueobject.CodeVO;
import net.civeira.phylax.features.access.relyingparty.domain.valueobject.EnabledVO;
import net.civeira.phylax.features.access.relyingparty.domain.valueobject.UidVO;
import net.civeira.phylax.features.access.relyingparty.domain.valueobject.VersionVO;

class RelyingPartyUnitTest {

  /**
   * @autogenerated CachedGenerator
   */
  @Test
  @DisplayName("Test a entity reference contruction")
  void test_relying_party_builder() {
    RelyingParty one = RelyingParty.builder().uidValue(UidVO.from("one"))
        .codeValue(CodeVO.from("one")).apiKeyValue(ApiKeyVO.from("one"))
        .enabledValue(EnabledVO.from(true)).versionValue(VersionVO.from(1)).build();
    RelyingParty withOne = one.withVersionValue(VersionVO.from(1));
    Assertions.assertEquals(2, withOne.withNextVersion().getVersion().get());
    RelyingParty deleted = one.delete();
    Assertions.assertEquals(1, deleted.getRecordedEvents().size(),
        "After delete, one event must be stored");
    Assertions.assertTrue(one.getRecordedEvents().isEmpty(),
        "No events can affect the original entity");
  }
}
