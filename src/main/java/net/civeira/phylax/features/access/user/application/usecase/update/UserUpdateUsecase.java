package net.civeira.phylax.features.access.user.application.usecase.update;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.user.User;
import net.civeira.phylax.features.access.user.UserFacade;
import net.civeira.phylax.features.access.user.UserRef;
import net.civeira.phylax.features.access.user.application.projection.UserStateProyection;
import net.civeira.phylax.features.access.user.application.request.UserStateChange;
import net.civeira.phylax.features.access.user.application.service.visibility.UsersVisibility;
import net.civeira.phylax.features.access.user.gateway.UserCacheGateway;
import net.civeira.phylax.features.access.user.gateway.UserWriteRepositoryGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class UserUpdateUsecase {

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final UserCacheGateway cache;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final UserFacade facade;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final UserWriteRepositoryGateway gateway;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<UserAllowUpdateProposal> updateAllowEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<UserUpdateProposal> updateProposalEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<UserUpdateEvent> updatedEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final UsersVisibility visibility;

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  public Allow allow(final Interaction query, final UserRef reference) {
    UserAllowUpdateProposal base = UserAllowUpdateProposal.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).reference(reference).build();
    updateAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    UserAllowUpdateProposal base = UserAllowUpdateProposal.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).build();
    updateAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * The slide with some values
   *
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @param reference
   * @param input
   * @return The slide with some values
   */
  public UserStateProyection update(final Interaction query, final UserRef reference,
      final UserStateChange input) {
    Allow detail = allow(query, reference);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    User original = visibility.retrieveVisibleForUpdate(query, reference.getUidValue())
        .orElseThrow(() -> new NotFoundException(""));
    UserStateChange filled = visibility.copyWithFixed(query, input);
    UserUpdateProposal proposal =
        UserUpdateProposal.builder().dto(filled).interaction(query).reference(reference).build();
    updateProposalEmitter.fire(proposal);
    UserStateChange dto = proposal.getDto();;
    User saved = gateway.update(original, facade.update(original, dto));
    cache.update(saved);
    UserUpdateEvent event = UserUpdateEvent.builder().payload(saved).interaction(query).build();
    updatedEmitter.fire(event);
    return visibility.copyWithHidden(query, event.getPayload());
  }
}
