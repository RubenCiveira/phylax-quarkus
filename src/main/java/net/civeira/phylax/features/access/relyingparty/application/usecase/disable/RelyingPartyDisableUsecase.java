package net.civeira.phylax.features.access.relyingparty.application.usecase.disable;

import java.time.Duration;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.BatchIdentificator;
import net.civeira.phylax.common.batch.BatchProgress;
import net.civeira.phylax.common.batch.BatchService;
import net.civeira.phylax.common.batch.ExecutorByDeferSteps;
import net.civeira.phylax.common.batch.ExecutorPlan;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.common.telemetry.Observed;
import net.civeira.phylax.common.telemetry.Trace;
import net.civeira.phylax.features.access.relyingparty.application.visibility.RelyingPartyVisibilityFilter;
import net.civeira.phylax.features.access.relyingparty.application.visibility.RelyingPartysVisibility;
import net.civeira.phylax.features.access.relyingparty.domain.RelyingParty;
import net.civeira.phylax.features.access.relyingparty.domain.RelyingPartyRef;
import net.civeira.phylax.features.access.relyingparty.domain.gateway.RelyingPartyAuditGateway;
import net.civeira.phylax.features.access.relyingparty.domain.gateway.RelyingPartyCacheGateway;
import net.civeira.phylax.features.access.relyingparty.domain.gateway.RelyingPartyWriteRepositoryGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class RelyingPartyDisableUsecase {

  /**
   * Service to register audit events
   *
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final RelyingPartyAuditGateway audit;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final BatchService batch;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final RelyingPartyCacheGateway cache;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final Event<RelyingPartyDisableAllowDecision> execAllowEmitter;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final Event<RelyingPartyDisableCheck> execProposalEmitter;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final RelyingPartyWriteRepositoryGateway gateway;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final RelyingPartysVisibility visibility;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  @Observed("disable an entity of relying party")
  public Allow allow(final Interaction query, final RelyingPartyRef reference) {
    Trace.setAttribute("ref", reference.getUid());
    Allow response = checkAllow(query, reference);
    Trace.setAttribute("allowed", response.isAllowed());
    return response;
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @return
   */
  @Observed("disable an entity of relying party")
  public Allow allow(final Interaction query) {
    Allow response = checkAllow(query);
    Trace.setAttribute("allowed", response.isAllowed());
    return response;
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param filter
   * @return The slide with some values
   */
  @Observed("disable a set of entity of relying party")
  public BatchIdentificator batchDisable(final Interaction query,
      final RelyingPartyDisableFilter filter) {
    Allow detail = checkAllow(query);
    if (!detail.isAllowed()) {
      Trace.addEvent(
          "The policies for relying party dont allow to disable: " + detail.getDescription());
      throw new NotAllowedException(detail.getDescription());
    }
    RelyingPartyVisibilityFilter filterWithVisibility =
        RelyingPartyVisibilityFilter.builder().uid(filter.getUid().orElse(null))
            .uids(filter.getUids().stream().toList()).search(filter.getSearch().orElse(null))
            .apiKey(filter.getApiKey().orElse(null)).code(filter.getCode().orElse(null)).build();
    RelyingPartyDisableAllInBatchCommand command = RelyingPartyDisableAllInBatchCommand.builder()
        .interaction(query).filter(filterWithVisibility).build();
    return batch.start(command.getInteraction().getActor().getName().orElse("-"),
        Duration.ofHours(6),
        ExecutorPlan.<RelyingPartyDisableAllInBatchCommand>builder().params(command)
            .name("disable-relying-party")
            .executor(
                ExecutorByDeferSteps.<RelyingParty, RelyingParty, RelyingPartyDisableAllInBatchCommand, RelyingPartyDisablesInBatchExecutor.RelyingPartyPaginableBatch>builder()
                    .initializer(RelyingPartyDisablesInBatchExecutor.class)
                    .counter(RelyingPartyDisablesInBatchExecutor.class)
                    .descriptor(RelyingPartyDisablesInBatchExecutor.class)
                    .reader(RelyingPartyDisablesInBatchExecutor.class)
                    .processor(RelyingPartyDisablesInBatchExecutor.class)
                    .writer(RelyingPartyDisablesInBatchExecutor.class)
                    .finalizer(RelyingPartyDisablesInBatchExecutor.class).build())
            .build());
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query a filter to retrieve only matching values
   * @return The slide with some values
   */
  public BatchProgress checkProgress(final RelyingPartyDisableStatus query) {
    return query.getActor().getName()
        .flatMap(name -> batch.retrieve(query.getTaskId(), query.getConnection().getLocale(), name))
        .orElseThrow(() -> new NotFoundException());
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param reference
   * @return The slide with some values
   */
  @Observed("disable an entity of relying party")
  public RelyingPartyDisableProjection disable(final Interaction query,
      final RelyingPartyRef reference) {
    Trace.setAttribute("ref", reference.getUid());
    Allow detail = checkAllow(query, reference);
    if (!detail.isAllowed()) {
      Trace.addEvent(
          "The policies for relying party dont allow to disable: " + detail.getDescription());
      throw new NotAllowedException(detail.getDescription());
    }
    RelyingParty original = visibility.retrieveVisibleForUpdate(query, reference.getUid())
        .orElseThrow(() -> new NotFoundException(""));
    RelyingParty saved = doDisable(query, original);
    cacheUpdate(saved);
    // Register audit operation
    audit.updated("disable", saved, original, query);
    return RelyingPartyDisableProjection.from(visibility.copyWithHidden(query, saved));
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param saved
   */
  void cacheUpdate(final RelyingParty saved) {
    cache.update(saved);
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  Allow checkAllow(final Interaction query, final RelyingPartyRef reference) {
    RelyingPartyDisableAllowDecision base = RelyingPartyDisableAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).reference(reference).build();
    execAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @return
   */
  Allow checkAllow(final Interaction query) {
    RelyingPartyDisableAllowDecision base = RelyingPartyDisableAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).build();
    execAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param interaction
   * @param original
   * @return
   */
  RelyingParty doDisable(final Interaction interaction, final RelyingParty original) {
    RelyingPartyDisableCheck proposal =
        RelyingPartyDisableCheck.builder().reference(original).interaction(interaction).build();
    execProposalEmitter.fire(proposal);
    RelyingParty result = original.disable();
    RelyingParty saved = gateway.update(original, result);
    return saved;
  }
}
