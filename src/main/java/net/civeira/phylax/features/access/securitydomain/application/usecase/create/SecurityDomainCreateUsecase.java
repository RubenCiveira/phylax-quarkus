package net.civeira.phylax.features.access.securitydomain.application.usecase.create;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.securitydomain.application.visibility.SecurityDomainsVisibility;
import net.civeira.phylax.features.access.securitydomain.domain.SecurityDomain;
import net.civeira.phylax.features.access.securitydomain.domain.SecurityDomainChangeSet;
import net.civeira.phylax.features.access.securitydomain.domain.gateway.SecurityDomainCacheGateway;
import net.civeira.phylax.features.access.securitydomain.domain.gateway.SecurityDomainWriteRepositoryGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class SecurityDomainCreateUsecase {

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final SecurityDomainCacheGateway cache;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Event<SecurityDomainCreateAllowDecision> createAllowEmitter;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Event<SecurityDomainCreateCheck> createCheckEmitter;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Event<SecurityDomainCreateEnrich> createEnrichEmitter;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final SecurityDomainWriteRepositoryGateway gateway;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final SecurityDomainsVisibility visibility;

  /**
   * @autogenerated CreateUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    SecurityDomainCreateAllowDecision proposal = SecurityDomainCreateAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allow by default").build()).query(query)
        .build();
    createAllowEmitter.fire(proposal);
    return proposal.getDetail();
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated CreateUsecaseGenerator
   * @param query
   * @param input
   * @return The slide with some values
   */
  public SecurityDomainCreateProjection create(final Interaction query,
      final SecurityDomainCreateInput input) {
    Allow detail = allow(query);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    SecurityDomainCreateInput filled = SecurityDomainCreateInput
        .fromChangeSet(visibility.copyWithFixed(query, input.toChangeSet()));
    SecurityDomainCreateCheck check =
        SecurityDomainCreateCheck.builder().interaction(query).input(filled).build();
    createCheckEmitter.fire(check);
    SecurityDomainCreateEnrich proposal =
        SecurityDomainCreateEnrich.builder().interaction(query).input(filled).build();
    createEnrichEmitter.fire(proposal);
    SecurityDomainChangeSet dto = proposal.getInput().toChangeSet();
    SecurityDomain entity = SecurityDomain.create(dto);
    SecurityDomain created =
        gateway.create(entity, attempt -> visibility.checkVisibility(query, attempt.getUid()));
    cache.update(created);
    return SecurityDomainCreateProjection.from(visibility.copyWithHidden(query, created));
  }
}
