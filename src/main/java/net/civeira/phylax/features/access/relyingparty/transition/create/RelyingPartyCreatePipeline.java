package net.civeira.phylax.features.access.relyingparty.transition.create;

import java.util.ArrayList;
import java.util.List;
import java.util.function.Function;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.inject.Instance;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.features.access.relyingparty.RelyingParty;
import net.civeira.phylax.features.access.relyingparty.command.RelyingPartyWriteAttributes;
import net.civeira.phylax.features.access.relyingparty.transition.RelyingPartyChangeProposal;

@ApplicationScoped
@RequiredArgsConstructor
public class RelyingPartyCreatePipeline {

  /**
   * @autogenerated UpdateEntityRuleGenerator
   */
  private final Instance<RelyingPartyCreateStep> steps;

  /**
   * @autogenerated UpdateEntityRuleGenerator
   * @param attributes
   * @param last
   * @return
   */
  public RelyingParty create(final RelyingPartyWriteAttributes attributes,
      final Function<RelyingPartyChangeProposal, RelyingParty> last) {
    Function<RelyingPartyCreateTransition, RelyingParty> finalHandler = t -> {
      if (t.hasErrors()) {
        throw t.asConstraintException();
      }
      return last.apply(t.getData());
    };
    List<RelyingPartyCreateStep> reversed = new ArrayList<>(steps.stream().toList()).reversed();
    Function<RelyingPartyCreateTransition, RelyingParty> pipeline = reversed.stream().reduce(
        finalHandler, (next, step) -> transition -> step.create(transition, next), (f1, f2) -> f1);
    return pipeline.apply(RelyingPartyCreateTransition.builder()
        .data(new RelyingPartyChangeProposal(attributes)).build());
  }
}
