package net.civeira.phylax.features.access.useridentity.domain.valueobject;

import java.util.Optional;

import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.value.validation.ConstraintFailList;
import net.civeira.phylax.features.access.relyingparty.domain.RelyingPartyChangeSet;
import net.civeira.phylax.features.access.relyingparty.domain.RelyingPartyRef;
import net.civeira.phylax.features.access.relyingparty.domain.RelyingPartyReference;
import net.civeira.phylax.features.access.useridentity.domain.UserIdentity;

@RequiredArgsConstructor
public class RelyingPartyValueHolder<T> {

  /**
   * @autogenerated ValueHolderGenerator
   */
  private final T holder;

  /**
   * @autogenerated ValueHolderGenerator
   */
  private Optional<RelyingPartyVO> value = Optional.empty();

  /**
   * @autogenerated ValueHolderGenerator
   * @return
   */
  public Optional<RelyingPartyRef> getRelyingParty() {
    return value.flatMap(RelyingPartyVO::getRelyingParty);
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @return
   */
  public Optional<RelyingPartyVO> getRelyingPartyValue() {
    return value;
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @param original
   * @param fails
   * @return
   */
  public RelyingPartyVO readRelyingParty(final Optional<UserIdentity> original,
      final ConstraintFailList fails) {
    return value.orElse(
        original.map(UserIdentity::getRelyingPartyValue).orElseGet(RelyingPartyVO::nullValue));
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @param relyingParty
   * @return
   */
  public T relyingParty(RelyingPartyVO relyingParty) {
    value = Optional.of(relyingParty);
    return holder;
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @param relyingParty
   * @return
   */
  public T relyingParty(Optional<RelyingPartyVO> relyingParty) {
    value = relyingParty;
    return holder;
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @param relyingParty
   * @return
   */
  public T relyingParty(RelyingPartyRef relyingParty) {
    value = Optional.of(RelyingPartyVO.from(relyingParty));
    return holder;
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @param relyingParty
   * @return
   */
  public T relyingParty(RelyingPartyChangeSet relyingParty) {
    value = Optional
        .of(RelyingPartyVO.from(RelyingPartyReference.of(relyingParty.getUid().orElseThrow())));
    return holder;
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @return
   */
  public T relyingPartyNull() {
    value = Optional.empty();
    return holder;
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @return
   */
  public T unsetRelyingParty() {
    value = Optional.empty();
    return holder;
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @param field
   * @return
   */
  public T unsetRelyingPartyIfMatches(final String field) {
    if ("relyingParty".equals(field)) {
      value = Optional.empty();
    }
    return holder;
  }
}
