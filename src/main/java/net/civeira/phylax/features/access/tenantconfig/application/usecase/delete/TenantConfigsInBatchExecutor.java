package net.civeira.phylax.features.access.tenantconfig.application.usecase.delete;

import java.util.List;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.SpanKind;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Context;
import io.quarkus.arc.Unremovable;
import jakarta.enterprise.context.ApplicationScoped;
import lombok.Data;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.stepper.ItemDescriptor;
import net.civeira.phylax.common.batch.stepper.ItemProcessor;
import net.civeira.phylax.common.batch.stepper.ItemReader;
import net.civeira.phylax.common.batch.stepper.ItemWriter;
import net.civeira.phylax.common.batch.stepper.StepContext;
import net.civeira.phylax.common.batch.stepper.StepCounter;
import net.civeira.phylax.common.batch.stepper.StepFinalizer;
import net.civeira.phylax.common.batch.stepper.StepInitializer;
import net.civeira.phylax.common.exception.ExecutionException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.features.access.tenantconfig.application.visibility.TenantConfigsVisibility;
import net.civeira.phylax.features.access.tenantconfig.domain.TenantConfig;
import net.civeira.phylax.features.access.tenantconfig.domain.gateway.TenantConfigCursor;

@Unremovable
@ApplicationScoped
@RequiredArgsConstructor
class TenantConfigsInBatchExecutor implements
    StepCounter<TenantConfigDeleteAllInBatchCommand, TenantConfigsInBatchExecutor.TenantConfigPaginableBatch>,
    StepFinalizer<TenantConfigDeleteAllInBatchCommand, TenantConfigsInBatchExecutor.TenantConfigPaginableBatch>,
    StepInitializer<TenantConfigDeleteAllInBatchCommand, TenantConfigsInBatchExecutor.TenantConfigPaginableBatch>,
    ItemReader<TenantConfig, TenantConfigDeleteAllInBatchCommand, TenantConfigsInBatchExecutor.TenantConfigPaginableBatch>,
    ItemWriter<TenantConfig, TenantConfigDeleteAllInBatchCommand, TenantConfigsInBatchExecutor.TenantConfigPaginableBatch>,
    ItemProcessor<TenantConfig, TenantConfig, TenantConfigDeleteAllInBatchCommand, TenantConfigsInBatchExecutor.TenantConfigPaginableBatch>,
    ItemDescriptor<TenantConfig, TenantConfigDeleteAllInBatchCommand, TenantConfigsInBatchExecutor.TenantConfigPaginableBatch> {

  /**
   * @autogenerated EntityGenerator
   */
  @Data
  public static class TenantConfigPaginableBatch {

    /**
     * @autogenerated EntityGenerator
     */
    private String since;
  }

  /**
   * @autogenerated EntityGenerator
   */
  private final int size = 10;

  /**
   * @autogenerated EntityGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated EntityGenerator
   */
  private final TenantConfigDeleteUsecase usecase;

  /**
   * @autogenerated EntityGenerator
   */
  private final TenantConfigsVisibility visibility;

  /**
   * @autogenerated EntityGenerator
   * @param context
   * @return
   */
  @Override
  public long approximatedItems(
      final StepContext<TenantConfigDeleteAllInBatchCommand, TenantConfigPaginableBatch> context) {
    Span span = tracer
        .spanBuilder(
            "Count selected items to the batch delete for a set of entities of tenant config")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      return visibility.countVisibles(context.getParams().getInteraction(),
          context.getParams().getFilter());
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   */
  @Override
  public void finish(
      final StepContext<TenantConfigDeleteAllInBatchCommand, TenantConfigPaginableBatch> context) {
    usecase.flush();
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   */
  @Override
  public void init(
      final StepContext<TenantConfigDeleteAllInBatchCommand, TenantConfigPaginableBatch> context) {
    context.setState(new TenantConfigPaginableBatch());
  }

  /**
   * @autogenerated EntityGenerator
   * @param item
   * @param context
   * @return
   */
  @Override
  public String itemDescription(final TenantConfig item,
      final StepContext<TenantConfigDeleteAllInBatchCommand, TenantConfigPaginableBatch> context) {
    return item.getUid();
  }

  /**
   * @autogenerated EntityGenerator
   * @param item
   * @param context
   * @return
   */
  @Override
  public TenantConfig process(final TenantConfig item,
      final StepContext<TenantConfigDeleteAllInBatchCommand, TenantConfigPaginableBatch> context) {
    Span span = tracer
        .spanBuilder(
            "Prepare individual data for the batch delete on a set of entities of tenant config")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      Allow allowed = usecase.checkAllow(context.getParams().getInteraction(), item);
      if (!allowed.isAllowed()) {
        span.addEvent(
            "The policies for tenant config dont allow to delete: " + allowed.getDescription());
        throw new ExecutionException("not-allowed", null);
      }
      return item;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   * @return
   */
  @Override
  public List<TenantConfig> read(
      final StepContext<TenantConfigDeleteAllInBatchCommand, TenantConfigPaginableBatch> context) {
    Span span = tracer
        .spanBuilder(
            "Read selected entitites for a batch delete for a set of entities of tenant config")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      List<TenantConfig> page = visibility.listVisiblesForUpdate(
          context.getParams().getInteraction(), context.getParams().getFilter(),
          TenantConfigCursor.builder().limit(size).sinceUid(context.getState().getSince()).build());
      context.getState().setSince(page.isEmpty() ? null : page.get(page.size() - 1).getUid());
      span.setAttribute("size", page.size());
      return page;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated EntityGenerator
   * @param items
   * @param context
   */
  @Override
  public void write(final List<TenantConfig> items,
      final StepContext<TenantConfigDeleteAllInBatchCommand, TenantConfigPaginableBatch> context) {
    Span span =
        tracer.spanBuilder("Perform the batch delete for a set of entities of tenant config")
            .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
            .startSpan();
    try {
      span.setAttribute("size", items.size());
      items.forEach(item -> usecase.delete(context.getParams().getInteraction(), item));
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }
}
