package net.civeira.phylax.features.access.role.domain;

import lombok.NoArgsConstructor;
import lombok.experimental.Delegate;
import net.civeira.phylax.common.value.Uuid;
import net.civeira.phylax.features.access.role.domain.valueobject.NameValueHolder;
import net.civeira.phylax.features.access.role.domain.valueobject.RelyingPartyValueHolder;
import net.civeira.phylax.features.access.role.domain.valueobject.UidValueHolder;
import net.civeira.phylax.features.access.role.domain.valueobject.VersionValueHolder;

/**
 * A dto transfer to hold role attribute values
 */
@NoArgsConstructor
public class RoleChangeSet {

  /**
   * The user name to identify on the login screen
   *
   * @autogenerated EntityChangeSetGenerator
   */
  @Delegate
  private NameValueHolder<RoleChangeSet> nameValue = new NameValueHolder<>(this);

  /**
   * Domains without a tenant could be used only for admins
   *
   * @autogenerated EntityChangeSetGenerator
   */
  @Delegate
  private RelyingPartyValueHolder<RoleChangeSet> relyingPartyValue =
      new RelyingPartyValueHolder<>(this);

  /**
   * A uid string to identify the entity
   *
   * @autogenerated EntityChangeSetGenerator
   */
  @Delegate
  private UidValueHolder<RoleChangeSet> uidValue = new UidValueHolder<>(this);

  /**
   * Campo con el n√∫mero de version de role para controlar bloqueos optimistas
   *
   * @autogenerated EntityChangeSetGenerator
   */
  @Delegate
  private VersionValueHolder<RoleChangeSet> versionValue = new VersionValueHolder<>(this);

  /**
   * @autogenerated EntityChangeSetGenerator
   * @return
   */
  public RoleChangeSet newUid() {
    return version(0).uid(Uuid.comb().toString());
  }

  /**
   * Unset a value by the property name. Unset values will no provide command change neither provide
   * information to the output context (hidden or readonly fields, or policy restrictions).
   *
   * @autogenerated EntityChangeSetGenerator
   * @param field The field name to unset.
   */
  public void unset(final String field) {
    uidValue.unsetUidIfMatches(field);
    nameValue.unsetNameIfMatches(field);
    relyingPartyValue.unsetRelyingPartyIfMatches(field);
    versionValue.unsetVersionIfMatches(field);
  }
}
