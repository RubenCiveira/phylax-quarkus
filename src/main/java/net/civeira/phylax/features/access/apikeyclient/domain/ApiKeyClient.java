package net.civeira.phylax.features.access.apikeyclient.domain;

import java.util.ArrayList;
import java.util.HashMap;
import java.util.List;
import java.util.Map;
import java.util.Optional;

import lombok.AccessLevel;
import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.EqualsAndHashCode;
import lombok.Getter;
import lombok.NonNull;
import lombok.ToString;
import lombok.With;
import lombok.experimental.Delegate;
import net.civeira.phylax.common.exception.ConstraintException;
import net.civeira.phylax.common.value.validation.ConstraintFailList;
import net.civeira.phylax.features.access.apikeyclient.domain.event.ApiKeyClientCreateEvent;
import net.civeira.phylax.features.access.apikeyclient.domain.event.ApiKeyClientDeleteEvent;
import net.civeira.phylax.features.access.apikeyclient.domain.event.ApiKeyClientDisableEvent;
import net.civeira.phylax.features.access.apikeyclient.domain.event.ApiKeyClientEnableEvent;
import net.civeira.phylax.features.access.apikeyclient.domain.event.ApiKeyClientEvent;
import net.civeira.phylax.features.access.apikeyclient.domain.event.ApiKeyClientUpdateEvent;
import net.civeira.phylax.features.access.apikeyclient.domain.valueobject.CodeVO;
import net.civeira.phylax.features.access.apikeyclient.domain.valueobject.EnabledVO;
import net.civeira.phylax.features.access.apikeyclient.domain.valueobject.KeyVO;
import net.civeira.phylax.features.access.apikeyclient.domain.valueobject.ScopesVO;
import net.civeira.phylax.features.access.apikeyclient.domain.valueobject.UidVO;
import net.civeira.phylax.features.access.apikeyclient.domain.valueobject.VersionVO;

/**
 * Las diferentes aplicaciones que pueden usar el sistema a través de un api.
 */
@Builder
@Getter
@With
@ToString
@AllArgsConstructor(access = AccessLevel.PRIVATE)
@EqualsAndHashCode
public class ApiKeyClient implements ApiKeyClientRef {

  /**
   * Prepare a new api key client with the provided values
   *
   * @autogenerated EntityGenerator
   * @param change A set of values to create a new api key client
   * @throws ConstraintException If there are some constraints fails on the changes.
   * @return A well formed api key client.
   */
  public static ApiKeyClient create(final ApiKeyClientChangeSet change) throws ConstraintException {
    change.enabled(false);
    ApiKeyClient instance = new ApiKeyClient(change, Optional.empty());
    instance.addEvent(ApiKeyClientCreateEvent.builder().payload(instance).build());
    return instance;
  }

  /**
   * El código identificativo de la aplicación
   *
   * @autogenerated EntityGenerator
   */
  @Delegate
  @NonNull
  private CodeVO codeValue;

  /**
   * The indicator to allow the account be used without deleting it
   *
   * @autogenerated EntityGenerator
   */
  @Delegate
  @NonNull
  private EnabledVO enabledValue;

  /**
   * The key for the api
   *
   * @autogenerated EntityGenerator
   */
  @Delegate
  @Builder.Default
  private KeyVO keyValue = KeyVO.nullValue();

  /**
   * List of events
   *
   * @autogenerated EntityGenerator
   */
  @Builder.Default
  private List<ApiKeyClientEvent> recordedEvents = List.of();

  /**
   * A coma separated scopes for the api key
   *
   * @autogenerated EntityGenerator
   */
  @Delegate
  @Builder.Default
  private ScopesVO scopesValue = ScopesVO.nullValue();

  /**
   * El identificador de la aplicacion
   *
   * @autogenerated EntityGenerator
   */
  @Delegate
  @NonNull
  private UidVO uidValue;

  /**
   * Campo con el número de version de api key client para controlar bloqueos optimistas
   *
   * @autogenerated EntityGenerator
   */
  @Delegate
  @Builder.Default
  private VersionVO versionValue = VersionVO.nullValue();

  /**
   * @autogenerated EntityGenerator
   * @param attribute
   * @param previous
   */
  private ApiKeyClient(final ApiKeyClientChangeSet attribute,
      final Optional<ApiKeyClient> previous) {
    ConstraintFailList list = new ConstraintFailList();
    this.uidValue = attribute.readUid(previous, list);
    this.codeValue = attribute.readCode(previous, list);
    this.keyValue = attribute.readKey(previous, list);
    this.enabledValue = attribute.readEnabled(previous, list);
    this.scopesValue = attribute.readScopes(previous, list);
    this.versionValue = attribute.readVersion(previous, list);
    if (list.hasErrors()) {
      throw new ConstraintException("Invalid values on ApiKeyClient", list);
    }
    this.recordedEvents = previous.map(ApiKeyClient::getRecordedEvents).orElseGet(List::of);
  }

  /**
   * Apply changes to delete a api key client
   *
   * @autogenerated EntityGenerator
   * @return A instance of api key client ready to be deleted
   */
  public ApiKeyClient delete() {
    ApiKeyClient instance = new ApiKeyClient(new ApiKeyClientChangeSet(), Optional.of(this));
    instance.addEvent(ApiKeyClientDeleteEvent.builder().payload(instance).build());
    return instance;
  }

  /**
   * Apply disable on api key client.
   *
   * @autogenerated EntityGenerator
   * @return A modified instance of api key client
   */
  public ApiKeyClient disable() {
    ApiKeyClientChangeSet attr = new ApiKeyClientChangeSet();
    attr.enabled(false);
    ApiKeyClient instance = new ApiKeyClient(attr, Optional.of(this));
    instance.addEvent(ApiKeyClientDisableEvent.builder().payload(instance).build());
    return instance;
  }

  /**
   * Apply enable on api key client.
   *
   * @autogenerated EntityGenerator
   * @return A modified instance of api key client
   */
  public ApiKeyClient enable() {
    ApiKeyClientChangeSet attr = new ApiKeyClientChangeSet();
    attr.enabled(true);
    ApiKeyClient instance = new ApiKeyClient(attr, Optional.of(this));
    instance.addEvent(ApiKeyClientEnableEvent.builder().payload(instance).build());
    return instance;
  }

  /**
   * @autogenerated EntityGenerator
   * @return
   */
  public Map<String, String> toMap() {
    Map<String, String> map = new HashMap<>(6);
    uidToMap(map);
    codeToMap(map);
    keyToMap(map);
    enabledToMap(map);
    scopesToMap(map);
    versionToMap(map);
    return map;
  }

  /**
   * Modify the values for some of the properties of a api key client
   *
   * @autogenerated EntityGenerator
   * @param change The properties to be modified
   * @throws ConstraintException If there are some constraints fails on the changes.
   * @return A modified instance of api key client
   */
  public ApiKeyClient update(final ApiKeyClientChangeSet change) throws ConstraintException {
    change.unsetEnabled();
    ApiKeyClient instance = new ApiKeyClient(change, Optional.of(this));
    instance.addEvent(ApiKeyClientUpdateEvent.builder().payload(instance).build());
    return instance;
  }

  /**
   * @autogenerated EntityGenerator
   * @return
   */
  public ApiKeyClient withNextVersion() {
    return withVersionValue(VersionVO.from(nextVersion()));
  }

  /**
   * @autogenerated EntityGenerator
   * @param event
   */
  private void addEvent(final ApiKeyClientEvent event) {
    List<ApiKeyClientEvent> events = new ArrayList<>(this.recordedEvents);
    events.add(event);
    this.recordedEvents = List.copyOf(events);
  }
}
