package net.civeira.phylax.features.access.securityscope.application.service.visibility;

import java.util.Optional;
import java.util.function.Consumer;

import lombok.Builder;
import lombok.Data;
import lombok.EqualsAndHashCode;
import lombok.NonNull;
import lombok.With;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.securityscope.SecurityScope;
import net.civeira.phylax.features.access.securityscope.application.request.SecurityScopeStateChange;

@Data
@Builder(toBuilder = true)
@With
@EqualsAndHashCode(onlyExplicitlyIncluded = true)
public class SecurityScopePresetProposal {

  /**
   * @autogenerated PresetProposalGenerator
   */
  @NonNull
  private final Interaction interaction;

  /**
   * @autogenerated PresetProposalGenerator
   */
  @NonNull
  private SecurityScopeStateChange dto;

  /**
   * @autogenerated PresetProposalGenerator
   */
  private SecurityScope original;

  /**
   * @autogenerated PresetProposalGenerator
   * @return
   */
  public Optional<SecurityScope> getOriginal() {
    return Optional.ofNullable(original);
  }

  /**
   * @autogenerated PresetProposalGenerator
   * @param mapper
   */
  public void peek(Consumer<SecurityScopeStateChange> mapper) {
    mapper.accept(dto);
  }
}
