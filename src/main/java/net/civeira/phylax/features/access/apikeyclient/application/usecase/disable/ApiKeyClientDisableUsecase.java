package net.civeira.phylax.features.access.apikeyclient.application.usecase.disable;

import java.time.Duration;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.SpanKind;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Context;
import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.BatchIdentificator;
import net.civeira.phylax.common.batch.BatchProgress;
import net.civeira.phylax.common.batch.BatchService;
import net.civeira.phylax.common.batch.ExecutorByDeferSteps;
import net.civeira.phylax.common.batch.ExecutorPlan;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.apikeyclient.application.visibility.ApiKeyClientVisibilityFilter;
import net.civeira.phylax.features.access.apikeyclient.application.visibility.ApiKeyClientsVisibility;
import net.civeira.phylax.features.access.apikeyclient.domain.ApiKeyClient;
import net.civeira.phylax.features.access.apikeyclient.domain.ApiKeyClientRef;
import net.civeira.phylax.features.access.apikeyclient.domain.gateway.ApiKeyClientAuditGateway;
import net.civeira.phylax.features.access.apikeyclient.domain.gateway.ApiKeyClientCacheGateway;
import net.civeira.phylax.features.access.apikeyclient.domain.gateway.ApiKeyClientWriteRepositoryGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class ApiKeyClientDisableUsecase {

  /**
   * Service to register audit events
   *
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final ApiKeyClientAuditGateway audit;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final BatchService batch;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final ApiKeyClientCacheGateway cache;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final Event<ApiKeyClientDisableAllowDecision> execAllowEmitter;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final Event<ApiKeyClientDisableCheck> execProposalEmitter;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final ApiKeyClientWriteRepositoryGateway gateway;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final ApiKeyClientsVisibility visibility;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  public Allow allow(final Interaction query, final ApiKeyClientRef reference) {
    Span span = tracer.spanBuilder("Checking if is allowed to update an entity of api key client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      span.setAttribute("ref", reference.getUid());
      Allow response = checkAllow(query, reference);
      span.setAttribute("allowed", response.isAllowed());
      return response;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    Span span = tracer.spanBuilder("Checking if is allowed to update an entity of api key client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      Allow response = checkAllow(query);
      span.setAttribute("allowed", response.isAllowed());
      return response;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query a filter to retrieve only matching values
   * @return The slide with some values
   */
  public BatchProgress checkProgress(final ApiKeyClientDisableStatus query) {
    return query.getActor().getName()
        .flatMap(name -> batch.retrieve(query.getTaskId(), query.getConnection().getLocale(), name))
        .orElseThrow(() -> new NotFoundException());
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param reference
   * @return The slide with some values
   */
  public ApiKeyClientDisableProjection disable(final Interaction query,
      final ApiKeyClientRef reference) {
    Span span = tracer.spanBuilder("disable an entity of api key client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      span.setAttribute("ref", reference.getUid());
      Allow detail = checkAllow(query, reference);
      if (!detail.isAllowed()) {
        span.addEvent(
            "The policies for api key client dont allow to disable: " + detail.getDescription());
        throw new NotAllowedException(detail.getDescription());
      }
      ApiKeyClient original = visibility.retrieveVisibleForUpdate(query, reference.getUid())
          .orElseThrow(() -> new NotFoundException(""));
      ApiKeyClient saved = disable(query, original);
      flush();
      // Register audit operation
      audit.updated("disable", saved, original, query);
      return ApiKeyClientDisableProjection.from(visibility.copyWithHidden(query, saved));
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param filter
   * @return The slide with some values
   */
  public BatchIdentificator disable(final Interaction query,
      final ApiKeyClientDisableFilter filter) {
    Span span = tracer.spanBuilder("disable a set of entities of api key client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      Allow detail = checkAllow(query);
      if (!detail.isAllowed()) {
        span.addEvent(
            "The policies for api key client dont allow to disable: " + detail.getDescription());
        throw new NotAllowedException(detail.getDescription());
      }
      ApiKeyClientVisibilityFilter filterWithVisibility = ApiKeyClientVisibilityFilter.builder()
          .uid(filter.getUid().orElse(null)).uids(filter.getUids().stream().toList())
          .search(filter.getSearch().orElse(null)).key(filter.getKey().orElse(null)).build();
      ApiKeyClientDisableAllInBatchCommand command = ApiKeyClientDisableAllInBatchCommand.builder()
          .interaction(query).filter(filterWithVisibility).build();
      return batch.start(command.getInteraction().getActor().getName().orElse("-"),
          Duration.ofHours(6),
          ExecutorPlan.<ApiKeyClientDisableAllInBatchCommand>builder().params(command)
              .name("disable-api-key-client")
              .executor(
                  ExecutorByDeferSteps.<ApiKeyClient, ApiKeyClient, ApiKeyClientDisableAllInBatchCommand, ApiKeyClientDisablesInBatchExecutor.ApiKeyClientPaginableBatch>builder()
                      .initializer(ApiKeyClientDisablesInBatchExecutor.class)
                      .counter(ApiKeyClientDisablesInBatchExecutor.class)
                      .descriptor(ApiKeyClientDisablesInBatchExecutor.class)
                      .reader(ApiKeyClientDisablesInBatchExecutor.class)
                      .processor(ApiKeyClientDisablesInBatchExecutor.class)
                      .writer(ApiKeyClientDisablesInBatchExecutor.class)
                      .finalizer(ApiKeyClientDisablesInBatchExecutor.class).build())
              .build());
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  Allow checkAllow(final Interaction query, final ApiKeyClientRef reference) {
    ApiKeyClientDisableAllowDecision base = ApiKeyClientDisableAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).reference(reference).build();
    execAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @return
   */
  Allow checkAllow(final Interaction query) {
    ApiKeyClientDisableAllowDecision base = ApiKeyClientDisableAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).build();
    execAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param interaction
   * @param original
   * @return
   */
  ApiKeyClient disable(final Interaction interaction, final ApiKeyClient original) {
    ApiKeyClientDisableCheck proposal =
        ApiKeyClientDisableCheck.builder().reference(original).interaction(interaction).build();
    execProposalEmitter.fire(proposal);
    ApiKeyClient result = original.disable();
    ApiKeyClient saved = gateway.update(original, result);
    return saved;
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  void flush() {
    cache.evict();
  }
}
