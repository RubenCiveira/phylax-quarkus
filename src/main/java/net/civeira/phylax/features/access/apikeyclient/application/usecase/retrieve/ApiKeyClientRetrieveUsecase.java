package net.civeira.phylax.features.access.apikeyclient.application.usecase.retrieve;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.common.telemetry.Observed;
import net.civeira.phylax.common.telemetry.Trace;
import net.civeira.phylax.features.access.apikeyclient.application.visibility.ApiKeyClientsVisibility;
import net.civeira.phylax.features.access.apikeyclient.domain.ApiKeyClientRef;
import net.civeira.phylax.features.access.apikeyclient.domain.gateway.ApiKeyClientCached;

@ApplicationScoped
@RequiredArgsConstructor
public class ApiKeyClientRetrieveUsecase {

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final Event<ApiKeyClientRetrieveAllowDecision> retrieveAllowEmitter;

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final ApiKeyClientsVisibility visibility;

  /**
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  @Observed("Checking if is allowed to read an entity of api key client")
  public Allow allow(final Interaction query, final ApiKeyClientRef reference) {
    Trace.setAttribute("ref", reference.getUid());
    Allow response = checkAllow(query, reference);
    Trace.setAttribute("allowed", response.isAllowed());
    return response;
  }

  /**
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @return
   */
  @Observed("Checking if is allowed to read an entity of api key client")
  public Allow allow(final Interaction query) {
    Allow response = checkAllow(query);
    Trace.setAttribute("allowed", response.isAllowed());
    return response;
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @param reference
   * @return The slide with some values
   */
  @Observed("Retrieving an entity of api key client")
  public ApiKeyClientRetrieveProjection retrieve(final Interaction query,
      final ApiKeyClientRef reference) {
    Trace.setAttribute("ref", reference.getUid());
    Allow detail = allow(query, reference);
    if (!detail.isAllowed()) {
      Trace.addEvent(
          "The policies for api key client dont allow to read: " + detail.getDescription());
      throw new NotAllowedException(detail.getDescription());
    }
    ApiKeyClientCached retrieveCachedVisible =
        visibility.retrieveCachedVisible(query, reference.getUid());
    return retrieveCachedVisible.first().map(first -> visibility.copyWithHidden(query, first))
        .map(ApiKeyClientRetrieveProjection::from).orElseThrow(() -> new NotFoundException(""));
  }

  /**
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  private Allow checkAllow(final Interaction query, final ApiKeyClientRef reference) {
    ApiKeyClientRetrieveAllowDecision base = ApiKeyClientRetrieveAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).reference(reference).build();
    retrieveAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @return
   */
  private Allow checkAllow(final Interaction query) {
    ApiKeyClientRetrieveAllowDecision base = ApiKeyClientRetrieveAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).build();
    retrieveAllowEmitter.fire(base);
    return base.getDetail();
  }
}
