package net.civeira.phylax.features.access.securityscope.application.usecase.list;

import java.util.List;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.algorithms.metadata.TimestampedList;
import net.civeira.phylax.common.algorithms.metadata.WrapMetadata;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.securityscope.application.visibility.SecurityScopeVisibilityFilter;
import net.civeira.phylax.features.access.securityscope.application.visibility.SecurityScopesVisibility;
import net.civeira.phylax.features.access.securityscope.domain.gateway.SecurityScopeCached;
import net.civeira.phylax.features.access.securityscope.domain.gateway.SecurityScopeCursor;

@ApplicationScoped
@RequiredArgsConstructor
public class SecurityScopeListUsecase {

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final Event<SecurityScopeListAllowDecision> listAllowEmitter;

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final SecurityScopesVisibility visibility;

  /**
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    SecurityScopeListAllowDecision proposal = SecurityScopeListAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allow by default").build()).query(query)
        .build();
    listAllowEmitter.fire(proposal);
    return proposal.getDetail();
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @param filter
   * @param cursor
   * @return The slide with some values
   */
  public List<SecurityScopeListProjection> list(final Interaction query,
      final SecurityScopeListFilter filter, final SecurityScopeListCursor cursor) {
    Allow detail = allow(query);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    SecurityScopeVisibilityFilter visibleFilter = SecurityScopeVisibilityFilter.builder()
        .uid(filter.getUid().orElse(null)).uids(filter.getUids().stream().toList())
        .search(filter.getSearch().orElse(null)).resource(filter.getResource().orElse(null))
        .trustedClient(filter.getTrustedClient().orElse(null))
        .trustedClients(filter.getTrustedClients())
        .relyingParty(filter.getRelyingParty().orElse(null))
        .relyingPartys(filter.getRelyingPartys()).build();
    SecurityScopeCursor gatewayCursor = SecurityScopeCursor.builder()
        .limit(cursor.getLimit().orElse(null)).sinceUid(cursor.getSinceUid().orElse(null)).build();
    SecurityScopeCached values = visibility.listCachedVisibles(query, visibleFilter, gatewayCursor);
    List<SecurityScopeListProjection> list =
        values.getValue().stream().map(value -> visibility.copyWithHidden(query, value))
            .map(SecurityScopeListProjection::from).toList();
    return new TimestampedList<>(WrapMetadata.<List<SecurityScopeListProjection>>builder()
        .data(list).since(values.getSince().toInstant()).build());
  }
}
