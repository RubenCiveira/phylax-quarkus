package net.civeira.phylax.features.access.apikeyclient.infrastructure.driven;

import java.util.List;
import java.util.Optional;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.SpanKind;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Context;
import jakarta.enterprise.context.ApplicationScoped;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.algorithms.Slider;
import net.civeira.phylax.features.access.apikeyclient.domain.ApiKeyClient;
import net.civeira.phylax.features.access.apikeyclient.domain.ApiKeyClientRef;
import net.civeira.phylax.features.access.apikeyclient.domain.gateway.ApiKeyClientCursor;
import net.civeira.phylax.features.access.apikeyclient.domain.gateway.ApiKeyClientFilter;
import net.civeira.phylax.features.access.apikeyclient.domain.gateway.ApiKeyClientReadRepositoryGateway;
import net.civeira.phylax.features.access.apikeyclient.infrastructure.repository.ApiKeyClientRepository;

@ApplicationScoped
@RequiredArgsConstructor
public class ApiKeyClientReadGatewayAdapter implements ApiKeyClientReadRepositoryGateway {

  /**
   * @autogenerated ReadAdaterGatewayGenerator
   */
  private final ApiKeyClientRepository repository;

  /**
   * @autogenerated ReadAdaterGatewayGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated ReadAdaterGatewayGenerator
   * @param filter
   * @return
   */
  @Override
  public long count(ApiKeyClientFilter filter) {
    Span span = tracer.spanBuilder("Count on the repository the matches entities of api key client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.count(filter);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated ReadAdaterGatewayGenerator
   * @param uid
   * @param filter
   * @return
   */
  @Override
  public boolean exists(String uid, Optional<ApiKeyClientFilter> filter) {
    Span span = tracer.spanBuilder("Check if exists on the repository a entity of api key client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.exists(uid, filter);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated ReadAdaterGatewayGenerator
   * @param filter
   * @return
   */
  @Override
  public Optional<ApiKeyClient> find(ApiKeyClientFilter filter) {
    Span span = tracer.spanBuilder("Find on the repository one entity of api key client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.find(filter);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated ReadAdaterGatewayGenerator
   * @param filter
   * @return
   */
  @Override
  public List<ApiKeyClient> list(ApiKeyClientFilter filter) {
    Span span = tracer.spanBuilder("Retrieve from repository a list for entities of api key client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.list(filter);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated ReadAdaterGatewayGenerator
   * @param reference
   * @return
   */
  @Override
  public ApiKeyClient resolve(ApiKeyClientRef reference) {
    Span span = tracer.spanBuilder("Enrich from the repository a entity of api key client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.resolve(reference);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated ReadAdaterGatewayGenerator
   * @param uid
   * @param filter
   * @return
   */
  @Override
  public Optional<ApiKeyClient> retrieve(String uid, Optional<ApiKeyClientFilter> filter) {
    Span span = tracer.spanBuilder("Check if exists on the repository a entity of api key client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.retrieve(uid, filter);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated ReadAdaterGatewayGenerator
   * @param filter
   * @param cursor
   * @return
   */
  @Override
  public Slider<ApiKeyClient> slide(ApiKeyClientFilter filter, ApiKeyClientCursor cursor) {
    Span span = tracer
        .spanBuilder("Retrieve from repository a slide for entities of api key client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.slide(filter, cursor);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }
}
