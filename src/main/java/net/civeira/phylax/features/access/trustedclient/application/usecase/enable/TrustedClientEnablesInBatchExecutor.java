package net.civeira.phylax.features.access.trustedclient.application.usecase.enable;

import java.util.List;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.SpanKind;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Context;
import io.quarkus.arc.Unremovable;
import jakarta.enterprise.context.ApplicationScoped;
import lombok.Data;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.stepper.ItemDescriptor;
import net.civeira.phylax.common.batch.stepper.ItemProcessor;
import net.civeira.phylax.common.batch.stepper.ItemReader;
import net.civeira.phylax.common.batch.stepper.ItemWriter;
import net.civeira.phylax.common.batch.stepper.StepContext;
import net.civeira.phylax.common.batch.stepper.StepCounter;
import net.civeira.phylax.common.batch.stepper.StepFinalizer;
import net.civeira.phylax.common.batch.stepper.StepInitializer;
import net.civeira.phylax.common.exception.ExecutionException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.features.access.trustedclient.application.visibility.TrustedClientsVisibility;
import net.civeira.phylax.features.access.trustedclient.domain.TrustedClient;
import net.civeira.phylax.features.access.trustedclient.domain.gateway.TrustedClientCursor;

@Unremovable
@ApplicationScoped
@RequiredArgsConstructor
class TrustedClientEnablesInBatchExecutor implements
    StepCounter<TrustedClientEnableAllInBatchCommand, TrustedClientEnablesInBatchExecutor.TrustedClientPaginableBatch>,
    StepFinalizer<TrustedClientEnableAllInBatchCommand, TrustedClientEnablesInBatchExecutor.TrustedClientPaginableBatch>,
    StepInitializer<TrustedClientEnableAllInBatchCommand, TrustedClientEnablesInBatchExecutor.TrustedClientPaginableBatch>,
    ItemReader<TrustedClient, TrustedClientEnableAllInBatchCommand, TrustedClientEnablesInBatchExecutor.TrustedClientPaginableBatch>,
    ItemWriter<TrustedClient, TrustedClientEnableAllInBatchCommand, TrustedClientEnablesInBatchExecutor.TrustedClientPaginableBatch>,
    ItemProcessor<TrustedClient, TrustedClient, TrustedClientEnableAllInBatchCommand, TrustedClientEnablesInBatchExecutor.TrustedClientPaginableBatch>,
    ItemDescriptor<TrustedClient, TrustedClientEnableAllInBatchCommand, TrustedClientEnablesInBatchExecutor.TrustedClientPaginableBatch> {

  /**
   * @autogenerated EntityGenerator
   */
  @Data
  public static class TrustedClientPaginableBatch {

    /**
     * @autogenerated EntityGenerator
     */
    private String since;
  }

  /**
   * @autogenerated EntityGenerator
   */
  private final int size = 10;

  /**
   * @autogenerated EntityGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated EntityGenerator
   */
  private final TrustedClientEnableUsecase usecase;

  /**
   * @autogenerated EntityGenerator
   */
  private final TrustedClientsVisibility visibility;

  /**
   * @autogenerated EntityGenerator
   * @param context
   * @return
   */
  @Override
  public long approximatedItems(
      final StepContext<TrustedClientEnableAllInBatchCommand, TrustedClientPaginableBatch> context) {
    Span span = tracer
        .spanBuilder(
            "Count selected items to the batch enable for a set of entities of trusted client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      return visibility.countVisibles(context.getParams().getInteraction(),
          context.getParams().getFilter());
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   */
  @Override
  public void finish(
      final StepContext<TrustedClientEnableAllInBatchCommand, TrustedClientPaginableBatch> context) {
    usecase.flush();
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   */
  @Override
  public void init(
      final StepContext<TrustedClientEnableAllInBatchCommand, TrustedClientPaginableBatch> context) {
    context.setState(new TrustedClientPaginableBatch());
  }

  /**
   * @autogenerated EntityGenerator
   * @param item
   * @param context
   * @return
   */
  @Override
  public String itemDescription(final TrustedClient item,
      final StepContext<TrustedClientEnableAllInBatchCommand, TrustedClientPaginableBatch> context) {
    return item.getUid();
  }

  /**
   * @autogenerated EntityGenerator
   * @param item
   * @param context
   * @return
   */
  @Override
  public TrustedClient process(final TrustedClient item,
      final StepContext<TrustedClientEnableAllInBatchCommand, TrustedClientPaginableBatch> context) {
    Span span = tracer
        .spanBuilder(
            "Prepare individual data for the batch enable on a set of entities of trusted client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      Allow allowed = usecase.checkAllow(context.getParams().getInteraction(), item);
      if (!allowed.isAllowed()) {
        span.addEvent(
            "The policies for trusted client dont allow to delete: " + allowed.getDescription());
        throw new ExecutionException("not-allowed", null);
      }
      return item;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   * @return
   */
  @Override
  public List<TrustedClient> read(
      final StepContext<TrustedClientEnableAllInBatchCommand, TrustedClientPaginableBatch> context) {
    Span span = tracer
        .spanBuilder(
            "Read selected entitites for a batch enable for a set of entities of trusted client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      List<TrustedClient> page = visibility.listVisiblesForUpdate(
          context.getParams().getInteraction(), context.getParams().getFilter(), TrustedClientCursor
              .builder().limit(size).sinceUid(context.getState().getSince()).build());
      context.getState().setSince(page.isEmpty() ? null : page.get(page.size() - 1).getUid());
      return page;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated EntityGenerator
   * @param items
   * @param context
   */
  @Override
  public void write(final List<TrustedClient> items,
      final StepContext<TrustedClientEnableAllInBatchCommand, TrustedClientPaginableBatch> context) {
    Span span =
        tracer.spanBuilder("Perform the batch enable for a set of entities of trusted client")
            .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
            .startSpan();
    try {
      span.setAttribute("size", items.size());
      items.forEach(item -> usecase.enable(context.getParams().getInteraction(), item));
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }
}
