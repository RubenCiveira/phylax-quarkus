package net.civeira.phylax.features.access.tenant.application.usecase.update;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.tenant.Tenant;
import net.civeira.phylax.features.access.tenant.TenantFacade;
import net.civeira.phylax.features.access.tenant.TenantRef;
import net.civeira.phylax.features.access.tenant.application.projection.TenantStateProyection;
import net.civeira.phylax.features.access.tenant.application.request.TenantStateChange;
import net.civeira.phylax.features.access.tenant.application.service.visibility.TenantsVisibility;
import net.civeira.phylax.features.access.tenant.gateway.TenantCacheGateway;
import net.civeira.phylax.features.access.tenant.gateway.TenantWriteRepositoryGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class TenantUpdateUsecase {

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final TenantCacheGateway cache;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final TenantFacade facade;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final TenantWriteRepositoryGateway gateway;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<TenantAllowUpdateProposal> updateAllowEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<TenantUpdateProposal> updateProposalEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<TenantUpdateEvent> updatedEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final TenantsVisibility visibility;

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  public Allow allow(final Interaction query, final TenantRef reference) {
    TenantAllowUpdateProposal base = TenantAllowUpdateProposal.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).reference(reference).build();
    updateAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    TenantAllowUpdateProposal base = TenantAllowUpdateProposal.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).build();
    updateAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * The slide with some values
   *
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @param reference
   * @param input
   * @return The slide with some values
   */
  public TenantStateProyection update(final Interaction query, final TenantRef reference,
      final TenantStateChange input) {
    Allow detail = allow(query, reference);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    Tenant original = visibility.retrieveVisibleForUpdate(query, reference.getUidValue())
        .orElseThrow(() -> new NotFoundException(""));
    TenantStateChange filled = visibility.copyWithFixed(query, input);
    TenantUpdateProposal proposal =
        TenantUpdateProposal.builder().dto(filled).interaction(query).reference(reference).build();
    updateProposalEmitter.fire(proposal);
    TenantStateChange dto = proposal.getDto();;
    Tenant saved = gateway.update(original, facade.update(original, dto));
    cache.update(saved);
    TenantUpdateEvent event = TenantUpdateEvent.builder().payload(saved).interaction(query).build();
    updatedEmitter.fire(event);
    return visibility.copyWithHidden(query, event.getPayload());
  }
}
