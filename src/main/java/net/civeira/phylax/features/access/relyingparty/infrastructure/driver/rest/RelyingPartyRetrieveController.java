package net.civeira.phylax.features.access.relyingparty.infrastructure.driver.rest;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.SpanKind;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Context;
import jakarta.enterprise.context.ApplicationScoped;
import jakarta.ws.rs.core.Response;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.infrastructure.CurrentRequest;
import net.civeira.phylax.features.access.relyingparty.application.usecase.retrieve.RelyingPartyRetrieveProjection;
import net.civeira.phylax.features.access.relyingparty.application.usecase.retrieve.RelyingPartyRetrieveUsecase;
import net.civeira.phylax.features.access.relyingparty.domain.RelyingPartyReference;
import net.civeira.phylax.generated.openapi.model.RelyingPartyApiDto;

@RequiredArgsConstructor
@ApplicationScoped
public class RelyingPartyRetrieveController {

  /**
   * @autogenerated RetrieveControllerGenerator
   */
  private final CurrentRequest currentRequest;

  /**
   * @autogenerated RetrieveControllerGenerator
   */
  private final RelyingPartyRetrieveUsecase retrieve;

  /**
   * @autogenerated RetrieveControllerGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated RetrieveControllerGenerator
   * @param uid
   * @return
   */
  public Response relyingPartyApiRetrieve(final String uid) {
    Span span = tracer.spanBuilder("Api to retrieve an entity of relying party")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      span.setAttribute("ref", uid);
      RelyingPartyRetrieveProjection retrieved =
          retrieve.retrieve(currentRequest.interaction(), RelyingPartyReference.of(uid));
      /* .header("Last-Modified", value.getSince().format(DateTimeFormatter.RFC_1123_DATE_TIME))) */
      return Response.ok(toApiModel(retrieved)).build();
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated RetrieveControllerGenerator
   * @param dto
   * @return
   */
  private RelyingPartyApiDto toApiModel(RelyingPartyRetrieveProjection dto) {
    Span span = tracer
        .spanBuilder(
            "Mapper to build api content to response from model for entity of relying party")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      RelyingPartyApiDto relyingPartyApiDto = new RelyingPartyApiDto();
      relyingPartyApiDto.setUid(dto.getUid());
      relyingPartyApiDto.setCode(dto.getCode());
      relyingPartyApiDto.setApiKey(dto.getApiKey());
      relyingPartyApiDto.setEnabled(dto.getEnabled());
      relyingPartyApiDto.setVersion(dto.getVersion());
      return relyingPartyApiDto;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }
}
