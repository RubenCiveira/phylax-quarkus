package net.civeira.phylax.features.access.securityscope.valueobject;

import java.util.Optional;

import lombok.EqualsAndHashCode;
import lombok.Getter;
import lombok.RequiredArgsConstructor;
import lombok.With;
import net.civeira.phylax.common.exception.ConstraintException;
import net.civeira.phylax.common.value.validation.AbstractFailList;
import net.civeira.phylax.common.value.validation.ConstraintFail;
import net.civeira.phylax.common.value.validation.ConstraintFailList;
import net.civeira.phylax.features.access.relyingparty.RelyingPartyRef;
import net.civeira.phylax.features.access.relyingparty.RelyingPartyReference;

/**
 * Value Object representing el relying party de security scope of securityScope.
 * <p>
 * This class ensures that the relying party is a {@code String} and encapsulates its validation and
 * formatting logic. It is immutable and compliant with DDD (Domain-Driven Design) principles.
 * </p>
 * <p>
 * Use {@link #from(String)} or {@link #tryFrom(Object)} to safely create instances. Validation
 * errors are reported via {@link ConstraintFailList} or thrown as {@link ConstraintException}.
 * </p>
 */
@Getter
@RequiredArgsConstructor
@With
@EqualsAndHashCode(onlyExplicitlyIncluded = true)
public class RelyingPartyVO {

  /**
   * An empty instance
   *
   * @autogenerated ValueObjectGenerator
   * @param relyingParty
   * @return An empty instance
   */
  public static RelyingPartyVO from(final RelyingPartyRef relyingParty) {
    return tryFrom(relyingParty);
  }

  /**
   * An empty instance
   *
   * @autogenerated ValueObjectGenerator
   * @param relyingPartyUid
   * @return An empty instance
   */
  public static RelyingPartyVO fromReference(final String relyingPartyUid) {
    return null == relyingPartyUid ? nullValue() : from(RelyingPartyReference.of(relyingPartyUid));
  }

  /**
   * An empty instance
   *
   * @autogenerated ValueObjectGenerator
   * @return An empty instance
   */
  public static RelyingPartyVO nullValue() {
    return new RelyingPartyVO(null);
  }

  /**
   * An empty instance
   *
   * @autogenerated ValueObjectGenerator
   * @param relyingParty
   * @return An empty instance
   */
  public static RelyingPartyVO tryFrom(final Object relyingParty) {
    ConstraintFailList list = new ConstraintFailList();
    RelyingPartyVO result = tryFrom(relyingParty, list);
    if (list.hasErrors()) {
      throw new ConstraintException(list);
    }
    return result;
  }

  /**
   * An empty instance
   *
   * @autogenerated ValueObjectGenerator
   * @param relyingParty temptative value
   * @param fails Error list
   * @return An empty instance
   */
  public static <T extends AbstractFailList> RelyingPartyVO tryFrom(final Object relyingParty,
      final T fails) {
    if (null == relyingParty) {
      return new RelyingPartyVO(null);
    } else if (relyingParty instanceof RelyingPartyRef castedRelyingParty) {
      return new RelyingPartyVO(castedRelyingParty);
    } else {
      fails.add(new ConstraintFail("wrong-type", "relyingParty", relyingParty.getClass(),
          "A RelyingPartyRef type is expected for relyingParty"));
      return null;
    }
  }

  /**
   * An empty instance
   *
   * @autogenerated ValueObjectGenerator
   * @param relyingPartyUid
   * @return An empty instance
   */
  public static RelyingPartyVO tryFromReference(final String relyingPartyUid) {
    return tryFrom(RelyingPartyReference.of(relyingPartyUid));
  }

  /**
   * An empty instance
   *
   * @autogenerated ValueObjectGenerator
   * @param relyingPartyUid
   * @param fails Error list
   * @return An empty instance
   */
  public static RelyingPartyVO tryFromReference(final String relyingPartyUid,
      final ConstraintFailList fails) {
    return tryFrom(RelyingPartyReference.of(relyingPartyUid), fails);
  }

  /**
   * The actual value of the name.
   *
   * @autogenerated ValueObjectGenerator
   */
  private final RelyingPartyRef value;

  /**
   * @autogenerated ValueObjectGenerator
   * @return
   */
  public Optional<String> getReferenceValue() {
    return Optional.ofNullable(value).map(RelyingPartyRef::getUidValue);
  }

  /**
   * @autogenerated ValueObjectGenerator
   * @return
   */
  public Optional<RelyingPartyRef> getValue() {
    return Optional.ofNullable(value);
  }

  /**
   * the string value wrapped in square brackets.
   *
   * @autogenerated ValueObjectGenerator
   * @return the string value wrapped in square brackets.
   */
  @Override
  public String toString() {
    return "[" + value + "]";
  }
}
