  public TenantTermsOfUse update(TenantTermsOfUse entity) {
    try (SqlTemplate template = new SqlTemplate(datasource, tracer)) {
      SqlCommand sq = template.createSqlCommand(
          "update \"tenant_terms_of_use\" set  \"tenant\" = :tenant, \"text\" = :text, \"attached\" = :attached, \"activation_date\" = :activationDate, \"version\" = \"version\" + 1 where \"uid\" = :uid and \"version\" = :version");
      sq.with(UID, SqlParameterValue.of(entity.getUid().getValue()));
      sq.with(TENANT, SqlParameterValue.of(entity.getTenant().getReferenceValue()));
      sq.with(TEXT, SqlParameterValue.of(entity.getText().getValue()));
      sq.with(ATTACHED, entity.getAttached().getValue().map(SqlParameterValue::of)
          .orElseGet(SqlParameterValue::ofNullString));
      sq.with(ACTIVATION_DATE, entity.getActivationDate().getValue().map(SqlParameterValue::of)
          .orElseGet(SqlParameterValue::ofNullOffsetDateTime));
      sq.with(VERSION, entity.getVersion().getValue().map(SqlParameterValue::of)
          .orElseGet(SqlParameterValue::ofNullInteger));
      int num = sq.execute();
      if (0 == num) {
        throw new OptimistLockException("No delete from");
      }
      return entity.nextVersion();
    }
  }

  /**
   * @autogenerated RepositoryJdbcGenerator
   * @return
   */
  private SqlConverter<TenantTermsOfUse> converter() {
    return row -> {
      try {
        return Optional.of(TenantTermsOfUse.builder().uid(UidVO.from(row.getString(UID)))
            .tenant(TenantVO.fromReference(row.getString(TENANT)))
            .text(TextVO.from(row.getString(TEXT)))
            .attached(AttachedVO.from(row.getString(ATTACHED)))
            .activationDate(
                ActivationDateVO.from(null == row.getTimestamp(ACTIVATION_DATE_SNAKE) ? null
                    : OffsetDateTime.ofInstant(
                        Instant.ofEpochMilli(row.getTimestamp(ACTIVATION_DATE_SNAKE).getTime()),
                        ZoneId.systemDefault())))
            .version(VersionVO.from(row.getInt(VERSION))).build());
      } catch (ConstraintException ce) {
        log.error("Unable to map data for {}", row.getString(1), ce);
        return Optional.empty();
      }
    };
  }

  /**
   * @autogenerated RepositoryJdbcGenerator
   * @param template
   * @param filter
   * @return
   */
  private <T> SqlSchematicQuery<T> filteredQuery(SqlTemplate template,
      TenantTermsOfUseFilter filter) {
    SqlSchematicQuery<T> sq = template.createSqlSchematicQuery(TENANT_TERMS_OF_USE_SNAKE);
    sq.selectFields(TENANT_TERMS_OF_USE_UID, TENANT_TERMS_OF_USE_TENANT, TENANT_TERMS_OF_USE_TEXT,
        TENANT_TERMS_OF_USE_ATTACHED, TENANT_TERMS_OF_USE_ACTIVATION_DATE,
        TENANT_TERMS_OF_USE_VERSION);
    filter.getUid().ifPresent(uid -> sq.where(UID, SqlOperator.EQ, SqlParameterValue.of(uid)));
    if (!filter.getUids().isEmpty()) {
      sq.where(UID, SqlOperator.IN, SqlListParameterValue.strings(filter.getUids()));
    }
    filter.getSearch().ifPresent(
        search -> sq.where("uid", SqlOperator.LIKE, SqlParameterValue.of("%" + search + "%")));
    filter.getTenant().ifPresent(
        tenant -> sq.where(TENANT, SqlOperator.EQ, SqlParameterValue.of(tenant.getUidValue())));
    if (!filter.getTenants().isEmpty()) {
      sq.where(TENANT, SqlOperator.IN, SqlListParameterValue.strings(filter.getTenants()));
    }
    filter.getTenantTenantAccesible()
        .ifPresent(tenantTenantAccesible -> sq.where(TENANT_TERMS_OF_USE_TENANT, SqlOperator.EQ,
            SqlParameterValue.of(tenantTenantAccesible)));
    return sq;
  }

