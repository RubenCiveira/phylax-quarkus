package net.civeira.phylax.features.access.tenant.transition.update;

import java.util.ArrayList;
import java.util.List;
import java.util.function.Function;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.inject.Instance;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.features.access.tenant.Tenant;
import net.civeira.phylax.features.access.tenant.command.TenantWriteAttributes;
import net.civeira.phylax.features.access.tenant.transition.TenantChangeProposal;

@ApplicationScoped
@RequiredArgsConstructor
public class TenantUpdatePipeline {

  /**
   * @autogenerated UpdateEntityRuleGenerator
   */
  private final Instance<TenantUpdateStep> steps;

  /**
   * @autogenerated UpdateEntityRuleGenerator
   * @param attributes
   * @param original
   * @param last
   * @return
   */
  public Tenant update(final TenantWriteAttributes attributes, final Tenant original,
      final Function<TenantChangeProposal, Tenant> last) {
    Function<TenantUpdateTransition, Tenant> finalHandler = t -> {
      if (t.hasErrors()) {
        throw t.asConstraintException();
      }
      return last.apply(t.getData());
    };
    List<TenantUpdateStep> reversed = new ArrayList<>(steps.stream().toList()).reversed();
    Function<TenantUpdateTransition, Tenant> pipeline = reversed.stream().reduce(finalHandler,
        (next, step) -> transition -> step.update(transition, next), (f1, f2) -> f1);
    return pipeline.apply(TenantUpdateTransition.builder()
        .data(new TenantChangeProposal(attributes)).original(original).build());
  }
}
