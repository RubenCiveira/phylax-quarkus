package net.civeira.phylax.features.access.scopeassignation.application.service.visibility;

import java.util.Optional;
import java.util.function.Consumer;

import lombok.Builder;
import lombok.Data;
import lombok.EqualsAndHashCode;
import lombok.NonNull;
import lombok.With;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.scopeassignation.ScopeAssignation;
import net.civeira.phylax.features.access.scopeassignation.application.request.ScopeAssignationStateChange;

@Data
@Builder(toBuilder = true)
@With
@EqualsAndHashCode(onlyExplicitlyIncluded = true)
public class ScopeAssignationPresetProposal {

  /**
   * @autogenerated PresetProposalGenerator
   */
  @NonNull
  private final Interaction interaction;

  /**
   * @autogenerated PresetProposalGenerator
   */
  @NonNull
  private ScopeAssignationStateChange dto;

  /**
   * @autogenerated PresetProposalGenerator
   */
  private ScopeAssignation original;

  /**
   * @autogenerated PresetProposalGenerator
   * @return
   */
  public Optional<ScopeAssignation> getOriginal() {
    return Optional.ofNullable(original);
  }

  /**
   * @autogenerated PresetProposalGenerator
   * @param mapper
   */
  public void peek(Consumer<ScopeAssignationStateChange> mapper) {
    mapper.accept(dto);
  }
}
