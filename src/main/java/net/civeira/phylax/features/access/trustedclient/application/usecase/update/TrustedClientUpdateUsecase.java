package net.civeira.phylax.features.access.trustedclient.application.usecase.update;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.common.telemetry.Observed;
import net.civeira.phylax.common.telemetry.Trace;
import net.civeira.phylax.features.access.trustedclient.application.visibility.TrustedClientsVisibility;
import net.civeira.phylax.features.access.trustedclient.domain.TrustedClient;
import net.civeira.phylax.features.access.trustedclient.domain.TrustedClientChangeSet;
import net.civeira.phylax.features.access.trustedclient.domain.TrustedClientRef;
import net.civeira.phylax.features.access.trustedclient.domain.gateway.TrustedClientAuditGateway;
import net.civeira.phylax.features.access.trustedclient.domain.gateway.TrustedClientCacheGateway;
import net.civeira.phylax.features.access.trustedclient.domain.gateway.TrustedClientWriteRepositoryGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class TrustedClientUpdateUsecase {

  /**
   * Service to register audit events
   *
   * @autogenerated UpdateUsecaseGenerator
   */
  private final TrustedClientAuditGateway audit;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final TrustedClientCacheGateway cache;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final TrustedClientWriteRepositoryGateway gateway;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<TrustedClientUpdateAllowDecision> updateAllowEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<TrustedClientUpdateCheck> updateCheckEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<TrustedClientUpdateEnrich> updateEnrichEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final TrustedClientsVisibility visibility;

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  @Observed("Checking if is allowed to update an entity of trusted client")
  public Allow allow(final Interaction query, final TrustedClientRef reference) {
    Trace.setAttribute("ref", reference.getUid());
    Allow response = checkAllow(query, reference);
    Trace.setAttribute("allowed", response.isAllowed());
    return response;
  }

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @return
   */
  @Observed("Checking if is allowed to update an entity of trusted client")
  public Allow allow(final Interaction query) {
    Allow response = checkAllow(query);
    Trace.setAttribute("allowed", response.isAllowed());
    return response;
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @param reference
   * @param input
   * @return The slide with some values
   */
  @Observed("Updating an entity of trusted client")
  public TrustedClientUpdateProjection update(final Interaction query,
      final TrustedClientRef reference, final TrustedClientUpdateInput input) {
    Trace.setAttribute("ref", reference.getUid());
    Allow detail = checkAllow(query, reference);
    if (!detail.isAllowed()) {
      Trace.addEvent(
          "The policies for trusted client dont allow to update: " + detail.getDescription());
      throw new NotAllowedException(detail.getDescription());
    }
    TrustedClient original = visibility.retrieveVisibleForUpdate(query, reference.getUid())
        .orElseThrow(() -> new NotFoundException(""));
    TrustedClientUpdateInput filled = TrustedClientUpdateInput
        .fromChangeSet(visibility.copyWithFixed(query, input.toChangeSet()));
    Trace.addEvent(
        "The input value is filled with the fixed values for the creation of trusted client");
    TrustedClientUpdateCheck check = TrustedClientUpdateCheck.builder().input(filled)
        .interaction(query).reference(reference).build();
    updateCheckEmitter.fire(check);
    Trace.addEvent("The input value is checked with the policies for the update of trusted client");
    TrustedClientUpdateEnrich proposal = TrustedClientUpdateEnrich.builder().input(filled)
        .interaction(query).reference(reference).build();
    updateEnrichEmitter.fire(proposal);
    TrustedClientChangeSet dto = proposal.getInput().toChangeSet();
    Trace.addEvent("The update of entity for trusted client is ready");
    TrustedClient saved = gateway.update(original, original.update(dto));
    Trace.addEvent("The updated entity of trusted client was saved");
    cache.update(saved);
    // Register audit operation
    audit.updated("update", saved, original, query);
    return TrustedClientUpdateProjection.from(visibility.copyWithHidden(query, saved));
  }

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  private Allow checkAllow(final Interaction query, final TrustedClientRef reference) {
    TrustedClientUpdateAllowDecision base = TrustedClientUpdateAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).reference(reference).build();
    updateAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @return
   */
  private Allow checkAllow(final Interaction query) {
    TrustedClientUpdateAllowDecision base = TrustedClientUpdateAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).build();
    updateAllowEmitter.fire(base);
    return base.getDetail();
  }
}
