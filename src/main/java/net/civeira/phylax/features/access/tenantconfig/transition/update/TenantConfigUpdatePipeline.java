package net.civeira.phylax.features.access.tenantconfig.transition.update;

import java.util.ArrayList;
import java.util.List;
import java.util.function.Function;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.inject.Instance;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.features.access.tenantconfig.TenantConfig;
import net.civeira.phylax.features.access.tenantconfig.command.TenantConfigWriteAttributes;
import net.civeira.phylax.features.access.tenantconfig.transition.TenantConfigChangeProposal;

@ApplicationScoped
@RequiredArgsConstructor
public class TenantConfigUpdatePipeline {

  /**
   * @autogenerated UpdateEntityRuleGenerator
   */
  private final Instance<TenantConfigUpdateStep> steps;

  /**
   * @autogenerated UpdateEntityRuleGenerator
   * @param attributes
   * @param original
   * @param last
   * @return
   */
  public TenantConfig update(final TenantConfigWriteAttributes attributes,
      final TenantConfig original, final Function<TenantConfigChangeProposal, TenantConfig> last) {
    Function<TenantConfigUpdateTransition, TenantConfig> finalHandler = t -> {
      if (t.hasErrors()) {
        throw t.asConstraintException();
      }
      return last.apply(t.getData());
    };
    List<TenantConfigUpdateStep> reversed = new ArrayList<>(steps.stream().toList()).reversed();
    Function<TenantConfigUpdateTransition, TenantConfig> pipeline = reversed.stream().reduce(
        finalHandler, (next, step) -> transition -> step.update(transition, next), (f1, f2) -> f1);
    return pipeline.apply(TenantConfigUpdateTransition.builder()
        .data(new TenantConfigChangeProposal(attributes)).original(original).build());
  }
}
