package net.civeira.phylax.features.access.role.application.usecase.delete;

import java.time.Duration;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.SpanKind;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Context;
import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.BatchIdentificator;
import net.civeira.phylax.common.batch.BatchProgress;
import net.civeira.phylax.common.batch.BatchService;
import net.civeira.phylax.common.batch.ExecutorByDeferSteps;
import net.civeira.phylax.common.batch.ExecutorPlan;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.role.application.visibility.RoleVisibilityFilter;
import net.civeira.phylax.features.access.role.application.visibility.RolesVisibility;
import net.civeira.phylax.features.access.role.domain.Role;
import net.civeira.phylax.features.access.role.domain.RoleRef;
import net.civeira.phylax.features.access.role.domain.gateway.RoleCacheGateway;
import net.civeira.phylax.features.access.role.domain.gateway.RoleWriteRepositoryGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class RoleDeleteUsecase {

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  private final BatchService batch;

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  private final RoleCacheGateway cache;

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  private final Event<RoleDeleteAllowDecision> deleteAllowEmitter;

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  private final Event<RoleDeleteCheck> deleteCheckEmitter;

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  private final RoleWriteRepositoryGateway gateway;

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  private final RolesVisibility visibility;

  /**
   * @autogenerated DeleteUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  public Allow allow(final Interaction query, final RoleRef reference) {
    Span span = tracer.spanBuilder("Checking if is allowed to delete an entity of role")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      span.setAttribute("ref", reference.getUid());
      Allow response = checkAllow(query, reference);
      span.setAttribute("allowed", response.isAllowed());
      return response;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated DeleteUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    Span span = tracer.spanBuilder("Checking if is allowed to delete an entity of role")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      Allow response = checkAllow(query);
      span.setAttribute("allowed", response.isAllowed());
      return response;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated DeleteUsecaseGenerator
   * @param query a filter to retrieve only matching values
   * @return The slide with some values
   */
  public BatchProgress checkProgress(final RoleCheckBatchDeleteStatus query) {
    return query.getActor().getName()
        .flatMap(name -> batch.retrieve(query.getTaskId(), query.getConnection().getLocale(), name))
        .orElseThrow(() -> new NotFoundException());
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated DeleteUsecaseGenerator
   * @param query
   * @param reference
   */
  public void delete(final Interaction query, final RoleRef reference) {
    Span span = tracer.spanBuilder("Deleting an entity of role")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      span.setAttribute("ref", reference.getUid());
      Allow detail = checkAllow(query, reference);
      if (!detail.isAllowed()) {
        span.addEvent("The policies for role dont allow to delete: " + detail.getDescription());
        throw new NotAllowedException(detail.getDescription());
      }
      Role original = visibility.retrieveVisibleForUpdate(query, reference.getUid())
          .orElseThrow(() -> new NotFoundException(""));
      delete(query, original);
      flush();
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated DeleteUsecaseGenerator
   * @param query
   * @param filter
   * @return The slide with some values
   */
  public BatchIdentificator delete(final Interaction query, final RoleDeleteFilter filter) {
    Span span = tracer.spanBuilder("Deleting a set of entities of role")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      Allow detail = checkAllow(query);
      if (!detail.isAllowed()) {
        span.addEvent("The policies for role dont allow to delete: " + detail.getDescription());
        throw new NotAllowedException(detail.getDescription());
      }
      RoleVisibilityFilter filterOnVisibles = RoleVisibilityFilter.builder()
          .uid(filter.getUid().orElse(null)).uids(filter.getUids().stream().toList())
          .search(filter.getSearch().orElse(null)).name(filter.getName().orElse(null))
          .relyingParty(filter.getRelyingParty().orElse(null))
          .relyingPartys(filter.getRelyingPartys()).build();
      RoleDeleteAllInBatchCommand command =
          RoleDeleteAllInBatchCommand.builder().interaction(query).filter(filterOnVisibles).build();
      return batch.start(query.getActor().getName().orElse("-"), Duration.ofHours(6), ExecutorPlan
          .<RoleDeleteAllInBatchCommand>builder().params(command).name("delete-color")
          .executor(
              ExecutorByDeferSteps.<Role, Role, RoleDeleteAllInBatchCommand, RolesInBatchExecutor.RolePaginableBatch>builder()
                  .initializer(RolesInBatchExecutor.class).counter(RolesInBatchExecutor.class)
                  .descriptor(RolesInBatchExecutor.class).reader(RolesInBatchExecutor.class)
                  .processor(RolesInBatchExecutor.class).writer(RolesInBatchExecutor.class)
                  .finalizer(RolesInBatchExecutor.class).build())
          .build());
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated DeleteUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  Allow checkAllow(final Interaction query, final RoleRef reference) {
    RoleDeleteAllowDecision base = RoleDeleteAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).reference(reference).build();
    deleteAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated DeleteUsecaseGenerator
   * @param query
   * @return
   */
  Allow checkAllow(final Interaction query) {
    RoleDeleteAllowDecision base = RoleDeleteAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).build();
    deleteAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated DeleteUsecaseGenerator
   * @param interaction
   * @param original
   */
  void delete(final Interaction interaction, final Role original) {
    RoleDeleteCheck proposal =
        RoleDeleteCheck.builder().reference(original).interaction(interaction).build();
    deleteCheckEmitter.fire(proposal);
    Role clean = original.delete();
    gateway.delete(clean);
  }

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  void flush() {
    cache.evict();
  }
}
