package net.civeira.phylax.features.access.tenantconfig.application.service.visibility;

import java.util.ArrayList;
import java.util.HashSet;
import java.util.Iterator;
import java.util.List;
import java.util.Optional;
import java.util.Set;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.tenant.application.service.visibility.TenantsVisibility;
import net.civeira.phylax.features.access.tenantconfig.TenantConfig;
import net.civeira.phylax.features.access.tenantconfig.TenantConfigFacade;
import net.civeira.phylax.features.access.tenantconfig.TenantConfigRef;
import net.civeira.phylax.features.access.tenantconfig.application.projection.TenantConfigStateProyection;
import net.civeira.phylax.features.access.tenantconfig.application.request.TenantConfigStateChange;
import net.civeira.phylax.features.access.tenantconfig.gateway.TenantConfigCacheGateway;
import net.civeira.phylax.features.access.tenantconfig.gateway.TenantConfigCached;
import net.civeira.phylax.features.access.tenantconfig.gateway.TenantConfigReadRepositoryGateway;
import net.civeira.phylax.features.access.tenantconfig.gateway.TenantConfigWriteRepositoryGateway;
import net.civeira.phylax.features.access.tenantconfig.query.TenantConfigCursor;
import net.civeira.phylax.features.access.tenantconfig.query.TenantConfigFilter;
import net.civeira.phylax.features.access.tenantconfig.valueobject.TenantVO;

@ApplicationScoped
@RequiredArgsConstructor
public class TenantConfigsVisibility {

  /**
   * @autogenerated VisibilityServiceGenerator
   */
  private final TenantConfigFacade aggregate;

  /**
   * @autogenerated VisibilityServiceGenerator
   */
  private final TenantConfigCacheGateway cache;

  /**
   * @autogenerated VisibilityServiceGenerator
   */
  private final Event<TenantConfigFilterProposal> filterProposalEmitter;

  /**
   * @autogenerated VisibilityServiceGenerator
   */
  private final Event<TenantConfigFixedFieldsProposal> fixedFieldsEmitter;

  /**
   * @autogenerated VisibilityServiceGenerator
   */
  private final Event<TenantConfigVisibleContentProposal> guardEmitter;

  /**
   * @autogenerated VisibilityServiceGenerator
   */
  private final Event<TenantConfigHiddenFieldsProposal> hiddenFieldsEmitter;

  /**
   * @autogenerated VisibilityServiceGenerator
   */
  private final Event<TenantConfigPresetProposal> presetEmitter;

  /**
   * @autogenerated VisibilityServiceGenerator
   */
  private final TenantConfigReadRepositoryGateway tenantConfigReadRepositoryGateway;

  /**
   * @autogenerated VisibilityServiceGenerator
   */
  private final TenantConfigWriteRepositoryGateway tenantConfigWriteRepositoryGateway;

  /**
   * @autogenerated VisibilityServiceGenerator
   */
  private final TenantsVisibility tenantsVisibility;

  /**
   * The input entity with the copy values without hidden
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param uid
   * @return The input entity with the copy values without hidden
   */
  public boolean checkVisibility(Interaction prev, String uid) {
    return retrieveVisible(prev, uid).isPresent();
  }

  /**
   * The input entity with the copy values without hidden
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param uids
   * @return The input entity with the copy values without hidden
   */
  public boolean checkVisibility(Interaction prev, List<String> uids) {
    return uids.size() == listVisibles(prev, TenantConfigFilter.builder().uids(uids).build(),
        TenantConfigCursor.builder().build()).size();
  }

  /**
   * The input entity with the copy values without hidden
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param source The source interaction
   * @param original The source interaction
   * @return The input entity with the copy values without hidden
   */
  public TenantConfigStateChange copyWithFixed(Interaction prev, TenantConfigStateChange source,
      TenantConfig original) {
    TenantConfigStateChange withReferences = visiblesReferences(prev, source);
    fieldsToFix(prev, original).forEach(withReferences::unset);
    TenantConfigPresetProposal proposal = TenantConfigPresetProposal.builder().dto(withReferences)
        .interaction(prev).original(original).build();
    presetEmitter.fire(proposal);
    return proposal.getDto();
  }

  /**
   * The input entity with the copy values without hidden
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param source The source interaction
   * @return The input entity with the copy values without hidden
   */
  public TenantConfigStateChange copyWithFixed(Interaction prev, TenantConfigStateChange source) {
    TenantConfigStateChange withReferences = visiblesReferences(prev, source);
    fieldsToFix(prev).forEach(withReferences::unset);
    TenantConfigPresetProposal proposal =
        TenantConfigPresetProposal.builder().dto(withReferences).interaction(prev).build();
    presetEmitter.fire(proposal);
    return proposal.getDto();
  }

  /**
   * The input dto with hidden values
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param tenantConfig
   * @return The input dto with hidden values
   */
  public TenantConfigStateProyection copyWithHidden(Interaction prev, TenantConfig tenantConfig) {
    TenantConfigStateProyection target = new TenantConfigStateProyection(tenantConfig);
    fieldsToHide(prev, tenantConfig).forEach(target::unset);
    return target;
  }

  /**
   * The input entity with the copy values without hidden
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param filter The filter to retrieve values
   * @return The input entity with the copy values without hidden
   */
  public long countVisibles(Interaction prev, TenantConfigFilter filter) {
    return tenantConfigReadRepositoryGateway.count(applyPreVisibilityFilter(prev, filter));
  }

  /**
   * initialsFixFields
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @return initialsFixFields
   */
  public Set<String> fieldsToFix(Interaction prev) {
    Set<String> fields = new HashSet<>(fieldsToHide(prev));
    fields.addAll(aggregate.readonlyFields());
    TenantConfigFixedFieldsProposal proposal =
        TenantConfigFixedFieldsProposal.builder().fields(fields).query(prev).build();
    fixedFieldsEmitter.fire(proposal);
    return proposal.getFields();
  }

  /**
   * initialsFixFields
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param tenantConfig
   * @return initialsFixFields
   */
  public Set<String> fieldsToFix(Interaction prev, TenantConfigRef tenantConfig) {
    Set<String> fields = new HashSet<>(fieldsToHide(prev, tenantConfig));
    fields.addAll(aggregate.readonlyFields());
    TenantConfigFixedFieldsProposal proposal = TenantConfigFixedFieldsProposal.builder()
        .fields(fields).tenantConfig(tenantConfig).query(prev).build();
    fixedFieldsEmitter.fire(proposal);
    return proposal.getFields();
  }

  /**
   * initialsHideFields
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @return initialsHideFields
   */
  public Set<String> fieldsToHide(Interaction prev) {
    Set<String> fields = new HashSet<>();
    TenantConfigHiddenFieldsProposal proposal =
        TenantConfigHiddenFieldsProposal.builder().fields(fields).query(prev).build();
    hiddenFieldsEmitter.fire(proposal);
    return proposal.getFields();
  }

  /**
   * initialsHideFields
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param tenantConfig
   * @return initialsHideFields
   */
  public Set<String> fieldsToHide(Interaction prev, TenantConfigRef tenantConfig) {
    Set<String> fields = new HashSet<>(fieldsToHide(prev));
    TenantConfigHiddenFieldsProposal proposal = TenantConfigHiddenFieldsProposal.builder()
        .fields(fields).tenantConfig(tenantConfig).query(prev).build();
    hiddenFieldsEmitter.fire(proposal);
    return proposal.getFields();
  }

  /**
   * The input entity with the copy values without hidden
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param filter The filter to retrieve values
   * @param cursor The filter to retrieve values
   * @return The input entity with the copy values without hidden
   */
  public TenantConfigCached listCachedVisibles(Interaction prev, TenantConfigFilter filter,
      TenantConfigCursor cursor) {
    TenantConfigFilter visibleFilter = applyPreVisibilityFilter(prev, filter);
    return cache.retrieve(visibleFilter, cursor)
        .orElseGet(() -> cache.store(visibleFilter, cursor, listVisibles(prev, filter, cursor)));
  }

  /**
   * The input entity with the copy values without hidden
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param filter The filter to retrieve values
   * @param cursor The filter to retrieve values
   * @return The input entity with the copy values without hidden
   */
  public List<TenantConfig> listVisibles(Interaction prev, TenantConfigFilter filter,
      TenantConfigCursor cursor) {
    return queryItems(prev, applyPreVisibilityFilter(prev, filter), cursor);
  }

  /**
   * The input entity with the copy values without hidden
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param filter The filter to retrieve values
   * @param cursor The filter to retrieve values
   * @return The input entity with the copy values without hidden
   */
  public List<TenantConfig> listVisiblesForUpdate(Interaction prev, TenantConfigFilter filter,
      TenantConfigCursor cursor) {
    return queryItemsForUpdate(prev, applyPreVisibilityFilter(prev, filter), cursor);
  }

  /**
   * The input entity with the copy values without hidden
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param uid
   * @return The input entity with the copy values without hidden
   */
  public TenantConfigCached retrieveCachedVisible(Interaction prev, String uid) {
    TenantConfigCursor cursor = TenantConfigCursor.builder().limit(1).build();
    TenantConfigFilter visibleFilter =
        applyPreVisibilityFilter(prev, TenantConfigFilter.builder().uid(uid).build());
    return cache.retrieve(visibleFilter, cursor).orElseGet(() -> {
      List<TenantConfig> list =
          retrieveVisible(prev, uid).<List<TenantConfig>>map(List::of).orElseGet(List::of);
      return cache.store(visibleFilter, cursor, list);
    });
  }

  /**
   * The input entity with the copy values without hidden
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param uid
   * @return The input entity with the copy values without hidden
   */
  public Optional<TenantConfig> retrieveVisible(Interaction prev, String uid) {
    TenantConfigFilter filter =
        applyPreVisibilityFilter(prev, TenantConfigFilter.builder().uid(uid).build());
    return queryItem(prev, uid, filter);
  }

  /**
   * The input entity with the copy values without hidden
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param uid
   * @return The input entity with the copy values without hidden
   */
  public Optional<TenantConfig> retrieveVisibleForUpdate(Interaction prev, String uid) {
    TenantConfigFilter filter =
        applyPreVisibilityFilter(prev, TenantConfigFilter.builder().uid(uid).build());
    return queryItemForUpdate(prev, uid, filter);
  }

  /**
   * The self filter modified with the prepared values.
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param filter The filter to retrieve values
   * @return The self filter modified with the prepared values.
   */
  private TenantConfigFilter applyPreVisibilityFilter(Interaction prev, TenantConfigFilter filter) {
    TenantConfigFilterProposal proposal =
        TenantConfigFilterProposal.builder().filter(filter).interaction(prev).build();
    filterProposalEmitter.fire(proposal);
    return proposal.getFilter();
  }

  /**
   * The input dto with hidden values
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param tenantConfigRef The source interaction
   * @return The input dto with hidden values
   */
  private boolean evaluatePostVisibility(Interaction prev, TenantConfig tenantConfigRef) {
    TenantConfigVisibleContentProposal proposal = TenantConfigVisibleContentProposal.builder()
        .visible(true).entity(tenantConfigRef).interaction(prev).build();
    guardEmitter.fire(proposal);
    return Boolean.TRUE.equals(proposal.getVisible());
  }

  /**
   * The input entity with the copy values without hidden
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param uid
   * @param filter The filter to retrieve values
   * @return The input entity with the copy values without hidden
   */
  private Optional<TenantConfig> queryItem(Interaction prev, String uid,
      TenantConfigFilter filter) {
    return tenantConfigReadRepositoryGateway.retrieve(uid, Optional.of(filter))
        .filter(values -> evaluatePostVisibility(prev, values));
  }

  /**
   * The input entity with the copy values without hidden
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param uid
   * @param filter The filter to retrieve values
   * @return The input entity with the copy values without hidden
   */
  private Optional<TenantConfig> queryItemForUpdate(Interaction prev, String uid,
      TenantConfigFilter filter) {
    return tenantConfigWriteRepositoryGateway.retrieveForUpdate(uid, Optional.of(filter))
        .filter(values -> evaluatePostVisibility(prev, values));
  }

  /**
   * The input entity with the copy values without hidden
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param filter The filter to retrieve values
   * @param cursor The filter to retrieve values
   * @return The input entity with the copy values without hidden
   */
  private List<TenantConfig> queryItems(Interaction prev, TenantConfigFilter filter,
      TenantConfigCursor cursor) {
    List<TenantConfig> list = new ArrayList<>();
    Iterator<TenantConfig> slide = tenantConfigReadRepositoryGateway.slide(filter, cursor)
        .slide(values -> evaluatePostVisibility(prev, values));
    while (slide.hasNext()) {
      list.add(slide.next());
    }
    return list;
  }

  /**
   * The input entity with the copy values without hidden
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param filter The filter to retrieve values
   * @param cursor The filter to retrieve values
   * @return The input entity with the copy values without hidden
   */
  private List<TenantConfig> queryItemsForUpdate(Interaction prev, TenantConfigFilter filter,
      TenantConfigCursor cursor) {
    List<TenantConfig> list = new ArrayList<>();
    Iterator<TenantConfig> slide = tenantConfigWriteRepositoryGateway.slideForUpdate(filter, cursor)
        .slide(values -> evaluatePostVisibility(prev, values));
    while (slide.hasNext()) {
      list.add(slide.next());
    }
    return list;
  }

  /**
   * The input entity with the copy values without hidden
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param source The source interaction
   * @return The input entity with the copy values without hidden
   */
  private TenantConfigStateChange visiblesReferences(Interaction prev,
      TenantConfigStateChange source) {
    source.getTenant().map(TenantVO::getReferenceValue).ifPresent(tenant -> {
      boolean visible = tenantsVisibility.checkVisibility(prev, tenant);
      if (!visible) {
        throw new NotFoundException("No tenant - " + tenant);
      }
    });
    return source;
  }
}
