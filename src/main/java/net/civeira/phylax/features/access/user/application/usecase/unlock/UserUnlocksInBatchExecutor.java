package net.civeira.phylax.features.access.user.application.usecase.unlock;

import java.util.List;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.SpanKind;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Context;
import io.quarkus.arc.Unremovable;
import jakarta.enterprise.context.ApplicationScoped;
import lombok.Data;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.stepper.ItemDescriptor;
import net.civeira.phylax.common.batch.stepper.ItemProcessor;
import net.civeira.phylax.common.batch.stepper.ItemReader;
import net.civeira.phylax.common.batch.stepper.ItemWriter;
import net.civeira.phylax.common.batch.stepper.StepContext;
import net.civeira.phylax.common.batch.stepper.StepCounter;
import net.civeira.phylax.common.batch.stepper.StepFinalizer;
import net.civeira.phylax.common.batch.stepper.StepInitializer;
import net.civeira.phylax.common.exception.ExecutionException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.features.access.user.application.visibility.UsersVisibility;
import net.civeira.phylax.features.access.user.domain.User;
import net.civeira.phylax.features.access.user.domain.gateway.UserAuditGateway;
import net.civeira.phylax.features.access.user.domain.gateway.UserCursor;

@Unremovable
@ApplicationScoped
@RequiredArgsConstructor
class UserUnlocksInBatchExecutor implements
    StepCounter<UserUnlockAllInBatchCommand, UserUnlocksInBatchExecutor.UserPaginableBatch>,
    StepFinalizer<UserUnlockAllInBatchCommand, UserUnlocksInBatchExecutor.UserPaginableBatch>,
    StepInitializer<UserUnlockAllInBatchCommand, UserUnlocksInBatchExecutor.UserPaginableBatch>,
    ItemReader<User, UserUnlockAllInBatchCommand, UserUnlocksInBatchExecutor.UserPaginableBatch>,
    ItemWriter<User, UserUnlockAllInBatchCommand, UserUnlocksInBatchExecutor.UserPaginableBatch>,
    ItemProcessor<User, User, UserUnlockAllInBatchCommand, UserUnlocksInBatchExecutor.UserPaginableBatch>,
    ItemDescriptor<User, UserUnlockAllInBatchCommand, UserUnlocksInBatchExecutor.UserPaginableBatch> {

  /**
   * @autogenerated EntityGenerator
   */
  @Data
  public static class UserPaginableBatch {

    /**
     * @autogenerated EntityGenerator
     */
    private String since;
  }

  /**
   * Service to register audit events
   *
   * @autogenerated EntityGenerator
   */
  private final UserAuditGateway audit;

  /**
   * @autogenerated EntityGenerator
   */
  private final int size = 10;

  /**
   * @autogenerated EntityGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated EntityGenerator
   */
  private final UserUnlockUsecase usecase;

  /**
   * @autogenerated EntityGenerator
   */
  private final UsersVisibility visibility;

  /**
   * @autogenerated EntityGenerator
   * @param context
   * @return
   */
  @Override
  public long approximatedItems(
      final StepContext<UserUnlockAllInBatchCommand, UserPaginableBatch> context) {
    Span span =
        tracer.spanBuilder("Count selected items to the batch unlock for a set of entities of user")
            .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
            .startSpan();
    try {
      return visibility.countVisibles(context.getParams().getInteraction(),
          context.getParams().getFilter());
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   */
  @Override
  public void finish(final StepContext<UserUnlockAllInBatchCommand, UserPaginableBatch> context) {
    /* do nothing */
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   */
  @Override
  public void init(final StepContext<UserUnlockAllInBatchCommand, UserPaginableBatch> context) {
    context.setState(new UserPaginableBatch());
  }

  /**
   * @autogenerated EntityGenerator
   * @param item
   * @param context
   * @return
   */
  @Override
  public String itemDescription(final User item,
      final StepContext<UserUnlockAllInBatchCommand, UserPaginableBatch> context) {
    return item.getUid();
  }

  /**
   * @autogenerated EntityGenerator
   * @param item
   * @param context
   * @return
   */
  @Override
  public User process(final User item,
      final StepContext<UserUnlockAllInBatchCommand, UserPaginableBatch> context) {
    Span span = tracer
        .spanBuilder("Prepare individual data for the batch unlock on a set of entities of user")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      Allow allowed = usecase.checkAllow(context.getParams().getInteraction(), item);
      if (!allowed.isAllowed()) {
        span.addEvent("The policies for user dont allow to delete: " + allowed.getDescription());
        throw new ExecutionException("not-allowed", null);
      }
      return item;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   * @return
   */
  @Override
  public List<User> read(
      final StepContext<UserUnlockAllInBatchCommand, UserPaginableBatch> context) {
    Span span = tracer
        .spanBuilder("Read selected entitites for a batch unlock for a set of entities of user")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      List<User> page = visibility.listVisiblesForUpdate(context.getParams().getInteraction(),
          context.getParams().getFilter(),
          UserCursor.builder().limit(size).sinceUid(context.getState().getSince()).build());
      context.getState().setSince(page.isEmpty() ? null : page.get(page.size() - 1).getUid());
      return page;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated EntityGenerator
   * @param items
   * @param context
   */
  @Override
  public void write(final List<User> items,
      final StepContext<UserUnlockAllInBatchCommand, UserPaginableBatch> context) {
    Span span = tracer.spanBuilder("Perform the batch unlock for a set of entities of user")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      span.setAttribute("size", items.size());
      items.forEach(item -> {
        User changed = usecase.doUnlock(context.getParams().getInteraction(), item);
        usecase.cacheUpdate(changed);
        audit.updated("batch-unlock", changed, item, context.getParams().getInteraction());
      });
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }
}
