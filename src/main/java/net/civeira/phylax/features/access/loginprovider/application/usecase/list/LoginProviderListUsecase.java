package net.civeira.phylax.features.access.loginprovider.application.usecase.list;

import java.util.List;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.algorithms.metadata.TimestampedList;
import net.civeira.phylax.common.algorithms.metadata.WrapMetadata;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.loginprovider.application.visibility.LoginProviderVisibilityFilter;
import net.civeira.phylax.features.access.loginprovider.application.visibility.LoginProvidersVisibility;
import net.civeira.phylax.features.access.loginprovider.domain.gateway.LoginProviderCached;
import net.civeira.phylax.features.access.loginprovider.domain.gateway.LoginProviderCursor;
import net.civeira.phylax.features.access.loginprovider.domain.gateway.LoginProviderOrder;

@ApplicationScoped
@RequiredArgsConstructor
public class LoginProviderListUsecase {

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final Event<LoginProviderListAllowDecision> listAllowEmitter;

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final LoginProvidersVisibility visibility;

  /**
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    LoginProviderListAllowDecision proposal = LoginProviderListAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allow by default").build()).query(query)
        .build();
    listAllowEmitter.fire(proposal);
    return proposal.getDetail();
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @param filter
   * @param cursor
   * @return The slide with some values
   */
  public List<LoginProviderListProjection> list(final Interaction query,
      final LoginProviderListFilter filter, final LoginProviderListCursor cursor) {
    Allow detail = allow(query);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    LoginProviderVisibilityFilter visibleFilter = LoginProviderVisibilityFilter.builder()
        .uid(filter.getUid().orElse(null)).uids(filter.getUids().stream().toList())
        .search(filter.getSearch().orElse(null)).name(filter.getName().orElse(null))
        .tenant(filter.getTenant().orElse(null)).tenants(filter.getTenants())
        .tenantTenantAccesible(filter.getTenantTenantAccesible().orElse(null)).build();
    LoginProviderCursor gatewayCursor = LoginProviderCursor.builder()
        .order(cursor.getOrder().stream().map(Object::toString).map(LoginProviderOrder::valueOf)
            .toList())
        .sinceName(cursor.getSinceName().orElse(null)).sinceName(cursor.getSinceName().orElse(null))
        .build();
    LoginProviderCached values = visibility.listCachedVisibles(query, visibleFilter, gatewayCursor);
    List<LoginProviderListProjection> list =
        values.getValue().stream().map(value -> visibility.copyWithHidden(query, value))
            .map(LoginProviderListProjection::from).toList();
    return new TimestampedList<>(WrapMetadata.<List<LoginProviderListProjection>>builder()
        .data(list).since(values.getSince().toInstant()).build());
  }
}
