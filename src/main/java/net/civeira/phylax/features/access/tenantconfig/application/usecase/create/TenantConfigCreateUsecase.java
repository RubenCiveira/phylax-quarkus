package net.civeira.phylax.features.access.tenantconfig.application.usecase.create;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.tenantconfig.TenantConfig;
import net.civeira.phylax.features.access.tenantconfig.TenantConfigFacade;
import net.civeira.phylax.features.access.tenantconfig.application.projection.TenantConfigStateProyection;
import net.civeira.phylax.features.access.tenantconfig.application.request.TenantConfigStateChange;
import net.civeira.phylax.features.access.tenantconfig.application.service.visibility.TenantConfigsVisibility;
import net.civeira.phylax.features.access.tenantconfig.gateway.TenantConfigCacheGateway;
import net.civeira.phylax.features.access.tenantconfig.gateway.TenantConfigWriteRepositoryGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class TenantConfigCreateUsecase {

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final TenantConfigCacheGateway cache;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Event<TenantConfigAllowCreateProposal> createAllowEmitter;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Event<TenantConfigCreateProposal> createProposalEmitter;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Event<TenantConfigCreateEvent> createdEmitter;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final TenantConfigFacade facade;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final TenantConfigWriteRepositoryGateway gateway;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final TenantConfigsVisibility visibility;

  /**
   * @autogenerated CreateUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    TenantConfigAllowCreateProposal proposal = TenantConfigAllowCreateProposal.builder()
        .detail(Allow.builder().allowed(true).description("Allow by default").build()).query(query)
        .build();
    createAllowEmitter.fire(proposal);
    return proposal.getDetail();
  }

  /**
   * The slide with some values
   *
   * @autogenerated CreateUsecaseGenerator
   * @param query
   * @param input
   * @return The slide with some values
   */
  public TenantConfigStateProyection create(final Interaction query,
      final TenantConfigStateChange input) {
    Allow detail = allow(query);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    TenantConfigStateChange filled = visibility.copyWithFixed(query, input);
    TenantConfigCreateProposal proposal =
        TenantConfigCreateProposal.builder().interaction(query).dto(filled).build();
    createProposalEmitter.fire(proposal);
    TenantConfigStateChange dto = proposal.getDto();
    TenantConfig entity = facade.create(dto);
    TenantConfig created =
        gateway.create(entity, attempt -> visibility.checkVisibility(query, attempt.getUidValue()));
    cache.update(created);
    TenantConfigCreateEvent event =
        TenantConfigCreateEvent.builder().payload(created).interaction(query).build();
    createdEmitter.fire(event);
    return visibility.copyWithHidden(query, event.getPayload());
  }
}
