package net.civeira.phylax.features.access.useridentity.application.usecase.update;

import java.util.Optional;
import java.util.function.Consumer;
import java.util.function.UnaryOperator;

import lombok.Builder;
import lombok.Data;
import lombok.EqualsAndHashCode;
import lombok.NonNull;
import lombok.With;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.useridentity.UserIdentityRef;
import net.civeira.phylax.features.access.useridentity.application.request.UserIdentityStateChange;

@Data
@Builder(toBuilder = true)
@With
@EqualsAndHashCode(onlyExplicitlyIncluded = true)
public class UserIdentityUpdateProposal {

  /**
   * @autogenerated UpdateProposalGenerator
   */
  @NonNull
  private final Interaction interaction;

  /**
   * @autogenerated UpdateProposalGenerator
   */
  private final UserIdentityRef reference;

  /**
   * @autogenerated UpdateProposalGenerator
   */
  @NonNull
  private UserIdentityStateChange dto;

  /**
   * @autogenerated UpdateProposalGenerator
   * @return
   */
  public Optional<UserIdentityRef> getReference() {
    return Optional.ofNullable(reference);
  }

  /**
   * @autogenerated UpdateProposalGenerator
   * @param mapper
   */
  public void map(UnaryOperator<UserIdentityStateChange> mapper) {
    dto = mapper.apply(dto);
  }

  /**
   * @autogenerated UpdateProposalGenerator
   * @param mapper
   */
  public void peek(Consumer<UserIdentityStateChange> mapper) {
    mapper.accept(dto);
  }
}
