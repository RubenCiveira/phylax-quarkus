package net.civeira.phylax.features.access.useraccesstemporalcode.transition.resetmfatemporalcode;

import java.util.ArrayList;
import java.util.List;
import java.util.function.Function;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.inject.Instance;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.features.access.useraccesstemporalcode.UserAccessTemporalCode;
import net.civeira.phylax.features.access.useraccesstemporalcode.transition.UserAccessTemporalCodeChangeProposal;

@ApplicationScoped
@RequiredArgsConstructor
public class UserAccessTemporalCodeResetMfaTemporalCodePipeline {

  /**
   * @autogenerated UpdateEntityRuleGenerator
   */
  private final Instance<UserAccessTemporalCodeResetMfaTemporalCodeStep> steps;

  /**
   * @autogenerated UpdateEntityRuleGenerator
   * @param original
   * @param last
   * @return
   */
  public UserAccessTemporalCode resetMfaTemporalCode(final UserAccessTemporalCode original,
      final Function<UserAccessTemporalCodeChangeProposal, UserAccessTemporalCode> last) {
    Function<UserAccessTemporalCodeResetMfaTemporalCodeTransition, UserAccessTemporalCode> finalHandler =
        t -> {
          if (t.hasErrors()) {
            throw t.asConstraintException();
          }
          return last.apply(t.getData());
        };
    List<UserAccessTemporalCodeResetMfaTemporalCodeStep> reversed =
        new ArrayList<>(steps.stream().toList()).reversed();
    Function<UserAccessTemporalCodeResetMfaTemporalCodeTransition, UserAccessTemporalCode> pipeline =
        reversed.stream().reduce(finalHandler,
            (next, step) -> transition -> step.resetMfaTemporalCode(transition, next),
            (f1, f2) -> f1);
    return pipeline.apply(UserAccessTemporalCodeResetMfaTemporalCodeTransition.builder()
        .data(new UserAccessTemporalCodeChangeProposal(original)).original(original).build());
  }
}
