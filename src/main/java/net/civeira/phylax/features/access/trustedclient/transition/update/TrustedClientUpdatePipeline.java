package net.civeira.phylax.features.access.trustedclient.transition.update;

import java.util.ArrayList;
import java.util.List;
import java.util.function.Function;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.inject.Instance;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.features.access.trustedclient.TrustedClient;
import net.civeira.phylax.features.access.trustedclient.command.TrustedClientWriteAttributes;
import net.civeira.phylax.features.access.trustedclient.transition.TrustedClientChangeProposal;

@ApplicationScoped
@RequiredArgsConstructor
public class TrustedClientUpdatePipeline {

  /**
   * @autogenerated UpdateEntityRuleGenerator
   */
  private final Instance<TrustedClientUpdateStep> steps;

  /**
   * @autogenerated UpdateEntityRuleGenerator
   * @param attributes
   * @param original
   * @param last
   * @return
   */
  public TrustedClient update(final TrustedClientWriteAttributes attributes,
      final TrustedClient original,
      final Function<TrustedClientChangeProposal, TrustedClient> last) {
    Function<TrustedClientUpdateTransition, TrustedClient> finalHandler = t -> {
      if (t.hasErrors()) {
        throw t.asConstraintException();
      }
      return last.apply(t.getData());
    };
    List<TrustedClientUpdateStep> reversed = new ArrayList<>(steps.stream().toList()).reversed();
    Function<TrustedClientUpdateTransition, TrustedClient> pipeline = reversed.stream().reduce(
        finalHandler, (next, step) -> transition -> step.update(transition, next), (f1, f2) -> f1);
    return pipeline.apply(TrustedClientUpdateTransition.builder()
        .data(new TrustedClientChangeProposal(attributes)).original(original).build());
  }
}
