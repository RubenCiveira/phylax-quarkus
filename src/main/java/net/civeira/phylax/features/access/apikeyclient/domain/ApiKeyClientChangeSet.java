package net.civeira.phylax.features.access.apikeyclient.domain;

import lombok.NoArgsConstructor;
import lombok.experimental.Delegate;
import net.civeira.phylax.common.value.Uuid;
import net.civeira.phylax.features.access.apikeyclient.domain.valueobject.CodeValueHolder;
import net.civeira.phylax.features.access.apikeyclient.domain.valueobject.EnabledValueHolder;
import net.civeira.phylax.features.access.apikeyclient.domain.valueobject.KeyValueHolder;
import net.civeira.phylax.features.access.apikeyclient.domain.valueobject.ScopesValueHolder;
import net.civeira.phylax.features.access.apikeyclient.domain.valueobject.UidValueHolder;
import net.civeira.phylax.features.access.apikeyclient.domain.valueobject.VersionValueHolder;

/**
 * A dto transfer to hold api key client attribute values
 */
@NoArgsConstructor
public class ApiKeyClientChangeSet {

  /**
   * El código identificativo de la aplicación
   *
   * @autogenerated EntityChangeSetGenerator
   */
  @Delegate
  private CodeValueHolder<ApiKeyClientChangeSet> codeValue = new CodeValueHolder<>(this);

  /**
   * The indicator to allow the account be used without deleting it
   *
   * @autogenerated EntityChangeSetGenerator
   */
  @Delegate
  private EnabledValueHolder<ApiKeyClientChangeSet> enabledValue = new EnabledValueHolder<>(this);

  /**
   * The key for the api
   *
   * @autogenerated EntityChangeSetGenerator
   */
  @Delegate
  private KeyValueHolder<ApiKeyClientChangeSet> keyValue = new KeyValueHolder<>(this);

  /**
   * A coma separated scopes for the api key
   *
   * @autogenerated EntityChangeSetGenerator
   */
  @Delegate
  private ScopesValueHolder<ApiKeyClientChangeSet> scopesValue = new ScopesValueHolder<>(this);

  /**
   * El identificador de la aplicacion
   *
   * @autogenerated EntityChangeSetGenerator
   */
  @Delegate
  private UidValueHolder<ApiKeyClientChangeSet> uidValue = new UidValueHolder<>(this);

  /**
   * Campo con el número de version de api key client para controlar bloqueos optimistas
   *
   * @autogenerated EntityChangeSetGenerator
   */
  @Delegate
  private VersionValueHolder<ApiKeyClientChangeSet> versionValue = new VersionValueHolder<>(this);

  /**
   * @autogenerated EntityChangeSetGenerator
   * @return
   */
  public ApiKeyClientChangeSet newUid() {
    return version(0).uid(Uuid.comb().toString());
  }

  /**
   * Unset a value by the property name. Unset values will no provide command change neither provide
   * information to the output context (hidden or readonly fields, or policy restrictions).
   *
   * @autogenerated EntityChangeSetGenerator
   * @param field The field name to unset.
   */
  public void unset(final String field) {
    uidValue.unsetUidIfMatches(field);
    codeValue.unsetCodeIfMatches(field);
    keyValue.unsetKeyIfMatches(field);
    enabledValue.unsetEnabledIfMatches(field);
    scopesValue.unsetScopesIfMatches(field);
    versionValue.unsetVersionIfMatches(field);
  }
}
