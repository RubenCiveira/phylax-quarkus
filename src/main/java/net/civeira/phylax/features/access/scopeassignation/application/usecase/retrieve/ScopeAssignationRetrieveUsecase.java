package net.civeira.phylax.features.access.scopeassignation.application.usecase.retrieve;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.scopeassignation.ScopeAssignationRef;
import net.civeira.phylax.features.access.scopeassignation.application.projection.ScopeAssignationStateProyection;
import net.civeira.phylax.features.access.scopeassignation.application.service.visibility.ScopeAssignationsVisibility;
import net.civeira.phylax.features.access.scopeassignation.gateway.ScopeAssignationCached;

@ApplicationScoped
@RequiredArgsConstructor
public class ScopeAssignationRetrieveUsecase {

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final Event<ScopeAssignationAllowRetrieveProposal> retrieveAllowEmitter;

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final ScopeAssignationsVisibility visibility;

  /**
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  public Allow allow(final Interaction query, final ScopeAssignationRef reference) {
    ScopeAssignationAllowRetrieveProposal base = ScopeAssignationAllowRetrieveProposal.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).reference(reference).build();
    retrieveAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    ScopeAssignationAllowRetrieveProposal base = ScopeAssignationAllowRetrieveProposal.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).build();
    retrieveAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * The slide with some values
   *
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @param reference
   * @return The slide with some values
   */
  public ScopeAssignationStateProyection retrieve(final Interaction query,
      final ScopeAssignationRef reference) {
    Allow detail = allow(query, reference);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    ScopeAssignationCached retrieveCachedVisible =
        visibility.retrieveCachedVisible(query, reference.getUidValue());
    return retrieveCachedVisible.first().map(first -> visibility.copyWithHidden(query, first))
        .orElseThrow(() -> new NotFoundException(""));
  }
}
