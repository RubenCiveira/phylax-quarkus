package net.civeira.phylax.features.access.tenantloginprovider.application.usecase.enable;

import java.time.Duration;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.SpanKind;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Context;
import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.BatchIdentificator;
import net.civeira.phylax.common.batch.BatchProgress;
import net.civeira.phylax.common.batch.BatchService;
import net.civeira.phylax.common.batch.ExecutorByDeferSteps;
import net.civeira.phylax.common.batch.ExecutorPlan;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.tenantloginprovider.application.visibility.TenantLoginProviderVisibilityFilter;
import net.civeira.phylax.features.access.tenantloginprovider.application.visibility.TenantLoginProvidersVisibility;
import net.civeira.phylax.features.access.tenantloginprovider.domain.TenantLoginProvider;
import net.civeira.phylax.features.access.tenantloginprovider.domain.TenantLoginProviderRef;
import net.civeira.phylax.features.access.tenantloginprovider.domain.gateway.TenantLoginProviderCacheGateway;
import net.civeira.phylax.features.access.tenantloginprovider.domain.gateway.TenantLoginProviderWriteRepositoryGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class TenantLoginProviderEnableUsecase {

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final BatchService batch;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final TenantLoginProviderCacheGateway cache;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final Event<TenantLoginProviderEnableAllowDecision> execAllowEmitter;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final Event<TenantLoginProviderEnableCheck> execProposalEmitter;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final TenantLoginProviderWriteRepositoryGateway gateway;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final TenantLoginProvidersVisibility visibility;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  public Allow allow(final Interaction query, final TenantLoginProviderRef reference) {
    Span span =
        tracer.spanBuilder("Checking if is allowed to update an entity of tenant login provider")
            .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
            .startSpan();
    try {
      span.setAttribute("ref", reference.getUid());
      Allow response = checkAllow(query, reference);
      span.setAttribute("allowed", response.isAllowed());
      return response;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    Span span =
        tracer.spanBuilder("Checking if is allowed to update an entity of tenant login provider")
            .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
            .startSpan();
    try {
      Allow response = checkAllow(query);
      span.setAttribute("allowed", response.isAllowed());
      return response;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query a filter to retrieve only matching values
   * @return The slide with some values
   */
  public BatchProgress checkProgress(final TenantLoginProviderEnableStatus query) {
    return query.getActor().getName()
        .flatMap(name -> batch.retrieve(query.getTaskId(), query.getConnection().getLocale(), name))
        .orElseThrow(() -> new NotFoundException());
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param reference
   * @return The slide with some values
   */
  public TenantLoginProviderEnableProjection enable(final Interaction query,
      final TenantLoginProviderRef reference) {
    Span span = tracer.spanBuilder("enable an entity of tenant login provider")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      span.setAttribute("ref", reference.getUid());
      Allow detail = checkAllow(query, reference);
      if (!detail.isAllowed()) {
        span.addEvent("The policies for tenant login provider dont allow to enable: "
            + detail.getDescription());
        throw new NotAllowedException(detail.getDescription());
      }
      TenantLoginProvider original = visibility.retrieveVisibleForUpdate(query, reference.getUid())
          .orElseThrow(() -> new NotFoundException(""));
      TenantLoginProvider saved = enable(query, original);
      flush();
      return TenantLoginProviderEnableProjection.from(visibility.copyWithHidden(query, saved));
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param filter
   * @return The slide with some values
   */
  public BatchIdentificator enable(final Interaction query,
      final TenantLoginProviderEnableFilter filter) {
    Span span = tracer.spanBuilder("enable a set of entities of tenant login provider")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      Allow detail = checkAllow(query);
      if (!detail.isAllowed()) {
        span.addEvent("The policies for tenant login provider dont allow to enable: "
            + detail.getDescription());
        throw new NotAllowedException(detail.getDescription());
      }
      TenantLoginProviderVisibilityFilter filterWithVisibility = TenantLoginProviderVisibilityFilter
          .builder().uid(filter.getUid().orElse(null)).uids(filter.getUids().stream().toList())
          .search(filter.getSearch().orElse(null)).name(filter.getName().orElse(null))
          .tenant(filter.getTenant().orElse(null)).tenants(filter.getTenants())
          .tenantTenantAccesible(filter.getTenantTenantAccesible().orElse(null)).build();
      TenantLoginProviderEnableAllInBatchCommand command =
          TenantLoginProviderEnableAllInBatchCommand.builder().interaction(query)
              .filter(filterWithVisibility).build();
      return batch.start(command.getInteraction().getActor().getName().orElse("-"),
          Duration.ofHours(6),
          ExecutorPlan.<TenantLoginProviderEnableAllInBatchCommand>builder().params(command)
              .name("enable-tenant-login-provider")
              .executor(
                  ExecutorByDeferSteps.<TenantLoginProvider, TenantLoginProvider, TenantLoginProviderEnableAllInBatchCommand, TenantLoginProviderEnablesInBatchExecutor.TenantLoginProviderPaginableBatch>builder()
                      .initializer(TenantLoginProviderEnablesInBatchExecutor.class)
                      .counter(TenantLoginProviderEnablesInBatchExecutor.class)
                      .descriptor(TenantLoginProviderEnablesInBatchExecutor.class)
                      .reader(TenantLoginProviderEnablesInBatchExecutor.class)
                      .processor(TenantLoginProviderEnablesInBatchExecutor.class)
                      .writer(TenantLoginProviderEnablesInBatchExecutor.class)
                      .finalizer(TenantLoginProviderEnablesInBatchExecutor.class).build())
              .build());
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  Allow checkAllow(final Interaction query, final TenantLoginProviderRef reference) {
    TenantLoginProviderEnableAllowDecision base = TenantLoginProviderEnableAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).reference(reference).build();
    execAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @return
   */
  Allow checkAllow(final Interaction query) {
    TenantLoginProviderEnableAllowDecision base = TenantLoginProviderEnableAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).build();
    execAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param interaction
   * @param original
   * @return
   */
  TenantLoginProvider enable(final Interaction interaction, final TenantLoginProvider original) {
    TenantLoginProviderEnableCheck proposal = TenantLoginProviderEnableCheck.builder()
        .reference(original).interaction(interaction).build();
    execProposalEmitter.fire(proposal);
    TenantLoginProvider result = original.enable();
    TenantLoginProvider saved = gateway.update(original, result);
    return saved;
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  void flush() {
    cache.evict();
  }
}
