package net.civeira.phylax.features.access.tenantconfig.application.usecase.list;

import java.util.List;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.algorithms.metadata.TimestampedList;
import net.civeira.phylax.common.algorithms.metadata.WrapMetadata;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.tenantconfig.application.projection.TenantConfigStateProyection;
import net.civeira.phylax.features.access.tenantconfig.application.service.visibility.TenantConfigsVisibility;
import net.civeira.phylax.features.access.tenantconfig.gateway.TenantConfigCached;
import net.civeira.phylax.features.access.tenantconfig.query.TenantConfigCursor;
import net.civeira.phylax.features.access.tenantconfig.query.TenantConfigFilter;

@ApplicationScoped
@RequiredArgsConstructor
public class TenantConfigListUsecase {

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final Event<TenantConfigAllowListProposal> listAllowEmitter;

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final TenantConfigsVisibility visibility;

  /**
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    TenantConfigAllowListProposal proposal = TenantConfigAllowListProposal.builder()
        .detail(Allow.builder().allowed(true).description("Allow by default").build()).query(query)
        .build();
    listAllowEmitter.fire(proposal);
    return proposal.getDetail();
  }

  /**
   * The slide with some values
   *
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @param filter
   * @param cursor
   * @return The slide with some values
   */
  public List<TenantConfigStateProyection> list(final Interaction query,
      final TenantConfigFilter filter, final TenantConfigCursor cursor) {
    Allow detail = allow(query);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    TenantConfigCached values = visibility.listCachedVisibles(query, filter, cursor);
    List<TenantConfigStateProyection> list =
        values.getValue().stream().map(value -> visibility.copyWithHidden(query, value)).toList();
    return new TimestampedList<>(WrapMetadata.<List<TenantConfigStateProyection>>builder()
        .data(list).since(values.getSince().toInstant()).build());
  }
}
