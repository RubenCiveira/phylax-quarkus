package net.civeira.phylax.features.access.tenanttermsofuse.application.usecase.update;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.common.telemetry.Observed;
import net.civeira.phylax.common.telemetry.Trace;
import net.civeira.phylax.features.access.tenanttermsofuse.application.visibility.TenantTermsOfUsesVisibility;
import net.civeira.phylax.features.access.tenanttermsofuse.domain.TenantTermsOfUse;
import net.civeira.phylax.features.access.tenanttermsofuse.domain.TenantTermsOfUseChangeSet;
import net.civeira.phylax.features.access.tenanttermsofuse.domain.TenantTermsOfUseRef;
import net.civeira.phylax.features.access.tenanttermsofuse.domain.gateway.TenantTermsOfUseAuditGateway;
import net.civeira.phylax.features.access.tenanttermsofuse.domain.gateway.TenantTermsOfUseCacheGateway;
import net.civeira.phylax.features.access.tenanttermsofuse.domain.gateway.TenantTermsOfUseWriteRepositoryGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class TenantTermsOfUseUpdateUsecase {

  /**
   * Service to register audit events
   *
   * @autogenerated UpdateUsecaseGenerator
   */
  private final TenantTermsOfUseAuditGateway audit;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final TenantTermsOfUseCacheGateway cache;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final TenantTermsOfUseWriteRepositoryGateway gateway;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<TenantTermsOfUseUpdateAllowDecision> updateAllowEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<TenantTermsOfUseUpdateCheck> updateCheckEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<TenantTermsOfUseUpdateEnrich> updateEnrichEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final TenantTermsOfUsesVisibility visibility;

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  @Observed("Checking if is allowed to update an entity of tenant terms of use")
  public Allow allow(final Interaction query, final TenantTermsOfUseRef reference) {
    Trace.setAttribute("ref", reference.getUid());
    Allow response = checkAllow(query, reference);
    Trace.setAttribute("allowed", response.isAllowed());
    return response;
  }

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @return
   */
  @Observed("Checking if is allowed to update an entity of tenant terms of use")
  public Allow allow(final Interaction query) {
    Allow response = checkAllow(query);
    Trace.setAttribute("allowed", response.isAllowed());
    return response;
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @param reference
   * @param input
   * @return The slide with some values
   */
  @Observed("Updating an entity of tenant terms of use")
  public TenantTermsOfUseUpdateProjection update(final Interaction query,
      final TenantTermsOfUseRef reference, final TenantTermsOfUseUpdateInput input) {
    Trace.setAttribute("ref", reference.getUid());
    Allow detail = checkAllow(query, reference);
    if (!detail.isAllowed()) {
      Trace.addEvent(
          "The policies for tenant terms of use dont allow to update: " + detail.getDescription());
      throw new NotAllowedException(detail.getDescription());
    }
    TenantTermsOfUse original = visibility.retrieveVisibleForUpdate(query, reference.getUid())
        .orElseThrow(() -> new NotFoundException(""));
    TenantTermsOfUseUpdateInput filled = TenantTermsOfUseUpdateInput
        .fromChangeSet(visibility.copyWithFixed(query, input.toChangeSet()));
    Trace.addEvent(
        "The input value is filled with the fixed values for the creation of tenant terms of use");
    TenantTermsOfUseUpdateCheck check = TenantTermsOfUseUpdateCheck.builder().input(filled)
        .interaction(query).reference(reference).build();
    updateCheckEmitter.fire(check);
    Trace.addEvent(
        "The input value is checked with the policies for the update of tenant terms of use");
    TenantTermsOfUseUpdateEnrich proposal = TenantTermsOfUseUpdateEnrich.builder().input(filled)
        .interaction(query).reference(reference).build();
    updateEnrichEmitter.fire(proposal);
    TenantTermsOfUseChangeSet dto = proposal.getInput().toChangeSet();
    Trace.addEvent("The update of entity for tenant terms of use is ready");
    TenantTermsOfUse saved = gateway.update(original, original.update(dto));
    Trace.addEvent("The updated entity of tenant terms of use was saved");
    cache.update(saved);
    // Register audit operation
    audit.updated("update", saved, original, query);
    return TenantTermsOfUseUpdateProjection.from(visibility.copyWithHidden(query, saved));
  }

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  private Allow checkAllow(final Interaction query, final TenantTermsOfUseRef reference) {
    TenantTermsOfUseUpdateAllowDecision base = TenantTermsOfUseUpdateAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).reference(reference).build();
    updateAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @return
   */
  private Allow checkAllow(final Interaction query) {
    TenantTermsOfUseUpdateAllowDecision base = TenantTermsOfUseUpdateAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).build();
    updateAllowEmitter.fire(base);
    return base.getDetail();
  }
}
