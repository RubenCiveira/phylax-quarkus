package net.civeira.phylax.features.access.tenant.infrastructure.driven;

import java.util.List;
import java.util.Optional;
import java.util.function.Predicate;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.SpanKind;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Context;
import jakarta.enterprise.context.ApplicationScoped;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.algorithms.Slider;
import net.civeira.phylax.features.access.tenant.domain.Tenant;
import net.civeira.phylax.features.access.tenant.domain.TenantRef;
import net.civeira.phylax.features.access.tenant.domain.gateway.TenantCursor;
import net.civeira.phylax.features.access.tenant.domain.gateway.TenantFilter;
import net.civeira.phylax.features.access.tenant.domain.gateway.TenantWriteRepositoryGateway;
import net.civeira.phylax.features.access.tenant.infrastructure.event.TenantEventDispatcher;
import net.civeira.phylax.features.access.tenant.infrastructure.repository.TenantRepository;

@ApplicationScoped
@RequiredArgsConstructor
public class TenantWriteGatewayAdapter implements TenantWriteRepositoryGateway {

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   */
  private final TenantEventDispatcher eventDispatcher;

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   */
  private final TenantRepository repository;

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param filter
   * @return
   */
  @Override
  public long countForUpdate(TenantFilter filter) {
    Span span = tracer
        .spanBuilder("Count on the repository for update the matches entities of tenant")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.count(filter);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param entity
   * @return
   */
  @Override
  public Tenant create(Tenant entity) {
    Span span = tracer.spanBuilder("Create on the repository an entity of tenant")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      Tenant result = repository.create(entity);
      eventDispatcher.dispatch(entity);
      return result;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param entity
   * @param verifier
   * @return
   */
  @Override
  public Tenant create(Tenant entity, Predicate<Tenant> verifier) {
    Span span = tracer
        .spanBuilder("Create on the repository an entity of tenant and verify its visibility")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      Tenant result = repository.create(entity, verifier);
      eventDispatcher.dispatch(entity);
      return result;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param entity
   */
  @Override
  public void delete(Tenant entity) {
    Span span = tracer.spanBuilder("Delete from the repository a slide for entities of tenant")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      repository.delete(entity);
      eventDispatcher.dispatch(entity);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param uid
   * @param filter
   * @return
   */
  @Override
  public boolean existsForUpdate(String uid, Optional<TenantFilter> filter) {
    Span span = tracer
        .spanBuilder("Check if exists on the repository for update a entity of tenant")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.existsForUpdate(uid, filter);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param filter
   * @return
   */
  @Override
  public Optional<Tenant> findForUpdate(TenantFilter filter) {
    Span span = tracer.spanBuilder("Find on the repository for update one entity of tenant")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.findForUpdate(filter);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param filter
   * @return
   */
  @Override
  public List<Tenant> listForUpdate(TenantFilter filter) {
    Span span = tracer
        .spanBuilder("Retrieve from repository for update a list for entities of tenant")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.listForUpdate(filter);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param reference
   * @return
   */
  @Override
  public Tenant resolveForUpdate(TenantRef reference) {
    Span span = tracer.spanBuilder("Enrich from the repository for update a entity of tenant")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.resolveForUpdate(reference);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param uid
   * @param filter
   * @return
   */
  @Override
  public Optional<Tenant> retrieveForUpdate(String uid, Optional<TenantFilter> filter) {
    Span span = tracer.spanBuilder("Retrieve from repository a slide for entities of tenant")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.retrieveForUpdate(uid, filter);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param filter
   * @param cursor
   * @return
   */
  @Override
  public Slider<Tenant> slideForUpdate(TenantFilter filter, TenantCursor cursor) {
    Span span = tracer
        .spanBuilder("Retrieve from repository for update a slide for entities of tenant")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.slideForUpdate(filter, cursor);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param reference
   * @param entity
   * @return
   */
  @Override
  public Tenant update(TenantRef reference, Tenant entity) {
    Tenant result = repository.update(entity);
    eventDispatcher.dispatch(entity);
    return result;
  }
}
