package net.civeira.phylax.features.access.user.transition.update;

import java.util.ArrayList;
import java.util.List;
import java.util.function.Function;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.inject.Instance;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.features.access.user.User;
import net.civeira.phylax.features.access.user.command.UserWriteAttributes;
import net.civeira.phylax.features.access.user.transition.UserChangeProposal;

@ApplicationScoped
@RequiredArgsConstructor
public class UserUpdatePipeline {

  /**
   * @autogenerated UpdateEntityRuleGenerator
   */
  private final Instance<UserUpdateStep> steps;

  /**
   * @autogenerated UpdateEntityRuleGenerator
   * @param attributes
   * @param original
   * @param last
   * @return
   */
  public User update(final UserWriteAttributes attributes, final User original,
      final Function<UserChangeProposal, User> last) {
    Function<UserUpdateTransition, User> finalHandler = t -> {
      if (t.hasErrors()) {
        throw t.asConstraintException();
      }
      return last.apply(t.getData());
    };
    List<UserUpdateStep> reversed = new ArrayList<>(steps.stream().toList()).reversed();
    Function<UserUpdateTransition, User> pipeline = reversed.stream().reduce(finalHandler,
        (next, step) -> transition -> step.update(transition, next), (f1, f2) -> f1);
    return pipeline.apply(UserUpdateTransition.builder().data(new UserChangeProposal(attributes))
        .original(original).build());
  }
}
