package net.civeira.phylax.features.access.tenanttermsofuse.valueobject;

import lombok.EqualsAndHashCode;
import lombok.Getter;
import lombok.RequiredArgsConstructor;
import lombok.With;
import net.civeira.phylax.common.exception.ConstraintException;
import net.civeira.phylax.common.value.validation.AbstractFailList;
import net.civeira.phylax.common.value.validation.ConstraintFail;
import net.civeira.phylax.common.value.validation.ConstraintFailList;

/**
 * Value Object representing the content of the Terms of Use document. of tenantTermsOfUse.
 * <p>
 * This class ensures that the text is a non-null {@code String} and encapsulates its validation and
 * formatting logic. It is immutable and compliant with DDD (Domain-Driven Design) principles.
 * </p>
 * <p>
 * Use {@link #from(String)} or {@link #tryFrom(Object)} to safely create instances. Validation
 * errors are reported via {@link ConstraintFailList} or thrown as {@link ConstraintException}.
 * </p>
 */
@Getter
@RequiredArgsConstructor
@With
@EqualsAndHashCode(onlyExplicitlyIncluded = true)
public class TextVO {

  /**
   * An empty instance
   *
   * @autogenerated ValueObjectGenerator
   * @param text
   * @return An empty instance
   */
  public static TextVO from(final String text) {
    return tryFrom(text);
  }

  /**
   * An empty instance
   *
   * @autogenerated ValueObjectGenerator
   * @param text
   * @return An empty instance
   */
  public static TextVO tryFrom(final Object text) {
    ConstraintFailList list = new ConstraintFailList();
    TextVO result = tryFrom(text, list);
    if (list.hasErrors()) {
      throw new ConstraintException(list);
    }
    return result;
  }

  /**
   * An empty instance
   *
   * @autogenerated ValueObjectGenerator
   * @param text temptative value
   * @param fails Error list
   * @return An empty instance
   */
  public static <T extends AbstractFailList> TextVO tryFrom(final Object text, final T fails) {
    if (null == text) {
      fails.add(new ConstraintFail("not-null", "text", null, "Cant be null"));
      return null;
    } else if (text instanceof String castedText) {
      return new TextVO(castedText);
    } else {
      fails.add(new ConstraintFail("wrong-type", "text", text.getClass(),
          "A String type is expected for text"));
      return null;
    }
  }

  /**
   * The actual value of the name. * It is guaranteed to be a non-null String after successful
   * value.validation.
   *
   * @autogenerated ValueObjectGenerator
   */
  private final String value;

  /**
   * the string value wrapped in square brackets.
   *
   * @autogenerated ValueObjectGenerator
   * @return the string value wrapped in square brackets.
   */
  @Override
  public String toString() {
    return "[" + value + "]";
  }
}
