package net.civeira.phylax.features.access.apikeyclient.infrastructure.driver.rest;

import java.util.List;
import java.util.stream.Stream;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.transaction.Transactional;
import jakarta.ws.rs.core.Response;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.BatchIdentificator;
import net.civeira.phylax.common.batch.BatchProgress;
import net.civeira.phylax.common.infrastructure.CurrentRequest;
import net.civeira.phylax.features.access.apikeyclient.application.usecase.disable.ApiKeyClientDisableFilter;
import net.civeira.phylax.features.access.apikeyclient.application.usecase.disable.ApiKeyClientDisableProjection;
import net.civeira.phylax.features.access.apikeyclient.application.usecase.disable.ApiKeyClientDisableStatus;
import net.civeira.phylax.features.access.apikeyclient.application.usecase.disable.ApiKeyClientDisableUsecase;
import net.civeira.phylax.features.access.apikeyclient.domain.ApiKeyClientReference;
import net.civeira.phylax.generated.openapi.model.ApiKeyClientApiDto;

@RequiredArgsConstructor
@ApplicationScoped
public class ApiKeyClientDisableController {

  /**
   * @autogenerated ActionControllerGenerator
   */
  private final CurrentRequest currentRequest;

  /**
   * @autogenerated ActionControllerGenerator
   */
  private final ApiKeyClientDisableUsecase disable;

  /**
   * @autogenerated ActionControllerGenerator
   * @param uids
   * @param search
   * @param key
   * @return
   */
  public Response apiKeyClientApiBatchDisable(final List<String> uids, final String search,
      final String key) {
    ApiKeyClientDisableFilter.ApiKeyClientDisableFilterBuilder filterBuilder =
        ApiKeyClientDisableFilter.builder();
    filterBuilder =
        filterBuilder.uids(uids.stream().flatMap(part -> Stream.of(part.split(","))).toList());
    filterBuilder = filterBuilder.search(search);
    filterBuilder = filterBuilder.uids(
        null == uids ? null : uids.stream().flatMap(part -> Stream.of(part.split(","))).toList());
    filterBuilder = filterBuilder.search(search);
    filterBuilder = filterBuilder.key(key);
    ApiKeyClientDisableFilter filter = filterBuilder.build();
    BatchIdentificator task = disable.disable(currentRequest.interaction(), filter);
    /* .header("Last-Modified", value.getSince().format(DateTimeFormatter.RFC_1123_DATE_TIME)) */
    return Response.ok(task).build();
  }

  /**
   * @autogenerated ActionControllerGenerator
   * @param batchId
   * @return
   */
  public Response apiKeyClientApiBatchDisableQuery(final String batchId) {
    BatchProgress task = disable.checkProgress(
        ApiKeyClientDisableStatus.builder().taskId(batchId).build(currentRequest.interaction()));
    return Response.ok(task).build();
  }

  /**
   * @autogenerated ActionControllerGenerator
   * @param uid
   * @return
   */
  @Transactional
  public Response apiKeyClientApiDisable(final String uid) {
    ApiKeyClientDisableProjection updated =
        disable.disable(currentRequest.interaction(), ApiKeyClientReference.of(uid));
    return Response.ok(toApiModel(updated)).build();
  }

  /**
   * @autogenerated ActionControllerGenerator
   * @param dto
   * @return
   */
  private ApiKeyClientApiDto toApiModel(ApiKeyClientDisableProjection dto) {
    ApiKeyClientApiDto apiKeyClientApiDto = new ApiKeyClientApiDto();
    apiKeyClientApiDto.setUid(dto.getUid());
    apiKeyClientApiDto.setCode(dto.getCode());
    apiKeyClientApiDto.setKey(dto.getKey());
    apiKeyClientApiDto.setEnabled(dto.getEnabled());
    apiKeyClientApiDto.setScopes(dto.getScopes());
    apiKeyClientApiDto.setVersion(dto.getVersion());
    return apiKeyClientApiDto;
  }
}
