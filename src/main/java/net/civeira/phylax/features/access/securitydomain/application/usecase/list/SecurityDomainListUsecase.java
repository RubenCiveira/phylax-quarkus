package net.civeira.phylax.features.access.securitydomain.application.usecase.list;

import java.util.List;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.algorithms.metadata.TimestampedList;
import net.civeira.phylax.common.algorithms.metadata.WrapMetadata;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.securitydomain.application.visibility.SecurityDomainVisibilityFilter;
import net.civeira.phylax.features.access.securitydomain.application.visibility.SecurityDomainsVisibility;
import net.civeira.phylax.features.access.securitydomain.domain.gateway.SecurityDomainCached;
import net.civeira.phylax.features.access.securitydomain.domain.gateway.SecurityDomainCursor;
import net.civeira.phylax.features.access.securitydomain.domain.gateway.SecurityDomainOrder;

@ApplicationScoped
@RequiredArgsConstructor
public class SecurityDomainListUsecase {

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final Event<SecurityDomainListAllowDecision> listAllowEmitter;

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final SecurityDomainsVisibility visibility;

  /**
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    SecurityDomainListAllowDecision proposal = SecurityDomainListAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allow by default").build()).query(query)
        .build();
    listAllowEmitter.fire(proposal);
    return proposal.getDetail();
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @param filter
   * @param cursor
   * @return The slide with some values
   */
  public List<SecurityDomainListProjection> list(final Interaction query,
      final SecurityDomainListFilter filter, final SecurityDomainListCursor cursor) {
    Allow detail = allow(query);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    SecurityDomainVisibilityFilter visibleFilter =
        SecurityDomainVisibilityFilter.builder().uid(filter.getUid().orElse(null))
            .uids(filter.getUids().stream().toList()).search(filter.getSearch().orElse(null))
            .enabled(filter.getEnabled().orElse(null)).name(filter.getName().orElse(null)).build();
    SecurityDomainCursor gatewayCursor = SecurityDomainCursor.builder()
        .order(cursor.getOrder().stream().map(Object::toString).map(SecurityDomainOrder::valueOf)
            .toList())
        .sinceName(cursor.getSinceName().orElse(null)).sinceName(cursor.getSinceName().orElse(null))
        .build();
    SecurityDomainCached values =
        visibility.listCachedVisibles(query, visibleFilter, gatewayCursor);
    List<SecurityDomainListProjection> list =
        values.getValue().stream().map(value -> visibility.copyWithHidden(query, value))
            .map(SecurityDomainListProjection::from).toList();
    return new TimestampedList<>(WrapMetadata.<List<SecurityDomainListProjection>>builder()
        .data(list).since(values.getSince().toInstant()).build());
  }
}
