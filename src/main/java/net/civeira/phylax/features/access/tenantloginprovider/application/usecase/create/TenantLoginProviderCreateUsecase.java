package net.civeira.phylax.features.access.tenantloginprovider.application.usecase.create;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.common.telemetry.Observed;
import net.civeira.phylax.common.telemetry.Trace;
import net.civeira.phylax.features.access.tenantloginprovider.application.visibility.TenantLoginProvidersVisibility;
import net.civeira.phylax.features.access.tenantloginprovider.domain.TenantLoginProvider;
import net.civeira.phylax.features.access.tenantloginprovider.domain.TenantLoginProviderChangeSet;
import net.civeira.phylax.features.access.tenantloginprovider.domain.gateway.TenantLoginProviderAuditGateway;
import net.civeira.phylax.features.access.tenantloginprovider.domain.gateway.TenantLoginProviderCacheGateway;
import net.civeira.phylax.features.access.tenantloginprovider.domain.gateway.TenantLoginProviderWriteRepositoryGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class TenantLoginProviderCreateUsecase {

  /**
   * Service to register audit events
   *
   * @autogenerated CreateUsecaseGenerator
   */
  private final TenantLoginProviderAuditGateway audit;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final TenantLoginProviderCacheGateway cache;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Event<TenantLoginProviderCreateAllowDecision> createAllowEmitter;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Event<TenantLoginProviderCreateCheck> createCheckEmitter;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Event<TenantLoginProviderCreateEnrich> createEnrichEmitter;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final TenantLoginProviderWriteRepositoryGateway gateway;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final TenantLoginProvidersVisibility visibility;

  /**
   * @autogenerated CreateUsecaseGenerator
   * @param query
   * @return
   */
  @Observed("Checking if is allowed to create an entity of tenant login provider")
  public Allow allow(final Interaction query) {
    Allow response = checkAllow(query);
    Trace.setAttribute("allowed", response.isAllowed());
    return response;
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated CreateUsecaseGenerator
   * @param query
   * @param input
   * @return The slide with some values
   */
  @Observed("Creating an entity of tenant login provider")
  public TenantLoginProviderCreateProjection create(final Interaction query,
      final TenantLoginProviderCreateInput input) {
    // Check if the user can perform the action
    Allow detail = checkAllow(query);
    if (!detail.isAllowed()) {
      Trace.addEvent("The policies for tenant login provider dont allow to create: "
          + detail.getDescription());
      throw new NotAllowedException(detail.getDescription());
    }
    // Append the fixed fields to the requested fields
    TenantLoginProviderCreateInput filled = TenantLoginProviderCreateInput
        .fromChangeSet(visibility.copyWithFixed(query, input.toChangeSet()));
    Trace.addEvent(
        "The input value is filled with the fixed values for the creation of tenant login provider");
    // Pass bussines rules to validate the the requested data filled
    TenantLoginProviderCreateCheck check =
        TenantLoginProviderCreateCheck.builder().interaction(query).input(filled).build();
    createCheckEmitter.fire(check);
    Trace.addEvent(
        "The input value is checked with the policies for the creation of tenant login provider");
    // Pass bussines rules to calculate derivated fields for the requested data filled
    TenantLoginProviderCreateEnrich proposal =
        TenantLoginProviderCreateEnrich.builder().interaction(query).input(filled).build();
    createEnrichEmitter.fire(proposal);
    // Convert the result input request data to a entity change set
    TenantLoginProviderChangeSet dto = proposal.getInput().toChangeSet();
    // Run the creation from the domain model
    TenantLoginProvider entity = TenantLoginProvider.create(dto);
    Trace.addEvent("A created entity for tenant login provider is ready");
    // Use the write gateway to store the entity on a persistent way
    TenantLoginProvider created =
        gateway.create(entity, attempt -> visibility.checkVisibility(query, attempt.getUid()));
    Trace.addEvent("The created entity of tenant login provider was saved");
    // Update the cache
    cache.update(created);
    // Register audit operation
    audit.created("create", created, query);
    // Convert the entity into a proyection and filter non visible fields
    return TenantLoginProviderCreateProjection.from(visibility.copyWithHidden(query, created));
  }

  /**
   * @autogenerated CreateUsecaseGenerator
   * @param query
   * @return
   */
  private Allow checkAllow(final Interaction query) {
    TenantLoginProviderCreateAllowDecision proposal = TenantLoginProviderCreateAllowDecision
        .builder().detail(Allow.builder().allowed(true).description("Allow by default").build())
        .query(query).build();
    createAllowEmitter.fire(proposal);
    return proposal.getDetail();
  }
}
