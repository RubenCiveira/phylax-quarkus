package net.civeira.phylax.features.access.role.application.usecase.retrieve;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.role.application.visibility.RolesVisibility;
import net.civeira.phylax.features.access.role.domain.RoleRef;
import net.civeira.phylax.features.access.role.domain.gateway.RoleCached;

@ApplicationScoped
@RequiredArgsConstructor
public class RoleRetrieveUsecase {

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final Event<RoleRetrieveAllowDecision> retrieveAllowEmitter;

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final RolesVisibility visibility;

  /**
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  public Allow allow(final Interaction query, final RoleRef reference) {
    RoleRetrieveAllowDecision base = RoleRetrieveAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).reference(reference).build();
    retrieveAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    RoleRetrieveAllowDecision base = RoleRetrieveAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).build();
    retrieveAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @param reference
   * @return The slide with some values
   */
  public RoleRetrieveProjection retrieve(final Interaction query, final RoleRef reference) {
    Allow detail = allow(query, reference);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    RoleCached retrieveCachedVisible = visibility.retrieveCachedVisible(query, reference.getUid());
    return retrieveCachedVisible.first().map(first -> visibility.copyWithHidden(query, first))
        .map(RoleRetrieveProjection::from).orElseThrow(() -> new NotFoundException(""));
  }
}
