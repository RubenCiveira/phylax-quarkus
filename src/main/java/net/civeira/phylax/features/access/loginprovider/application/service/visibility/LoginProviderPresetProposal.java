package net.civeira.phylax.features.access.loginprovider.application.service.visibility;

import java.util.Optional;
import java.util.function.Consumer;

import lombok.Builder;
import lombok.Data;
import lombok.EqualsAndHashCode;
import lombok.NonNull;
import lombok.With;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.loginprovider.LoginProvider;
import net.civeira.phylax.features.access.loginprovider.application.request.LoginProviderStateChange;

@Data
@Builder(toBuilder = true)
@With
@EqualsAndHashCode(onlyExplicitlyIncluded = true)
public class LoginProviderPresetProposal {

  /**
   * @autogenerated PresetProposalGenerator
   */
  @NonNull
  private final Interaction interaction;

  /**
   * @autogenerated PresetProposalGenerator
   */
  @NonNull
  private LoginProviderStateChange dto;

  /**
   * @autogenerated PresetProposalGenerator
   */
  private LoginProvider original;

  /**
   * @autogenerated PresetProposalGenerator
   * @return
   */
  public Optional<LoginProvider> getOriginal() {
    return Optional.ofNullable(original);
  }

  /**
   * @autogenerated PresetProposalGenerator
   * @param mapper
   */
  public void peek(Consumer<LoginProviderStateChange> mapper) {
    mapper.accept(dto);
  }
}
