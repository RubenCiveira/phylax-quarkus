package net.civeira.phylax.features.access.scopeassignation.transition.create;

import java.util.ArrayList;
import java.util.List;
import java.util.function.Function;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.inject.Instance;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.features.access.scopeassignation.ScopeAssignation;
import net.civeira.phylax.features.access.scopeassignation.command.ScopeAssignationWriteAttributes;
import net.civeira.phylax.features.access.scopeassignation.transition.ScopeAssignationChangeProposal;

@ApplicationScoped
@RequiredArgsConstructor
public class ScopeAssignationCreatePipeline {

  /**
   * @autogenerated UpdateEntityRuleGenerator
   */
  private final Instance<ScopeAssignationCreateStep> steps;

  /**
   * @autogenerated UpdateEntityRuleGenerator
   * @param attributes
   * @param last
   * @return
   */
  public ScopeAssignation create(final ScopeAssignationWriteAttributes attributes,
      final Function<ScopeAssignationChangeProposal, ScopeAssignation> last) {
    Function<ScopeAssignationCreateTransition, ScopeAssignation> finalHandler = t -> {
      if (t.hasErrors()) {
        throw t.asConstraintException();
      }
      return last.apply(t.getData());
    };
    List<ScopeAssignationCreateStep> reversed = new ArrayList<>(steps.stream().toList()).reversed();
    Function<ScopeAssignationCreateTransition, ScopeAssignation> pipeline =
        reversed.stream().reduce(finalHandler,
            (next, step) -> transition -> step.create(transition, next), (f1, f2) -> f1);
    return pipeline.apply(ScopeAssignationCreateTransition.builder()
        .data(new ScopeAssignationChangeProposal(attributes)).build());
  }
}
