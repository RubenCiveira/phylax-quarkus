package net.civeira.phylax.features.access.securityscope.transition.update;

import java.util.ArrayList;
import java.util.List;
import java.util.function.Function;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.inject.Instance;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.features.access.securityscope.SecurityScope;
import net.civeira.phylax.features.access.securityscope.command.SecurityScopeWriteAttributes;
import net.civeira.phylax.features.access.securityscope.transition.SecurityScopeChangeProposal;

@ApplicationScoped
@RequiredArgsConstructor
public class SecurityScopeUpdatePipeline {

  /**
   * @autogenerated UpdateEntityRuleGenerator
   */
  private final Instance<SecurityScopeUpdateStep> steps;

  /**
   * @autogenerated UpdateEntityRuleGenerator
   * @param attributes
   * @param original
   * @param last
   * @return
   */
  public SecurityScope update(final SecurityScopeWriteAttributes attributes,
      final SecurityScope original,
      final Function<SecurityScopeChangeProposal, SecurityScope> last) {
    Function<SecurityScopeUpdateTransition, SecurityScope> finalHandler = t -> {
      if (t.hasErrors()) {
        throw t.asConstraintException();
      }
      return last.apply(t.getData());
    };
    List<SecurityScopeUpdateStep> reversed = new ArrayList<>(steps.stream().toList()).reversed();
    Function<SecurityScopeUpdateTransition, SecurityScope> pipeline = reversed.stream().reduce(
        finalHandler, (next, step) -> transition -> step.update(transition, next), (f1, f2) -> f1);
    return pipeline.apply(SecurityScopeUpdateTransition.builder()
        .data(new SecurityScopeChangeProposal(attributes)).original(original).build());
  }
}
