package net.civeira.phylax.features.access.loginprovider.valueobject;

import java.util.Optional;

import lombok.EqualsAndHashCode;
import lombok.Getter;
import lombok.RequiredArgsConstructor;
import lombok.With;
import net.civeira.phylax.common.exception.ConstraintException;
import net.civeira.phylax.common.value.validation.AbstractFailList;
import net.civeira.phylax.common.value.validation.ConstraintFail;
import net.civeira.phylax.common.value.validation.ConstraintFailList;

/**
 * Value Object representing private key used internally to validate codes returned by the identity
 * provider. of loginProvider.
 * <p>
 * This class ensures that the private key is a {@code String} and encapsulates its validation and
 * formatting logic. It is immutable and compliant with DDD (Domain-Driven Design) principles.
 * </p>
 * <p>
 * Use {@link #from(String)} or {@link #tryFrom(Object)} to safely create instances. Validation
 * errors are reported via {@link ConstraintFailList} or thrown as {@link ConstraintException}.
 * </p>
 */
@Getter
@RequiredArgsConstructor
@With
@EqualsAndHashCode(onlyExplicitlyIncluded = true)
public class PrivateKeyVO {

  /**
   * An empty instance
   *
   * @autogenerated ValueObjectGenerator
   * @param privateKey
   * @return An empty instance
   */
  public static PrivateKeyVO from(final String privateKey) {
    return tryFrom(privateKey);
  }

  /**
   * An empty instance
   *
   * @autogenerated ValueObjectGenerator
   * @return An empty instance
   */
  public static PrivateKeyVO nullValue() {
    return new PrivateKeyVO(null);
  }

  /**
   * An empty instance
   *
   * @autogenerated ValueObjectGenerator
   * @param privateKey
   * @return An empty instance
   */
  public static PrivateKeyVO tryFrom(final Object privateKey) {
    ConstraintFailList list = new ConstraintFailList();
    PrivateKeyVO result = tryFrom(privateKey, list);
    if (list.hasErrors()) {
      throw new ConstraintException(list);
    }
    return result;
  }

  /**
   * An empty instance
   *
   * @autogenerated ValueObjectGenerator
   * @param privateKey temptative value
   * @param fails Error list
   * @return An empty instance
   */
  public static <T extends AbstractFailList> PrivateKeyVO tryFrom(final Object privateKey,
      final T fails) {
    if (null == privateKey) {
      return new PrivateKeyVO(null);
    } else if (privateKey instanceof String castedPrivateKey) {
      return new PrivateKeyVO(castedPrivateKey);
    } else {
      fails.add(new ConstraintFail("wrong-type", "privateKey", privateKey.getClass(),
          "A String type is expected for privateKey"));
      return null;
    }
  }

  /**
   * The actual value of the name.
   *
   * @autogenerated ValueObjectGenerator
   */
  private final String value;

  /**
   * @autogenerated ValueObjectGenerator
   * @return
   */
  public Optional<String> getValue() {
    return Optional.ofNullable(value);
  }

  /**
   * the string value wrapped in square brackets.
   *
   * @autogenerated ValueObjectGenerator
   * @return the string value wrapped in square brackets.
   */
  @Override
  public String toString() {
    return "[" + value + "]";
  }
}
