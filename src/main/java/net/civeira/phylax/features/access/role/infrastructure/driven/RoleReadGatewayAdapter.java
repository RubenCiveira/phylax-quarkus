package net.civeira.phylax.features.access.role.infrastructure.driven;

import java.util.List;
import java.util.Optional;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.SpanKind;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Context;
import jakarta.enterprise.context.ApplicationScoped;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.algorithms.Slider;
import net.civeira.phylax.features.access.role.domain.Role;
import net.civeira.phylax.features.access.role.domain.RoleRef;
import net.civeira.phylax.features.access.role.domain.gateway.RoleCursor;
import net.civeira.phylax.features.access.role.domain.gateway.RoleFilter;
import net.civeira.phylax.features.access.role.domain.gateway.RoleReadRepositoryGateway;
import net.civeira.phylax.features.access.role.infrastructure.repository.RoleRepository;

@ApplicationScoped
@RequiredArgsConstructor
public class RoleReadGatewayAdapter implements RoleReadRepositoryGateway {

  /**
   * @autogenerated ReadAdaterGatewayGenerator
   */
  private final RoleRepository repository;

  /**
   * @autogenerated ReadAdaterGatewayGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated ReadAdaterGatewayGenerator
   * @param filter
   * @return
   */
  @Override
  public long count(RoleFilter filter) {
    Span span = tracer.spanBuilder("Count on the repository the matches entities of role")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.count(filter);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated ReadAdaterGatewayGenerator
   * @param uid
   * @param filter
   * @return
   */
  @Override
  public boolean exists(String uid, Optional<RoleFilter> filter) {
    Span span = tracer.spanBuilder("Check if exists on the repository a entity of role")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.exists(uid, filter);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated ReadAdaterGatewayGenerator
   * @param filter
   * @return
   */
  @Override
  public Optional<Role> find(RoleFilter filter) {
    Span span = tracer.spanBuilder("Find on the repository one entity of role")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.find(filter);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated ReadAdaterGatewayGenerator
   * @param filter
   * @return
   */
  @Override
  public List<Role> list(RoleFilter filter) {
    Span span = tracer.spanBuilder("Retrieve from repository a list for entities of role")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.list(filter);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated ReadAdaterGatewayGenerator
   * @param reference
   * @return
   */
  @Override
  public Role resolve(RoleRef reference) {
    Span span = tracer.spanBuilder("Enrich from the repository a entity of role")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.resolve(reference);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated ReadAdaterGatewayGenerator
   * @param uid
   * @param filter
   * @return
   */
  @Override
  public Optional<Role> retrieve(String uid, Optional<RoleFilter> filter) {
    Span span = tracer.spanBuilder("Check if exists on the repository a entity of role")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.retrieve(uid, filter);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated ReadAdaterGatewayGenerator
   * @param filter
   * @param cursor
   * @return
   */
  @Override
  public Slider<Role> slide(RoleFilter filter, RoleCursor cursor) {
    Span span = tracer.spanBuilder("Retrieve from repository a slide for entities of role")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.slide(filter, cursor);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }
}
