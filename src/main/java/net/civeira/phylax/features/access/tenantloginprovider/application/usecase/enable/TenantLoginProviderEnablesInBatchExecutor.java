package net.civeira.phylax.features.access.tenantloginprovider.application.usecase.enable;

import java.util.List;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.SpanKind;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Context;
import io.quarkus.arc.Unremovable;
import jakarta.enterprise.context.ApplicationScoped;
import lombok.Data;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.stepper.ItemDescriptor;
import net.civeira.phylax.common.batch.stepper.ItemProcessor;
import net.civeira.phylax.common.batch.stepper.ItemReader;
import net.civeira.phylax.common.batch.stepper.ItemWriter;
import net.civeira.phylax.common.batch.stepper.StepContext;
import net.civeira.phylax.common.batch.stepper.StepCounter;
import net.civeira.phylax.common.batch.stepper.StepFinalizer;
import net.civeira.phylax.common.batch.stepper.StepInitializer;
import net.civeira.phylax.common.exception.ExecutionException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.features.access.tenantloginprovider.application.visibility.TenantLoginProvidersVisibility;
import net.civeira.phylax.features.access.tenantloginprovider.domain.TenantLoginProvider;
import net.civeira.phylax.features.access.tenantloginprovider.domain.gateway.TenantLoginProviderCursor;

@Unremovable
@ApplicationScoped
@RequiredArgsConstructor
class TenantLoginProviderEnablesInBatchExecutor implements
    StepCounter<TenantLoginProviderEnableAllInBatchCommand, TenantLoginProviderEnablesInBatchExecutor.TenantLoginProviderPaginableBatch>,
    StepFinalizer<TenantLoginProviderEnableAllInBatchCommand, TenantLoginProviderEnablesInBatchExecutor.TenantLoginProviderPaginableBatch>,
    StepInitializer<TenantLoginProviderEnableAllInBatchCommand, TenantLoginProviderEnablesInBatchExecutor.TenantLoginProviderPaginableBatch>,
    ItemReader<TenantLoginProvider, TenantLoginProviderEnableAllInBatchCommand, TenantLoginProviderEnablesInBatchExecutor.TenantLoginProviderPaginableBatch>,
    ItemWriter<TenantLoginProvider, TenantLoginProviderEnableAllInBatchCommand, TenantLoginProviderEnablesInBatchExecutor.TenantLoginProviderPaginableBatch>,
    ItemProcessor<TenantLoginProvider, TenantLoginProvider, TenantLoginProviderEnableAllInBatchCommand, TenantLoginProviderEnablesInBatchExecutor.TenantLoginProviderPaginableBatch>,
    ItemDescriptor<TenantLoginProvider, TenantLoginProviderEnableAllInBatchCommand, TenantLoginProviderEnablesInBatchExecutor.TenantLoginProviderPaginableBatch> {

  /**
   * @autogenerated EntityGenerator
   */
  @Data
  public static class TenantLoginProviderPaginableBatch {

    /**
     * @autogenerated EntityGenerator
     */
    private String since;
  }

  /**
   * @autogenerated EntityGenerator
   */
  private final int size = 10;

  /**
   * @autogenerated EntityGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated EntityGenerator
   */
  private final TenantLoginProviderEnableUsecase usecase;

  /**
   * @autogenerated EntityGenerator
   */
  private final TenantLoginProvidersVisibility visibility;

  /**
   * @autogenerated EntityGenerator
   * @param context
   * @return
   */
  @Override
  public long approximatedItems(
      final StepContext<TenantLoginProviderEnableAllInBatchCommand, TenantLoginProviderPaginableBatch> context) {
    Span span = tracer.spanBuilder(
        "Count selected items to the batch enable for a set of entities of tenant login provider")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      return visibility.countVisibles(context.getParams().getInteraction(),
          context.getParams().getFilter());
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   */
  @Override
  public void finish(
      final StepContext<TenantLoginProviderEnableAllInBatchCommand, TenantLoginProviderPaginableBatch> context) {
    usecase.flush();
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   */
  @Override
  public void init(
      final StepContext<TenantLoginProviderEnableAllInBatchCommand, TenantLoginProviderPaginableBatch> context) {
    context.setState(new TenantLoginProviderPaginableBatch());
  }

  /**
   * @autogenerated EntityGenerator
   * @param item
   * @param context
   * @return
   */
  @Override
  public String itemDescription(final TenantLoginProvider item,
      final StepContext<TenantLoginProviderEnableAllInBatchCommand, TenantLoginProviderPaginableBatch> context) {
    return item.getUid();
  }

  /**
   * @autogenerated EntityGenerator
   * @param item
   * @param context
   * @return
   */
  @Override
  public TenantLoginProvider process(final TenantLoginProvider item,
      final StepContext<TenantLoginProviderEnableAllInBatchCommand, TenantLoginProviderPaginableBatch> context) {
    Span span = tracer.spanBuilder(
        "Prepare individual data for the batch enable on a set of entities of tenant login provider")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      Allow allowed = usecase.checkAllow(context.getParams().getInteraction(), item);
      if (!allowed.isAllowed()) {
        span.addEvent("The policies for tenant login provider dont allow to delete: "
            + allowed.getDescription());
        throw new ExecutionException("not-allowed", null);
      }
      return item;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   * @return
   */
  @Override
  public List<TenantLoginProvider> read(
      final StepContext<TenantLoginProviderEnableAllInBatchCommand, TenantLoginProviderPaginableBatch> context) {
    Span span = tracer.spanBuilder(
        "Read selected entitites for a batch enable for a set of entities of tenant login provider")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      List<TenantLoginProvider> page =
          visibility.listVisiblesForUpdate(context.getParams().getInteraction(),
              context.getParams().getFilter(), TenantLoginProviderCursor.builder().limit(size)
                  .sinceUid(context.getState().getSince()).build());
      context.getState().setSince(page.isEmpty() ? null : page.get(page.size() - 1).getUid());
      return page;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated EntityGenerator
   * @param items
   * @param context
   */
  @Override
  public void write(final List<TenantLoginProvider> items,
      final StepContext<TenantLoginProviderEnableAllInBatchCommand, TenantLoginProviderPaginableBatch> context) {
    Span span = tracer
        .spanBuilder("Perform the batch enable for a set of entities of tenant login provider")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      span.setAttribute("size", items.size());
      items.forEach(item -> usecase.enable(context.getParams().getInteraction(), item));
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }
}
