package net.civeira.phylax.features.access.tenant.application.usecase.enable;

import java.time.Duration;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.BatchIdentificator;
import net.civeira.phylax.common.batch.BatchProgress;
import net.civeira.phylax.common.batch.BatchService;
import net.civeira.phylax.common.batch.ExecutorByDeferSteps;
import net.civeira.phylax.common.batch.ExecutorPlan;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.tenant.Tenant;
import net.civeira.phylax.features.access.tenant.TenantFacade;
import net.civeira.phylax.features.access.tenant.TenantRef;
import net.civeira.phylax.features.access.tenant.application.projection.TenantStateProyection;
import net.civeira.phylax.features.access.tenant.application.service.visibility.TenantsVisibility;
import net.civeira.phylax.features.access.tenant.gateway.TenantCacheGateway;
import net.civeira.phylax.features.access.tenant.gateway.TenantWriteRepositoryGateway;
import net.civeira.phylax.features.access.tenant.query.TenantFilter;

@ApplicationScoped
@RequiredArgsConstructor
public class TenantEnableUsecase {

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final BatchService batch;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final TenantCacheGateway cache;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final Event<TenantAllowEnableProposal> execAllowEmitter;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final Event<TenantEnableProposal> execProposalEmitter;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final Event<TenantEnableEvent> executedEmitter;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final TenantFacade facade;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final TenantWriteRepositoryGateway gateway;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final TenantsVisibility visibility;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  public Allow allow(final Interaction query, final TenantRef reference) {
    TenantAllowEnableProposal base = TenantAllowEnableProposal.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).reference(reference).build();
    execAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    TenantAllowEnableProposal base = TenantAllowEnableProposal.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).build();
    execAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * The slide with some values
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query a filter to retrieve only matching values
   * @return The slide with some values
   */
  public BatchProgress checkProgress(final TenantEnableStatus query) {
    return query.getActor().getName()
        .flatMap(name -> batch.retrieve(query.getTaskId(), query.getConnection().getLocale(), name))
        .orElseThrow(() -> new NotFoundException());
  }

  /**
   * The slide with some values
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param reference
   * @return The slide with some values
   */
  public TenantStateProyection enable(final Interaction query, final TenantRef reference) {
    Allow detail = allow(query, reference);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    Tenant original = visibility.retrieveVisibleForUpdate(query, reference.getUidValue())
        .orElseThrow(() -> new NotFoundException(""));
    Tenant saved = enable(query, original);
    flush();
    return visibility.copyWithHidden(query, saved);
  }

  /**
   * The slide with some values
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param filter
   * @return The slide with some values
   */
  public BatchIdentificator enable(final Interaction query, final TenantFilter filter) {
    Allow detail = allow(query);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    TenantEnableAllInBatchCommand command =
        TenantEnableAllInBatchCommand.builder().interaction(query).filter(filter).build();
    return batch.start(command.getInteraction().getActor().getName().orElse("-"),
        Duration.ofHours(6),
        ExecutorPlan.<TenantEnableAllInBatchCommand>builder().params(command).name("enable-color")
            .executor(
                ExecutorByDeferSteps.<Tenant, Tenant, TenantEnableAllInBatchCommand, TenantEnablesInBatchExecutor.TenantPaginableBatch>builder()
                    .initializer(TenantEnablesInBatchExecutor.class)
                    .counter(TenantEnablesInBatchExecutor.class)
                    .descriptor(TenantEnablesInBatchExecutor.class)
                    .reader(TenantEnablesInBatchExecutor.class)
                    .processor(TenantEnablesInBatchExecutor.class)
                    .writer(TenantEnablesInBatchExecutor.class)
                    .finalizer(TenantEnablesInBatchExecutor.class).build())
            .build());
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param interaction
   * @param original
   * @return
   */
  Tenant enable(final Interaction interaction, final Tenant original) {
    Tenant filled = facade.enable(original);
    TenantEnableProposal proposal =
        TenantEnableProposal.builder().entity(filled).interaction(interaction).build();
    execProposalEmitter.fire(proposal);
    Tenant result = proposal.getEntity();
    Tenant saved = gateway.update(original, result);
    TenantEnableEvent event =
        TenantEnableEvent.builder().payload(saved).interaction(interaction).build();
    executedEmitter.fire(event);
    return event.getPayload();
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  void flush() {
    cache.evict();
  }
}
