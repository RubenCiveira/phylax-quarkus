package net.civeira.phylax.features.access.securityscope.application.usecase.disable;

import java.time.Duration;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.BatchIdentificator;
import net.civeira.phylax.common.batch.BatchProgress;
import net.civeira.phylax.common.batch.BatchService;
import net.civeira.phylax.common.batch.ExecutorByDeferSteps;
import net.civeira.phylax.common.batch.ExecutorPlan;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.securityscope.SecurityScope;
import net.civeira.phylax.features.access.securityscope.SecurityScopeFacade;
import net.civeira.phylax.features.access.securityscope.SecurityScopeRef;
import net.civeira.phylax.features.access.securityscope.application.projection.SecurityScopeStateProyection;
import net.civeira.phylax.features.access.securityscope.application.service.visibility.SecurityScopesVisibility;
import net.civeira.phylax.features.access.securityscope.gateway.SecurityScopeCacheGateway;
import net.civeira.phylax.features.access.securityscope.gateway.SecurityScopeWriteRepositoryGateway;
import net.civeira.phylax.features.access.securityscope.query.SecurityScopeFilter;

@ApplicationScoped
@RequiredArgsConstructor
public class SecurityScopeDisableUsecase {

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final BatchService batch;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final SecurityScopeCacheGateway cache;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final Event<SecurityScopeAllowDisableProposal> execAllowEmitter;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final Event<SecurityScopeDisableProposal> execProposalEmitter;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final Event<SecurityScopeDisableEvent> executedEmitter;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final SecurityScopeFacade facade;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final SecurityScopeWriteRepositoryGateway gateway;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final SecurityScopesVisibility visibility;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  public Allow allow(final Interaction query, final SecurityScopeRef reference) {
    SecurityScopeAllowDisableProposal base = SecurityScopeAllowDisableProposal.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).reference(reference).build();
    execAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    SecurityScopeAllowDisableProposal base = SecurityScopeAllowDisableProposal.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).build();
    execAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * The slide with some values
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query a filter to retrieve only matching values
   * @return The slide with some values
   */
  public BatchProgress checkProgress(final SecurityScopeDisableStatus query) {
    return query.getActor().getName()
        .flatMap(name -> batch.retrieve(query.getTaskId(), query.getConnection().getLocale(), name))
        .orElseThrow(() -> new NotFoundException());
  }

  /**
   * The slide with some values
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param reference
   * @return The slide with some values
   */
  public SecurityScopeStateProyection disable(final Interaction query,
      final SecurityScopeRef reference) {
    Allow detail = allow(query, reference);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    SecurityScope original = visibility.retrieveVisibleForUpdate(query, reference.getUidValue())
        .orElseThrow(() -> new NotFoundException(""));
    SecurityScope saved = disable(query, original);
    flush();
    return visibility.copyWithHidden(query, saved);
  }

  /**
   * The slide with some values
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param filter
   * @return The slide with some values
   */
  public BatchIdentificator disable(final Interaction query, final SecurityScopeFilter filter) {
    Allow detail = allow(query);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    SecurityScopeDisableAllInBatchCommand command =
        SecurityScopeDisableAllInBatchCommand.builder().interaction(query).filter(filter).build();
    return batch.start(command.getInteraction().getActor().getName().orElse("-"),
        Duration.ofHours(6),
        ExecutorPlan.<SecurityScopeDisableAllInBatchCommand>builder().params(command)
            .name("disable-color")
            .executor(
                ExecutorByDeferSteps.<SecurityScope, SecurityScope, SecurityScopeDisableAllInBatchCommand, SecurityScopeDisablesInBatchExecutor.SecurityScopePaginableBatch>builder()
                    .initializer(SecurityScopeDisablesInBatchExecutor.class)
                    .counter(SecurityScopeDisablesInBatchExecutor.class)
                    .descriptor(SecurityScopeDisablesInBatchExecutor.class)
                    .reader(SecurityScopeDisablesInBatchExecutor.class)
                    .processor(SecurityScopeDisablesInBatchExecutor.class)
                    .writer(SecurityScopeDisablesInBatchExecutor.class)
                    .finalizer(SecurityScopeDisablesInBatchExecutor.class).build())
            .build());
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param interaction
   * @param original
   * @return
   */
  SecurityScope disable(final Interaction interaction, final SecurityScope original) {
    SecurityScope filled = facade.disable(original);
    SecurityScopeDisableProposal proposal =
        SecurityScopeDisableProposal.builder().entity(filled).interaction(interaction).build();
    execProposalEmitter.fire(proposal);
    SecurityScope result = proposal.getEntity();
    SecurityScope saved = gateway.update(original, result);
    SecurityScopeDisableEvent event =
        SecurityScopeDisableEvent.builder().payload(saved).interaction(interaction).build();
    executedEmitter.fire(event);
    return event.getPayload();
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  void flush() {
    cache.evict();
  }
}
