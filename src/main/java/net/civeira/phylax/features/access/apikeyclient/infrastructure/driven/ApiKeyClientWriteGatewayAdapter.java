package net.civeira.phylax.features.access.apikeyclient.infrastructure.driven;

import java.util.List;
import java.util.Optional;
import java.util.function.Predicate;

import jakarta.enterprise.context.ApplicationScoped;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.algorithms.Slider;
import net.civeira.phylax.features.access.apikeyclient.domain.ApiKeyClient;
import net.civeira.phylax.features.access.apikeyclient.domain.ApiKeyClientRef;
import net.civeira.phylax.features.access.apikeyclient.domain.gateway.ApiKeyClientCursor;
import net.civeira.phylax.features.access.apikeyclient.domain.gateway.ApiKeyClientFilter;
import net.civeira.phylax.features.access.apikeyclient.domain.gateway.ApiKeyClientWriteRepositoryGateway;
import net.civeira.phylax.features.access.apikeyclient.infrastructure.event.ApiKeyClientEventDispacher;
import net.civeira.phylax.features.access.apikeyclient.infrastructure.repository.ApiKeyClientRepository;

@ApplicationScoped
@RequiredArgsConstructor
public class ApiKeyClientWriteGatewayAdapter implements ApiKeyClientWriteRepositoryGateway {

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   */
  private final ApiKeyClientEventDispacher eventDispacher;

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   */
  private final ApiKeyClientRepository repository;

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param filter
   * @return
   */
  @Override
  public long countForUpdate(ApiKeyClientFilter filter) {
    return repository.count(filter);
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param entity
   * @return
   */
  @Override
  public ApiKeyClient create(ApiKeyClient entity) {
    ApiKeyClient result = repository.create(entity);
    eventDispacher.dispatch(entity);
    return result;
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param entity
   * @param verifier
   * @return
   */
  @Override
  public ApiKeyClient create(ApiKeyClient entity, Predicate<ApiKeyClient> verifier) {
    ApiKeyClient result = repository.create(entity, verifier);
    eventDispacher.dispatch(entity);
    return result;
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param entity
   */
  @Override
  public void delete(ApiKeyClient entity) {
    repository.delete(entity);
    eventDispacher.dispatch(entity);
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param uid
   * @param filter
   * @return
   */
  @Override
  public boolean existsForUpdate(String uid, Optional<ApiKeyClientFilter> filter) {
    return repository.existsForUpdate(uid, filter);
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param filter
   * @return
   */
  @Override
  public Optional<ApiKeyClient> findForUpdate(ApiKeyClientFilter filter) {
    return repository.findForUpdate(filter);
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param filter
   * @return
   */
  @Override
  public List<ApiKeyClient> listForUpdate(ApiKeyClientFilter filter) {
    return repository.listForUpdate(filter);
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param reference
   * @return
   */
  @Override
  public ApiKeyClient resolveForUpdate(ApiKeyClientRef reference) {
    return repository.resolveForUpdate(reference);
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param uid
   * @param filter
   * @return
   */
  @Override
  public Optional<ApiKeyClient> retrieveForUpdate(String uid, Optional<ApiKeyClientFilter> filter) {
    return repository.retrieveForUpdate(uid, filter);
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param filter
   * @param cursor
   * @return
   */
  @Override
  public Slider<ApiKeyClient> slideForUpdate(ApiKeyClientFilter filter, ApiKeyClientCursor cursor) {
    return repository.slideForUpdate(filter, cursor);
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param reference
   * @param entity
   * @return
   */
  @Override
  public ApiKeyClient update(ApiKeyClientRef reference, ApiKeyClient entity) {
    ApiKeyClient result = repository.update(entity);
    eventDispacher.dispatch(entity);
    return result;
  }
}
