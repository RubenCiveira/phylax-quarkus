package net.civeira.phylax.features.access.trustedclient.application.usecase.delete;

import java.time.Duration;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.SpanKind;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Context;
import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.BatchIdentificator;
import net.civeira.phylax.common.batch.BatchProgress;
import net.civeira.phylax.common.batch.BatchService;
import net.civeira.phylax.common.batch.ExecutorByDeferSteps;
import net.civeira.phylax.common.batch.ExecutorPlan;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.trustedclient.application.visibility.TrustedClientVisibilityFilter;
import net.civeira.phylax.features.access.trustedclient.application.visibility.TrustedClientsVisibility;
import net.civeira.phylax.features.access.trustedclient.domain.TrustedClient;
import net.civeira.phylax.features.access.trustedclient.domain.TrustedClientRef;
import net.civeira.phylax.features.access.trustedclient.domain.gateway.TrustedClientAuditGateway;
import net.civeira.phylax.features.access.trustedclient.domain.gateway.TrustedClientCacheGateway;
import net.civeira.phylax.features.access.trustedclient.domain.gateway.TrustedClientWriteRepositoryGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class TrustedClientDeleteUsecase {

  /**
   * Service to register audit events
   *
   * @autogenerated DeleteUsecaseGenerator
   */
  private final TrustedClientAuditGateway audit;

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  private final BatchService batch;

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  private final TrustedClientCacheGateway cache;

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  private final Event<TrustedClientDeleteAllowDecision> deleteAllowEmitter;

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  private final Event<TrustedClientDeleteCheck> deleteCheckEmitter;

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  private final TrustedClientWriteRepositoryGateway gateway;

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  private final TrustedClientsVisibility visibility;

  /**
   * @autogenerated DeleteUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  public Allow allow(final Interaction query, final TrustedClientRef reference) {
    Span span = tracer.spanBuilder("Checking if is allowed to delete an entity of trusted client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      span.setAttribute("ref", reference.getUid());
      Allow response = checkAllow(query, reference);
      span.setAttribute("allowed", response.isAllowed());
      return response;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated DeleteUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    Span span = tracer.spanBuilder("Checking if is allowed to delete an entity of trusted client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      Allow response = checkAllow(query);
      span.setAttribute("allowed", response.isAllowed());
      return response;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated DeleteUsecaseGenerator
   * @param query a filter to retrieve only matching values
   * @return The slide with some values
   */
  public BatchProgress checkProgress(final TrustedClientCheckBatchDeleteStatus query) {
    return query.getActor().getName()
        .flatMap(name -> batch.retrieve(query.getTaskId(), query.getConnection().getLocale(), name))
        .orElseThrow(() -> new NotFoundException());
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated DeleteUsecaseGenerator
   * @param query
   * @param reference
   */
  public void delete(final Interaction query, final TrustedClientRef reference) {
    Span span = tracer.spanBuilder("Deleting an entity of trusted client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      span.setAttribute("ref", reference.getUid());
      Allow detail = checkAllow(query, reference);
      if (!detail.isAllowed()) {
        span.addEvent(
            "The policies for trusted client dont allow to delete: " + detail.getDescription());
        throw new NotAllowedException(detail.getDescription());
      }
      TrustedClient original = visibility.retrieveVisibleForUpdate(query, reference.getUid())
          .orElseThrow(() -> new NotFoundException(""));
      delete(query, original);
      flush();
      // Register audit operation
      audit.deleted("delete", original, query);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated DeleteUsecaseGenerator
   * @param query
   * @param filter
   * @return The slide with some values
   */
  public BatchIdentificator delete(final Interaction query,
      final TrustedClientDeleteFilter filter) {
    Span span = tracer.spanBuilder("Deleting a set of entities of trusted client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      Allow detail = checkAllow(query);
      if (!detail.isAllowed()) {
        span.addEvent(
            "The policies for trusted client dont allow to delete: " + detail.getDescription());
        throw new NotAllowedException(detail.getDescription());
      }
      TrustedClientVisibilityFilter filterOnVisibles = TrustedClientVisibilityFilter.builder()
          .uid(filter.getUid().orElse(null)).uids(filter.getUids().stream().toList())
          .search(filter.getSearch().orElse(null)).code(filter.getCode().orElse(null)).build();
      TrustedClientDeleteAllInBatchCommand command = TrustedClientDeleteAllInBatchCommand.builder()
          .interaction(query).filter(filterOnVisibles).build();
      return batch.start(query.getActor().getName().orElse("-"), Duration.ofHours(6), ExecutorPlan
          .<TrustedClientDeleteAllInBatchCommand>builder().params(command).name("delete-color")
          .executor(
              ExecutorByDeferSteps.<TrustedClient, TrustedClient, TrustedClientDeleteAllInBatchCommand, TrustedClientsInBatchExecutor.TrustedClientPaginableBatch>builder()
                  .initializer(TrustedClientsInBatchExecutor.class)
                  .counter(TrustedClientsInBatchExecutor.class)
                  .descriptor(TrustedClientsInBatchExecutor.class)
                  .reader(TrustedClientsInBatchExecutor.class)
                  .processor(TrustedClientsInBatchExecutor.class)
                  .writer(TrustedClientsInBatchExecutor.class)
                  .finalizer(TrustedClientsInBatchExecutor.class).build())
          .build());
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated DeleteUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  Allow checkAllow(final Interaction query, final TrustedClientRef reference) {
    TrustedClientDeleteAllowDecision base = TrustedClientDeleteAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).reference(reference).build();
    deleteAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated DeleteUsecaseGenerator
   * @param query
   * @return
   */
  Allow checkAllow(final Interaction query) {
    TrustedClientDeleteAllowDecision base = TrustedClientDeleteAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).build();
    deleteAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated DeleteUsecaseGenerator
   * @param interaction
   * @param original
   */
  void delete(final Interaction interaction, final TrustedClient original) {
    TrustedClientDeleteCheck proposal =
        TrustedClientDeleteCheck.builder().reference(original).interaction(interaction).build();
    deleteCheckEmitter.fire(proposal);
    TrustedClient clean = original.delete();
    gateway.delete(clean);
  }

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  void flush() {
    cache.evict();
  }
}
