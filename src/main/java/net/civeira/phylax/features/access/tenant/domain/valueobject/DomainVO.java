package net.civeira.phylax.features.access.tenant.domain.valueobject;

import lombok.EqualsAndHashCode;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.ConstraintException;
import net.civeira.phylax.common.value.validation.AbstractFailList;
import net.civeira.phylax.common.value.validation.ConstraintFail;
import net.civeira.phylax.common.value.validation.ConstraintFailList;

/**
 * Value Object representing a domain suffix to identify the account of tenant.
 * <p>
 * This class ensures that the domain is a non-null {@code String} and encapsulates its validation
 * and formatting logic. It is immutable and compliant with DDD (Domain-Driven Design) principles.
 * </p>
 * <p>
 * Use {@link #from(String)} or {@link #tryFrom(Object)} to safely create instances. Validation
 * errors are reported via {@link ConstraintFailList} or thrown as {@link ConstraintException}.
 * </p>
 */
@RequiredArgsConstructor
@EqualsAndHashCode
public class DomainVO {

  /**
   * Crear an empty instance with no value
   *
   * @autogenerated ValueObjectGenerator
   * @param domain
   * @return An empty instance
   */
  public static DomainVO from(final String domain) {
    return tryFrom(domain);
  }

  /**
   * Crear an empty instance with no value
   *
   * @autogenerated ValueObjectGenerator
   * @param domain
   * @return An empty instance
   */
  public static DomainVO tryFrom(final Object domain) {
    ConstraintFailList list = new ConstraintFailList();
    DomainVO result = tryFrom(domain, list);
    if (list.hasErrors()) {
      throw new ConstraintException(list);
    }
    return result;
  }

  /**
   * Crear an empty instance with no value
   *
   * @autogenerated ValueObjectGenerator
   * @param domain temptative value
   * @param fails Error list
   * @return An empty instance
   */
  public static <T extends AbstractFailList> DomainVO tryFrom(final Object domain, final T fails) {
    if (null == domain) {
      fails.add(new ConstraintFail("not-null", "domain", null, "Cant be null"));
      return null;
    } else if (domain instanceof String castedDomain) {
      return new DomainVO(castedDomain);
    } else {
      fails.add(new ConstraintFail("wrong-type", "domain", domain.getClass(),
          "A String type is expected for domain"));
      return null;
    }
  }

  /**
   * The actual value of the name. * It is guaranteed to be a non-null String after successful
   * value.validation.
   *
   * @autogenerated ValueObjectGenerator
   */
  private final String value;

  /**
   * Get the vo value
   *
   * @autogenerated ValueObjectGenerator
   * @return
   */
  public String getDomain() {
    return value;
  }

  /**
   * Returns a formatted string representation of the name.
   *
   * @autogenerated ValueObjectGenerator
   * @return the string value wrapped in square brackets.
   */
  @Override
  public String toString() {
    return "[" + value + "]";
  }
}
