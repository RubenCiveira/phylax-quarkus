import jakarta.enterprise.context.ApplicationScoped;
import jakarta.transaction.Transactional;
import jakarta.ws.rs.core.Response;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.BatchIdentificator;
import net.civeira.phylax.common.batch.BatchProgress;
import net.civeira.phylax.common.batch.BatchProgress.GlobalStatus;
import net.civeira.phylax.common.batch.BatchStepProgress.ErrorInfo;
import net.civeira.phylax.common.batch.BatchStepProgress.Status;
import net.civeira.phylax.common.infrastructure.CurrentRequest;
import net.civeira.phylax.common.telemetry.ApiObserved;
import net.civeira.phylax.common.telemetry.Observed;
import net.civeira.phylax.common.telemetry.Trace;
import net.civeira.phylax.features.access.tenant.domain.TenantReference;
import net.civeira.phylax.features.access.tenanttermsofuse.application.usecase.disable.TenantTermsOfUseDisableFilter;
import net.civeira.phylax.features.access.tenanttermsofuse.application.usecase.disable.TenantTermsOfUseDisableProjection;
import net.civeira.phylax.features.access.tenanttermsofuse.application.usecase.disable.TenantTermsOfUseDisableStatus;
import net.civeira.phylax.features.access.tenanttermsofuse.application.usecase.disable.TenantTermsOfUseDisableUsecase;
import net.civeira.phylax.features.access.tenanttermsofuse.domain.TenantTermsOfUseReference;
