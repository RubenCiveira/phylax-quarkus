import net.civeira.phylax.generated.openapi.model.TenantApiRef;
import net.civeira.phylax.generated.openapi.model.TenantTermsOfUseApiDto;

@RequiredArgsConstructor
@ApplicationScoped
public class TenantTermsOfUseDisableController {

  /**
   * @autogenerated ActionControllerGenerator
   */
  private final CurrentRequest currentRequest;

  /**
   * @autogenerated ActionControllerGenerator
   */
  private final TenantTermsOfUseDisableUsecase disable;

  /**
   * @autogenerated ActionControllerGenerator
   * @param uids
   * @param search
   * @param tenant
   * @param tenants
   * @return
   */
  @Observed("Api to disable on massive entity of tenant terms of use")
  public Response tenantTermsOfUseApiBatchDisable(final List<String> uids, final String search,
      final String tenant, final List<String> tenants) {
    TenantTermsOfUseDisableFilter.TenantTermsOfUseDisableFilterBuilder filterBuilder =
        TenantTermsOfUseDisableFilter.builder();
    filterBuilder =
        filterBuilder.uids(uids.stream().flatMap(part -> Stream.of(part.split(","))).toList());
    filterBuilder = filterBuilder.search(search);
    filterBuilder = filterBuilder.uids(
        null == uids ? null : uids.stream().flatMap(part -> Stream.of(part.split(","))).toList());
    filterBuilder = filterBuilder.search(search);
    if (null != tenant) {
      filterBuilder = filterBuilder.tenant(TenantReference.of(tenant));
    }
    filterBuilder = filterBuilder.tenants(null == tenants ? null
        : tenants.stream().flatMap(part -> Stream.of(part.split(","))).toList());
    TenantTermsOfUseDisableFilter filter = filterBuilder.build();
    BatchIdentificator task = disable.batchDisable(currentRequest.interaction(), filter);
    /* .header("Last-Modified", value.getSince().format(DateTimeFormatter.RFC_1123_DATE_TIME)) */
    BatchTaskLocalizator response = new BatchTaskLocalizator();
    response.setUid(task.getUid());
    response.setSteps(task.getSteps());
    return Response.ok(response).build();
  }

  /**
   * @autogenerated ActionControllerGenerator
   * @param batchId
   * @return
   */
  @Observed("Api to check masive disable on entity of tenant terms of use")
  public Response tenantTermsOfUseApiBatchDisableQuery(final String batchId) {
    BatchProgress task = disable.checkProgress(TenantTermsOfUseDisableStatus.builder()
        .taskId(batchId).build(currentRequest.interaction()));
    BatchTaskDetail response = new BatchTaskDetail();
    response.setUid(task.getUid());
    GlobalStatus taskStatus = task.getStatus();
    if (null != taskStatus) {
      response.setDetailStatus(DetailStatusEnum.valueOf(taskStatus.name()));
    }
    Instant taskStart = task.getStartTime();
    if (null != taskStart) {
      response.setStartTime(OffsetDateTime.ofInstant(taskStart, ZoneId.systemDefault()));
    }
    Instant taskEnd = task.getEndTime();
    if (null != taskEnd) {
      response.setEndTime(OffsetDateTime.ofInstant(taskEnd, ZoneId.systemDefault()));
    }
    response.setSteps(task.getSteps().stream().map(step -> {
      BatchStepProgress bsp = new BatchStepProgress();
      Status stepStatus = step.getStatus();
      if (null != stepStatus) {
        bsp.setStepStatus(StepStatusEnum.valueOf(stepStatus.name()));
      }
      Instant stepStart = step.getStartTime();
      if (null != stepStart) {
        bsp.setStartTime(OffsetDateTime.ofInstant(stepStart, ZoneId.systemDefault()));
      }
      Instant stepEnd = step.getEndTime();
      if (null != stepEnd) {
        bsp.setEndTime(OffsetDateTime.ofInstant(stepEnd, ZoneId.systemDefault()));
      }
      bsp.setError(step.getError());
      bsp.setName(step.getName());
      bsp.setOks(step.getOks());
      bsp.setProcessedItems(step.getProcessedItems());
      bsp.setTotalItems(step.getTotalItems());
      bsp.setErrors(new ArrayList<>());
      bsp.setWarns(new ArrayList<>());
      Map<String, ErrorInfo> stepErrors = step.getErrors();
      if (null != stepErrors) {
        bsp.getErrors()
            .addAll(stepErrors.entrySet().stream()
                .map(entry -> new BatchResultErrorItem().item(entry.getKey())
                    .fails(entry.getValue().getFails().stream()
                        .map(fail -> new MultipleErrorCodes().code(fail.getCode())
                            .violation(fail.getViolation())
                            .wrongValues(fail.getWrongValues().stream()
                                .map(wv -> new ConstraintFail().error(wv.getErrorMessage())
                                    .field(wv.getField()).wrong(String.valueOf(wv.getWrongValue())))
                                .toList()))
                        .toList()))
                .toList());
      }
      Map<String, ErrorInfo> stepWarns = step.getWarns();
      if (null != stepWarns) {
        bsp.getWarns()
            .addAll(stepErrors.entrySet().stream()
                .map(entry -> new BatchResultErrorItem().item(entry.getKey())
                    .fails(entry.getValue().getFails().stream()
                        .map(fail -> new MultipleErrorCodes().code(fail.getCode())
                            .violation(fail.getViolation())
                            .wrongValues(fail.getWrongValues().stream()
                                .map(wv -> new ConstraintFail().error(wv.getErrorMessage())
                                    .field(wv.getField()).wrong(String.valueOf(wv.getWrongValue())))
                                .toList()))
                        .toList()))
                .toList());
      }
      return bsp;
    }).toList());
    return Response.ok(response).build();
  }

  /**
   * @autogenerated ActionControllerGenerator
   * @param uid
   * @return
   */
  @Transactional
  @ApiObserved("Api to disable an entity of tenant terms of use")
  public Response tenantTermsOfUseApiDisable(final String uid) {
    Trace.setAttribute("ref", uid);
    TenantTermsOfUseDisableProjection updated =
        disable.disable(currentRequest.interaction(), TenantTermsOfUseReference.of(uid));
    return Response.ok(toApiModel(updated)).build();
  }

  /**
   * @autogenerated ActionControllerGenerator
   * @param dto
   * @return
   */
  private TenantTermsOfUseApiDto toApiModel(TenantTermsOfUseDisableProjection dto) {
    TenantTermsOfUseApiDto tenantTermsOfUseApiDto = new TenantTermsOfUseApiDto();
    tenantTermsOfUseApiDto.setUid(dto.getUid());
    tenantTermsOfUseApiDto.setTenant(new TenantApiRef().$ref(dto.getTenantReference()));
    tenantTermsOfUseApiDto.setText(dto.getText());
    tenantTermsOfUseApiDto.setEnabled(dto.getEnabled());
    String attached = dto.getAttached();
    if (null != attached) {
      tenantTermsOfUseApiDto.setAttached(currentRequest.getPublicHost()
          + "/api/access/tenants-terms-of-use/" + dto.getUidOrDefault("-") + "/attached");
    }
    tenantTermsOfUseApiDto.setActivationDate(dto.getActivationDate());
    tenantTermsOfUseApiDto.setVersion(dto.getVersion());
    return tenantTermsOfUseApiDto;
  }
}
