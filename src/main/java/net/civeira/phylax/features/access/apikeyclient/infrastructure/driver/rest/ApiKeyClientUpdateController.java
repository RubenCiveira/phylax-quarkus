package net.civeira.phylax.features.access.apikeyclient.infrastructure.driver.rest;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.SpanKind;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Context;
import jakarta.enterprise.context.ApplicationScoped;
import jakarta.transaction.Transactional;
import jakarta.ws.rs.core.Response;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.infrastructure.CurrentRequest;
import net.civeira.phylax.features.access.apikeyclient.application.usecase.update.ApiKeyClientUpdateInput;
import net.civeira.phylax.features.access.apikeyclient.application.usecase.update.ApiKeyClientUpdateProjection;
import net.civeira.phylax.features.access.apikeyclient.application.usecase.update.ApiKeyClientUpdateUsecase;
import net.civeira.phylax.features.access.apikeyclient.domain.ApiKeyClientReference;
import net.civeira.phylax.generated.openapi.model.ApiKeyClientApiDto;

@RequiredArgsConstructor
@ApplicationScoped
public class ApiKeyClientUpdateController {

  /**
   * @autogenerated UpdateControllerGenerator
   */
  private final CurrentRequest currentRequest;

  /**
   * @autogenerated UpdateControllerGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated UpdateControllerGenerator
   */
  private final ApiKeyClientUpdateUsecase update;

  /**
   * @autogenerated UpdateControllerGenerator
   * @param uid
   * @param apiKeyClient
   * @return
   */
  @Transactional
  public Response apiKeyClientApiUpdate(final String uid, final ApiKeyClientApiDto apiKeyClient) {
    Span span = tracer.spanBuilder("Api to retrieve an entity of api key client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      span.setAttribute("ref", uid);
      ApiKeyClientUpdateProjection updated = update.update(currentRequest.interaction(),
          ApiKeyClientReference.of(uid), toDomainModel(apiKeyClient));
      return Response.ok(toApiModel(updated)).build();
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated UpdateControllerGenerator
   * @param dto
   * @return
   */
  private ApiKeyClientApiDto toApiModel(ApiKeyClientUpdateProjection dto) {
    Span span = tracer
        .spanBuilder(
            "Mapper to build api content to response from model for entity of api key client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      ApiKeyClientApiDto apiKeyClientApiDto = new ApiKeyClientApiDto();
      apiKeyClientApiDto.setUid(dto.getUid());
      apiKeyClientApiDto.setCode(dto.getCode());
      apiKeyClientApiDto.setKey(dto.getKey());
      apiKeyClientApiDto.setEnabled(dto.getEnabled());
      apiKeyClientApiDto.setScopes(dto.getScopes());
      apiKeyClientApiDto.setVersion(dto.getVersion());
      return apiKeyClientApiDto;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated UpdateControllerGenerator
   * @param apiKeyClientApiDto
   * @return
   */
  private ApiKeyClientUpdateInput toDomainModel(ApiKeyClientApiDto apiKeyClientApiDto) {
    Span span = tracer
        .spanBuilder(
            "Mapper to build model content to request from api for entity of api key client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      ApiKeyClientUpdateInput dto = new ApiKeyClientUpdateInput();
      if (null != apiKeyClientApiDto.getUid()) {
        dto.setUid(apiKeyClientApiDto.getUid());
      }
      if (null != apiKeyClientApiDto.getCode()) {
        dto.setCode(apiKeyClientApiDto.getCode());
      }
      if (null != apiKeyClientApiDto.getKey()) {
        dto.setKey(apiKeyClientApiDto.getKey());
      }
      if (null != apiKeyClientApiDto.getEnabled()) {
        dto.setEnabled(apiKeyClientApiDto.getEnabled());
      }
      if (null != apiKeyClientApiDto.getScopes()) {
        dto.setScopes(apiKeyClientApiDto.getScopes());
      }
      if (null != apiKeyClientApiDto.getVersion()) {
        dto.setVersion(apiKeyClientApiDto.getVersion());
      }
      return dto;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }
}
