package net.civeira.phylax.features.access.tenantconfig.infrastructure.driven;

import jakarta.enterprise.context.ApplicationScoped;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.infrastructure.audit.AuditEvent;
import net.civeira.phylax.common.infrastructure.audit.AuditWriteService;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.tenantconfig.domain.TenantConfig;
import net.civeira.phylax.features.access.tenantconfig.domain.gateway.TenantConfigAuditGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class TenantConfigAuditAdapter implements TenantConfigAuditGateway {

  /**
   * @autogenerated AuditAdapterGenerator
   */
  private final AuditWriteService writer;

  /**
   * @autogenerated AuditAdapterGenerator
   * @param usecase The usecase responsible to the action
   * @param tenantConfig
   * @param interaction The interaction
   */
  @Override
  public void created(final String usecase, final TenantConfig tenantConfig,
      final Interaction interaction) {
    writer.record(AuditEvent.builder().operation("create").usecase(usecase)
        .entityType("tenantConfig").entityId(tenantConfig.getUid()).newValue(tenantConfig.toMap())
        .performedBy(interaction.getActor().getName().orElse("<<no-user>>"))
        .tenant(interaction.getActor().getTenant().orElse("<<no-tenant>>"))
        .timestamp(interaction.getConnection().getStartTime())
        .sourceRequest(interaction.getConnection().getRequest())
        .remoteAddress(interaction.getConnection().getRemote().orElse("<<no-device>>"))
        .remoteApplication(
            interaction.getConnection().getRemoteApplication().orElse("<<no-application>>"))
        .remoteDevice(interaction.getConnection().getRemoteDevice().orElse("<<no-device>>"))
        .claims(interaction.getActor().getClaims()).build());
  }

  /**
   * @autogenerated AuditAdapterGenerator
   * @param usecase The usecase responsible to the action
   * @param tenantConfig
   * @param interaction The interaction
   */
  @Override
  public void deleted(final String usecase, final TenantConfig tenantConfig,
      final Interaction interaction) {
    writer.record(AuditEvent.builder().operation("delete").usecase(usecase)
        .entityType("tenantConfig").entityId(tenantConfig.getUid()).oldValue(tenantConfig.toMap())
        .performedBy(interaction.getActor().getName().orElse("<<no-user>>"))
        .tenant(interaction.getActor().getTenant().orElse("<<no-tenant>>"))
        .timestamp(interaction.getConnection().getStartTime())
        .sourceRequest(interaction.getConnection().getRequest())
        .remoteAddress(interaction.getConnection().getRemote().orElse("<<no-device>>"))
        .remoteApplication(
            interaction.getConnection().getRemoteApplication().orElse("<<no-application>>"))
        .remoteDevice(interaction.getConnection().getRemoteDevice().orElse("<<no-device>>"))
        .claims(interaction.getActor().getClaims()).build());
  }

  /**
   * @autogenerated AuditAdapterGenerator
   * @param usecase The usecase responsible to the action
   * @param tenantConfig
   * @param tenantConfigOriginal
   * @param interaction The interaction
   */
  @Override
  public void updated(final String usecase, final TenantConfig tenantConfig,
      final TenantConfig tenantConfigOriginal, final Interaction interaction) {
    writer.record(AuditEvent.builder().operation("update").usecase(usecase)
        .entityType("tenantConfig").entityId(tenantConfig.getUid()).newValue(tenantConfig.toMap())
        .oldValue(tenantConfigOriginal.toMap())
        .performedBy(interaction.getActor().getName().orElse("<<no-user>>"))
        .tenant(interaction.getActor().getTenant().orElse("<<no-tenant>>"))
        .timestamp(interaction.getConnection().getStartTime())
        .sourceRequest(interaction.getConnection().getRequest())
        .remoteAddress(interaction.getConnection().getRemote().orElse("<<no-device>>"))
        .remoteApplication(
            interaction.getConnection().getRemoteApplication().orElse("<<no-application>>"))
        .remoteDevice(interaction.getConnection().getRemoteDevice().orElse("<<no-device>>"))
        .claims(interaction.getActor().getClaims()).build());
  }
}
