package net.civeira.phylax.features.access.trustedclient.application.usecase.retrieve;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.trustedclient.application.visibility.TrustedClientsVisibility;
import net.civeira.phylax.features.access.trustedclient.domain.TrustedClientRef;
import net.civeira.phylax.features.access.trustedclient.domain.gateway.TrustedClientCached;

@ApplicationScoped
@RequiredArgsConstructor
public class TrustedClientRetrieveUsecase {

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final Event<TrustedClientRetrieveAllowDecision> retrieveAllowEmitter;

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final TrustedClientsVisibility visibility;

  /**
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  public Allow allow(final Interaction query, final TrustedClientRef reference) {
    TrustedClientRetrieveAllowDecision base = TrustedClientRetrieveAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).reference(reference).build();
    retrieveAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    TrustedClientRetrieveAllowDecision base = TrustedClientRetrieveAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).build();
    retrieveAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @param reference
   * @return The slide with some values
   */
  public TrustedClientRetrieveProjection retrieve(final Interaction query,
      final TrustedClientRef reference) {
    Allow detail = allow(query, reference);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    TrustedClientCached retrieveCachedVisible =
        visibility.retrieveCachedVisible(query, reference.getUid());
    return retrieveCachedVisible.first().map(first -> visibility.copyWithHidden(query, first))
        .map(TrustedClientRetrieveProjection::from).orElseThrow(() -> new NotFoundException(""));
  }
}
