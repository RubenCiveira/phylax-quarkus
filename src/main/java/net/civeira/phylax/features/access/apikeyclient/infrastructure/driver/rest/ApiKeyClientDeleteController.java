package net.civeira.phylax.features.access.apikeyclient.infrastructure.driver.rest;

import java.util.List;
import java.util.stream.Stream;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.SpanKind;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Context;
import jakarta.enterprise.context.ApplicationScoped;
import jakarta.transaction.Transactional;
import jakarta.ws.rs.core.Response;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.BatchIdentificator;
import net.civeira.phylax.common.batch.BatchProgress;
import net.civeira.phylax.common.infrastructure.CurrentRequest;
import net.civeira.phylax.features.access.apikeyclient.application.usecase.delete.ApiKeyClientCheckBatchDeleteStatus;
import net.civeira.phylax.features.access.apikeyclient.application.usecase.delete.ApiKeyClientDeleteFilter;
import net.civeira.phylax.features.access.apikeyclient.application.usecase.delete.ApiKeyClientDeleteUsecase;
import net.civeira.phylax.features.access.apikeyclient.domain.ApiKeyClientReference;

@RequiredArgsConstructor
@ApplicationScoped
public class ApiKeyClientDeleteController {

  /**
   * @autogenerated DeleteControllerGenerator
   */
  private final CurrentRequest currentRequest;

  /**
   * @autogenerated DeleteControllerGenerator
   */
  private final ApiKeyClientDeleteUsecase delete;

  /**
   * @autogenerated DeleteControllerGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated DeleteControllerGenerator
   * @param uids
   * @param search
   * @param key
   * @return
   */
  public Response apiKeyClientApiBatchDelete(final List<String> uids, final String search,
      final String key) {
    Span span = tracer.spanBuilder("Api to delete masive entities of api key client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      ApiKeyClientDeleteFilter.ApiKeyClientDeleteFilterBuilder filterBuilder =
          ApiKeyClientDeleteFilter.builder();
      filterBuilder =
          filterBuilder.uids(uids.stream().flatMap(part -> Stream.of(part.split(","))).toList());
      filterBuilder = filterBuilder.search(search);
      filterBuilder = filterBuilder.uids(
          null == uids ? null : uids.stream().flatMap(part -> Stream.of(part.split(","))).toList());
      filterBuilder = filterBuilder.search(search);
      filterBuilder = filterBuilder.key(key);
      ApiKeyClientDeleteFilter filter = filterBuilder.build();
      BatchIdentificator task = delete.delete(currentRequest.interaction(), filter);
      /* .header("Last-Modified", value.getSince().format(DateTimeFormatter.RFC_1123_DATE_TIME)) */
      return Response.ok(task).build();
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated DeleteControllerGenerator
   * @param batchId
   * @return
   */
  public Response apiKeyClientApiBatchDeleteQuery(final String batchId) {
    Span span = tracer
        .spanBuilder("Api to check the status of massive delete for entities of api key client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      BatchProgress task = delete.checkProgress(ApiKeyClientCheckBatchDeleteStatus.builder()
          .taskId(batchId).build(currentRequest.interaction()));
      return Response.ok(task).build();
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated DeleteControllerGenerator
   * @param uid
   * @return
   */
  @Transactional
  public Response apiKeyClientApiDelete(final String uid) {
    Span span = tracer.spanBuilder("Api to delete an entity of api key client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      span.setAttribute("ref", uid);
      delete.delete(currentRequest.interaction(), ApiKeyClientReference.of(uid));
      return Response.noContent().build();
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }
}
