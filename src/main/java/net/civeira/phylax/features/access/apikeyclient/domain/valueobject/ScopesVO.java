package net.civeira.phylax.features.access.apikeyclient.domain.valueobject;

import java.util.Optional;

import lombok.EqualsAndHashCode;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.ConstraintException;
import net.civeira.phylax.common.value.validation.AbstractFailList;
import net.civeira.phylax.common.value.validation.ConstraintFail;
import net.civeira.phylax.common.value.validation.ConstraintFailList;

/**
 * Value Object representing a coma separated scopes for the api key of apiKeyClient.
 * <p>
 * This class ensures that the scopes is a {@code String} and encapsulates its validation and
 * formatting logic. It is immutable and compliant with DDD (Domain-Driven Design) principles.
 * </p>
 * <p>
 * Use {@link #from(String)} or {@link #tryFrom(Object)} to safely create instances. Validation
 * errors are reported via {@link ConstraintFailList} or thrown as {@link ConstraintException}.
 * </p>
 */
@RequiredArgsConstructor
@EqualsAndHashCode
public class ScopesVO {

  /**
   * Crear an empty instance with no value
   *
   * @autogenerated ValueObjectGenerator
   * @param scopes
   * @return An empty instance
   */
  public static ScopesVO from(final String scopes) {
    return tryFrom(scopes);
  }

  /**
   * Crear an empty instance with no value
   *
   * @autogenerated ValueObjectGenerator
   * @return An empty instance
   */
  public static ScopesVO nullValue() {
    return new ScopesVO(null);
  }

  /**
   * Crear an empty instance with no value
   *
   * @autogenerated ValueObjectGenerator
   * @param scopes
   * @return An empty instance
   */
  public static ScopesVO tryFrom(final Object scopes) {
    ConstraintFailList list = new ConstraintFailList();
    ScopesVO result = tryFrom(scopes, list);
    if (list.hasErrors()) {
      throw new ConstraintException(list);
    }
    return result;
  }

  /**
   * Crear an empty instance with no value
   *
   * @autogenerated ValueObjectGenerator
   * @param scopes temptative value
   * @param fails Error list
   * @return An empty instance
   */
  public static <T extends AbstractFailList> ScopesVO tryFrom(final Object scopes, final T fails) {
    if (null == scopes) {
      return new ScopesVO(null);
    } else if (scopes instanceof String castedScopes) {
      return new ScopesVO(castedScopes);
    } else {
      fails.add(new ConstraintFail("wrong-type", "scopes", scopes.getClass(),
          "A String type is expected for scopes"));
      return null;
    }
  }

  /**
   * The actual value of the name.
   *
   * @autogenerated ValueObjectGenerator
   */
  private final String value;

  /**
   * Get the vo value
   *
   * @autogenerated ValueObjectGenerator
   * @return
   */
  public Optional<String> getScopes() {
    return Optional.ofNullable(value);
  }

  /**
   * Returns a formatted string representation of the name.
   *
   * @autogenerated ValueObjectGenerator
   * @return the string value wrapped in square brackets.
   */
  @Override
  public String toString() {
    return "[" + value + "]";
  }
}
