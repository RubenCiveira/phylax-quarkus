package net.civeira.phylax.features.access.scopeassignation.application.usecase.delete;

import java.util.List;

import io.quarkus.arc.Unremovable;
import jakarta.enterprise.context.ApplicationScoped;
import lombok.Data;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.stepper.ItemDescriptor;
import net.civeira.phylax.common.batch.stepper.ItemProcessor;
import net.civeira.phylax.common.batch.stepper.ItemReader;
import net.civeira.phylax.common.batch.stepper.ItemWriter;
import net.civeira.phylax.common.batch.stepper.StepContext;
import net.civeira.phylax.common.batch.stepper.StepCounter;
import net.civeira.phylax.common.batch.stepper.StepFinalizer;
import net.civeira.phylax.common.batch.stepper.StepInitializer;
import net.civeira.phylax.common.exception.ExecutionException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.features.access.scopeassignation.ScopeAssignation;
import net.civeira.phylax.features.access.scopeassignation.application.service.visibility.ScopeAssignationsVisibility;
import net.civeira.phylax.features.access.scopeassignation.query.ScopeAssignationCursor;

@Unremovable
@ApplicationScoped
@RequiredArgsConstructor
class ScopeAssignationsInBatchExecutor implements
    StepCounter<ScopeAssignationDeleteAllInBatchCommand, ScopeAssignationsInBatchExecutor.ScopeAssignationPaginableBatch>,
    StepFinalizer<ScopeAssignationDeleteAllInBatchCommand, ScopeAssignationsInBatchExecutor.ScopeAssignationPaginableBatch>,
    StepInitializer<ScopeAssignationDeleteAllInBatchCommand, ScopeAssignationsInBatchExecutor.ScopeAssignationPaginableBatch>,
    ItemReader<ScopeAssignation, ScopeAssignationDeleteAllInBatchCommand, ScopeAssignationsInBatchExecutor.ScopeAssignationPaginableBatch>,
    ItemWriter<ScopeAssignation, ScopeAssignationDeleteAllInBatchCommand, ScopeAssignationsInBatchExecutor.ScopeAssignationPaginableBatch>,
    ItemProcessor<ScopeAssignation, ScopeAssignation, ScopeAssignationDeleteAllInBatchCommand, ScopeAssignationsInBatchExecutor.ScopeAssignationPaginableBatch>,
    ItemDescriptor<ScopeAssignation, ScopeAssignationDeleteAllInBatchCommand, ScopeAssignationsInBatchExecutor.ScopeAssignationPaginableBatch> {

  /**
   * @autogenerated EntityGenerator
   */
  @Data
  public static class ScopeAssignationPaginableBatch {

    /**
     * @autogenerated EntityGenerator
     */
    private String since;
  }

  /**
   * @autogenerated EntityGenerator
   */
  private final int size = 10;

  /**
   * @autogenerated EntityGenerator
   */
  private final ScopeAssignationDeleteUsecase usecase;

  /**
   * @autogenerated EntityGenerator
   */
  private final ScopeAssignationsVisibility visibility;

  /**
   * @autogenerated EntityGenerator
   * @param context
   * @return
   */
  @Override
  public long approximatedItems(
      final StepContext<ScopeAssignationDeleteAllInBatchCommand, ScopeAssignationPaginableBatch> context) {
    return visibility.countVisibles(context.getParams().getInteraction(),
        context.getParams().getFilter());
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   */
  @Override
  public void finish(
      final StepContext<ScopeAssignationDeleteAllInBatchCommand, ScopeAssignationPaginableBatch> context) {
    usecase.flush();
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   */
  @Override
  public void init(
      final StepContext<ScopeAssignationDeleteAllInBatchCommand, ScopeAssignationPaginableBatch> context) {
    context.setState(new ScopeAssignationPaginableBatch());
  }

  /**
   * @autogenerated EntityGenerator
   * @param item
   * @param context
   * @return
   */
  @Override
  public String itemDescription(final ScopeAssignation item,
      final StepContext<ScopeAssignationDeleteAllInBatchCommand, ScopeAssignationPaginableBatch> context) {
    return item.getUidValue();
  }

  /**
   * @autogenerated EntityGenerator
   * @param item
   * @param context
   * @return
   */
  @Override
  public ScopeAssignation process(final ScopeAssignation item,
      final StepContext<ScopeAssignationDeleteAllInBatchCommand, ScopeAssignationPaginableBatch> context) {
    Allow allowed = usecase.allow(context.getParams().getInteraction(), item);
    if (!allowed.isAllowed()) {
      throw new ExecutionException("not-allowed", null);
    }
    return item;
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   * @return
   */
  @Override
  public List<ScopeAssignation> read(
      final StepContext<ScopeAssignationDeleteAllInBatchCommand, ScopeAssignationPaginableBatch> context) {
    List<ScopeAssignation> page =
        visibility.listVisiblesForUpdate(context.getParams().getInteraction(),
            context.getParams().getFilter(), ScopeAssignationCursor.builder().limit(size)
                .sinceUid(context.getState().getSince()).build());
    context.getState().setSince(page.isEmpty() ? null : page.get(page.size() - 1).getUidValue());
    return page;
  }

  /**
   * @autogenerated EntityGenerator
   * @param items
   * @param context
   */
  @Override
  public void write(final List<ScopeAssignation> items,
      final StepContext<ScopeAssignationDeleteAllInBatchCommand, ScopeAssignationPaginableBatch> context) {
    items.forEach(item -> usecase.delete(context.getParams().getInteraction(), item));
  }
}
