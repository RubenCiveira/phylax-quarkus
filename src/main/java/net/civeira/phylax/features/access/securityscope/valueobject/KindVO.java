package net.civeira.phylax.features.access.securityscope.valueobject;

import java.util.Optional;

import lombok.EqualsAndHashCode;
import lombok.Getter;
import lombok.RequiredArgsConstructor;
import lombok.With;
import net.civeira.phylax.common.exception.ConstraintException;
import net.civeira.phylax.common.value.validation.AbstractFailList;
import net.civeira.phylax.common.value.validation.ConstraintFail;
import net.civeira.phylax.common.value.validation.ConstraintFailList;
import net.civeira.phylax.features.access.securityscope.SecurityScopeKindOptions;

/**
 * Value Object representing el kind de security scope of securityScope.
 * <p>
 * This class ensures that the kind is a {@code String} and encapsulates its validation and
 * formatting logic. It is immutable and compliant with DDD (Domain-Driven Design) principles.
 * </p>
 * <p>
 * Use {@link #from(String)} or {@link #tryFrom(Object)} to safely create instances. Validation
 * errors are reported via {@link ConstraintFailList} or thrown as {@link ConstraintException}.
 * </p>
 */
@Getter
@RequiredArgsConstructor
@With
@EqualsAndHashCode(onlyExplicitlyIncluded = true)
public class KindVO {

  /**
   * An empty instance
   *
   * @autogenerated ValueObjectGenerator
   * @param kind
   * @return An empty instance
   */
  public static KindVO from(final SecurityScopeKindOptions kind) {
    return tryFrom(kind);
  }

  /**
   * An empty instance
   *
   * @autogenerated ValueObjectGenerator
   * @return An empty instance
   */
  public static KindVO nullValue() {
    return new KindVO(null);
  }

  /**
   * An empty instance
   *
   * @autogenerated ValueObjectGenerator
   * @param kind
   * @return An empty instance
   */
  public static KindVO tryFrom(final Object kind) {
    ConstraintFailList list = new ConstraintFailList();
    KindVO result = tryFrom(kind, list);
    if (list.hasErrors()) {
      throw new ConstraintException(list);
    }
    return result;
  }

  /**
   * An empty instance
   *
   * @autogenerated ValueObjectGenerator
   * @param kind temptative value
   * @param fails Error list
   * @return An empty instance
   */
  public static <T extends AbstractFailList> KindVO tryFrom(final Object kind, final T fails) {
    if (null == kind) {
      return new KindVO(null);
    } else if (kind instanceof SecurityScopeKindOptions castedKind) {
      return new KindVO(castedKind);
    } else if (kind instanceof String castedKind) {
      try {
        return new KindVO(SecurityScopeKindOptions.valueOf(castedKind));
      } catch (IllegalArgumentException err) {
        fails.add(new ConstraintFail("wrong-option", "kind", castedKind,
            "A valid enum option is expected for kind"));
        return null;
      }
    } else {
      fails.add(new ConstraintFail("wrong-type", "kind", kind.getClass(),
          "A SecurityScopeKindOptions type is expected for kind"));
      return null;
    }
  }

  /**
   * The actual value of the name.
   *
   * @autogenerated ValueObjectGenerator
   */
  private final SecurityScopeKindOptions value;

  /**
   * @autogenerated ValueObjectGenerator
   * @return
   */
  public Optional<SecurityScopeKindOptions> getValue() {
    return Optional.ofNullable(value);
  }

  /**
   * the string value wrapped in square brackets.
   *
   * @autogenerated ValueObjectGenerator
   * @return the string value wrapped in square brackets.
   */
  @Override
  public String toString() {
    return "[" + value + "]";
  }
}
