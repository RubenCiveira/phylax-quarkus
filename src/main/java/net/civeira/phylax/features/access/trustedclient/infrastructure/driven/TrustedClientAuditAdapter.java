package net.civeira.phylax.features.access.trustedclient.infrastructure.driven;

import jakarta.enterprise.context.ApplicationScoped;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.infrastructure.audit.AuditEvent;
import net.civeira.phylax.common.infrastructure.audit.AuditWriteService;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.trustedclient.domain.TrustedClient;
import net.civeira.phylax.features.access.trustedclient.domain.gateway.TrustedClientAuditGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class TrustedClientAuditAdapter implements TrustedClientAuditGateway {

  /**
   * @autogenerated AuditAdapterGenerator
   */
  private final AuditWriteService writer;

  /**
   * @autogenerated AuditAdapterGenerator
   * @param usecase The usecase responsible to the action
   * @param trustedClient
   * @param interaction The interaction
   */
  @Override
  public void created(final String usecase, final TrustedClient trustedClient,
      final Interaction interaction) {
    writer.record(
        AuditEvent.builder().operation("create").usecase(usecase).entityType("trustedClient")
            .entityId(trustedClient.getUid()).newValue(trustedClient.toMap())
            .performedBy(interaction.getActor().getName().orElse("<<no-user>>"))
            .tenant(interaction.getActor().getTenant().orElse("<<no-tenant>>"))
            .timestamp(interaction.getConnection().getStartTime())
            .sourceRequest(interaction.getConnection().getRequest())
            .remoteAddress(interaction.getConnection().getRemote().orElse("<<no-device>>"))
            .remoteApplication(
                interaction.getConnection().getRemoteApplication().orElse("<<no-application>>"))
            .remoteDevice(interaction.getConnection().getRemoteDevice().orElse("<<no-device>>"))
            .claims(interaction.getActor().getClaims()).build());
  }

  /**
   * @autogenerated AuditAdapterGenerator
   * @param usecase The usecase responsible to the action
   * @param trustedClient
   * @param interaction The interaction
   */
  @Override
  public void deleted(final String usecase, final TrustedClient trustedClient,
      final Interaction interaction) {
    writer.record(
        AuditEvent.builder().operation("delete").usecase(usecase).entityType("trustedClient")
            .entityId(trustedClient.getUid()).oldValue(trustedClient.toMap())
            .performedBy(interaction.getActor().getName().orElse("<<no-user>>"))
            .tenant(interaction.getActor().getTenant().orElse("<<no-tenant>>"))
            .timestamp(interaction.getConnection().getStartTime())
            .sourceRequest(interaction.getConnection().getRequest())
            .remoteAddress(interaction.getConnection().getRemote().orElse("<<no-device>>"))
            .remoteApplication(
                interaction.getConnection().getRemoteApplication().orElse("<<no-application>>"))
            .remoteDevice(interaction.getConnection().getRemoteDevice().orElse("<<no-device>>"))
            .claims(interaction.getActor().getClaims()).build());
  }

  /**
   * @autogenerated AuditAdapterGenerator
   * @param usecase The usecase responsible to the action
   * @param trustedClient
   * @param trustedClientOriginal
   * @param interaction The interaction
   */
  @Override
  public void updated(final String usecase, final TrustedClient trustedClient,
      final TrustedClient trustedClientOriginal, final Interaction interaction) {
    writer.record(AuditEvent.builder().operation("update").usecase(usecase)
        .entityType("trustedClient").entityId(trustedClient.getUid())
        .newValue(trustedClient.toMap()).oldValue(trustedClientOriginal.toMap())
        .performedBy(interaction.getActor().getName().orElse("<<no-user>>"))
        .tenant(interaction.getActor().getTenant().orElse("<<no-tenant>>"))
        .timestamp(interaction.getConnection().getStartTime())
        .sourceRequest(interaction.getConnection().getRequest())
        .remoteAddress(interaction.getConnection().getRemote().orElse("<<no-device>>"))
        .remoteApplication(
            interaction.getConnection().getRemoteApplication().orElse("<<no-application>>"))
        .remoteDevice(interaction.getConnection().getRemoteDevice().orElse("<<no-device>>"))
        .claims(interaction.getActor().getClaims()).build());
  }
}
