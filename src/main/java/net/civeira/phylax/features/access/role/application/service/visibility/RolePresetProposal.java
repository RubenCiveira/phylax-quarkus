package net.civeira.phylax.features.access.role.application.service.visibility;

import java.util.Optional;
import java.util.function.Consumer;

import lombok.Builder;
import lombok.Data;
import lombok.EqualsAndHashCode;
import lombok.NonNull;
import lombok.With;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.role.Role;
import net.civeira.phylax.features.access.role.application.request.RoleStateChange;

@Data
@Builder(toBuilder = true)
@With
@EqualsAndHashCode(onlyExplicitlyIncluded = true)
public class RolePresetProposal {

  /**
   * @autogenerated PresetProposalGenerator
   */
  @NonNull
  private final Interaction interaction;

  /**
   * @autogenerated PresetProposalGenerator
   */
  @NonNull
  private RoleStateChange dto;

  /**
   * @autogenerated PresetProposalGenerator
   */
  private Role original;

  /**
   * @autogenerated PresetProposalGenerator
   * @return
   */
  public Optional<Role> getOriginal() {
    return Optional.ofNullable(original);
  }

  /**
   * @autogenerated PresetProposalGenerator
   * @param mapper
   */
  public void peek(Consumer<RoleStateChange> mapper) {
    mapper.accept(dto);
  }
}
