import net.civeira.phylax.generated.openapi.model.TenantLoginProviderApiDto.SourceEnum;

@RequiredArgsConstructor
@ApplicationScoped
public class TenantLoginProviderEnableController {

  /**
   * @autogenerated ActionControllerGenerator
   */
  private final CurrentRequest currentRequest;

  /**
   * @autogenerated ActionControllerGenerator
   */
  private final TenantLoginProviderEnableUsecase enable;

  /**
   * @autogenerated ActionControllerGenerator
   * @param uids
   * @param search
   * @param name
   * @param tenant
   * @param tenants
   * @return
   */
  @Observed("Api to enable on massive entity of tenant login provider")
  public Response tenantLoginProviderApiBatchEnable(final List<String> uids, final String search,
      final String name, final String tenant, final List<String> tenants) {
    TenantLoginProviderEnableFilter.TenantLoginProviderEnableFilterBuilder filterBuilder =
        TenantLoginProviderEnableFilter.builder();
    filterBuilder =
        filterBuilder.uids(uids.stream().flatMap(part -> Stream.of(part.split(","))).toList());
    filterBuilder = filterBuilder.search(search);
    filterBuilder = filterBuilder.uids(
        null == uids ? null : uids.stream().flatMap(part -> Stream.of(part.split(","))).toList());
    filterBuilder = filterBuilder.search(search);
    filterBuilder = filterBuilder.name(name);
    if (null != tenant) {
      filterBuilder = filterBuilder.tenant(TenantReference.of(tenant));
    }
    filterBuilder = filterBuilder.tenants(null == tenants ? null
        : tenants.stream().flatMap(part -> Stream.of(part.split(","))).toList());
    TenantLoginProviderEnableFilter filter = filterBuilder.build();
    BatchIdentificator task = enable.batchEnable(currentRequest.interaction(), filter);
    /* .header("Last-Modified", value.getSince().format(DateTimeFormatter.RFC_1123_DATE_TIME)) */
    BatchTaskLocalizator response = new BatchTaskLocalizator();
    response.setUid(task.getUid());
    response.setSteps(task.getSteps());
    return Response.ok(response).build();
  }

  /**
   * @autogenerated ActionControllerGenerator
   * @param batchId
   * @return
   */
  @Observed("Api to check masive enable on entity of tenant login provider")
  public Response tenantLoginProviderApiBatchEnableQuery(final String batchId) {
    BatchProgress task = enable.checkProgress(TenantLoginProviderEnableStatus.builder()
        .taskId(batchId).build(currentRequest.interaction()));
    BatchTaskDetail response = new BatchTaskDetail();
    response.setUid(task.getUid());
    GlobalStatus taskStatus = task.getStatus();
    if (null != taskStatus) {
      response.setDetailStatus(DetailStatusEnum.valueOf(taskStatus.name()));
    }
    Instant taskStart = task.getStartTime();
    if (null != taskStart) {
      response.setStartTime(OffsetDateTime.ofInstant(taskStart, ZoneId.systemDefault()));
    }
    Instant taskEnd = task.getEndTime();
    if (null != taskEnd) {
      response.setEndTime(OffsetDateTime.ofInstant(taskEnd, ZoneId.systemDefault()));
    }
    response.setSteps(task.getSteps().stream().map(step -> {
      BatchStepProgress bsp = new BatchStepProgress();
      Status stepStatus = step.getStatus();
      if (null != stepStatus) {
        bsp.setStepStatus(StepStatusEnum.valueOf(stepStatus.name()));
      }
      Instant stepStart = step.getStartTime();
      if (null != stepStart) {
        bsp.setStartTime(OffsetDateTime.ofInstant(stepStart, ZoneId.systemDefault()));
      }
      Instant stepEnd = step.getEndTime();
      if (null != stepEnd) {
        bsp.setEndTime(OffsetDateTime.ofInstant(stepEnd, ZoneId.systemDefault()));
      }
      bsp.setError(step.getError());
      bsp.setName(step.getName());
      bsp.setOks(step.getOks());
      bsp.setProcessedItems(step.getProcessedItems());
      bsp.setTotalItems(step.getTotalItems());
      bsp.setErrors(new ArrayList<>());
      bsp.setWarns(new ArrayList<>());
      Map<String, ErrorInfo> stepErrors = step.getErrors();
      if (null != stepErrors) {
        bsp.getErrors()
            .addAll(stepErrors.entrySet().stream()
                .map(entry -> new BatchResultErrorItem().item(entry.getKey())
                    .fails(entry.getValue().getFails().stream()
                        .map(fail -> new MultipleErrorCodes().code(fail.getCode())
                            .violation(fail.getViolation())
                            .wrongValues(fail.getWrongValues().stream()
                                .map(wv -> new ConstraintFail().error(wv.getErrorMessage())
                                    .field(wv.getField()).wrong(String.valueOf(wv.getWrongValue())))
                                .toList()))
                        .toList()))
                .toList());
      }
      Map<String, ErrorInfo> stepWarns = step.getWarns();
      if (null != stepWarns) {
        bsp.getWarns()
            .addAll(stepErrors.entrySet().stream()
                .map(entry -> new BatchResultErrorItem().item(entry.getKey())
                    .fails(entry.getValue().getFails().stream()
                        .map(fail -> new MultipleErrorCodes().code(fail.getCode())
                            .violation(fail.getViolation())
                            .wrongValues(fail.getWrongValues().stream()
                                .map(wv -> new ConstraintFail().error(wv.getErrorMessage())
                                    .field(wv.getField()).wrong(String.valueOf(wv.getWrongValue())))
                                .toList()))
                        .toList()))
                .toList());
      }
      return bsp;
    }).toList());
    return Response.ok(response).build();
  }

  /**
   * @autogenerated ActionControllerGenerator
   * @param uid
   * @return
   */
  @Transactional
  @ApiObserved("Api to enable an entity of tenant login provider")
  public Response tenantLoginProviderApiEnable(final String uid) {
    Trace.setAttribute("ref", uid);
    TenantLoginProviderEnableProjection updated =
        enable.enable(currentRequest.interaction(), TenantLoginProviderReference.of(uid));
    return Response.ok(toApiModel(updated)).build();
  }

  /**
   * @autogenerated ActionControllerGenerator
   * @param domainEnum
   * @return
   */
  private SourceEnum sourceEnumToApi(TenantLoginProviderSourceOptions domainEnum) {
    SourceEnum result;
    if (domainEnum == TenantLoginProviderSourceOptions.GOOGLE) {
      result = SourceEnum.GOOGLE;
    } else if (domainEnum == TenantLoginProviderSourceOptions.GITHUB) {
      result = SourceEnum.GITHUB;
    } else if (domainEnum == TenantLoginProviderSourceOptions.SAML) {
      result = SourceEnum.SAML;
    } else {
      result = null;
    }
    return result;
  }

  /**
   * @autogenerated ActionControllerGenerator
   * @param dto
   * @return
   */
  private TenantLoginProviderApiDto toApiModel(TenantLoginProviderEnableProjection dto) {
    TenantLoginProviderApiDto tenantLoginProviderApiDto = new TenantLoginProviderApiDto();
    tenantLoginProviderApiDto.setUid(dto.getUid());
    tenantLoginProviderApiDto.setTenant(new TenantApiRef().$ref(dto.getTenantReference()));
    tenantLoginProviderApiDto.setName(dto.getName());
    tenantLoginProviderApiDto.setSource(sourceEnumToApi(dto.getSource()));
    tenantLoginProviderApiDto.setDisabled(dto.getDisabled());
    tenantLoginProviderApiDto.setDirectAccess(dto.getDirectAccess());
    tenantLoginProviderApiDto.setPublicKey(dto.getPublicKey());
    tenantLoginProviderApiDto.setPrivateKey(dto.getPrivateKey());
    tenantLoginProviderApiDto.setCertificate(dto.getCertificate());
    String metadata = dto.getMetadata();
    if (null != metadata) {
      tenantLoginProviderApiDto.setMetadata(currentRequest.getPublicHost()
          + "/api/access/login-providers/" + dto.getUidOrDefault("-") + "/metadata");
    }
    tenantLoginProviderApiDto.setUsersEnabledByDefault(dto.getUsersEnabledByDefault());
    tenantLoginProviderApiDto.setVersion(dto.getVersion());
    return tenantLoginProviderApiDto;
  }
}
