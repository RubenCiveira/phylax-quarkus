package net.civeira.phylax.features.access.relyingparty.application.usecase.enable;

import java.time.Duration;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.BatchIdentificator;
import net.civeira.phylax.common.batch.BatchProgress;
import net.civeira.phylax.common.batch.BatchService;
import net.civeira.phylax.common.batch.ExecutorByDeferSteps;
import net.civeira.phylax.common.batch.ExecutorPlan;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.relyingparty.RelyingParty;
import net.civeira.phylax.features.access.relyingparty.RelyingPartyFacade;
import net.civeira.phylax.features.access.relyingparty.RelyingPartyRef;
import net.civeira.phylax.features.access.relyingparty.application.projection.RelyingPartyStateProyection;
import net.civeira.phylax.features.access.relyingparty.application.service.visibility.RelyingPartysVisibility;
import net.civeira.phylax.features.access.relyingparty.gateway.RelyingPartyCacheGateway;
import net.civeira.phylax.features.access.relyingparty.gateway.RelyingPartyWriteRepositoryGateway;
import net.civeira.phylax.features.access.relyingparty.query.RelyingPartyFilter;

@ApplicationScoped
@RequiredArgsConstructor
public class RelyingPartyEnableUsecase {

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final BatchService batch;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final RelyingPartyCacheGateway cache;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final Event<RelyingPartyAllowEnableProposal> execAllowEmitter;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final Event<RelyingPartyEnableProposal> execProposalEmitter;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final Event<RelyingPartyEnableEvent> executedEmitter;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final RelyingPartyFacade facade;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final RelyingPartyWriteRepositoryGateway gateway;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final RelyingPartysVisibility visibility;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  public Allow allow(final Interaction query, final RelyingPartyRef reference) {
    RelyingPartyAllowEnableProposal base = RelyingPartyAllowEnableProposal.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).reference(reference).build();
    execAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    RelyingPartyAllowEnableProposal base = RelyingPartyAllowEnableProposal.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).build();
    execAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * The slide with some values
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query a filter to retrieve only matching values
   * @return The slide with some values
   */
  public BatchProgress checkProgress(final RelyingPartyEnableStatus query) {
    return query.getActor().getName()
        .flatMap(name -> batch.retrieve(query.getTaskId(), query.getConnection().getLocale(), name))
        .orElseThrow(() -> new NotFoundException());
  }

  /**
   * The slide with some values
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param reference
   * @return The slide with some values
   */
  public RelyingPartyStateProyection enable(final Interaction query,
      final RelyingPartyRef reference) {
    Allow detail = allow(query, reference);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    RelyingParty original = visibility.retrieveVisibleForUpdate(query, reference.getUidValue())
        .orElseThrow(() -> new NotFoundException(""));
    RelyingParty saved = enable(query, original);
    flush();
    return visibility.copyWithHidden(query, saved);
  }

  /**
   * The slide with some values
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param filter
   * @return The slide with some values
   */
  public BatchIdentificator enable(final Interaction query, final RelyingPartyFilter filter) {
    Allow detail = allow(query);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    RelyingPartyEnableAllInBatchCommand command =
        RelyingPartyEnableAllInBatchCommand.builder().interaction(query).filter(filter).build();
    return batch.start(command.getInteraction().getActor().getName().orElse("-"),
        Duration.ofHours(6),
        ExecutorPlan.<RelyingPartyEnableAllInBatchCommand>builder().params(command)
            .name("enable-color")
            .executor(
                ExecutorByDeferSteps.<RelyingParty, RelyingParty, RelyingPartyEnableAllInBatchCommand, RelyingPartyEnablesInBatchExecutor.RelyingPartyPaginableBatch>builder()
                    .initializer(RelyingPartyEnablesInBatchExecutor.class)
                    .counter(RelyingPartyEnablesInBatchExecutor.class)
                    .descriptor(RelyingPartyEnablesInBatchExecutor.class)
                    .reader(RelyingPartyEnablesInBatchExecutor.class)
                    .processor(RelyingPartyEnablesInBatchExecutor.class)
                    .writer(RelyingPartyEnablesInBatchExecutor.class)
                    .finalizer(RelyingPartyEnablesInBatchExecutor.class).build())
            .build());
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param interaction
   * @param original
   * @return
   */
  RelyingParty enable(final Interaction interaction, final RelyingParty original) {
    RelyingParty filled = facade.enable(original);
    RelyingPartyEnableProposal proposal =
        RelyingPartyEnableProposal.builder().entity(filled).interaction(interaction).build();
    execProposalEmitter.fire(proposal);
    RelyingParty result = proposal.getEntity();
    RelyingParty saved = gateway.update(original, result);
    RelyingPartyEnableEvent event =
        RelyingPartyEnableEvent.builder().payload(saved).interaction(interaction).build();
    executedEmitter.fire(event);
    return event.getPayload();
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  void flush() {
    cache.evict();
  }
}
