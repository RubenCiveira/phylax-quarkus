package net.civeira.phylax.features.access.trustedclient.domain.valueobject;

import lombok.EqualsAndHashCode;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.ConstraintException;
import net.civeira.phylax.common.value.validation.AbstractFailList;
import net.civeira.phylax.common.value.validation.ConstraintFail;
import net.civeira.phylax.common.value.validation.ConstraintFailList;

/**
 * Value Object representing el url de trusted client allowed redirect of
 * trustedClientAllowedRedirect.
 * <p>
 * This class ensures that the url is a non-null {@code String} and encapsulates its validation and
 * formatting logic. It is immutable and compliant with DDD (Domain-Driven Design) principles.
 * </p>
 * <p>
 * Use {@link #from(String)} or {@link #tryFrom(Object)} to safely create instances. Validation
 * errors are reported via {@link ConstraintFailList} or thrown as {@link ConstraintException}.
 * </p>
 */
@RequiredArgsConstructor
@EqualsAndHashCode
public class UrlVO {

  /**
   * Crear an empty instance with no value
   *
   * @autogenerated ValueObjectGenerator
   * @param url
   * @return An empty instance
   */
  public static UrlVO from(final String url) {
    return tryFrom(url);
  }

  /**
   * Crear an empty instance with no value
   *
   * @autogenerated ValueObjectGenerator
   * @param url
   * @return An empty instance
   */
  public static UrlVO tryFrom(final Object url) {
    ConstraintFailList list = new ConstraintFailList();
    UrlVO result = tryFrom(url, list);
    if (list.hasErrors()) {
      throw new ConstraintException(list);
    }
    return result;
  }

  /**
   * Crear an empty instance with no value
   *
   * @autogenerated ValueObjectGenerator
   * @param url temptative value
   * @param fails Error list
   * @return An empty instance
   */
  public static <T extends AbstractFailList> UrlVO tryFrom(final Object url, final T fails) {
    if (null == url) {
      fails.add(new ConstraintFail("not-null", "url", null, "Cant be null"));
      return null;
    } else if (url instanceof String castedUrl) {
      return new UrlVO(castedUrl);
    } else {
      fails.add(new ConstraintFail("wrong-type", "url", url.getClass(),
          "A String type is expected for url"));
      return null;
    }
  }

  /**
   * The actual value of the name. * It is guaranteed to be a non-null String after successful
   * value.validation.
   *
   * @autogenerated ValueObjectGenerator
   */
  private final String value;

  /**
   * Get the vo value
   *
   * @autogenerated ValueObjectGenerator
   * @return
   */
  public String getUrl() {
    return value;
  }

  /**
   * Returns a formatted string representation of the name.
   *
   * @autogenerated ValueObjectGenerator
   * @return the string value wrapped in square brackets.
   */
  @Override
  public String toString() {
    return "[" + value + "]";
  }
}
