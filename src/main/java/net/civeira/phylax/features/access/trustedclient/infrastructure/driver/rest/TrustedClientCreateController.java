package net.civeira.phylax.features.access.trustedclient.infrastructure.driver.rest;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.SpanKind;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Context;
import jakarta.enterprise.context.ApplicationScoped;
import jakarta.transaction.Transactional;
import jakarta.ws.rs.core.Response;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.infrastructure.CurrentRequest;
import net.civeira.phylax.features.access.trustedclient.application.usecase.create.TrustedClientCreateInput;
import net.civeira.phylax.features.access.trustedclient.application.usecase.create.TrustedClientCreateProjection;
import net.civeira.phylax.features.access.trustedclient.application.usecase.create.TrustedClientCreateUsecase;
import net.civeira.phylax.features.access.trustedclient.domain.AllowedRedirects;
import net.civeira.phylax.features.access.trustedclient.domain.AllowedRedirects.AllowedRedirectsBuilder;
import net.civeira.phylax.generated.openapi.model.AllowedRedirectsApiDto;
import net.civeira.phylax.generated.openapi.model.TrustedClientApiDto;

@RequiredArgsConstructor
@ApplicationScoped
public class TrustedClientCreateController {

  /**
   * @autogenerated CreateControllerGenerator
   */
  private final TrustedClientCreateUsecase create;

  /**
   * @autogenerated CreateControllerGenerator
   */
  private final CurrentRequest currentRequest;

  /**
   * @autogenerated CreateControllerGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated CreateControllerGenerator
   * @param trustedClient
   * @return
   */
  @Transactional
  public Response trustedClientApiCreate(TrustedClientApiDto trustedClient) {
    Span span = tracer.spanBuilder("Api to create an entity of trusted client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      TrustedClientCreateProjection created =
          create.create(currentRequest.interaction(), toDomainModel(trustedClient));
      return Response.ok(toApiModel(created)).build();
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated CreateControllerGenerator
   * @param dto
   * @return
   */
  private TrustedClientApiDto toApiModel(TrustedClientCreateProjection dto) {
    Span span = tracer
        .spanBuilder(
            "Mapper to build api content to response from model for entity of trusted client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      TrustedClientApiDto trustedClientApiDto = new TrustedClientApiDto();
      trustedClientApiDto.setUid(dto.getUid());
      trustedClientApiDto.setCode(dto.getCode());
      trustedClientApiDto.setPublicAllow(dto.getPublicAllow());
      trustedClientApiDto.setSecretOauth("*****");
      trustedClientApiDto.setEnabled(dto.getEnabled());
      trustedClientApiDto.setAllowedRedirects(
          dto.getAllowedRedirects().stream().map(this::toApiModelAllowedRedirects).toList());
      trustedClientApiDto.setVersion(dto.getVersion());
      return trustedClientApiDto;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated CreateControllerGenerator
   * @param dto
   * @return
   */
  private AllowedRedirectsApiDto toApiModelAllowedRedirects(AllowedRedirects dto) {
    Span span = tracer
        .spanBuilder(
            "Mapper to build api content to response from model for entity of trusted client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      AllowedRedirectsApiDto allowedRedirectsApiDto = new AllowedRedirectsApiDto();
      allowedRedirectsApiDto.setUid(dto.getUid());
      allowedRedirectsApiDto.setUrl(dto.getUrl());
      allowedRedirectsApiDto.setVersion(dto.getVersion().orElse(null));
      return allowedRedirectsApiDto;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated CreateControllerGenerator
   * @param trustedClientApiDto
   * @return
   */
  private TrustedClientCreateInput toDomainModel(TrustedClientApiDto trustedClientApiDto) {
    Span span = tracer
        .spanBuilder(
            "Mapper to build model content to request from api for entity of trusted client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      TrustedClientCreateInput dto = new TrustedClientCreateInput();
      if (null != trustedClientApiDto.getUid()) {
        dto.setUid(trustedClientApiDto.getUid());
      }
      if (null != trustedClientApiDto.getCode()) {
        dto.setCode(trustedClientApiDto.getCode());
      }
      if (null != trustedClientApiDto.getPublicAllow()) {
        dto.setPublicAllow(trustedClientApiDto.getPublicAllow());
      }
      if (null != trustedClientApiDto.getSecretOauth()
          && !"*****".equals(trustedClientApiDto.getSecretOauth())) {
        dto.setSecretOauth(trustedClientApiDto.getSecretOauth());
      }
      if (null != trustedClientApiDto.getEnabled()) {
        dto.setEnabled(trustedClientApiDto.getEnabled());
      }
      if (null != trustedClientApiDto.getAllowedRedirects()) {
        dto.setAllowedRedirects(trustedClientApiDto.getAllowedRedirects().stream()
            .map(this::toDomainModelAllowedRedirects).toList());
      }
      if (null != trustedClientApiDto.getVersion()) {
        dto.setVersion(trustedClientApiDto.getVersion());
      }
      return dto;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated CreateControllerGenerator
   * @param allowedRedirectsApiDto
   * @return
   */
  private AllowedRedirects toDomainModelAllowedRedirects(
      AllowedRedirectsApiDto allowedRedirectsApiDto) {
    Span span = tracer
        .spanBuilder(
            "Mapper to build model content to request from api for entity of trusted client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      AllowedRedirectsBuilder builder = AllowedRedirects.builder();
      if (null != allowedRedirectsApiDto.getUid()) {
        builder = builder.uid(allowedRedirectsApiDto.getUid());
      }
      if (null != allowedRedirectsApiDto.getUrl()) {
        builder = builder.url(allowedRedirectsApiDto.getUrl());
      }
      if (null != allowedRedirectsApiDto.getVersion()) {
        builder = builder.version(allowedRedirectsApiDto.getVersion());
      }
      return builder.build();
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }
}
