package net.civeira.phylax.features.access.tenant.domain.valueobject;

import lombok.EqualsAndHashCode;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.ConstraintException;
import net.civeira.phylax.common.value.validation.AbstractFailList;
import net.civeira.phylax.common.value.validation.ConstraintFail;
import net.civeira.phylax.common.value.validation.ConstraintFailList;

/**
 * Value Object representing el root de tenant of tenant.
 * <p>
 * This class ensures that the root is a {@code String} and encapsulates its validation and
 * formatting logic. It is immutable and compliant with DDD (Domain-Driven Design) principles.
 * </p>
 * <p>
 * Use {@link #from(String)} or {@link #tryFrom(Object)} to safely create instances. Validation
 * errors are reported via {@link ConstraintFailList} or thrown as {@link ConstraintException}.
 * </p>
 */
@RequiredArgsConstructor
@EqualsAndHashCode
public class RootVO {

  /**
   * Crear an empty instance with no value
   *
   * @autogenerated ValueObjectGenerator
   * @param root
   * @return An empty instance
   */
  public static RootVO from(final Boolean root) {
    return tryFrom(root);
  }

  /**
   * Crear an empty instance with no value
   *
   * @autogenerated ValueObjectGenerator
   * @return An empty instance
   */
  public static RootVO nullValue() {
    return new RootVO(null);
  }

  /**
   * Crear an empty instance with no value
   *
   * @autogenerated ValueObjectGenerator
   * @param root
   * @return An empty instance
   */
  public static RootVO tryFrom(final Object root) {
    ConstraintFailList list = new ConstraintFailList();
    RootVO result = tryFrom(root, list);
    if (list.hasErrors()) {
      throw new ConstraintException(list);
    }
    return result;
  }

  /**
   * Crear an empty instance with no value
   *
   * @autogenerated ValueObjectGenerator
   * @param root temptative value
   * @param fails Error list
   * @return An empty instance
   */
  public static <T extends AbstractFailList> RootVO tryFrom(final Object root, final T fails) {
    if (null == root) {
      return new RootVO(null);
    } else if (root instanceof Boolean castedRoot) {
      return new RootVO(castedRoot);
    } else {
      fails.add(new ConstraintFail("wrong-type", "root", root.getClass(),
          "A Boolean type is expected for root"));
      return null;
    }
  }

  /**
   * The actual value of the name.
   *
   * @autogenerated ValueObjectGenerator
   */
  private final Boolean value;

  /**
   * Get the vo value
   *
   * @autogenerated ValueObjectGenerator
   * @return
   */
  public Boolean isRoot() {
    return Boolean.TRUE.equals(value);
  }

  /**
   * Returns a formatted string representation of the name.
   *
   * @autogenerated ValueObjectGenerator
   * @return the string value wrapped in square brackets.
   */
  @Override
  public String toString() {
    return "[" + value + "]";
  }
}
