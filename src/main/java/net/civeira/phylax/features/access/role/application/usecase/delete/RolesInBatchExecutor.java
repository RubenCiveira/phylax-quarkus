package net.civeira.phylax.features.access.role.application.usecase.delete;

import java.util.List;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.SpanKind;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Context;
import io.quarkus.arc.Unremovable;
import jakarta.enterprise.context.ApplicationScoped;
import lombok.Data;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.stepper.ItemDescriptor;
import net.civeira.phylax.common.batch.stepper.ItemProcessor;
import net.civeira.phylax.common.batch.stepper.ItemReader;
import net.civeira.phylax.common.batch.stepper.ItemWriter;
import net.civeira.phylax.common.batch.stepper.StepContext;
import net.civeira.phylax.common.batch.stepper.StepCounter;
import net.civeira.phylax.common.batch.stepper.StepFinalizer;
import net.civeira.phylax.common.batch.stepper.StepInitializer;
import net.civeira.phylax.common.exception.ExecutionException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.features.access.role.application.visibility.RolesVisibility;
import net.civeira.phylax.features.access.role.domain.Role;
import net.civeira.phylax.features.access.role.domain.gateway.RoleAuditGateway;
import net.civeira.phylax.features.access.role.domain.gateway.RoleCursor;

@Unremovable
@ApplicationScoped
@RequiredArgsConstructor
class RolesInBatchExecutor
    implements StepCounter<RoleDeleteAllInBatchCommand, RolesInBatchExecutor.RolePaginableBatch>,
    StepFinalizer<RoleDeleteAllInBatchCommand, RolesInBatchExecutor.RolePaginableBatch>,
    StepInitializer<RoleDeleteAllInBatchCommand, RolesInBatchExecutor.RolePaginableBatch>,
    ItemReader<Role, RoleDeleteAllInBatchCommand, RolesInBatchExecutor.RolePaginableBatch>,
    ItemWriter<Role, RoleDeleteAllInBatchCommand, RolesInBatchExecutor.RolePaginableBatch>,
    ItemProcessor<Role, Role, RoleDeleteAllInBatchCommand, RolesInBatchExecutor.RolePaginableBatch>,
    ItemDescriptor<Role, RoleDeleteAllInBatchCommand, RolesInBatchExecutor.RolePaginableBatch> {

  /**
   * @autogenerated EntityGenerator
   */
  @Data
  public static class RolePaginableBatch {

    /**
     * @autogenerated EntityGenerator
     */
    private String since;
  }

  /**
   * Service to register audit events
   *
   * @autogenerated EntityGenerator
   */
  private final RoleAuditGateway audit;

  /**
   * @autogenerated EntityGenerator
   */
  private final int size = 10;

  /**
   * @autogenerated EntityGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated EntityGenerator
   */
  private final RoleDeleteUsecase usecase;

  /**
   * @autogenerated EntityGenerator
   */
  private final RolesVisibility visibility;

  /**
   * @autogenerated EntityGenerator
   * @param context
   * @return
   */
  @Override
  public long approximatedItems(
      final StepContext<RoleDeleteAllInBatchCommand, RolePaginableBatch> context) {
    Span span =
        tracer.spanBuilder("Count selected items to the batch delete for a set of entities of role")
            .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
            .startSpan();
    try {
      return visibility.countVisibles(context.getParams().getInteraction(),
          context.getParams().getFilter());
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   */
  @Override
  public void finish(final StepContext<RoleDeleteAllInBatchCommand, RolePaginableBatch> context) {
    usecase.cacheEvict();
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   */
  @Override
  public void init(final StepContext<RoleDeleteAllInBatchCommand, RolePaginableBatch> context) {
    context.setState(new RolePaginableBatch());
  }

  /**
   * @autogenerated EntityGenerator
   * @param item
   * @param context
   * @return
   */
  @Override
  public String itemDescription(final Role item,
      final StepContext<RoleDeleteAllInBatchCommand, RolePaginableBatch> context) {
    return item.getUid();
  }

  /**
   * @autogenerated EntityGenerator
   * @param item
   * @param context
   * @return
   */
  @Override
  public Role process(final Role item,
      final StepContext<RoleDeleteAllInBatchCommand, RolePaginableBatch> context) {
    Span span = tracer
        .spanBuilder("Prepare individual data for the batch delete on a set of entities of role")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      Allow allowed = usecase.checkAllow(context.getParams().getInteraction(), item);
      if (!allowed.isAllowed()) {
        span.addEvent("The policies for role dont allow to delete: " + allowed.getDescription());
        throw new ExecutionException("not-allowed", null);
      }
      return item;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   * @return
   */
  @Override
  public List<Role> read(
      final StepContext<RoleDeleteAllInBatchCommand, RolePaginableBatch> context) {
    Span span = tracer
        .spanBuilder("Read selected entitites for a batch delete for a set of entities of role")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      List<Role> page = visibility.listVisiblesForUpdate(context.getParams().getInteraction(),
          context.getParams().getFilter(),
          RoleCursor.builder().limit(size).sinceUid(context.getState().getSince()).build());
      context.getState().setSince(page.isEmpty() ? null : page.get(page.size() - 1).getUid());
      span.setAttribute("size", page.size());
      return page;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated EntityGenerator
   * @param items
   * @param context
   */
  @Override
  public void write(final List<Role> items,
      final StepContext<RoleDeleteAllInBatchCommand, RolePaginableBatch> context) {
    Span span = tracer.spanBuilder("Perform the batch delete for a set of entities of role")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      span.setAttribute("size", items.size());
      items.forEach(item -> {
        usecase.doDelete(context.getParams().getInteraction(), item);
        usecase.cacheEvict();
        audit.deleted("batch-delete", item, context.getParams().getInteraction());
      });
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }
}
