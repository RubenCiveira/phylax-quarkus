package net.civeira.phylax.features.access.securitydomain.application.usecase.enable;

import java.util.List;

import io.quarkus.arc.Unremovable;
import jakarta.enterprise.context.ApplicationScoped;
import lombok.Data;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.stepper.ItemDescriptor;
import net.civeira.phylax.common.batch.stepper.ItemProcessor;
import net.civeira.phylax.common.batch.stepper.ItemReader;
import net.civeira.phylax.common.batch.stepper.ItemWriter;
import net.civeira.phylax.common.batch.stepper.StepContext;
import net.civeira.phylax.common.batch.stepper.StepCounter;
import net.civeira.phylax.common.batch.stepper.StepFinalizer;
import net.civeira.phylax.common.batch.stepper.StepInitializer;
import net.civeira.phylax.common.exception.ExecutionException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.features.access.securitydomain.application.visibility.SecurityDomainsVisibility;
import net.civeira.phylax.features.access.securitydomain.domain.SecurityDomain;
import net.civeira.phylax.features.access.securitydomain.domain.gateway.SecurityDomainCursor;

@Unremovable
@ApplicationScoped
@RequiredArgsConstructor
class SecurityDomainEnablesInBatchExecutor implements
    StepCounter<SecurityDomainEnableAllInBatchCommand, SecurityDomainEnablesInBatchExecutor.SecurityDomainPaginableBatch>,
    StepFinalizer<SecurityDomainEnableAllInBatchCommand, SecurityDomainEnablesInBatchExecutor.SecurityDomainPaginableBatch>,
    StepInitializer<SecurityDomainEnableAllInBatchCommand, SecurityDomainEnablesInBatchExecutor.SecurityDomainPaginableBatch>,
    ItemReader<SecurityDomain, SecurityDomainEnableAllInBatchCommand, SecurityDomainEnablesInBatchExecutor.SecurityDomainPaginableBatch>,
    ItemWriter<SecurityDomain, SecurityDomainEnableAllInBatchCommand, SecurityDomainEnablesInBatchExecutor.SecurityDomainPaginableBatch>,
    ItemProcessor<SecurityDomain, SecurityDomain, SecurityDomainEnableAllInBatchCommand, SecurityDomainEnablesInBatchExecutor.SecurityDomainPaginableBatch>,
    ItemDescriptor<SecurityDomain, SecurityDomainEnableAllInBatchCommand, SecurityDomainEnablesInBatchExecutor.SecurityDomainPaginableBatch> {

  /**
   * @autogenerated EntityGenerator
   */
  @Data
  public static class SecurityDomainPaginableBatch {

    /**
     * @autogenerated EntityGenerator
     */
    private String since;
  }

  /**
   * @autogenerated EntityGenerator
   */
  private final int size = 10;

  /**
   * @autogenerated EntityGenerator
   */
  private final SecurityDomainEnableUsecase usecase;

  /**
   * @autogenerated EntityGenerator
   */
  private final SecurityDomainsVisibility visibility;

  /**
   * @autogenerated EntityGenerator
   * @param context
   * @return
   */
  @Override
  public long approximatedItems(
      final StepContext<SecurityDomainEnableAllInBatchCommand, SecurityDomainPaginableBatch> context) {
    return visibility.countVisibles(context.getParams().getInteraction(),
        context.getParams().getFilter());
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   */
  @Override
  public void finish(
      final StepContext<SecurityDomainEnableAllInBatchCommand, SecurityDomainPaginableBatch> context) {
    usecase.flush();
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   */
  @Override
  public void init(
      final StepContext<SecurityDomainEnableAllInBatchCommand, SecurityDomainPaginableBatch> context) {
    context.setState(new SecurityDomainPaginableBatch());
  }

  /**
   * @autogenerated EntityGenerator
   * @param item
   * @param context
   * @return
   */
  @Override
  public String itemDescription(final SecurityDomain item,
      final StepContext<SecurityDomainEnableAllInBatchCommand, SecurityDomainPaginableBatch> context) {
    return item.getUid();
  }

  /**
   * @autogenerated EntityGenerator
   * @param item
   * @param context
   * @return
   */
  @Override
  public SecurityDomain process(final SecurityDomain item,
      final StepContext<SecurityDomainEnableAllInBatchCommand, SecurityDomainPaginableBatch> context) {
    Allow allowed = usecase.allow(context.getParams().getInteraction(), item);
    if (!allowed.isAllowed()) {
      throw new ExecutionException("not-allowed", null);
    }
    return item;
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   * @return
   */
  @Override
  public List<SecurityDomain> read(
      final StepContext<SecurityDomainEnableAllInBatchCommand, SecurityDomainPaginableBatch> context) {
    List<SecurityDomain> page = visibility.listVisiblesForUpdate(
        context.getParams().getInteraction(), context.getParams().getFilter(),
        SecurityDomainCursor.builder().limit(size).sinceUid(context.getState().getSince()).build());
    context.getState().setSince(page.isEmpty() ? null : page.get(page.size() - 1).getUid());
    return page;
  }

  /**
   * @autogenerated EntityGenerator
   * @param items
   * @param context
   */
  @Override
  public void write(final List<SecurityDomain> items,
      final StepContext<SecurityDomainEnableAllInBatchCommand, SecurityDomainPaginableBatch> context) {
    items.forEach(item -> usecase.enable(context.getParams().getInteraction(), item));
  }
}
