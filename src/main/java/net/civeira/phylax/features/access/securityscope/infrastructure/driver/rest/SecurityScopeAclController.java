package net.civeira.phylax.features.access.securityscope.infrastructure.driver.rest;

import java.util.ArrayList;

import jakarta.ws.rs.core.Response;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.infrastructure.CurrentRequest;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.securityscope.application.service.visibility.SecurityScopesVisibility;
import net.civeira.phylax.features.access.securityscope.application.usecase.create.SecurityScopeCreateUsecase;
import net.civeira.phylax.features.access.securityscope.application.usecase.delete.SecurityScopeDeleteUsecase;
import net.civeira.phylax.features.access.securityscope.application.usecase.disable.SecurityScopeDisableUsecase;
import net.civeira.phylax.features.access.securityscope.application.usecase.enable.SecurityScopeEnableUsecase;
import net.civeira.phylax.features.access.securityscope.application.usecase.list.SecurityScopeListUsecase;
import net.civeira.phylax.features.access.securityscope.application.usecase.retrieve.SecurityScopeRetrieveUsecase;
import net.civeira.phylax.features.access.securityscope.application.usecase.update.SecurityScopeUpdateUsecase;
import net.civeira.phylax.generated.openapi.api.SecurityScopeAclApi;
import net.civeira.phylax.generated.openapi.model.CommonAllow;
import net.civeira.phylax.generated.openapi.model.SecurityScopeAclFields;
import net.civeira.phylax.generated.openapi.model.SecurityScopeAclGenericAllows;
import net.civeira.phylax.generated.openapi.model.SecurityScopeAclSpecificAllows;
import net.civeira.phylax.generated.openapi.model.SecurityScopeGenericAcl;
import net.civeira.phylax.generated.openapi.model.SecurityScopeSpecificAcl;

@RequiredArgsConstructor
public class SecurityScopeAclController implements SecurityScopeAclApi {

  /**
   * @autogenerated AclControllerGenerator
   */
  private final SecurityScopeCreateUsecase create;

  /**
   * SecurityScope
   *
   * @autogenerated AclControllerGenerator
   */
  private final CurrentRequest currentRequest;

  /**
   * @autogenerated AclControllerGenerator
   */
  private final SecurityScopeDeleteUsecase delete;

  /**
   * @autogenerated AclControllerGenerator
   */
  private final SecurityScopeDisableUsecase disable;

  /**
   * @autogenerated AclControllerGenerator
   */
  private final SecurityScopeEnableUsecase enable;

  /**
   * @autogenerated AclControllerGenerator
   */
  private final SecurityScopeListUsecase list;

  /**
   * @autogenerated AclControllerGenerator
   */
  private final SecurityScopeRetrieveUsecase retrieve;

  /**
   * @autogenerated AclControllerGenerator
   */
  private final SecurityScopeUpdateUsecase update;

  /**
   * @autogenerated AclControllerGenerator
   */
  private final SecurityScopesVisibility visibility;

  /**
   * @autogenerated AclControllerGenerator
   * @param uid
   * @return
   */
  @Override
  public Response securityScopeApiContextualAcl(final String uid) {
    Interaction interaction = currentRequest.interaction();
    SecurityScopeSpecificAcl response = new SecurityScopeSpecificAcl();
    response.setAllows(new SecurityScopeAclSpecificAllows());
    response.setFields(new SecurityScopeAclFields());
    response.getFields().setNoEditables(new ArrayList<>());
    response.getFields().setNoVisibles(new ArrayList<>());
    fixedFields(response.getFields(), interaction);
    hiddenFields(response.getFields(), interaction);
    updateAllows(response, interaction);
    deleteAllows(response, interaction);
    retrieveAllows(response, interaction);
    disableAllows(response, interaction);
    enableAllows(response, interaction);
    return Response.ok(response).build();
  }

  /**
   * @autogenerated AclControllerGenerator
   * @return
   */
  @Override
  public Response securityScopeApiGenericAcl() {
    Interaction interaction = currentRequest.interaction();
    SecurityScopeGenericAcl response = new SecurityScopeGenericAcl();
    response.setAllows(new SecurityScopeAclGenericAllows());
    response.setFields(new SecurityScopeAclFields());
    response.getFields().setNoEditables(new ArrayList<>());
    response.getFields().setNoVisibles(new ArrayList<>());
    fixedFields(response.getFields(), interaction);
    hiddenFields(response.getFields(), interaction);
    listAllows(response, interaction);
    createAllows(response, interaction);
    updateAllows(response, interaction);
    deleteAllows(response, interaction);
    retrieveAllows(response, interaction);
    disableAllows(response, interaction);
    enableAllows(response, interaction);
    return Response.ok(response).build();
  }

  /**
   * @autogenerated AclControllerGenerator
   * @param response
   * @param query
   */
  private void createAllows(final SecurityScopeGenericAcl response, final Interaction query) {
    Allow detail = create.allow(query);
    response.getAllows()
        .setCreate(new CommonAllow().allowed(detail.isAllowed()).reason(detail.getDescription()));
  }

  /**
   * @autogenerated AclControllerGenerator
   * @param response
   * @param query
   */
  private void deleteAllows(final SecurityScopeGenericAcl response, final Interaction query) {
    Allow detail = delete.allow(query);
    response.getAllows()
        .setDelete(new CommonAllow().allowed(detail.isAllowed()).reason(detail.getDescription()));
  }

  /**
   * @autogenerated AclControllerGenerator
   * @param response
   * @param query
   */
  private void deleteAllows(final SecurityScopeSpecificAcl response, final Interaction query) {
    Allow detail = delete.allow(query);
    response.getAllows()
        .setDelete(new CommonAllow().allowed(detail.isAllowed()).reason(detail.getDescription()));
  }

  /**
   * @autogenerated AclControllerGenerator
   * @param response
   * @param query
   */
  private void disableAllows(final SecurityScopeGenericAcl response, final Interaction query) {
    Allow detail = disable.allow(query);
    response.getAllows()
        .setDisable(new CommonAllow().allowed(detail.isAllowed()).reason(detail.getDescription()));
  }

  /**
   * @autogenerated AclControllerGenerator
   * @param response
   * @param query
   */
  private void disableAllows(final SecurityScopeSpecificAcl response, final Interaction query) {
    Allow detail = disable.allow(query);
    response.getAllows()
        .setDisable(new CommonAllow().allowed(detail.isAllowed()).reason(detail.getDescription()));
  }

  /**
   * @autogenerated AclControllerGenerator
   * @param response
   * @param query
   */
  private void enableAllows(final SecurityScopeGenericAcl response, final Interaction query) {
    Allow detail = enable.allow(query);
    response.getAllows()
        .setEnable(new CommonAllow().allowed(detail.isAllowed()).reason(detail.getDescription()));
  }

  /**
   * @autogenerated AclControllerGenerator
   * @param response
   * @param query
   */
  private void enableAllows(final SecurityScopeSpecificAcl response, final Interaction query) {
    Allow detail = enable.allow(query);
    response.getAllows()
        .setEnable(new CommonAllow().allowed(detail.isAllowed()).reason(detail.getDescription()));
  }

  /**
   * @autogenerated AclControllerGenerator
   * @param response
   * @param query
   */
  private void fixedFields(final SecurityScopeAclFields response, final Interaction query) {
    visibility.fieldsToFix(query).forEach(field -> response.getNoEditables().add(field));
  }

  /**
   * @autogenerated AclControllerGenerator
   * @param response
   * @param query
   */
  private void hiddenFields(final SecurityScopeAclFields response, final Interaction query) {
    visibility.fieldsToHide(query).forEach(field -> response.getNoVisibles().add(field));
  }

  /**
   * @autogenerated AclControllerGenerator
   * @param response
   * @param query
   */
  private void listAllows(final SecurityScopeGenericAcl response, final Interaction query) {
    Allow detail = list.allow(query);
    response.getAllows()
        .setList(new CommonAllow().allowed(detail.isAllowed()).reason(detail.getDescription()));
  }

  /**
   * @autogenerated AclControllerGenerator
   * @param response
   * @param query
   */
  private void retrieveAllows(final SecurityScopeGenericAcl response, final Interaction query) {
    Allow detail = retrieve.allow(query);
    response.getAllows()
        .setRetrieve(new CommonAllow().allowed(detail.isAllowed()).reason(detail.getDescription()));
  }

  /**
   * @autogenerated AclControllerGenerator
   * @param response
   * @param query
   */
  private void retrieveAllows(final SecurityScopeSpecificAcl response, final Interaction query) {
    Allow detail = retrieve.allow(query);
    response.getAllows()
        .setRetrieve(new CommonAllow().allowed(detail.isAllowed()).reason(detail.getDescription()));
  }

  /**
   * @autogenerated AclControllerGenerator
   * @param response
   * @param query
   */
  private void updateAllows(final SecurityScopeGenericAcl response, final Interaction query) {
    Allow detail = update.allow(query);
    response.getAllows()
        .setUpdate(new CommonAllow().allowed(detail.isAllowed()).reason(detail.getDescription()));
  }

  /**
   * @autogenerated AclControllerGenerator
   * @param response
   * @param query
   */
  private void updateAllows(final SecurityScopeSpecificAcl response, final Interaction query) {
    Allow detail = update.allow(query);
    response.getAllows()
        .setUpdate(new CommonAllow().allowed(detail.isAllowed()).reason(detail.getDescription()));
  }
}
