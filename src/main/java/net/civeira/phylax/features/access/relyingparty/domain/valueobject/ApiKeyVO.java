package net.civeira.phylax.features.access.relyingparty.domain.valueobject;

import java.util.Map;

import lombok.EqualsAndHashCode;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.ConstraintException;
import net.civeira.phylax.common.value.validation.AbstractFailList;
import net.civeira.phylax.common.value.validation.ConstraintFail;
import net.civeira.phylax.common.value.validation.ConstraintFailList;

/**
 * Value Object representing a identification for the aplication of relyingParty.
 * <p>
 * This class ensures that the api key is a non-null {@code String} and encapsulates its validation
 * and formatting logic. It is immutable and compliant with DDD (Domain-Driven Design) principles.
 * </p>
 * <p>
 * Use {@link #from(String)} or {@link #tryFrom(Object)} to safely create instances. Validation
 * errors are reported via {@link ConstraintFailList} or thrown as {@link ConstraintException}.
 * </p>
 */
@RequiredArgsConstructor
@EqualsAndHashCode
public class ApiKeyVO {

  /**
   * Crear an empty instance with no value
   *
   * @autogenerated ValueObjectGenerator
   * @param apiKey
   * @return An empty instance
   */
  public static ApiKeyVO from(final String apiKey) {
    return tryFrom(apiKey);
  }

  /**
   * Crear an empty instance with no value
   *
   * @autogenerated ValueObjectGenerator
   * @param apiKey
   * @return An empty instance
   */
  public static ApiKeyVO tryFrom(final Object apiKey) {
    ConstraintFailList list = new ConstraintFailList();
    ApiKeyVO result = tryFrom(apiKey, list);
    if (list.hasErrors()) {
      throw new ConstraintException("Invalid value for ApiKeyVO.api-key", list);
    }
    return result;
  }

  /**
   * Crear an empty instance with no value
   *
   * @autogenerated ValueObjectGenerator
   * @param apiKey temptative value
   * @param fails Error list
   * @return An empty instance
   */
  public static <T extends AbstractFailList> ApiKeyVO tryFrom(final Object apiKey, final T fails) {
    if (null == apiKey) {
      fails.add(new ConstraintFail("not-null", "apiKey", null, "Cant be null"));
      return null;
    } else if (apiKey instanceof String castedApiKey) {
      return new ApiKeyVO(castedApiKey);
    } else {
      fails.add(new ConstraintFail("wrong-type", "apiKey", apiKey.getClass(),
          "A String type is expected for apiKey"));
      return null;
    }
  }

  /**
   * The actual value of the name. * It is guaranteed to be a non-null String after successful
   * value.validation.
   *
   * @autogenerated ValueObjectGenerator
   */
  private final String value;

  /**
   * @autogenerated ValueObjectGenerator
   * @param map
   */
  public void apiKeyToMap(final Map<String, String> map) {
    if (null != value) {
      map.put("apiKey", String.valueOf(value));
    }
  }

  /**
   * Get the vo value
   *
   * @autogenerated ValueObjectGenerator
   * @return
   */
  public String getApiKey() {
    return value;
  }

  /**
   * Returns a formatted string representation of the name.
   *
   * @autogenerated ValueObjectGenerator
   * @return the string value wrapped in square brackets.
   */
  @Override
  public String toString() {
    return "[" + value + "]";
  }
}
