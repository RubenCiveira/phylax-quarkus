package net.civeira.phylax.features.access.relyingparty.infrastructure.driver.rest;

import java.util.List;
import java.util.stream.Stream;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.SpanKind;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Context;
import jakarta.enterprise.context.ApplicationScoped;
import jakarta.transaction.Transactional;
import jakarta.ws.rs.core.Response;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.BatchIdentificator;
import net.civeira.phylax.common.batch.BatchProgress;
import net.civeira.phylax.common.infrastructure.CurrentRequest;
import net.civeira.phylax.features.access.relyingparty.application.usecase.enable.RelyingPartyEnableFilter;
import net.civeira.phylax.features.access.relyingparty.application.usecase.enable.RelyingPartyEnableProjection;
import net.civeira.phylax.features.access.relyingparty.application.usecase.enable.RelyingPartyEnableStatus;
import net.civeira.phylax.features.access.relyingparty.application.usecase.enable.RelyingPartyEnableUsecase;
import net.civeira.phylax.features.access.relyingparty.domain.RelyingPartyReference;
import net.civeira.phylax.generated.openapi.model.RelyingPartyApiDto;

@RequiredArgsConstructor
@ApplicationScoped
public class RelyingPartyEnableController {

  /**
   * @autogenerated ActionControllerGenerator
   */
  private final CurrentRequest currentRequest;

  /**
   * @autogenerated ActionControllerGenerator
   */
  private final RelyingPartyEnableUsecase enable;

  /**
   * @autogenerated ActionControllerGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated ActionControllerGenerator
   * @param uids
   * @param search
   * @param apiKey
   * @param code
   * @return
   */
  public Response relyingPartyApiBatchEnable(final List<String> uids, final String search,
      final String apiKey, final String code) {
    Span span = tracer.spanBuilder("Api to enable on massive entitites of relying party")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      RelyingPartyEnableFilter.RelyingPartyEnableFilterBuilder filterBuilder =
          RelyingPartyEnableFilter.builder();
      filterBuilder =
          filterBuilder.uids(uids.stream().flatMap(part -> Stream.of(part.split(","))).toList());
      filterBuilder = filterBuilder.search(search);
      filterBuilder = filterBuilder.uids(
          null == uids ? null : uids.stream().flatMap(part -> Stream.of(part.split(","))).toList());
      filterBuilder = filterBuilder.search(search);
      filterBuilder = filterBuilder.apiKey(apiKey);
      filterBuilder = filterBuilder.code(code);
      RelyingPartyEnableFilter filter = filterBuilder.build();
      BatchIdentificator task = enable.enable(currentRequest.interaction(), filter);
      /* .header("Last-Modified", value.getSince().format(DateTimeFormatter.RFC_1123_DATE_TIME)) */
      return Response.ok(task).build();
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated ActionControllerGenerator
   * @param batchId
   * @return
   */
  public Response relyingPartyApiBatchEnableQuery(final String batchId) {
    Span span = tracer.spanBuilder("Api to check masive enable on entity of relying party")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      BatchProgress task = enable.checkProgress(
          RelyingPartyEnableStatus.builder().taskId(batchId).build(currentRequest.interaction()));
      return Response.ok(task).build();
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated ActionControllerGenerator
   * @param uid
   * @return
   */
  @Transactional
  public Response relyingPartyApiEnable(final String uid) {
    Span span = tracer.spanBuilder("Api to enable an entity of relying party")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      span.setAttribute("ref", uid);
      RelyingPartyEnableProjection updated =
          enable.enable(currentRequest.interaction(), RelyingPartyReference.of(uid));
      return Response.ok(toApiModel(updated)).build();
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated ActionControllerGenerator
   * @param dto
   * @return
   */
  private RelyingPartyApiDto toApiModel(RelyingPartyEnableProjection dto) {
    Span span = tracer
        .spanBuilder(
            "Mapper to build api content to response from model for entity of relying party")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      RelyingPartyApiDto relyingPartyApiDto = new RelyingPartyApiDto();
      relyingPartyApiDto.setUid(dto.getUid());
      relyingPartyApiDto.setCode(dto.getCode());
      relyingPartyApiDto.setApiKey(dto.getApiKey());
      relyingPartyApiDto.setEnabled(dto.getEnabled());
      relyingPartyApiDto.setVersion(dto.getVersion());
      return relyingPartyApiDto;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }
}
