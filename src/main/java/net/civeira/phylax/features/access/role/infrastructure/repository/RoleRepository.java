  public List<Role> listForUpdate(final String sql, final Map<String, SqlParameterValue> params,
      final int limit) {
    return list(sql, params, Integer.valueOf(limit), true);
  }

  /**
   * @autogenerated RepositoryJdbcGenerator
   * @param reference
   * @return
   */
  public Role resolve(RoleRef reference) {
    return reference instanceof Role role ? role
        : retrieve(reference.getUid(), Optional.empty()).orElseThrow(() -> new NotFoundException(
            "Trying to resolve inexistent Role: " + reference.getUid()));
  }

  /**
   * @autogenerated RepositoryJdbcGenerator
   * @param reference
   * @return
   */
  public Role resolveForUpdate(RoleRef reference) {
    return reference instanceof Role role ? role
        : retrieveForUpdate(reference.getUid(), Optional.empty())
            .orElseThrow(() -> new NotFoundException(
                "Trying to resolve inexistent Role: " + reference.getUid()));
  }

  /**
   * @autogenerated RepositoryJdbcGenerator
   * @param uid
   * @param filter
   * @return
   */
  public Optional<Role> retrieve(String uid, Optional<RoleFilter> filter) {
    try (SqlTemplate template = new SqlTemplate(datasource, tracer)) {
      RoleFilter readyFilter = filter.map(val -> val.withUid(uid))
          .orElseGet(() -> RoleFilter.builder().uid(uid).build());
      SqlSchematicQuery<Role> sq = filteredQuery(template, readyFilter);
      return sq.query(converter()).one();
    }
  }

  /**
   * @autogenerated RepositoryJdbcGenerator
   * @param uid
   * @param filter
   * @return
   */
  public Optional<Role> retrieveForUpdate(String uid, Optional<RoleFilter> filter) {
    try (SqlTemplate template = new SqlTemplate(datasource, tracer)) {
      RoleFilter readyFilter = filter.map(val -> val.withUid(uid))
          .orElseGet(() -> RoleFilter.builder().uid(uid).build());
      SqlSchematicQuery<Role> sq = filteredQuery(template, readyFilter);
      return sq.forUpdate().query(converter()).one();
    }
  }

