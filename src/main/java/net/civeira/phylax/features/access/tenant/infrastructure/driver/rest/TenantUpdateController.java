package net.civeira.phylax.features.access.tenant.infrastructure.driver.rest;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.transaction.Transactional;
import jakarta.ws.rs.core.Response;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.infrastructure.CurrentRequest;
import net.civeira.phylax.common.telemetry.ApiObserved;
import net.civeira.phylax.common.telemetry.Trace;
import net.civeira.phylax.features.access.tenant.application.usecase.update.TenantUpdateInput;
import net.civeira.phylax.features.access.tenant.application.usecase.update.TenantUpdateProjection;
import net.civeira.phylax.features.access.tenant.application.usecase.update.TenantUpdateUsecase;
import net.civeira.phylax.features.access.tenant.domain.TenantReference;
import net.civeira.phylax.generated.openapi.model.TenantApiDto;

@RequiredArgsConstructor
@ApplicationScoped
public class TenantUpdateController {

  /**
   * @autogenerated UpdateControllerGenerator
   */
  private final CurrentRequest currentRequest;

  /**
   * @autogenerated UpdateControllerGenerator
   */
  private final TenantUpdateUsecase update;

  /**
   * @autogenerated UpdateControllerGenerator
   * @param uid
   * @param tenant
   * @return
   */
  @Transactional
  @ApiObserved("Api to update an entity of tenant")
  public Response tenantApiUpdate(final String uid, final TenantApiDto tenant) {
    Trace.setAttribute("ref", uid);
    TenantUpdateProjection updated =
        update.update(currentRequest.interaction(), TenantReference.of(uid), toDomainModel(tenant));
    return Response.ok(toApiModel(updated)).build();
  }

  /**
   * @autogenerated UpdateControllerGenerator
   * @param dto
   * @return
   */
  private TenantApiDto toApiModel(TenantUpdateProjection dto) {
    TenantApiDto tenantApiDto = new TenantApiDto();
    tenantApiDto.setUid(dto.getUid());
    tenantApiDto.setName(dto.getName());
    tenantApiDto.setRoot(dto.getRoot());
    tenantApiDto.setDomain(dto.getDomain());
    tenantApiDto.setEnabled(dto.getEnabled());
    tenantApiDto.setMarkForDelete(dto.getMarkForDelete());
    tenantApiDto.setMarkForDeleteTime(dto.getMarkForDeleteTime());
    tenantApiDto.setVersion(dto.getVersion());
    return tenantApiDto;
  }

  /**
   * @autogenerated UpdateControllerGenerator
   * @param tenantApiDto
   * @return
   */
  private TenantUpdateInput toDomainModel(TenantApiDto tenantApiDto) {
    TenantUpdateInput dto = new TenantUpdateInput();
    if (null != tenantApiDto.getUid()) {
      dto.setUid(tenantApiDto.getUid());
    }
    if (null != tenantApiDto.getName()) {
      dto.setName(tenantApiDto.getName());
    }
    if (null != tenantApiDto.getRoot()) {
      dto.setRoot(tenantApiDto.getRoot());
    }
    if (null != tenantApiDto.getDomain()) {
      dto.setDomain(tenantApiDto.getDomain());
    }
    if (null != tenantApiDto.getEnabled()) {
      dto.setEnabled(tenantApiDto.getEnabled());
    }
    if (null != tenantApiDto.getMarkForDelete()) {
      dto.setMarkForDelete(tenantApiDto.getMarkForDelete());
    }
    if (null != tenantApiDto.getMarkForDeleteTime()) {
      dto.setMarkForDeleteTime(tenantApiDto.getMarkForDeleteTime());
    }
    if (null != tenantApiDto.getVersion()) {
      dto.setVersion(tenantApiDto.getVersion());
    }
    return dto;
  }
}
