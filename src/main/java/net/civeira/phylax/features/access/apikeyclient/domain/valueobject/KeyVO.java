package net.civeira.phylax.features.access.apikeyclient.domain.valueobject;

import java.util.Map;
import java.util.Optional;

import lombok.EqualsAndHashCode;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.ConstraintException;
import net.civeira.phylax.common.value.validation.AbstractFailList;
import net.civeira.phylax.common.value.validation.ConstraintFail;
import net.civeira.phylax.common.value.validation.ConstraintFailList;

/**
 * Value Object representing the key for the api of apiKeyClient.
 * <p>
 * This class ensures that the key is a {@code String} and encapsulates its validation and
 * formatting logic. It is immutable and compliant with DDD (Domain-Driven Design) principles.
 * </p>
 * <p>
 * Use {@link #from(String)} or {@link #tryFrom(Object)} to safely create instances. Validation
 * errors are reported via {@link ConstraintFailList} or thrown as {@link ConstraintException}.
 * </p>
 */
@RequiredArgsConstructor
@EqualsAndHashCode
public class KeyVO {

  /**
   * Crear an empty instance with no value
   *
   * @autogenerated ValueObjectGenerator
   * @param key
   * @return An empty instance
   */
  public static KeyVO from(final String key) {
    return tryFrom(key);
  }

  /**
   * Crear an empty instance with no value
   *
   * @autogenerated ValueObjectGenerator
   * @return An empty instance
   */
  public static KeyVO nullValue() {
    return new KeyVO(null);
  }

  /**
   * Crear an empty instance with no value
   *
   * @autogenerated ValueObjectGenerator
   * @param key
   * @return An empty instance
   */
  public static KeyVO tryFrom(final Object key) {
    ConstraintFailList list = new ConstraintFailList();
    KeyVO result = tryFrom(key, list);
    if (list.hasErrors()) {
      throw new ConstraintException("Invalid value for KeyVO.key", list);
    }
    return result;
  }

  /**
   * Crear an empty instance with no value
   *
   * @autogenerated ValueObjectGenerator
   * @param key temptative value
   * @param fails Error list
   * @return An empty instance
   */
  public static <T extends AbstractFailList> KeyVO tryFrom(final Object key, final T fails) {
    if (null == key) {
      return new KeyVO(null);
    } else if (key instanceof String castedKey) {
      return new KeyVO(castedKey);
    } else {
      fails.add(new ConstraintFail("wrong-type", "key", key.getClass(),
          "A String type is expected for key"));
      return null;
    }
  }

  /**
   * The actual value of the name.
   *
   * @autogenerated ValueObjectGenerator
   */
  private final String value;

  /**
   * Get the vo value
   *
   * @autogenerated ValueObjectGenerator
   * @return
   */
  public Optional<String> getKey() {
    return Optional.ofNullable(value);
  }

  /**
   * @autogenerated ValueObjectGenerator
   * @param map
   */
  public void keyToMap(final Map<String, String> map) {
    if (null != value) {
      map.put("key", String.valueOf(value));
    }
  }

  /**
   * Returns a formatted string representation of the name.
   *
   * @autogenerated ValueObjectGenerator
   * @return the string value wrapped in square brackets.
   */
  @Override
  public String toString() {
    return "[" + value + "]";
  }
}
