package net.civeira.phylax.features.access.useridentity.infrastructure.driven;

import java.util.List;
import java.util.Optional;
import java.util.function.Predicate;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.SpanKind;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Context;
import jakarta.enterprise.context.ApplicationScoped;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.algorithms.Slider;
import net.civeira.phylax.features.access.useridentity.domain.UserIdentity;
import net.civeira.phylax.features.access.useridentity.domain.UserIdentityRef;
import net.civeira.phylax.features.access.useridentity.domain.gateway.UserIdentityCursor;
import net.civeira.phylax.features.access.useridentity.domain.gateway.UserIdentityFilter;
import net.civeira.phylax.features.access.useridentity.domain.gateway.UserIdentityWriteRepositoryGateway;
import net.civeira.phylax.features.access.useridentity.infrastructure.event.UserIdentityEventDispatcher;
import net.civeira.phylax.features.access.useridentity.infrastructure.repository.UserIdentityRepository;

@ApplicationScoped
@RequiredArgsConstructor
public class UserIdentityWriteGatewayAdapter implements UserIdentityWriteRepositoryGateway {

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   */
  private final UserIdentityEventDispatcher eventDispatcher;

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   */
  private final UserIdentityRepository repository;

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param filter
   * @return
   */
  @Override
  public long countForUpdate(UserIdentityFilter filter) {
    Span span = tracer
        .spanBuilder("Count on the repository for update the matches entities of user identity")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.count(filter);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param entity
   * @return
   */
  @Override
  public UserIdentity create(UserIdentity entity) {
    Span span = tracer.spanBuilder("Create on the repository an entity of user identity")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      UserIdentity result = repository.create(entity);
      eventDispatcher.dispatch(entity);
      return result;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param entity
   * @param verifier
   * @return
   */
  @Override
  public UserIdentity create(UserIdentity entity, Predicate<UserIdentity> verifier) {
    Span span = tracer
        .spanBuilder(
            "Create on the repository an entity of user identity and verify its visibility")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      UserIdentity result = repository.create(entity, verifier);
      eventDispatcher.dispatch(entity);
      return result;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param entity
   */
  @Override
  public void delete(UserIdentity entity) {
    Span span = tracer
        .spanBuilder("Delete from the repository a slide for entities of user identity")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      repository.delete(entity);
      eventDispatcher.dispatch(entity);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param uid
   * @param filter
   * @return
   */
  @Override
  public boolean existsForUpdate(String uid, Optional<UserIdentityFilter> filter) {
    Span span = tracer
        .spanBuilder("Check if exists on the repository for update a entity of user identity")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.existsForUpdate(uid, filter);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param filter
   * @return
   */
  @Override
  public Optional<UserIdentity> findForUpdate(UserIdentityFilter filter) {
    Span span = tracer.spanBuilder("Find on the repository for update one entity of user identity")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.findForUpdate(filter);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param filter
   * @return
   */
  @Override
  public List<UserIdentity> listForUpdate(UserIdentityFilter filter) {
    Span span = tracer
        .spanBuilder("Retrieve from repository for update a list for entities of user identity")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.listForUpdate(filter);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param reference
   * @return
   */
  @Override
  public UserIdentity resolveForUpdate(UserIdentityRef reference) {
    Span span = tracer
        .spanBuilder("Enrich from the repository for update a entity of user identity")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.resolveForUpdate(reference);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param uid
   * @param filter
   * @return
   */
  @Override
  public Optional<UserIdentity> retrieveForUpdate(String uid, Optional<UserIdentityFilter> filter) {
    Span span = tracer.spanBuilder("Retrieve from repository a slide for entities of user identity")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.retrieveForUpdate(uid, filter);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param filter
   * @param cursor
   * @return
   */
  @Override
  public Slider<UserIdentity> slideForUpdate(UserIdentityFilter filter, UserIdentityCursor cursor) {
    Span span = tracer
        .spanBuilder("Retrieve from repository for update a slide for entities of user identity")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.slideForUpdate(filter, cursor);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param reference
   * @param entity
   * @return
   */
  @Override
  public UserIdentity update(UserIdentityRef reference, UserIdentity entity) {
    UserIdentity result = repository.update(entity);
    eventDispatcher.dispatch(entity);
    return result;
  }
}
