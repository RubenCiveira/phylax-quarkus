package net.civeira.phylax.features.access.tenanttermsofuse.transition.update;

import java.util.ArrayList;
import java.util.List;
import java.util.function.Function;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.inject.Instance;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.features.access.tenanttermsofuse.TenantTermsOfUse;
import net.civeira.phylax.features.access.tenanttermsofuse.command.TenantTermsOfUseWriteAttributes;
import net.civeira.phylax.features.access.tenanttermsofuse.transition.TenantTermsOfUseChangeProposal;

@ApplicationScoped
@RequiredArgsConstructor
public class TenantTermsOfUseUpdatePipeline {

  /**
   * @autogenerated UpdateEntityRuleGenerator
   */
  private final Instance<TenantTermsOfUseUpdateStep> steps;

  /**
   * @autogenerated UpdateEntityRuleGenerator
   * @param attributes
   * @param original
   * @param last
   * @return
   */
  public TenantTermsOfUse update(final TenantTermsOfUseWriteAttributes attributes,
      final TenantTermsOfUse original,
      final Function<TenantTermsOfUseChangeProposal, TenantTermsOfUse> last) {
    Function<TenantTermsOfUseUpdateTransition, TenantTermsOfUse> finalHandler = t -> {
      if (t.hasErrors()) {
        throw t.asConstraintException();
      }
      return last.apply(t.getData());
    };
    List<TenantTermsOfUseUpdateStep> reversed = new ArrayList<>(steps.stream().toList()).reversed();
    Function<TenantTermsOfUseUpdateTransition, TenantTermsOfUse> pipeline =
        reversed.stream().reduce(finalHandler,
            (next, step) -> transition -> step.update(transition, next), (f1, f2) -> f1);
    return pipeline.apply(TenantTermsOfUseUpdateTransition.builder()
        .data(new TenantTermsOfUseChangeProposal(attributes)).original(original).build());
  }
}
