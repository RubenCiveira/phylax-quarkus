package net.civeira.phylax.features.access.tenantconfig.application.service.visibility;

import java.util.Optional;
import java.util.function.Consumer;

import lombok.Builder;
import lombok.Data;
import lombok.EqualsAndHashCode;
import lombok.NonNull;
import lombok.With;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.tenantconfig.TenantConfig;
import net.civeira.phylax.features.access.tenantconfig.application.request.TenantConfigStateChange;

@Data
@Builder(toBuilder = true)
@With
@EqualsAndHashCode(onlyExplicitlyIncluded = true)
public class TenantConfigPresetProposal {

  /**
   * @autogenerated PresetProposalGenerator
   */
  @NonNull
  private final Interaction interaction;

  /**
   * @autogenerated PresetProposalGenerator
   */
  @NonNull
  private TenantConfigStateChange dto;

  /**
   * @autogenerated PresetProposalGenerator
   */
  private TenantConfig original;

  /**
   * @autogenerated PresetProposalGenerator
   * @return
   */
  public Optional<TenantConfig> getOriginal() {
    return Optional.ofNullable(original);
  }

  /**
   * @autogenerated PresetProposalGenerator
   * @param mapper
   */
  public void peek(Consumer<TenantConfigStateChange> mapper) {
    mapper.accept(dto);
  }
}
