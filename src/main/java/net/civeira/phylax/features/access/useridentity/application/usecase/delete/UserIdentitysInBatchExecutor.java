package net.civeira.phylax.features.access.useridentity.application.usecase.delete;

import java.util.List;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.SpanKind;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Context;
import io.quarkus.arc.Unremovable;
import jakarta.enterprise.context.ApplicationScoped;
import lombok.Data;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.stepper.ItemDescriptor;
import net.civeira.phylax.common.batch.stepper.ItemProcessor;
import net.civeira.phylax.common.batch.stepper.ItemReader;
import net.civeira.phylax.common.batch.stepper.ItemWriter;
import net.civeira.phylax.common.batch.stepper.StepContext;
import net.civeira.phylax.common.batch.stepper.StepCounter;
import net.civeira.phylax.common.batch.stepper.StepFinalizer;
import net.civeira.phylax.common.batch.stepper.StepInitializer;
import net.civeira.phylax.common.exception.ExecutionException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.features.access.useridentity.application.visibility.UserIdentitysVisibility;
import net.civeira.phylax.features.access.useridentity.domain.UserIdentity;
import net.civeira.phylax.features.access.useridentity.domain.gateway.UserIdentityAuditGateway;
import net.civeira.phylax.features.access.useridentity.domain.gateway.UserIdentityCursor;

@Unremovable
@ApplicationScoped
@RequiredArgsConstructor
class UserIdentitysInBatchExecutor implements
    StepCounter<UserIdentityDeleteAllInBatchCommand, UserIdentitysInBatchExecutor.UserIdentityPaginableBatch>,
    StepFinalizer<UserIdentityDeleteAllInBatchCommand, UserIdentitysInBatchExecutor.UserIdentityPaginableBatch>,
    StepInitializer<UserIdentityDeleteAllInBatchCommand, UserIdentitysInBatchExecutor.UserIdentityPaginableBatch>,
    ItemReader<UserIdentity, UserIdentityDeleteAllInBatchCommand, UserIdentitysInBatchExecutor.UserIdentityPaginableBatch>,
    ItemWriter<UserIdentity, UserIdentityDeleteAllInBatchCommand, UserIdentitysInBatchExecutor.UserIdentityPaginableBatch>,
    ItemProcessor<UserIdentity, UserIdentity, UserIdentityDeleteAllInBatchCommand, UserIdentitysInBatchExecutor.UserIdentityPaginableBatch>,
    ItemDescriptor<UserIdentity, UserIdentityDeleteAllInBatchCommand, UserIdentitysInBatchExecutor.UserIdentityPaginableBatch> {

  /**
   * @autogenerated EntityGenerator
   */
  @Data
  public static class UserIdentityPaginableBatch {

    /**
     * @autogenerated EntityGenerator
     */
    private String since;
  }

  /**
   * Service to register audit events
   *
   * @autogenerated EntityGenerator
   */
  private final UserIdentityAuditGateway audit;

  /**
   * @autogenerated EntityGenerator
   */
  private final int size = 10;

  /**
   * @autogenerated EntityGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated EntityGenerator
   */
  private final UserIdentityDeleteUsecase usecase;

  /**
   * @autogenerated EntityGenerator
   */
  private final UserIdentitysVisibility visibility;

  /**
   * @autogenerated EntityGenerator
   * @param context
   * @return
   */
  @Override
  public long approximatedItems(
      final StepContext<UserIdentityDeleteAllInBatchCommand, UserIdentityPaginableBatch> context) {
    Span span = tracer
        .spanBuilder(
            "Count selected items to the batch delete for a set of entities of user identity")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      return visibility.countVisibles(context.getParams().getInteraction(),
          context.getParams().getFilter());
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   */
  @Override
  public void finish(
      final StepContext<UserIdentityDeleteAllInBatchCommand, UserIdentityPaginableBatch> context) {
    usecase.cacheEvict();
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   */
  @Override
  public void init(
      final StepContext<UserIdentityDeleteAllInBatchCommand, UserIdentityPaginableBatch> context) {
    context.setState(new UserIdentityPaginableBatch());
  }

  /**
   * @autogenerated EntityGenerator
   * @param item
   * @param context
   * @return
   */
  @Override
  public String itemDescription(final UserIdentity item,
      final StepContext<UserIdentityDeleteAllInBatchCommand, UserIdentityPaginableBatch> context) {
    return item.getUid();
  }

  /**
   * @autogenerated EntityGenerator
   * @param item
   * @param context
   * @return
   */
  @Override
  public UserIdentity process(final UserIdentity item,
      final StepContext<UserIdentityDeleteAllInBatchCommand, UserIdentityPaginableBatch> context) {
    Span span = tracer
        .spanBuilder(
            "Prepare individual data for the batch delete on a set of entities of user identity")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      Allow allowed = usecase.checkAllow(context.getParams().getInteraction(), item);
      if (!allowed.isAllowed()) {
        span.addEvent(
            "The policies for user identity dont allow to delete: " + allowed.getDescription());
        throw new ExecutionException("not-allowed", null);
      }
      return item;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   * @return
   */
  @Override
  public List<UserIdentity> read(
      final StepContext<UserIdentityDeleteAllInBatchCommand, UserIdentityPaginableBatch> context) {
    Span span = tracer
        .spanBuilder(
            "Read selected entitites for a batch delete for a set of entities of user identity")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      List<UserIdentity> page = visibility.listVisiblesForUpdate(
          context.getParams().getInteraction(), context.getParams().getFilter(),
          UserIdentityCursor.builder().limit(size).sinceUid(context.getState().getSince()).build());
      context.getState().setSince(page.isEmpty() ? null : page.get(page.size() - 1).getUid());
      span.setAttribute("size", page.size());
      return page;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated EntityGenerator
   * @param items
   * @param context
   */
  @Override
  public void write(final List<UserIdentity> items,
      final StepContext<UserIdentityDeleteAllInBatchCommand, UserIdentityPaginableBatch> context) {
    Span span =
        tracer.spanBuilder("Perform the batch delete for a set of entities of user identity")
            .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
            .startSpan();
    try {
      span.setAttribute("size", items.size());
      items.forEach(item -> {
        usecase.doDelete(context.getParams().getInteraction(), item);
        usecase.cacheEvict();
        audit.deleted("batch-delete", item, context.getParams().getInteraction());
      });
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }
}
