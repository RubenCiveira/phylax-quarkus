package net.civeira.phylax.features.access.useridentity.application.usecase.delete;

import java.util.List;

import io.quarkus.arc.Unremovable;
import jakarta.enterprise.context.ApplicationScoped;
import lombok.Data;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.stepper.ItemDescriptor;
import net.civeira.phylax.common.batch.stepper.ItemProcessor;
import net.civeira.phylax.common.batch.stepper.ItemReader;
import net.civeira.phylax.common.batch.stepper.ItemWriter;
import net.civeira.phylax.common.batch.stepper.StepContext;
import net.civeira.phylax.common.batch.stepper.StepCounter;
import net.civeira.phylax.common.batch.stepper.StepFinalizer;
import net.civeira.phylax.common.batch.stepper.StepInitializer;
import net.civeira.phylax.common.exception.ExecutionException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.features.access.useridentity.UserIdentity;
import net.civeira.phylax.features.access.useridentity.application.service.visibility.UserIdentitysVisibility;
import net.civeira.phylax.features.access.useridentity.query.UserIdentityCursor;

@Unremovable
@ApplicationScoped
@RequiredArgsConstructor
class UserIdentitysInBatchExecutor implements
    StepCounter<UserIdentityDeleteAllInBatchCommand, UserIdentitysInBatchExecutor.UserIdentityPaginableBatch>,
    StepFinalizer<UserIdentityDeleteAllInBatchCommand, UserIdentitysInBatchExecutor.UserIdentityPaginableBatch>,
    StepInitializer<UserIdentityDeleteAllInBatchCommand, UserIdentitysInBatchExecutor.UserIdentityPaginableBatch>,
    ItemReader<UserIdentity, UserIdentityDeleteAllInBatchCommand, UserIdentitysInBatchExecutor.UserIdentityPaginableBatch>,
    ItemWriter<UserIdentity, UserIdentityDeleteAllInBatchCommand, UserIdentitysInBatchExecutor.UserIdentityPaginableBatch>,
    ItemProcessor<UserIdentity, UserIdentity, UserIdentityDeleteAllInBatchCommand, UserIdentitysInBatchExecutor.UserIdentityPaginableBatch>,
    ItemDescriptor<UserIdentity, UserIdentityDeleteAllInBatchCommand, UserIdentitysInBatchExecutor.UserIdentityPaginableBatch> {

  /**
   * @autogenerated EntityGenerator
   */
  @Data
  public static class UserIdentityPaginableBatch {

    /**
     * @autogenerated EntityGenerator
     */
    private String since;
  }

  /**
   * @autogenerated EntityGenerator
   */
  private final int size = 10;

  /**
   * @autogenerated EntityGenerator
   */
  private final UserIdentityDeleteUsecase usecase;

  /**
   * @autogenerated EntityGenerator
   */
  private final UserIdentitysVisibility visibility;

  /**
   * @autogenerated EntityGenerator
   * @param context
   * @return
   */
  @Override
  public long approximatedItems(
      final StepContext<UserIdentityDeleteAllInBatchCommand, UserIdentityPaginableBatch> context) {
    return visibility.countVisibles(context.getParams().getInteraction(),
        context.getParams().getFilter());
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   */
  @Override
  public void finish(
      final StepContext<UserIdentityDeleteAllInBatchCommand, UserIdentityPaginableBatch> context) {
    usecase.flush();
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   */
  @Override
  public void init(
      final StepContext<UserIdentityDeleteAllInBatchCommand, UserIdentityPaginableBatch> context) {
    context.setState(new UserIdentityPaginableBatch());
  }

  /**
   * @autogenerated EntityGenerator
   * @param item
   * @param context
   * @return
   */
  @Override
  public String itemDescription(final UserIdentity item,
      final StepContext<UserIdentityDeleteAllInBatchCommand, UserIdentityPaginableBatch> context) {
    return item.getUidValue();
  }

  /**
   * @autogenerated EntityGenerator
   * @param item
   * @param context
   * @return
   */
  @Override
  public UserIdentity process(final UserIdentity item,
      final StepContext<UserIdentityDeleteAllInBatchCommand, UserIdentityPaginableBatch> context) {
    Allow allowed = usecase.allow(context.getParams().getInteraction(), item);
    if (!allowed.isAllowed()) {
      throw new ExecutionException("not-allowed", null);
    }
    return item;
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   * @return
   */
  @Override
  public List<UserIdentity> read(
      final StepContext<UserIdentityDeleteAllInBatchCommand, UserIdentityPaginableBatch> context) {
    List<UserIdentity> page = visibility.listVisiblesForUpdate(context.getParams().getInteraction(),
        context.getParams().getFilter(),
        UserIdentityCursor.builder().limit(size).sinceUid(context.getState().getSince()).build());
    context.getState().setSince(page.isEmpty() ? null : page.get(page.size() - 1).getUidValue());
    return page;
  }

  /**
   * @autogenerated EntityGenerator
   * @param items
   * @param context
   */
  @Override
  public void write(final List<UserIdentity> items,
      final StepContext<UserIdentityDeleteAllInBatchCommand, UserIdentityPaginableBatch> context) {
    items.forEach(item -> usecase.delete(context.getParams().getInteraction(), item));
  }
}
