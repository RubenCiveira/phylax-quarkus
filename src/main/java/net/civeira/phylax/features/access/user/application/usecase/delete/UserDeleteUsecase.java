package net.civeira.phylax.features.access.user.application.usecase.delete;

import java.time.Duration;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.BatchIdentificator;
import net.civeira.phylax.common.batch.BatchProgress;
import net.civeira.phylax.common.batch.BatchService;
import net.civeira.phylax.common.batch.ExecutorByDeferSteps;
import net.civeira.phylax.common.batch.ExecutorPlan;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.user.User;
import net.civeira.phylax.features.access.user.UserFacade;
import net.civeira.phylax.features.access.user.UserRef;
import net.civeira.phylax.features.access.user.application.service.visibility.UsersVisibility;
import net.civeira.phylax.features.access.user.gateway.UserCacheGateway;
import net.civeira.phylax.features.access.user.gateway.UserWriteRepositoryGateway;
import net.civeira.phylax.features.access.user.query.UserFilter;

@ApplicationScoped
@RequiredArgsConstructor
public class UserDeleteUsecase {

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  private final BatchService batch;

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  private final UserCacheGateway cache;

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  private final Event<UserAllowDeleteProposal> deleteAllowEmitter;

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  private final Event<UserDeleteProposal> deleteProposalEmitter;

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  private final Event<UserDeleteEvent> deletedEmitter;

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  private final UserFacade facade;

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  private final UserWriteRepositoryGateway gateway;

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  private final UsersVisibility visibility;

  /**
   * @autogenerated DeleteUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  public Allow allow(final Interaction query, final UserRef reference) {
    UserAllowDeleteProposal base = UserAllowDeleteProposal.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).reference(reference).build();
    deleteAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated DeleteUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    UserAllowDeleteProposal base = UserAllowDeleteProposal.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).build();
    deleteAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * The slide with some values
   *
   * @autogenerated DeleteUsecaseGenerator
   * @param query a filter to retrieve only matching values
   * @return The slide with some values
   */
  public BatchProgress checkProgress(final UserCheckBatchDeleteStatus query) {
    return query.getActor().getName()
        .flatMap(name -> batch.retrieve(query.getTaskId(), query.getConnection().getLocale(), name))
        .orElseThrow(() -> new NotFoundException());
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated DeleteUsecaseGenerator
   * @param query
   * @param reference
   */
  public void delete(final Interaction query, final UserRef reference) {
    Allow detail = allow(query, reference);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    User original = visibility.retrieveVisibleForUpdate(query, reference.getUidValue())
        .orElseThrow(() -> new NotFoundException(""));
    delete(query, original);
    flush();
  }

  /**
   * The slide with some values
   *
   * @autogenerated DeleteUsecaseGenerator
   * @param query
   * @param filter
   * @return The slide with some values
   */
  public BatchIdentificator delete(final Interaction query, final UserFilter filter) {
    Allow detail = allow(query);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    UserDeleteAllInBatchCommand command =
        UserDeleteAllInBatchCommand.builder().interaction(query).filter(filter).build();
    return batch.start(query.getActor().getName().orElse("-"), Duration.ofHours(6), ExecutorPlan
        .<UserDeleteAllInBatchCommand>builder().params(command).name("delete-color")
        .executor(
            ExecutorByDeferSteps.<User, User, UserDeleteAllInBatchCommand, UsersInBatchExecutor.UserPaginableBatch>builder()
                .initializer(UsersInBatchExecutor.class).counter(UsersInBatchExecutor.class)
                .descriptor(UsersInBatchExecutor.class).reader(UsersInBatchExecutor.class)
                .processor(UsersInBatchExecutor.class).writer(UsersInBatchExecutor.class)
                .finalizer(UsersInBatchExecutor.class).build())
        .build());
  }

  /**
   * @autogenerated DeleteUsecaseGenerator
   * @param interaction
   * @param original
   */
  void delete(final Interaction interaction, final User original) {
    User clean = facade.delete(original);
    UserDeleteProposal proposal =
        UserDeleteProposal.builder().entity(clean).interaction(interaction).build();
    deleteProposalEmitter.fire(proposal);
    User result = proposal.getEntity();
    gateway.delete(result);
    UserDeleteEvent event =
        UserDeleteEvent.builder().payload(result).interaction(interaction).build();
    deletedEmitter.fire(event);
  }

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  void flush() {
    cache.evict();
  }
}
