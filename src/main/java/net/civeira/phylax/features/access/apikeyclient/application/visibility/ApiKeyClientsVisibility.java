package net.civeira.phylax.features.access.apikeyclient.application.visibility;

import java.util.ArrayList;
import java.util.HashSet;
import java.util.Iterator;
import java.util.List;
import java.util.Optional;
import java.util.Set;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.apikeyclient.domain.ApiKeyClient;
import net.civeira.phylax.features.access.apikeyclient.domain.ApiKeyClientChangeSet;
import net.civeira.phylax.features.access.apikeyclient.domain.ApiKeyClientRef;
import net.civeira.phylax.features.access.apikeyclient.domain.gateway.ApiKeyClientCacheGateway;
import net.civeira.phylax.features.access.apikeyclient.domain.gateway.ApiKeyClientCached;
import net.civeira.phylax.features.access.apikeyclient.domain.gateway.ApiKeyClientCursor;
import net.civeira.phylax.features.access.apikeyclient.domain.gateway.ApiKeyClientFilter;
import net.civeira.phylax.features.access.apikeyclient.domain.gateway.ApiKeyClientReadRepositoryGateway;
import net.civeira.phylax.features.access.apikeyclient.domain.gateway.ApiKeyClientWriteRepositoryGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class ApiKeyClientsVisibility {

  /**
   * @autogenerated VisibilityServiceGenerator
   */
  private final ApiKeyClientReadRepositoryGateway apiKeyClientReadRepositoryGateway;

  /**
   * @autogenerated VisibilityServiceGenerator
   */
  private final ApiKeyClientWriteRepositoryGateway apiKeyClientWriteRepositoryGateway;

  /**
   * @autogenerated VisibilityServiceGenerator
   */
  private final ApiKeyClientCacheGateway cache;

  /**
   * @autogenerated VisibilityServiceGenerator
   */
  private final Event<ApiKeyClientVisibilityCheck> filterProposalEmitter;

  /**
   * @autogenerated VisibilityServiceGenerator
   */
  private final Event<ApiKeyClientFixedFieldsCheck> fixedFieldsEmitter;

  /**
   * @autogenerated VisibilityServiceGenerator
   */
  private final Event<ApiKeyClientAccesibleCheck> guardEmitter;

  /**
   * @autogenerated VisibilityServiceGenerator
   */
  private final Event<ApiKeyClientHiddenFieldsCheck> hiddenFieldsEmitter;

  /**
   * @autogenerated VisibilityServiceGenerator
   */
  private final Event<ApiKeyClientEntityEnrichment> presetEmitter;

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param uid
   * @return The input entity with the copy values without hidden
   */
  public boolean checkVisibility(Interaction prev, String uid) {
    return retrieveVisible(prev, uid).isPresent();
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param uids
   * @return The input entity with the copy values without hidden
   */
  public boolean checkVisibility(Interaction prev, List<String> uids) {
    return uids
        .size() == listVisibles(prev, ApiKeyClientVisibilityFilter.builder().uids(uids).build(),
            ApiKeyClientCursor.builder().build()).size();
  }

  /**
   * Retrieve the list of fields
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param original The source interaction
   * @param source The source interaction
   * @return The input entity with the copy values without hidden
   */
  public ApiKeyClientChangeSet copyWithFixed(Interaction prev, ApiKeyClient original,
      ApiKeyClientChangeSet source) {
    fieldsToFix(prev, original).forEach(source::unset);
    ApiKeyClientEntityEnrichment proposal = ApiKeyClientEntityEnrichment.builder().dto(source)
        .interaction(prev).original(original).build();
    presetEmitter.fire(proposal);
    return proposal.getDto();
  }

  /**
   * Retrieve the list of fields
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param source The source interaction
   * @return The input entity with the copy values without hidden
   */
  public ApiKeyClientChangeSet copyWithFixed(Interaction prev, ApiKeyClientChangeSet source) {
    fieldsToFix(prev).forEach(source::unset);
    ApiKeyClientEntityEnrichment proposal =
        ApiKeyClientEntityEnrichment.builder().dto(source).interaction(prev).build();
    presetEmitter.fire(proposal);
    return proposal.getDto();
  }

  /**
   * Retrieve the list of fields
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param apiKeyClient
   * @return The input dto with hidden values
   */
  public ApiKeyClientVisibleProjection copyWithHidden(Interaction prev, ApiKeyClient apiKeyClient) {
    ApiKeyClientVisibleProjection target = new ApiKeyClientVisibleProjection(apiKeyClient);
    fieldsToHide(prev, apiKeyClient).forEach(target::unset);
    return target;
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param filter The filter to retrieve values
   * @return The input entity with the copy values without hidden
   */
  public long countVisibles(Interaction prev, ApiKeyClientVisibilityFilter filter) {
    return apiKeyClientReadRepositoryGateway.count(applyPreVisibilityFilter(prev, filter));
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @return initialsFixFields
   */
  public Set<String> fieldsToFix(Interaction prev) {
    Set<String> fields = new HashSet<>(fieldsToHide(prev));
    ApiKeyClientFixedFieldsCheck proposal =
        ApiKeyClientFixedFieldsCheck.builder().fields(fields).query(prev).build();
    fixedFieldsEmitter.fire(proposal);
    return proposal.getFields();
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param apiKeyClient
   * @return initialsFixFields
   */
  public Set<String> fieldsToFix(Interaction prev, ApiKeyClientRef apiKeyClient) {
    Set<String> fields = new HashSet<>(fieldsToHide(prev, apiKeyClient));
    ApiKeyClientFixedFieldsCheck proposal = ApiKeyClientFixedFieldsCheck.builder().fields(fields)
        .apiKeyClient(apiKeyClient).query(prev).build();
    fixedFieldsEmitter.fire(proposal);
    return proposal.getFields();
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @return initialsHideFields
   */
  public Set<String> fieldsToHide(Interaction prev) {
    Set<String> fields = new HashSet<>();
    ApiKeyClientHiddenFieldsCheck proposal =
        ApiKeyClientHiddenFieldsCheck.builder().fields(fields).query(prev).build();
    hiddenFieldsEmitter.fire(proposal);
    return proposal.getFields();
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param apiKeyClient
   * @return initialsHideFields
   */
  public Set<String> fieldsToHide(Interaction prev, ApiKeyClientRef apiKeyClient) {
    Set<String> fields = new HashSet<>(fieldsToHide(prev));
    ApiKeyClientHiddenFieldsCheck proposal = ApiKeyClientHiddenFieldsCheck.builder().fields(fields)
        .apiKeyClient(apiKeyClient).query(prev).build();
    hiddenFieldsEmitter.fire(proposal);
    return proposal.getFields();
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param filter The filter to retrieve values
   * @param cursor The filter to retrieve values
   * @return The input entity with the copy values without hidden
   */
  public ApiKeyClientCached listCachedVisibles(Interaction prev,
      ApiKeyClientVisibilityFilter filter, ApiKeyClientCursor cursor) {
    ApiKeyClientFilter visibleFilter = applyPreVisibilityFilter(prev, filter);
    return cache.retrieve(visibleFilter, cursor)
        .orElseGet(() -> cache.store(visibleFilter, cursor, listVisibles(prev, filter, cursor)));
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param filter The filter to retrieve values
   * @param cursor The filter to retrieve values
   * @return The input entity with the copy values without hidden
   */
  public List<ApiKeyClient> listVisibles(Interaction prev, ApiKeyClientVisibilityFilter filter,
      ApiKeyClientCursor cursor) {
    return queryItems(prev, applyPreVisibilityFilter(prev, filter), cursor);
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param filter The filter to retrieve values
   * @param cursor The filter to retrieve values
   * @return The input entity with the copy values without hidden
   */
  public List<ApiKeyClient> listVisiblesForUpdate(Interaction prev,
      ApiKeyClientVisibilityFilter filter, ApiKeyClientCursor cursor) {
    return queryItemsForUpdate(prev, applyPreVisibilityFilter(prev, filter), cursor);
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param uid
   * @return The input entity with the copy values without hidden
   */
  public ApiKeyClientCached retrieveCachedVisible(Interaction prev, String uid) {
    ApiKeyClientCursor cursor = ApiKeyClientCursor.builder().limit(1).build();
    ApiKeyClientFilter visibleFilter =
        applyPreVisibilityFilter(prev, ApiKeyClientVisibilityFilter.builder().uid(uid).build());
    return cache.retrieve(visibleFilter, cursor).orElseGet(() -> {
      List<ApiKeyClient> list =
          retrieveVisible(prev, uid).<List<ApiKeyClient>>map(List::of).orElseGet(List::of);
      return cache.store(visibleFilter, cursor, list);
    });
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param uid
   * @return The input entity with the copy values without hidden
   */
  public Optional<ApiKeyClient> retrieveVisible(Interaction prev, String uid) {
    ApiKeyClientFilter filter =
        applyPreVisibilityFilter(prev, ApiKeyClientVisibilityFilter.builder().uid(uid).build());
    return queryItem(prev, uid, filter);
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param uid
   * @return The input entity with the copy values without hidden
   */
  public Optional<ApiKeyClient> retrieveVisibleForUpdate(Interaction prev, String uid) {
    ApiKeyClientFilter filter =
        applyPreVisibilityFilter(prev, ApiKeyClientVisibilityFilter.builder().uid(uid).build());
    return queryItemForUpdate(prev, uid, filter);
  }

  /**
   * Retrieve the list of fields
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param filter The filter to retrieve values
   * @return The self filter modified with the prepared values.
   */
  private ApiKeyClientFilter applyPreVisibilityFilter(Interaction prev,
      ApiKeyClientVisibilityFilter filter) {
    ApiKeyClientVisibilityCheck proposal =
        ApiKeyClientVisibilityCheck.builder().filter(filter).interaction(prev).build();
    filterProposalEmitter.fire(proposal);
    ApiKeyClientVisibilityFilter modified = proposal.getFilter();
    return ApiKeyClientFilter.builder().uid(modified.getUid().orElse(null))
        .uids(modified.getUids().stream().toList()).search(modified.getSearch().orElse(null))
        .key(modified.getKey().orElse(null)).build();
  }

  /**
   * Retrieve the list of fields
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param apiKeyClientRef The source interaction
   * @return The input dto with hidden values
   */
  private boolean evaluatePostVisibility(Interaction prev, ApiKeyClient apiKeyClientRef) {
    ApiKeyClientAccesibleCheck proposal = ApiKeyClientAccesibleCheck.builder().visible(true)
        .entity(apiKeyClientRef).interaction(prev).build();
    guardEmitter.fire(proposal);
    return Boolean.TRUE.equals(proposal.getVisible());
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param uid
   * @param filter The filter to retrieve values
   * @return The input entity with the copy values without hidden
   */
  private Optional<ApiKeyClient> queryItem(Interaction prev, String uid,
      ApiKeyClientFilter filter) {
    return apiKeyClientReadRepositoryGateway.retrieve(uid, Optional.of(filter))
        .filter(values -> evaluatePostVisibility(prev, values));
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param uid
   * @param filter The filter to retrieve values
   * @return The input entity with the copy values without hidden
   */
  private Optional<ApiKeyClient> queryItemForUpdate(Interaction prev, String uid,
      ApiKeyClientFilter filter) {
    return apiKeyClientWriteRepositoryGateway.retrieveForUpdate(uid, Optional.of(filter))
        .filter(values -> evaluatePostVisibility(prev, values));
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param filter The filter to retrieve values
   * @param cursor The filter to retrieve values
   * @return The input entity with the copy values without hidden
   */
  private List<ApiKeyClient> queryItems(Interaction prev, ApiKeyClientFilter filter,
      ApiKeyClientCursor cursor) {
    List<ApiKeyClient> list = new ArrayList<>();
    Iterator<ApiKeyClient> slide = apiKeyClientReadRepositoryGateway.slide(filter, cursor)
        .slide(values -> evaluatePostVisibility(prev, values));
    while (slide.hasNext()) {
      list.add(slide.next());
    }
    return list;
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param filter The filter to retrieve values
   * @param cursor The filter to retrieve values
   * @return The input entity with the copy values without hidden
   */
  private List<ApiKeyClient> queryItemsForUpdate(Interaction prev, ApiKeyClientFilter filter,
      ApiKeyClientCursor cursor) {
    List<ApiKeyClient> list = new ArrayList<>();
    Iterator<ApiKeyClient> slide = apiKeyClientWriteRepositoryGateway.slideForUpdate(filter, cursor)
        .slide(values -> evaluatePostVisibility(prev, values));
    while (slide.hasNext()) {
      list.add(slide.next());
    }
    return list;
  }
}
