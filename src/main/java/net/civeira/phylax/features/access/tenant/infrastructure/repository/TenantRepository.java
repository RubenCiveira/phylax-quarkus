  /**
   * @autogenerated RepositoryJdbcGenerator
   * @param sq
   * @param offset
   * @param cursor
   * @param order
   * @return
   */
  private PartialWhere[] tryToOrderByDomainAsc(SqlSchematicQuery<Tenant> sq, PartialWhere[] offset,
      TenantCursor cursor, TenantOrder order) {
    if (order == TenantOrder.DOMAIN_ASC) {
      sq.addOrderAsc("domain");
      Optional<String> sinceDomain = cursor.getSinceDomain();
      if (sinceDomain.isPresent()) {
        String sinceDomainValue = sinceDomain.get();
        offset[0] = PartialWhere.or(offset[0], PartialWhere.and(offset[1],
            PartialWhere.where("domain", SqlOperator.GT, SqlParameterValue.of(sinceDomainValue))));
        offset[1] = PartialWhere.and(offset[1],
            PartialWhere.where("domain", SqlOperator.EQ, SqlParameterValue.of(sinceDomainValue)));
      }
    }
    return offset;
  }

