package net.civeira.phylax.features.access.tenantloginprovider.application.usecase.metadataretrieve;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.infrastructure.store.BinaryContent;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.tenantloginprovider.application.visibility.TenantLoginProvidersVisibility;
import net.civeira.phylax.features.access.tenantloginprovider.domain.TenantLoginProvider;
import net.civeira.phylax.features.access.tenantloginprovider.domain.TenantLoginProviderRef;
import net.civeira.phylax.features.access.tenantloginprovider.domain.gateway.TenantLoginProviderMetadataUploadGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class TenantLoginProviderMetadataRetrieveUsecase {

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final Event<TenantLoginProviderMetadataRetrieveAllowDecision> retrieveAllowEmitter;

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final TenantLoginProviderMetadataUploadGateway store;

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final TenantLoginProvidersVisibility visibility;

  /**
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  public Allow allow(final Interaction query, final TenantLoginProviderRef reference) {
    TenantLoginProviderMetadataRetrieveAllowDecision base =
        TenantLoginProviderMetadataRetrieveAllowDecision.builder()
            .detail(Allow.builder().allowed(true).description("Allowed by default").build())
            .query(query).reference(reference).build();
    retrieveAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    TenantLoginProviderMetadataRetrieveAllowDecision base =
        TenantLoginProviderMetadataRetrieveAllowDecision.builder()
            .detail(Allow.builder().allowed(true).description("Allowed by default").build())
            .query(query).build();
    retrieveAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @param reference
   * @return The slide with some values
   */
  public BinaryContent read(final Interaction query, final TenantLoginProviderRef reference) {
    Allow detail = allow(query, reference);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    TenantLoginProvider entity = visibility.retrieveVisible(query, reference.getUid())
        .orElseThrow(() -> new NotFoundException(""));
    return store.readMetadata(entity).orElseThrow(() -> new NotFoundException(""));
  }
}
