package net.civeira.phylax.features.access.role.application.usecase.list;

import java.util.List;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.SpanKind;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Context;
import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.algorithms.metadata.TimestampedList;
import net.civeira.phylax.common.algorithms.metadata.WrapMetadata;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.role.application.visibility.RoleVisibilityFilter;
import net.civeira.phylax.features.access.role.application.visibility.RolesVisibility;
import net.civeira.phylax.features.access.role.domain.gateway.RoleCached;
import net.civeira.phylax.features.access.role.domain.gateway.RoleCursor;
import net.civeira.phylax.features.access.role.domain.gateway.RoleOrder;

@ApplicationScoped
@RequiredArgsConstructor
public class RoleListUsecase {

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final Event<RoleListAllowDecision> listAllowEmitter;

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final RolesVisibility visibility;

  /**
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    Span span = tracer.spanBuilder("Checking if is allowed to list entities of role")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      Allow response = checkAllow(query);
      span.setAttribute("allowed", response.isAllowed());
      return response;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @param filter
   * @param cursor
   * @return The slide with some values
   */
  public List<RoleListProjection> list(final Interaction query, final RoleListFilter filter,
      final RoleListCursor cursor) {
    Span span = tracer.spanBuilder("Listing an entity of role")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      Allow detail = checkAllow(query);
      if (!detail.isAllowed()) {
        span.addEvent("The policies for role dont allow to list: " + detail.getDescription());
        throw new NotAllowedException(detail.getDescription());
      }
      RoleVisibilityFilter visibleFilter = RoleVisibilityFilter.builder()
          .uid(filter.getUid().orElse(null)).uids(filter.getUids().stream().toList())
          .search(filter.getSearch().orElse(null)).name(filter.getName().orElse(null))
          .relyingParty(filter.getRelyingParty().orElse(null))
          .relyingPartys(filter.getRelyingPartys()).build();
      RoleCursor gatewayCursor = RoleCursor.builder().limit(cursor.getLimit().orElse(null))
          .sinceUid(cursor.getSinceUid().orElse(null))
          .order(cursor.getOrder().stream().map(Object::toString).map(RoleOrder::valueOf).toList())
          .sinceName(cursor.getSinceName().orElse(null))
          .sinceName(cursor.getSinceName().orElse(null)).build();
      RoleCached values = visibility.listCachedVisibles(query, visibleFilter, gatewayCursor);
      List<RoleListProjection> list =
          values.getValue().stream().map(value -> visibility.copyWithHidden(query, value))
              .map(RoleListProjection::from).toList();
      return new TimestampedList<>(WrapMetadata.<List<RoleListProjection>>builder().data(list)
          .since(values.getSince().toInstant()).build());
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @return
   */
  private Allow checkAllow(final Interaction query) {
    RoleListAllowDecision proposal = RoleListAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allow by default").build()).query(query)
        .build();
    listAllowEmitter.fire(proposal);
    return proposal.getDetail();
  }
}
