package net.civeira.phylax.features.access.apikeyclient.application.usecase.delete;

import java.util.List;

import io.quarkus.arc.Unremovable;
import jakarta.enterprise.context.ApplicationScoped;
import lombok.Data;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.stepper.ItemDescriptor;
import net.civeira.phylax.common.batch.stepper.ItemProcessor;
import net.civeira.phylax.common.batch.stepper.ItemReader;
import net.civeira.phylax.common.batch.stepper.ItemWriter;
import net.civeira.phylax.common.batch.stepper.StepContext;
import net.civeira.phylax.common.batch.stepper.StepCounter;
import net.civeira.phylax.common.batch.stepper.StepFinalizer;
import net.civeira.phylax.common.batch.stepper.StepInitializer;
import net.civeira.phylax.common.exception.ExecutionException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.features.access.apikeyclient.application.visibility.ApiKeyClientsVisibility;
import net.civeira.phylax.features.access.apikeyclient.domain.ApiKeyClient;
import net.civeira.phylax.features.access.apikeyclient.domain.gateway.ApiKeyClientCursor;

@Unremovable
@ApplicationScoped
@RequiredArgsConstructor
class ApiKeyClientsInBatchExecutor implements
    StepCounter<ApiKeyClientDeleteAllInBatchCommand, ApiKeyClientsInBatchExecutor.ApiKeyClientPaginableBatch>,
    StepFinalizer<ApiKeyClientDeleteAllInBatchCommand, ApiKeyClientsInBatchExecutor.ApiKeyClientPaginableBatch>,
    StepInitializer<ApiKeyClientDeleteAllInBatchCommand, ApiKeyClientsInBatchExecutor.ApiKeyClientPaginableBatch>,
    ItemReader<ApiKeyClient, ApiKeyClientDeleteAllInBatchCommand, ApiKeyClientsInBatchExecutor.ApiKeyClientPaginableBatch>,
    ItemWriter<ApiKeyClient, ApiKeyClientDeleteAllInBatchCommand, ApiKeyClientsInBatchExecutor.ApiKeyClientPaginableBatch>,
    ItemProcessor<ApiKeyClient, ApiKeyClient, ApiKeyClientDeleteAllInBatchCommand, ApiKeyClientsInBatchExecutor.ApiKeyClientPaginableBatch>,
    ItemDescriptor<ApiKeyClient, ApiKeyClientDeleteAllInBatchCommand, ApiKeyClientsInBatchExecutor.ApiKeyClientPaginableBatch> {

  /**
   * @autogenerated EntityGenerator
   */
  @Data
  public static class ApiKeyClientPaginableBatch {

    /**
     * @autogenerated EntityGenerator
     */
    private String since;
  }

  /**
   * @autogenerated EntityGenerator
   */
  private final int size = 10;

  /**
   * @autogenerated EntityGenerator
   */
  private final ApiKeyClientDeleteUsecase usecase;

  /**
   * @autogenerated EntityGenerator
   */
  private final ApiKeyClientsVisibility visibility;

  /**
   * @autogenerated EntityGenerator
   * @param context
   * @return
   */
  @Override
  public long approximatedItems(
      final StepContext<ApiKeyClientDeleteAllInBatchCommand, ApiKeyClientPaginableBatch> context) {
    return visibility.countVisibles(context.getParams().getInteraction(),
        context.getParams().getFilter());
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   */
  @Override
  public void finish(
      final StepContext<ApiKeyClientDeleteAllInBatchCommand, ApiKeyClientPaginableBatch> context) {
    usecase.flush();
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   */
  @Override
  public void init(
      final StepContext<ApiKeyClientDeleteAllInBatchCommand, ApiKeyClientPaginableBatch> context) {
    context.setState(new ApiKeyClientPaginableBatch());
  }

  /**
   * @autogenerated EntityGenerator
   * @param item
   * @param context
   * @return
   */
  @Override
  public String itemDescription(final ApiKeyClient item,
      final StepContext<ApiKeyClientDeleteAllInBatchCommand, ApiKeyClientPaginableBatch> context) {
    return item.getUid();
  }

  /**
   * @autogenerated EntityGenerator
   * @param item
   * @param context
   * @return
   */
  @Override
  public ApiKeyClient process(final ApiKeyClient item,
      final StepContext<ApiKeyClientDeleteAllInBatchCommand, ApiKeyClientPaginableBatch> context) {
    Allow allowed = usecase.allow(context.getParams().getInteraction(), item);
    if (!allowed.isAllowed()) {
      throw new ExecutionException("not-allowed", null);
    }
    return item;
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   * @return
   */
  @Override
  public List<ApiKeyClient> read(
      final StepContext<ApiKeyClientDeleteAllInBatchCommand, ApiKeyClientPaginableBatch> context) {
    List<ApiKeyClient> page = visibility.listVisiblesForUpdate(context.getParams().getInteraction(),
        context.getParams().getFilter(),
        ApiKeyClientCursor.builder().limit(size).sinceUid(context.getState().getSince()).build());
    context.getState().setSince(page.isEmpty() ? null : page.get(page.size() - 1).getUid());
    return page;
  }

  /**
   * @autogenerated EntityGenerator
   * @param items
   * @param context
   */
  @Override
  public void write(final List<ApiKeyClient> items,
      final StepContext<ApiKeyClientDeleteAllInBatchCommand, ApiKeyClientPaginableBatch> context) {
    items.forEach(item -> usecase.delete(context.getParams().getInteraction(), item));
  }
}
