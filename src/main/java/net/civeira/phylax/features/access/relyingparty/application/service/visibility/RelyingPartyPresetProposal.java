package net.civeira.phylax.features.access.relyingparty.application.service.visibility;

import java.util.Optional;
import java.util.function.Consumer;

import lombok.Builder;
import lombok.Data;
import lombok.EqualsAndHashCode;
import lombok.NonNull;
import lombok.With;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.relyingparty.RelyingParty;
import net.civeira.phylax.features.access.relyingparty.application.request.RelyingPartyStateChange;

@Data
@Builder(toBuilder = true)
@With
@EqualsAndHashCode(onlyExplicitlyIncluded = true)
public class RelyingPartyPresetProposal {

  /**
   * @autogenerated PresetProposalGenerator
   */
  @NonNull
  private final Interaction interaction;

  /**
   * @autogenerated PresetProposalGenerator
   */
  @NonNull
  private RelyingPartyStateChange dto;

  /**
   * @autogenerated PresetProposalGenerator
   */
  private RelyingParty original;

  /**
   * @autogenerated PresetProposalGenerator
   * @return
   */
  public Optional<RelyingParty> getOriginal() {
    return Optional.ofNullable(original);
  }

  /**
   * @autogenerated PresetProposalGenerator
   * @param mapper
   */
  public void peek(Consumer<RelyingPartyStateChange> mapper) {
    mapper.accept(dto);
  }
}
