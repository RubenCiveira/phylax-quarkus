package net.civeira.phylax.features.access.securitydomain.transition.update;

import java.util.ArrayList;
import java.util.List;
import java.util.function.Function;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.inject.Instance;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.features.access.securitydomain.SecurityDomain;
import net.civeira.phylax.features.access.securitydomain.command.SecurityDomainWriteAttributes;
import net.civeira.phylax.features.access.securitydomain.transition.SecurityDomainChangeProposal;

@ApplicationScoped
@RequiredArgsConstructor
public class SecurityDomainUpdatePipeline {

  /**
   * @autogenerated UpdateEntityRuleGenerator
   */
  private final Instance<SecurityDomainUpdateStep> steps;

  /**
   * @autogenerated UpdateEntityRuleGenerator
   * @param attributes
   * @param original
   * @param last
   * @return
   */
  public SecurityDomain update(final SecurityDomainWriteAttributes attributes,
      final SecurityDomain original,
      final Function<SecurityDomainChangeProposal, SecurityDomain> last) {
    Function<SecurityDomainUpdateTransition, SecurityDomain> finalHandler = t -> {
      if (t.hasErrors()) {
        throw t.asConstraintException();
      }
      return last.apply(t.getData());
    };
    List<SecurityDomainUpdateStep> reversed = new ArrayList<>(steps.stream().toList()).reversed();
    Function<SecurityDomainUpdateTransition, SecurityDomain> pipeline = reversed.stream().reduce(
        finalHandler, (next, step) -> transition -> step.update(transition, next), (f1, f2) -> f1);
    return pipeline.apply(SecurityDomainUpdateTransition.builder()
        .data(new SecurityDomainChangeProposal(attributes)).original(original).build());
  }
}
