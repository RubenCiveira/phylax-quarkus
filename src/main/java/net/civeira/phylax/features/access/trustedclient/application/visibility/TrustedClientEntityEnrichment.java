package net.civeira.phylax.features.access.trustedclient.application.visibility;

import java.util.Optional;
import java.util.function.Consumer;

import lombok.Builder;
import lombok.Data;
import lombok.EqualsAndHashCode;
import lombok.NonNull;
import lombok.With;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.trustedclient.domain.TrustedClient;
import net.civeira.phylax.features.access.trustedclient.domain.TrustedClientChangeSet;

/**
 * Entity Enrichment events happen when a dto params is read throught the visibility service from
 * some input parameters to store on the repository.The programmers could add policies to fill
 * calculated values to store at the application level.
 */
@Data
@Builder(toBuilder = true)
@With
@EqualsAndHashCode(onlyExplicitlyIncluded = true)
public class TrustedClientEntityEnrichment {

  /**
   * @autogenerated EntityEnrichmentGenerator
   */
  @NonNull
  private final Interaction interaction;

  /**
   * @autogenerated EntityEnrichmentGenerator
   */
  @NonNull
  private TrustedClientChangeSet dto;

  /**
   * @autogenerated EntityEnrichmentGenerator
   */
  private TrustedClient original;

  /**
   * @autogenerated EntityEnrichmentGenerator
   * @return
   */
  public Optional<TrustedClient> getOriginal() {
    return Optional.ofNullable(original);
  }

  /**
   * @autogenerated EntityEnrichmentGenerator
   * @param mapper
   */
  public void peek(Consumer<TrustedClientChangeSet> mapper) {
    mapper.accept(dto);
  }
}
