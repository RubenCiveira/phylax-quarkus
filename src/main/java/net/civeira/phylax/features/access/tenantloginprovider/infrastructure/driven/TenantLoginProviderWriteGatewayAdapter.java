package net.civeira.phylax.features.access.tenantloginprovider.infrastructure.driven;

import java.util.List;
import java.util.Optional;
import java.util.function.Predicate;

import jakarta.enterprise.context.ApplicationScoped;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.algorithms.Slider;
import net.civeira.phylax.features.access.tenantloginprovider.domain.TenantLoginProvider;
import net.civeira.phylax.features.access.tenantloginprovider.domain.TenantLoginProviderRef;
import net.civeira.phylax.features.access.tenantloginprovider.domain.gateway.TenantLoginProviderCursor;
import net.civeira.phylax.features.access.tenantloginprovider.domain.gateway.TenantLoginProviderFilter;
import net.civeira.phylax.features.access.tenantloginprovider.domain.gateway.TenantLoginProviderWriteRepositoryGateway;
import net.civeira.phylax.features.access.tenantloginprovider.infrastructure.event.TenantLoginProviderEventDispacher;
import net.civeira.phylax.features.access.tenantloginprovider.infrastructure.repository.TenantLoginProviderRepository;

@ApplicationScoped
@RequiredArgsConstructor
public class TenantLoginProviderWriteGatewayAdapter
    implements TenantLoginProviderWriteRepositoryGateway {

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   */
  private final TenantLoginProviderEventDispacher eventDispacher;

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   */
  private final TenantLoginProviderRepository repository;

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   */
  private final TenantLoginProviderMetadataUploadGatewayAdapter tenantLoginProviderMetadataUploadGatewayAdapter;

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param filter
   * @return
   */
  @Override
  public long countForUpdate(TenantLoginProviderFilter filter) {
    return repository.count(filter);
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param entity
   * @return
   */
  @Override
  public TenantLoginProvider create(TenantLoginProvider entity) {
    tenantLoginProviderMetadataUploadGatewayAdapter.commitMetadata(entity, Optional.empty());
    TenantLoginProvider result = repository.create(entity);
    eventDispacher.dispatch(entity);
    return result;
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param entity
   * @param verifier
   * @return
   */
  @Override
  public TenantLoginProvider create(TenantLoginProvider entity,
      Predicate<TenantLoginProvider> verifier) {
    tenantLoginProviderMetadataUploadGatewayAdapter.commitMetadata(entity, Optional.empty());
    TenantLoginProvider result = repository.create(entity, verifier);
    eventDispacher.dispatch(entity);
    return result;
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param entity
   */
  @Override
  public void delete(TenantLoginProvider entity) {
    repository.delete(entity);
    tenantLoginProviderMetadataUploadGatewayAdapter.deleteMetadata(entity);
    eventDispacher.dispatch(entity);
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param uid
   * @param filter
   * @return
   */
  @Override
  public boolean existsForUpdate(String uid, Optional<TenantLoginProviderFilter> filter) {
    return repository.existsForUpdate(uid, filter);
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param filter
   * @return
   */
  @Override
  public Optional<TenantLoginProvider> findForUpdate(TenantLoginProviderFilter filter) {
    return repository.findForUpdate(filter);
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param filter
   * @return
   */
  @Override
  public List<TenantLoginProvider> listForUpdate(TenantLoginProviderFilter filter) {
    return repository.listForUpdate(filter);
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param reference
   * @return
   */
  @Override
  public TenantLoginProvider resolveForUpdate(TenantLoginProviderRef reference) {
    return repository.resolveForUpdate(reference);
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param uid
   * @param filter
   * @return
   */
  @Override
  public Optional<TenantLoginProvider> retrieveForUpdate(String uid,
      Optional<TenantLoginProviderFilter> filter) {
    return repository.retrieveForUpdate(uid, filter);
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param filter
   * @param cursor
   * @return
   */
  @Override
  public Slider<TenantLoginProvider> slideForUpdate(TenantLoginProviderFilter filter,
      TenantLoginProviderCursor cursor) {
    return repository.slideForUpdate(filter, cursor);
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param reference
   * @param entity
   * @return
   */
  @Override
  public TenantLoginProvider update(TenantLoginProviderRef reference, TenantLoginProvider entity) {
    TenantLoginProvider stored = resolveForUpdate(reference);
    tenantLoginProviderMetadataUploadGatewayAdapter.commitMetadata(entity, Optional.of(stored));
    TenantLoginProvider result = repository.update(entity);
    eventDispacher.dispatch(entity);
    return result;
  }
}
