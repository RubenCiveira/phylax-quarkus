package net.civeira.phylax.features.access.loginprovider.transition.create;

import java.util.ArrayList;
import java.util.List;
import java.util.function.Function;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.inject.Instance;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.features.access.loginprovider.LoginProvider;
import net.civeira.phylax.features.access.loginprovider.command.LoginProviderWriteAttributes;
import net.civeira.phylax.features.access.loginprovider.transition.LoginProviderChangeProposal;

@ApplicationScoped
@RequiredArgsConstructor
public class LoginProviderCreatePipeline {

  /**
   * @autogenerated UpdateEntityRuleGenerator
   */
  private final Instance<LoginProviderCreateStep> steps;

  /**
   * @autogenerated UpdateEntityRuleGenerator
   * @param attributes
   * @param last
   * @return
   */
  public LoginProvider create(final LoginProviderWriteAttributes attributes,
      final Function<LoginProviderChangeProposal, LoginProvider> last) {
    Function<LoginProviderCreateTransition, LoginProvider> finalHandler = t -> {
      if (t.hasErrors()) {
        throw t.asConstraintException();
      }
      return last.apply(t.getData());
    };
    List<LoginProviderCreateStep> reversed = new ArrayList<>(steps.stream().toList()).reversed();
    Function<LoginProviderCreateTransition, LoginProvider> pipeline = reversed.stream().reduce(
        finalHandler, (next, step) -> transition -> step.create(transition, next), (f1, f2) -> f1);
    return pipeline.apply(LoginProviderCreateTransition.builder()
        .data(new LoginProviderChangeProposal(attributes)).build());
  }
}
