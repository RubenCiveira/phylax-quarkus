package net.civeira.phylax.features.access.securitydomain.infrastructure.driver.rest;

import java.net.URLEncoder;
import java.nio.charset.StandardCharsets;
import java.util.Arrays;
import java.util.List;
import java.util.Objects;
import java.util.Optional;
import java.util.stream.Stream;

import jakarta.enterprise.context.RequestScoped;
import jakarta.ws.rs.core.Response;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.infrastructure.CurrentRequest;
import net.civeira.phylax.features.access.securitydomain.application.usecase.list.SecurityDomainListCursor;
import net.civeira.phylax.features.access.securitydomain.application.usecase.list.SecurityDomainListFilter;
import net.civeira.phylax.features.access.securitydomain.application.usecase.list.SecurityDomainListOrder;
import net.civeira.phylax.features.access.securitydomain.application.usecase.list.SecurityDomainListProjection;
import net.civeira.phylax.features.access.securitydomain.application.usecase.list.SecurityDomainListUsecase;
import net.civeira.phylax.generated.openapi.model.SecurityDomainApiDto;
import net.civeira.phylax.generated.openapi.model.SecurityDomainApiDtoList;

@RequiredArgsConstructor
@RequestScoped
public class SecurityDomainListController {

  /**
   * @autogenerated ListControllerGenerator
   */
  private static final String ENABLED_APPEND = "&enabled=";

  /**
   * @autogenerated ListControllerGenerator
   */
  private static final String LIMIT_APPEND = "&limit=";

  /**
   * @autogenerated ListControllerGenerator
   */
  private static final String NAME_APPEND = "&name=";

  /**
   * @autogenerated ListControllerGenerator
   */
  private static final String SEARCH_APPEND = "&search=";

  /**
   * @autogenerated ListControllerGenerator
   */
  private static final String SINCE_APPEND = "&since-uid=";

  /**
   * @autogenerated ListControllerGenerator
   */
  private static final String UID_APPEND = "&uid=";

  /**
   * SecurityDomain
   *
   * @autogenerated ListControllerGenerator
   */
  private final CurrentRequest currentRequest;

  /**
   * @autogenerated ListControllerGenerator
   */
  private final SecurityDomainListUsecase list;

  /**
   * @autogenerated ListControllerGenerator
   * @param uids
   * @param search
   * @param enabled
   * @param name
   * @param limit
   * @param sinceUid
   * @param sinceName
   * @param order
   * @return
   */
  public Response securityDomainApiList(final List<String> uids, final String search,
      final Boolean enabled, final String name, final Integer limit, final String sinceUid,
      final String sinceName, final String order) {
    List<SecurityDomainListOrder> orderSteps = null == order ? List.of()
        : Arrays.asList(order.split(",")).stream().map(this::mapOrder).filter(Objects::nonNull)
            .toList();
    SecurityDomainListFilter.SecurityDomainListFilterBuilder filterBuilder =
        SecurityDomainListFilter.builder();
    SecurityDomainListCursor.SecurityDomainListCursorBuilder cursorBuilder =
        SecurityDomainListCursor.builder();
    filterBuilder =
        filterBuilder.uids(uids.stream().flatMap(part -> Stream.of(part.split(","))).toList());
    filterBuilder = filterBuilder.search(search);
    filterBuilder = filterBuilder.enabled(enabled);
    filterBuilder = filterBuilder.name(name);
    cursorBuilder = cursorBuilder.limit(limit);
    cursorBuilder = cursorBuilder.sinceUid(sinceUid);
    cursorBuilder = cursorBuilder.sinceName(sinceName);
    cursorBuilder = cursorBuilder.order(orderSteps);
    SecurityDomainListFilter filter = filterBuilder.build();
    SecurityDomainListCursor cursor = cursorBuilder.build();
    List<SecurityDomainListProjection> listed =
        list.list(currentRequest.interaction(), filter, cursor);
    return currentRequest.cacheableResponse(listed, toListApiModel(listed, filter, cursor),
        "security-domain-" + ("" + filter + cursor).hashCode());
  }

  /**
   * @autogenerated ListControllerGenerator
   * @param label
   * @return
   */
  private SecurityDomainListOrder mapOrder(final String label) {
    if (null == label) {
      return null;
    } else if (label.trim().equals("name-asc")) {
      return SecurityDomainListOrder.NAME_ASC;
    } else if (label.trim().equals("name-desc")) {
      return SecurityDomainListOrder.NAME_DESC;
    } else {
      return null;
    }
  }

  /**
   * @autogenerated ListControllerGenerator
   * @param dto
   * @return
   */
  private SecurityDomainApiDto toApiModel(SecurityDomainListProjection dto) {
    SecurityDomainApiDto securityDomainApiDto = new SecurityDomainApiDto();
    securityDomainApiDto.setUid(dto.getUid());
    securityDomainApiDto.setName(dto.getName());
    securityDomainApiDto.setLevel(dto.getLevel());
    securityDomainApiDto.setReadAll(dto.getReadAll());
    securityDomainApiDto.setWriteAll(dto.getWriteAll());
    securityDomainApiDto.setManageAll(dto.getManageAll());
    securityDomainApiDto.setEnabled(dto.getEnabled());
    securityDomainApiDto.setVersion(dto.getVersion());
    return securityDomainApiDto;
  }

  /**
   * @autogenerated ListControllerGenerator
   * @param securityDomains
   * @param filter
   * @param cursor
   * @return
   */
  private SecurityDomainApiDtoList toListApiModel(
      List<SecurityDomainListProjection> securityDomains, SecurityDomainListFilter filter,
      SecurityDomainListCursor cursor) {
    Optional<SecurityDomainListProjection> last = securityDomains.isEmpty() ? Optional.empty()
        : Optional.of(securityDomains.get(securityDomains.size() - 1));
    StringBuilder self = new StringBuilder();
    StringBuilder next = new StringBuilder();
    StringBuilder first = new StringBuilder();
    if (!filter.getUids().isEmpty()) {
      String uidsValue =
          URLEncoder.encode(String.join(",", filter.getUids()), StandardCharsets.UTF_8);
      self.append(UID_APPEND + uidsValue);
      next.append(UID_APPEND + uidsValue);
      first.append(UID_APPEND + uidsValue);
    }
    filter.getSearch().ifPresent(search -> {
      String searchValue = URLEncoder.encode(search, StandardCharsets.UTF_8);
      self.append(SEARCH_APPEND + searchValue);
      next.append(SEARCH_APPEND + searchValue);
      first.append(SEARCH_APPEND + searchValue);
    });
    filter.getEnabled().ifPresent(filterEnabled -> {
      String enabledValue = URLEncoder.encode(Boolean.TRUE.equals(filterEnabled) ? "true" : "false",
          StandardCharsets.UTF_8);
      self.append(ENABLED_APPEND + enabledValue);
      next.append(ENABLED_APPEND + enabledValue);
      first.append(ENABLED_APPEND + enabledValue);
    });
    filter.getName().ifPresent(filterName -> {
      String nameValue = URLEncoder.encode(String.valueOf(filterName), StandardCharsets.UTF_8);
      self.append(NAME_APPEND + nameValue);
      next.append(NAME_APPEND + nameValue);
      first.append(NAME_APPEND + nameValue);
    });
    cursor.getLimit().ifPresent(limit -> {
      self.append(LIMIT_APPEND + limit);
      first.append(LIMIT_APPEND + limit);
      next.append(LIMIT_APPEND + limit);
    });
    cursor.getSinceUid().ifPresent(
        since -> self.append(SINCE_APPEND + URLEncoder.encode(since, StandardCharsets.UTF_8)));
    cursor.getSinceName().ifPresent(sinceName -> self
        .append("&since-name=" + URLEncoder.encode(sinceName, StandardCharsets.UTF_8)));
    if (!cursor.getOrder().isEmpty()) {
      String urlOrder = URLEncoder.encode(
          String.join(",",
              cursor.getOrder().stream().map(this::writeOrder).filter(Objects::nonNull).toList()),
          StandardCharsets.UTF_8);
      self.append("&order=" + urlOrder);
      next.append("&order=" + urlOrder);
      first.append("&order=" + urlOrder);
    }
    last.ifPresent(lastDto -> {
      next.append(SINCE_APPEND + URLEncoder.encode(lastDto.getUid(), StandardCharsets.UTF_8));;
      cursor.getSinceName().ifPresent(sinceName -> next
          .append("&since-name=" + URLEncoder.encode(sinceName, StandardCharsets.UTF_8)));
    });
    return new SecurityDomainApiDtoList()
        .items(securityDomains.stream().map(this::toApiModel).toList())
        .next(next.length() > 1 ? "?" + next.substring(1) : "")
        .self(self.length() > 1 ? "?" + self.substring(1) : "")
        .first(first.length() > 1 ? "?" + first.substring(1) : "");
  }

  /**
   * @autogenerated ListControllerGenerator
   * @param order
   * @return
   */
  private String writeOrder(final SecurityDomainListOrder order) {
    if (null == order) {
      return null;
    } else if (order == SecurityDomainListOrder.NAME_ASC) {
      return "name-asc";
    } else if (order == SecurityDomainListOrder.NAME_DESC) {
      return "name-desc";
    } else {
      return null;
    }
  }
}
