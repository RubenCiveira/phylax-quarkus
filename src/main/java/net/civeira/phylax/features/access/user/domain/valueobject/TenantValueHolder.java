package net.civeira.phylax.features.access.user.domain.valueobject;

import java.util.Optional;

import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.value.validation.ConstraintFail;
import net.civeira.phylax.common.value.validation.ConstraintFailList;
import net.civeira.phylax.features.access.tenant.domain.TenantChangeSet;
import net.civeira.phylax.features.access.tenant.domain.TenantRef;
import net.civeira.phylax.features.access.tenant.domain.TenantReference;
import net.civeira.phylax.features.access.user.domain.User;

@RequiredArgsConstructor
public class TenantValueHolder<T> {

  /**
   * @autogenerated ValueHolderGenerator
   */
  private final T holder;

  /**
   * @autogenerated ValueHolderGenerator
   */
  private Optional<TenantVO> value = Optional.empty();

  /**
   * @autogenerated ValueHolderGenerator
   * @return
   */
  public Optional<TenantRef> getTenant() {
    return value.map(TenantVO::getTenant);
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @return
   */
  public Optional<TenantVO> getTenantValue() {
    return value;
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @param original
   * @param fails
   * @return
   */
  public TenantVO readTenant(final Optional<User> original, final ConstraintFailList fails) {
    TenantVO result = value.orElse(original.map(User::getTenantValue).orElse(null));
    if (null == result) {
      fails.add(new ConstraintFail("REQUIRED", "tenant", null));
    }
    return result;
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @param tenant
   * @return
   */
  public T tenant(TenantVO tenant) {
    value = Optional.of(tenant);
    return holder;
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @param tenant
   * @return
   */
  public T tenant(Optional<TenantVO> tenant) {
    value = tenant;
    return holder;
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @param tenant
   * @return
   */
  public T tenant(TenantRef tenant) {
    value = Optional.of(TenantVO.from(tenant));
    return holder;
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @param tenant
   * @return
   */
  public T tenant(TenantChangeSet tenant) {
    value = Optional.of(TenantVO.from(TenantReference.of(tenant.getUid().orElseThrow())));
    return holder;
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @return
   */
  public T unsetTenant() {
    value = Optional.empty();
    return holder;
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @param field
   * @return
   */
  public T unsetTenantIfMatches(final String field) {
    if ("tenant".equals(field)) {
      value = Optional.empty();
    }
    return holder;
  }
}
