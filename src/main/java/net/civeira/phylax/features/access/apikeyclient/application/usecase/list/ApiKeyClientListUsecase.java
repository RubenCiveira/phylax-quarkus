package net.civeira.phylax.features.access.apikeyclient.application.usecase.list;

import java.util.List;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.SpanKind;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Context;
import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.algorithms.metadata.TimestampedList;
import net.civeira.phylax.common.algorithms.metadata.WrapMetadata;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.apikeyclient.application.visibility.ApiKeyClientVisibilityFilter;
import net.civeira.phylax.features.access.apikeyclient.application.visibility.ApiKeyClientsVisibility;
import net.civeira.phylax.features.access.apikeyclient.domain.gateway.ApiKeyClientCached;
import net.civeira.phylax.features.access.apikeyclient.domain.gateway.ApiKeyClientCursor;

@ApplicationScoped
@RequiredArgsConstructor
public class ApiKeyClientListUsecase {

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final Event<ApiKeyClientListAllowDecision> listAllowEmitter;

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final ApiKeyClientsVisibility visibility;

  /**
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    Span span = tracer.spanBuilder("Checking if is allowed to list entities of api key client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      Allow response = checkAllow(query);
      span.setAttribute("allowed", response.isAllowed());
      return response;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @param filter
   * @param cursor
   * @return The slide with some values
   */
  public List<ApiKeyClientListProjection> list(final Interaction query,
      final ApiKeyClientListFilter filter, final ApiKeyClientListCursor cursor) {
    Span span = tracer.spanBuilder("Listing an entity of api key client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      Allow detail = checkAllow(query);
      if (!detail.isAllowed()) {
        span.addEvent(
            "The policies for api key client dont allow to list: " + detail.getDescription());
        throw new NotAllowedException(detail.getDescription());
      }
      ApiKeyClientVisibilityFilter visibleFilter = ApiKeyClientVisibilityFilter.builder()
          .uid(filter.getUid().orElse(null)).uids(filter.getUids().stream().toList())
          .search(filter.getSearch().orElse(null)).key(filter.getKey().orElse(null)).build();
      ApiKeyClientCursor gatewayCursor =
          ApiKeyClientCursor.builder().limit(cursor.getLimit().orElse(null))
              .sinceUid(cursor.getSinceUid().orElse(null)).build();
      ApiKeyClientCached values =
          visibility.listCachedVisibles(query, visibleFilter, gatewayCursor);
      List<ApiKeyClientListProjection> list =
          values.getValue().stream().map(value -> visibility.copyWithHidden(query, value))
              .map(ApiKeyClientListProjection::from).toList();
      return new TimestampedList<>(WrapMetadata.<List<ApiKeyClientListProjection>>builder()
          .data(list).since(values.getSince().toInstant()).build());
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @return
   */
  private Allow checkAllow(final Interaction query) {
    ApiKeyClientListAllowDecision proposal = ApiKeyClientListAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allow by default").build()).query(query)
        .build();
    listAllowEmitter.fire(proposal);
    return proposal.getDetail();
  }
}
