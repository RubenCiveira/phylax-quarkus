package net.civeira.phylax.features.access.loginprovider.application.usecase.update;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.loginprovider.LoginProvider;
import net.civeira.phylax.features.access.loginprovider.LoginProviderFacade;
import net.civeira.phylax.features.access.loginprovider.LoginProviderRef;
import net.civeira.phylax.features.access.loginprovider.application.projection.LoginProviderStateProyection;
import net.civeira.phylax.features.access.loginprovider.application.request.LoginProviderStateChange;
import net.civeira.phylax.features.access.loginprovider.application.service.visibility.LoginProvidersVisibility;
import net.civeira.phylax.features.access.loginprovider.gateway.LoginProviderCacheGateway;
import net.civeira.phylax.features.access.loginprovider.gateway.LoginProviderWriteRepositoryGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class LoginProviderUpdateUsecase {

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final LoginProviderCacheGateway cache;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final LoginProviderFacade facade;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final LoginProviderWriteRepositoryGateway gateway;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<LoginProviderAllowUpdateProposal> updateAllowEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<LoginProviderUpdateProposal> updateProposalEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<LoginProviderUpdateEvent> updatedEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final LoginProvidersVisibility visibility;

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  public Allow allow(final Interaction query, final LoginProviderRef reference) {
    LoginProviderAllowUpdateProposal base = LoginProviderAllowUpdateProposal.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).reference(reference).build();
    updateAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    LoginProviderAllowUpdateProposal base = LoginProviderAllowUpdateProposal.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).build();
    updateAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * The slide with some values
   *
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @param reference
   * @param input
   * @return The slide with some values
   */
  public LoginProviderStateProyection update(final Interaction query,
      final LoginProviderRef reference, final LoginProviderStateChange input) {
    Allow detail = allow(query, reference);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    LoginProvider original = visibility.retrieveVisibleForUpdate(query, reference.getUidValue())
        .orElseThrow(() -> new NotFoundException(""));
    LoginProviderStateChange filled = visibility.copyWithFixed(query, input);
    LoginProviderUpdateProposal proposal = LoginProviderUpdateProposal.builder().dto(filled)
        .interaction(query).reference(reference).build();
    updateProposalEmitter.fire(proposal);
    LoginProviderStateChange dto = proposal.getDto();;
    LoginProvider saved = gateway.update(original, facade.update(original, dto));
    cache.update(saved);
    LoginProviderUpdateEvent event =
        LoginProviderUpdateEvent.builder().payload(saved).interaction(query).build();
    updatedEmitter.fire(event);
    return visibility.copyWithHidden(query, event.getPayload());
  }
}
