package net.civeira.phylax.features.access.tenantloginprovider.application.visibility;

import java.util.ArrayList;
import java.util.HashSet;
import java.util.Iterator;
import java.util.List;
import java.util.Optional;
import java.util.Set;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.tenant.application.visibility.TenantsVisibility;
import net.civeira.phylax.features.access.tenantloginprovider.domain.TenantLoginProvider;
import net.civeira.phylax.features.access.tenantloginprovider.domain.TenantLoginProviderChangeSet;
import net.civeira.phylax.features.access.tenantloginprovider.domain.TenantLoginProviderRef;
import net.civeira.phylax.features.access.tenantloginprovider.domain.gateway.TenantLoginProviderCacheGateway;
import net.civeira.phylax.features.access.tenantloginprovider.domain.gateway.TenantLoginProviderCached;
import net.civeira.phylax.features.access.tenantloginprovider.domain.gateway.TenantLoginProviderCursor;
import net.civeira.phylax.features.access.tenantloginprovider.domain.gateway.TenantLoginProviderFilter;
import net.civeira.phylax.features.access.tenantloginprovider.domain.gateway.TenantLoginProviderReadRepositoryGateway;
import net.civeira.phylax.features.access.tenantloginprovider.domain.gateway.TenantLoginProviderWriteRepositoryGateway;
import net.civeira.phylax.features.access.tenantloginprovider.domain.valueobject.TenantVO;

@ApplicationScoped
@RequiredArgsConstructor
public class TenantLoginProvidersVisibility {

  /**
   * @autogenerated VisibilityServiceGenerator
   */
  private final TenantLoginProviderCacheGateway cache;

  /**
   * @autogenerated VisibilityServiceGenerator
   */
  private final Event<TenantLoginProviderVisibilityCheck> filterProposalEmitter;

  /**
   * @autogenerated VisibilityServiceGenerator
   */
  private final Event<TenantLoginProviderFixedFieldsCheck> fixedFieldsEmitter;

  /**
   * @autogenerated VisibilityServiceGenerator
   */
  private final Event<TenantLoginProviderAccesibleCheck> guardEmitter;

  /**
   * @autogenerated VisibilityServiceGenerator
   */
  private final Event<TenantLoginProviderHiddenFieldsCheck> hiddenFieldsEmitter;

  /**
   * @autogenerated VisibilityServiceGenerator
   */
  private final Event<TenantLoginProviderEntityEnrichment> presetEmitter;

  /**
   * @autogenerated VisibilityServiceGenerator
   */
  private final TenantLoginProviderReadRepositoryGateway tenantLoginProviderReadRepositoryGateway;

  /**
   * @autogenerated VisibilityServiceGenerator
   */
  private final TenantLoginProviderWriteRepositoryGateway tenantLoginProviderWriteRepositoryGateway;

  /**
   * @autogenerated VisibilityServiceGenerator
   */
  private final TenantsVisibility tenantsVisibility;

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param uid
   * @return The input entity with the copy values without hidden
   */
  public boolean checkVisibility(Interaction prev, String uid) {
    return retrieveVisible(prev, uid).isPresent();
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param uids
   * @return The input entity with the copy values without hidden
   */
  public boolean checkVisibility(Interaction prev, List<String> uids) {
    return uids.size() == listVisibles(prev,
        TenantLoginProviderVisibilityFilter.builder().uids(uids).build(),
        TenantLoginProviderCursor.builder().build()).size();
  }

  /**
   * Retrieve the list of fields
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param source The source interaction
   * @param original The source interaction
   * @return The input entity with the copy values without hidden
   */
  public TenantLoginProviderChangeSet copyWithFixed(Interaction prev,
      TenantLoginProviderChangeSet source, TenantLoginProvider original) {
    TenantLoginProviderChangeSet withReferences = visiblesReferences(prev, source);
    fieldsToFix(prev, original).forEach(withReferences::unset);
    TenantLoginProviderEntityEnrichment proposal = TenantLoginProviderEntityEnrichment.builder()
        .dto(withReferences).interaction(prev).original(original).build();
    presetEmitter.fire(proposal);
    return proposal.getDto();
  }

  /**
   * Retrieve the list of fields
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param source The source interaction
   * @return The input entity with the copy values without hidden
   */
  public TenantLoginProviderChangeSet copyWithFixed(Interaction prev,
      TenantLoginProviderChangeSet source) {
    TenantLoginProviderChangeSet withReferences = visiblesReferences(prev, source);
    fieldsToFix(prev).forEach(withReferences::unset);
    TenantLoginProviderEntityEnrichment proposal =
        TenantLoginProviderEntityEnrichment.builder().dto(withReferences).interaction(prev).build();
    presetEmitter.fire(proposal);
    return proposal.getDto();
  }

  /**
   * Retrieve the list of fields
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param tenantLoginProvider
   * @return The input dto with hidden values
   */
  public TenantLoginProviderVisibleProjection copyWithHidden(Interaction prev,
      TenantLoginProvider tenantLoginProvider) {
    TenantLoginProviderVisibleProjection target =
        new TenantLoginProviderVisibleProjection(tenantLoginProvider);
    fieldsToHide(prev, tenantLoginProvider).forEach(target::unset);
    return target;
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param filter The filter to retrieve values
   * @return The input entity with the copy values without hidden
   */
  public long countVisibles(Interaction prev, TenantLoginProviderVisibilityFilter filter) {
    return tenantLoginProviderReadRepositoryGateway.count(applyPreVisibilityFilter(prev, filter));
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @return initialsFixFields
   */
  public Set<String> fieldsToFix(Interaction prev) {
    Set<String> fields = new HashSet<>(fieldsToHide(prev));
    TenantLoginProviderFixedFieldsCheck proposal =
        TenantLoginProviderFixedFieldsCheck.builder().fields(fields).query(prev).build();
    fixedFieldsEmitter.fire(proposal);
    return proposal.getFields();
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param tenantLoginProvider
   * @return initialsFixFields
   */
  public Set<String> fieldsToFix(Interaction prev, TenantLoginProviderRef tenantLoginProvider) {
    Set<String> fields = new HashSet<>(fieldsToHide(prev, tenantLoginProvider));
    TenantLoginProviderFixedFieldsCheck proposal = TenantLoginProviderFixedFieldsCheck.builder()
        .fields(fields).tenantLoginProvider(tenantLoginProvider).query(prev).build();
    fixedFieldsEmitter.fire(proposal);
    return proposal.getFields();
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @return initialsHideFields
   */
  public Set<String> fieldsToHide(Interaction prev) {
    Set<String> fields = new HashSet<>();
    TenantLoginProviderHiddenFieldsCheck proposal =
        TenantLoginProviderHiddenFieldsCheck.builder().fields(fields).query(prev).build();
    hiddenFieldsEmitter.fire(proposal);
    return proposal.getFields();
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param tenantLoginProvider
   * @return initialsHideFields
   */
  public Set<String> fieldsToHide(Interaction prev, TenantLoginProviderRef tenantLoginProvider) {
    Set<String> fields = new HashSet<>(fieldsToHide(prev));
    TenantLoginProviderHiddenFieldsCheck proposal = TenantLoginProviderHiddenFieldsCheck.builder()
        .fields(fields).tenantLoginProvider(tenantLoginProvider).query(prev).build();
    hiddenFieldsEmitter.fire(proposal);
    return proposal.getFields();
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param filter The filter to retrieve values
   * @param cursor The filter to retrieve values
   * @return The input entity with the copy values without hidden
   */
  public TenantLoginProviderCached listCachedVisibles(Interaction prev,
      TenantLoginProviderVisibilityFilter filter, TenantLoginProviderCursor cursor) {
    TenantLoginProviderFilter visibleFilter = applyPreVisibilityFilter(prev, filter);
    return cache.retrieve(visibleFilter, cursor)
        .orElseGet(() -> cache.store(visibleFilter, cursor, listVisibles(prev, filter, cursor)));
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param filter The filter to retrieve values
   * @param cursor The filter to retrieve values
   * @return The input entity with the copy values without hidden
   */
  public List<TenantLoginProvider> listVisibles(Interaction prev,
      TenantLoginProviderVisibilityFilter filter, TenantLoginProviderCursor cursor) {
    return queryItems(prev, applyPreVisibilityFilter(prev, filter), cursor);
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param filter The filter to retrieve values
   * @param cursor The filter to retrieve values
   * @return The input entity with the copy values without hidden
   */
  public List<TenantLoginProvider> listVisiblesForUpdate(Interaction prev,
      TenantLoginProviderVisibilityFilter filter, TenantLoginProviderCursor cursor) {
    return queryItemsForUpdate(prev, applyPreVisibilityFilter(prev, filter), cursor);
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param uid
   * @return The input entity with the copy values without hidden
   */
  public TenantLoginProviderCached retrieveCachedVisible(Interaction prev, String uid) {
    TenantLoginProviderCursor cursor = TenantLoginProviderCursor.builder().limit(1).build();
    TenantLoginProviderFilter visibleFilter = applyPreVisibilityFilter(prev,
        TenantLoginProviderVisibilityFilter.builder().uid(uid).build());
    return cache.retrieve(visibleFilter, cursor).orElseGet(() -> {
      List<TenantLoginProvider> list =
          retrieveVisible(prev, uid).<List<TenantLoginProvider>>map(List::of).orElseGet(List::of);
      return cache.store(visibleFilter, cursor, list);
    });
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param uid
   * @return The input entity with the copy values without hidden
   */
  public Optional<TenantLoginProvider> retrieveVisible(Interaction prev, String uid) {
    TenantLoginProviderFilter filter = applyPreVisibilityFilter(prev,
        TenantLoginProviderVisibilityFilter.builder().uid(uid).build());
    return queryItem(prev, uid, filter);
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param uid
   * @return The input entity with the copy values without hidden
   */
  public Optional<TenantLoginProvider> retrieveVisibleForUpdate(Interaction prev, String uid) {
    TenantLoginProviderFilter filter = applyPreVisibilityFilter(prev,
        TenantLoginProviderVisibilityFilter.builder().uid(uid).build());
    return queryItemForUpdate(prev, uid, filter);
  }

  /**
   * Retrieve the list of fields
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param filter The filter to retrieve values
   * @return The self filter modified with the prepared values.
   */
  private TenantLoginProviderFilter applyPreVisibilityFilter(Interaction prev,
      TenantLoginProviderVisibilityFilter filter) {
    TenantLoginProviderVisibilityCheck proposal =
        TenantLoginProviderVisibilityCheck.builder().filter(filter).interaction(prev).build();
    filterProposalEmitter.fire(proposal);
    TenantLoginProviderVisibilityFilter modified = proposal.getFilter();
    return TenantLoginProviderFilter.builder().uid(modified.getUid().orElse(null))
        .uids(modified.getUids().stream().toList()).search(modified.getSearch().orElse(null))
        .name(modified.getName().orElse(null)).tenant(modified.getTenant().orElse(null))
        .tenants(modified.getTenants())
        .tenantTenantAccesible(modified.getTenantTenantAccesible().orElse(null)).build();
  }

  /**
   * Retrieve the list of fields
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param tenantLoginProviderRef The source interaction
   * @return The input dto with hidden values
   */
  private boolean evaluatePostVisibility(Interaction prev,
      TenantLoginProvider tenantLoginProviderRef) {
    TenantLoginProviderAccesibleCheck proposal = TenantLoginProviderAccesibleCheck.builder()
        .visible(true).entity(tenantLoginProviderRef).interaction(prev).build();
    guardEmitter.fire(proposal);
    return Boolean.TRUE.equals(proposal.getVisible());
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param uid
   * @param filter The filter to retrieve values
   * @return The input entity with the copy values without hidden
   */
  private Optional<TenantLoginProvider> queryItem(Interaction prev, String uid,
      TenantLoginProviderFilter filter) {
    return tenantLoginProviderReadRepositoryGateway.retrieve(uid, Optional.of(filter))
        .filter(values -> evaluatePostVisibility(prev, values));
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param uid
   * @param filter The filter to retrieve values
   * @return The input entity with the copy values without hidden
   */
  private Optional<TenantLoginProvider> queryItemForUpdate(Interaction prev, String uid,
      TenantLoginProviderFilter filter) {
    return tenantLoginProviderWriteRepositoryGateway.retrieveForUpdate(uid, Optional.of(filter))
        .filter(values -> evaluatePostVisibility(prev, values));
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param filter The filter to retrieve values
   * @param cursor The filter to retrieve values
   * @return The input entity with the copy values without hidden
   */
  private List<TenantLoginProvider> queryItems(Interaction prev, TenantLoginProviderFilter filter,
      TenantLoginProviderCursor cursor) {
    List<TenantLoginProvider> list = new ArrayList<>();
    Iterator<TenantLoginProvider> slide = tenantLoginProviderReadRepositoryGateway
        .slide(filter, cursor).slide(values -> evaluatePostVisibility(prev, values));
    while (slide.hasNext()) {
      list.add(slide.next());
    }
    return list;
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param filter The filter to retrieve values
   * @param cursor The filter to retrieve values
   * @return The input entity with the copy values without hidden
   */
  private List<TenantLoginProvider> queryItemsForUpdate(Interaction prev,
      TenantLoginProviderFilter filter, TenantLoginProviderCursor cursor) {
    List<TenantLoginProvider> list = new ArrayList<>();
    Iterator<TenantLoginProvider> slide = tenantLoginProviderWriteRepositoryGateway
        .slideForUpdate(filter, cursor).slide(values -> evaluatePostVisibility(prev, values));
    while (slide.hasNext()) {
      list.add(slide.next());
    }
    return list;
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param source The source interaction
   * @return The input entity with the copy values without hidden
   */
  private TenantLoginProviderChangeSet visiblesReferences(Interaction prev,
      TenantLoginProviderChangeSet source) {
    source.getTenant().map(TenantVO::getTenantUid).ifPresent(tenant -> {
      boolean visible = tenantsVisibility.checkVisibility(prev, tenant);
      if (!visible) {
        throw new NotFoundException("No tenant - " + tenant);
      }
    });
    return source;
  }
}
