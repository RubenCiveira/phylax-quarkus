package net.civeira.phylax.features.access.role.application.usecase.create;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.role.Role;
import net.civeira.phylax.features.access.role.RoleFacade;
import net.civeira.phylax.features.access.role.application.projection.RoleStateProyection;
import net.civeira.phylax.features.access.role.application.request.RoleStateChange;
import net.civeira.phylax.features.access.role.application.service.visibility.RolesVisibility;
import net.civeira.phylax.features.access.role.gateway.RoleCacheGateway;
import net.civeira.phylax.features.access.role.gateway.RoleWriteRepositoryGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class RoleCreateUsecase {

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final RoleCacheGateway cache;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Event<RoleAllowCreateProposal> createAllowEmitter;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Event<RoleCreateProposal> createProposalEmitter;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Event<RoleCreateEvent> createdEmitter;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final RoleFacade facade;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final RoleWriteRepositoryGateway gateway;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final RolesVisibility visibility;

  /**
   * @autogenerated CreateUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    RoleAllowCreateProposal proposal = RoleAllowCreateProposal.builder()
        .detail(Allow.builder().allowed(true).description("Allow by default").build()).query(query)
        .build();
    createAllowEmitter.fire(proposal);
    return proposal.getDetail();
  }

  /**
   * The slide with some values
   *
   * @autogenerated CreateUsecaseGenerator
   * @param query
   * @param input
   * @return The slide with some values
   */
  public RoleStateProyection create(final Interaction query, final RoleStateChange input) {
    Allow detail = allow(query);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    RoleStateChange filled = visibility.copyWithFixed(query, input);
    RoleCreateProposal proposal =
        RoleCreateProposal.builder().interaction(query).dto(filled).build();
    createProposalEmitter.fire(proposal);
    RoleStateChange dto = proposal.getDto();
    Role entity = facade.create(dto);
    Role created =
        gateway.create(entity, attempt -> visibility.checkVisibility(query, attempt.getUidValue()));
    cache.update(created);
    RoleCreateEvent event = RoleCreateEvent.builder().payload(created).interaction(query).build();
    createdEmitter.fire(event);
    return visibility.copyWithHidden(query, event.getPayload());
  }
}
