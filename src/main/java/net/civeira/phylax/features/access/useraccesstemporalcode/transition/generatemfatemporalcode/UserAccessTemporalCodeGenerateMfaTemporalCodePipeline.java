package net.civeira.phylax.features.access.useraccesstemporalcode.transition.generatemfatemporalcode;

import java.time.OffsetDateTime;
import java.util.ArrayList;
import java.util.List;
import java.util.function.Function;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.inject.Instance;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.features.access.useraccesstemporalcode.UserAccessTemporalCode;
import net.civeira.phylax.features.access.useraccesstemporalcode.transition.UserAccessTemporalCodeChangeProposal;

@ApplicationScoped
@RequiredArgsConstructor
public class UserAccessTemporalCodeGenerateMfaTemporalCodePipeline {

  /**
   * @autogenerated UpdateEntityRuleGenerator
   */
  private final Instance<UserAccessTemporalCodeGenerateMfaTemporalCodeStep> steps;

  /**
   * @autogenerated UpdateEntityRuleGenerator
   * @param tempSecondFactorSeed
   * @param tempSecondFactorSeedExpiration
   * @param original
   * @param last
   * @return
   */
  public UserAccessTemporalCode generateMfaTemporalCode(final String tempSecondFactorSeed,
      final OffsetDateTime tempSecondFactorSeedExpiration, final UserAccessTemporalCode original,
      final Function<UserAccessTemporalCodeChangeProposal, UserAccessTemporalCode> last) {
    Function<UserAccessTemporalCodeGenerateMfaTemporalCodeTransition, UserAccessTemporalCode> finalHandler =
        t -> {
          if (t.hasErrors()) {
            throw t.asConstraintException();
          }
          return last.apply(t.getData());
        };
    List<UserAccessTemporalCodeGenerateMfaTemporalCodeStep> reversed =
        new ArrayList<>(steps.stream().toList()).reversed();
    Function<UserAccessTemporalCodeGenerateMfaTemporalCodeTransition, UserAccessTemporalCode> pipeline =
        reversed.stream().reduce(finalHandler,
            (next, step) -> transition -> step.generateMfaTemporalCode(transition, next),
            (f1, f2) -> f1);
    return pipeline.apply(UserAccessTemporalCodeGenerateMfaTemporalCodeTransition.builder()
        .data(new UserAccessTemporalCodeChangeProposal(original)).original(original).build());
  }
}
