package net.civeira.phylax.features.access.apikeyclient.application.usecase.enable;

import java.util.List;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.SpanKind;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Context;
import io.quarkus.arc.Unremovable;
import jakarta.enterprise.context.ApplicationScoped;
import lombok.Data;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.stepper.ItemDescriptor;
import net.civeira.phylax.common.batch.stepper.ItemProcessor;
import net.civeira.phylax.common.batch.stepper.ItemReader;
import net.civeira.phylax.common.batch.stepper.ItemWriter;
import net.civeira.phylax.common.batch.stepper.StepContext;
import net.civeira.phylax.common.batch.stepper.StepCounter;
import net.civeira.phylax.common.batch.stepper.StepFinalizer;
import net.civeira.phylax.common.batch.stepper.StepInitializer;
import net.civeira.phylax.common.exception.ExecutionException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.features.access.apikeyclient.application.visibility.ApiKeyClientsVisibility;
import net.civeira.phylax.features.access.apikeyclient.domain.ApiKeyClient;
import net.civeira.phylax.features.access.apikeyclient.domain.gateway.ApiKeyClientAuditGateway;
import net.civeira.phylax.features.access.apikeyclient.domain.gateway.ApiKeyClientCursor;

@Unremovable
@ApplicationScoped
@RequiredArgsConstructor
class ApiKeyClientEnablesInBatchExecutor implements
    StepCounter<ApiKeyClientEnableAllInBatchCommand, ApiKeyClientEnablesInBatchExecutor.ApiKeyClientPaginableBatch>,
    StepFinalizer<ApiKeyClientEnableAllInBatchCommand, ApiKeyClientEnablesInBatchExecutor.ApiKeyClientPaginableBatch>,
    StepInitializer<ApiKeyClientEnableAllInBatchCommand, ApiKeyClientEnablesInBatchExecutor.ApiKeyClientPaginableBatch>,
    ItemReader<ApiKeyClient, ApiKeyClientEnableAllInBatchCommand, ApiKeyClientEnablesInBatchExecutor.ApiKeyClientPaginableBatch>,
    ItemWriter<ApiKeyClient, ApiKeyClientEnableAllInBatchCommand, ApiKeyClientEnablesInBatchExecutor.ApiKeyClientPaginableBatch>,
    ItemProcessor<ApiKeyClient, ApiKeyClient, ApiKeyClientEnableAllInBatchCommand, ApiKeyClientEnablesInBatchExecutor.ApiKeyClientPaginableBatch>,
    ItemDescriptor<ApiKeyClient, ApiKeyClientEnableAllInBatchCommand, ApiKeyClientEnablesInBatchExecutor.ApiKeyClientPaginableBatch> {

  /**
   * @autogenerated EntityGenerator
   */
  @Data
  public static class ApiKeyClientPaginableBatch {

    /**
     * @autogenerated EntityGenerator
     */
    private String since;
  }

  /**
   * Service to register audit events
   *
   * @autogenerated EntityGenerator
   */
  private final ApiKeyClientAuditGateway audit;

  /**
   * @autogenerated EntityGenerator
   */
  private final int size = 10;

  /**
   * @autogenerated EntityGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated EntityGenerator
   */
  private final ApiKeyClientEnableUsecase usecase;

  /**
   * @autogenerated EntityGenerator
   */
  private final ApiKeyClientsVisibility visibility;

  /**
   * @autogenerated EntityGenerator
   * @param context
   * @return
   */
  @Override
  public long approximatedItems(
      final StepContext<ApiKeyClientEnableAllInBatchCommand, ApiKeyClientPaginableBatch> context) {
    Span span = tracer
        .spanBuilder(
            "Count selected items to the batch enable for a set of entities of api key client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      return visibility.countVisibles(context.getParams().getInteraction(),
          context.getParams().getFilter());
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   */
  @Override
  public void finish(
      final StepContext<ApiKeyClientEnableAllInBatchCommand, ApiKeyClientPaginableBatch> context) {
    /* do nothing */
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   */
  @Override
  public void init(
      final StepContext<ApiKeyClientEnableAllInBatchCommand, ApiKeyClientPaginableBatch> context) {
    context.setState(new ApiKeyClientPaginableBatch());
  }

  /**
   * @autogenerated EntityGenerator
   * @param item
   * @param context
   * @return
   */
  @Override
  public String itemDescription(final ApiKeyClient item,
      final StepContext<ApiKeyClientEnableAllInBatchCommand, ApiKeyClientPaginableBatch> context) {
    return item.getUid();
  }

  /**
   * @autogenerated EntityGenerator
   * @param item
   * @param context
   * @return
   */
  @Override
  public ApiKeyClient process(final ApiKeyClient item,
      final StepContext<ApiKeyClientEnableAllInBatchCommand, ApiKeyClientPaginableBatch> context) {
    Span span = tracer
        .spanBuilder(
            "Prepare individual data for the batch enable on a set of entities of api key client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      Allow allowed = usecase.checkAllow(context.getParams().getInteraction(), item);
      if (!allowed.isAllowed()) {
        span.addEvent(
            "The policies for api key client dont allow to delete: " + allowed.getDescription());
        throw new ExecutionException("not-allowed", null);
      }
      return item;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   * @return
   */
  @Override
  public List<ApiKeyClient> read(
      final StepContext<ApiKeyClientEnableAllInBatchCommand, ApiKeyClientPaginableBatch> context) {
    Span span = tracer
        .spanBuilder(
            "Read selected entitites for a batch enable for a set of entities of api key client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      List<ApiKeyClient> page = visibility.listVisiblesForUpdate(
          context.getParams().getInteraction(), context.getParams().getFilter(),
          ApiKeyClientCursor.builder().limit(size).sinceUid(context.getState().getSince()).build());
      context.getState().setSince(page.isEmpty() ? null : page.get(page.size() - 1).getUid());
      return page;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated EntityGenerator
   * @param items
   * @param context
   */
  @Override
  public void write(final List<ApiKeyClient> items,
      final StepContext<ApiKeyClientEnableAllInBatchCommand, ApiKeyClientPaginableBatch> context) {
    Span span =
        tracer.spanBuilder("Perform the batch enable for a set of entities of api key client")
            .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
            .startSpan();
    try {
      span.setAttribute("size", items.size());
      items.forEach(item -> {
        ApiKeyClient changed = usecase.doEnable(context.getParams().getInteraction(), item);
        usecase.cacheUpdate(changed);
        audit.updated("batch-enable", changed, item, context.getParams().getInteraction());
      });
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }
}
