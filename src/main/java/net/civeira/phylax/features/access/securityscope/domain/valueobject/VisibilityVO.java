package net.civeira.phylax.features.access.securityscope.domain.valueobject;

import java.util.Optional;

import lombok.EqualsAndHashCode;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.ConstraintException;
import net.civeira.phylax.common.value.validation.AbstractFailList;
import net.civeira.phylax.common.value.validation.ConstraintFail;
import net.civeira.phylax.common.value.validation.ConstraintFailList;
import net.civeira.phylax.features.access.securityscope.domain.SecurityScopeVisibilityOptions;

/**
 * Value Object representing el visibility de security scope of securityScope.
 * <p>
 * This class ensures that the visibility is a {@code String} and encapsulates its validation and
 * formatting logic. It is immutable and compliant with DDD (Domain-Driven Design) principles.
 * </p>
 * <p>
 * Use {@link #from(String)} or {@link #tryFrom(Object)} to safely create instances. Validation
 * errors are reported via {@link ConstraintFailList} or thrown as {@link ConstraintException}.
 * </p>
 */
@RequiredArgsConstructor
@EqualsAndHashCode
public class VisibilityVO {

  /**
   * Crear an empty instance with no value
   *
   * @autogenerated ValueObjectGenerator
   * @param visibility
   * @return An empty instance
   */
  public static VisibilityVO from(final SecurityScopeVisibilityOptions visibility) {
    return tryFrom(visibility);
  }

  /**
   * Crear an empty instance with no value
   *
   * @autogenerated ValueObjectGenerator
   * @return An empty instance
   */
  public static VisibilityVO nullValue() {
    return new VisibilityVO(null);
  }

  /**
   * Crear an empty instance with no value
   *
   * @autogenerated ValueObjectGenerator
   * @param visibility
   * @return An empty instance
   */
  public static VisibilityVO tryFrom(final Object visibility) {
    ConstraintFailList list = new ConstraintFailList();
    VisibilityVO result = tryFrom(visibility, list);
    if (list.hasErrors()) {
      throw new ConstraintException(list);
    }
    return result;
  }

  /**
   * Crear an empty instance with no value
   *
   * @autogenerated ValueObjectGenerator
   * @param visibility temptative value
   * @param fails Error list
   * @return An empty instance
   */
  public static <T extends AbstractFailList> VisibilityVO tryFrom(final Object visibility,
      final T fails) {
    if (null == visibility) {
      return new VisibilityVO(null);
    } else if (visibility instanceof SecurityScopeVisibilityOptions castedVisibility) {
      return new VisibilityVO(castedVisibility);
    } else if (visibility instanceof String castedVisibility) {
      try {
        return new VisibilityVO(SecurityScopeVisibilityOptions.valueOf(castedVisibility));
      } catch (IllegalArgumentException err) {
        fails.add(new ConstraintFail("wrong-option", "visibility", castedVisibility,
            "A valid enum option is expected for visibility"));
        return null;
      }
    } else {
      fails.add(new ConstraintFail("wrong-type", "visibility", visibility.getClass(),
          "A SecurityScopeVisibilityOptions type is expected for visibility"));
      return null;
    }
  }

  /**
   * The actual value of the name.
   *
   * @autogenerated ValueObjectGenerator
   */
  private final SecurityScopeVisibilityOptions value;

  /**
   * Get the vo value
   *
   * @autogenerated ValueObjectGenerator
   * @return
   */
  public Optional<SecurityScopeVisibilityOptions> getVisibility() {
    return Optional.ofNullable(value);
  }

  /**
   * Returns a formatted string representation of the name.
   *
   * @autogenerated ValueObjectGenerator
   * @return the string value wrapped in square brackets.
   */
  @Override
  public String toString() {
    return "[" + value + "]";
  }
}
