package net.civeira.phylax.features.access.apikeyclient.application.usecase.create;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.SpanKind;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Context;
import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.apikeyclient.application.visibility.ApiKeyClientsVisibility;
import net.civeira.phylax.features.access.apikeyclient.domain.ApiKeyClient;
import net.civeira.phylax.features.access.apikeyclient.domain.ApiKeyClientChangeSet;
import net.civeira.phylax.features.access.apikeyclient.domain.gateway.ApiKeyClientAuditGateway;
import net.civeira.phylax.features.access.apikeyclient.domain.gateway.ApiKeyClientCacheGateway;
import net.civeira.phylax.features.access.apikeyclient.domain.gateway.ApiKeyClientWriteRepositoryGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class ApiKeyClientCreateUsecase {

  /**
   * Service to register audit events
   *
   * @autogenerated CreateUsecaseGenerator
   */
  private final ApiKeyClientAuditGateway audit;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final ApiKeyClientCacheGateway cache;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Event<ApiKeyClientCreateAllowDecision> createAllowEmitter;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Event<ApiKeyClientCreateCheck> createCheckEmitter;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Event<ApiKeyClientCreateEnrich> createEnrichEmitter;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final ApiKeyClientWriteRepositoryGateway gateway;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final ApiKeyClientsVisibility visibility;

  /**
   * @autogenerated CreateUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    Span span = tracer.spanBuilder("Checking if is allowed to create an entity of api key client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      Allow response = checkAllow(query);
      span.setAttribute("allowed", response.isAllowed());
      return response;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated CreateUsecaseGenerator
   * @param query
   * @param input
   * @return The slide with some values
   */
  public ApiKeyClientCreateProjection create(final Interaction query,
      final ApiKeyClientCreateInput input) {
    Span span = tracer.spanBuilder("Creating an entity of api key client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      // Check if the user can perform the action
      Allow detail = checkAllow(query);
      if (!detail.isAllowed()) {
        span.addEvent(
            "The policies for api key client dont allow to create: " + detail.getDescription());
        throw new NotAllowedException(detail.getDescription());
      }
      // Append the fixed fields to the requested fields
      ApiKeyClientCreateInput filled = ApiKeyClientCreateInput
          .fromChangeSet(visibility.copyWithFixed(query, input.toChangeSet()));
      span.addEvent(
          "The input value is filled with the fixed values for the creation of api key client");
      // Pass bussines rules to validate the the requested data filled
      ApiKeyClientCreateCheck check =
          ApiKeyClientCreateCheck.builder().interaction(query).input(filled).build();
      createCheckEmitter.fire(check);
      span.addEvent(
          "The input value is checked with the policies for the creation of api key client");
      // Pass bussines rules to calculate derivated fields for the requested data filled
      ApiKeyClientCreateEnrich proposal =
          ApiKeyClientCreateEnrich.builder().interaction(query).input(filled).build();
      createEnrichEmitter.fire(proposal);
      // Convert the result input request data to a entity change set
      ApiKeyClientChangeSet dto = proposal.getInput().toChangeSet();
      // Run the creation from the domain model
      ApiKeyClient entity = ApiKeyClient.create(dto);
      span.addEvent("A created entity for api key client is ready");
      // Use the write gateway to store the entity on a persistent way
      ApiKeyClient created =
          gateway.create(entity, attempt -> visibility.checkVisibility(query, attempt.getUid()));
      span.addEvent("The created entity of api key client was saved");
      // Update the cache
      cache.update(created);
      // Register audit operation
      audit.created("create", created, query);
      // Convert the entity into a proyection and filter non visible fields
      return ApiKeyClientCreateProjection.from(visibility.copyWithHidden(query, created));
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated CreateUsecaseGenerator
   * @param query
   * @return
   */
  private Allow checkAllow(final Interaction query) {
    ApiKeyClientCreateAllowDecision proposal = ApiKeyClientCreateAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allow by default").build()).query(query)
        .build();
    createAllowEmitter.fire(proposal);
    return proposal.getDetail();
  }
}
