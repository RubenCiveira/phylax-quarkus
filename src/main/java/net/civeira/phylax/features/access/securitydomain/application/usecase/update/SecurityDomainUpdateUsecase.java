package net.civeira.phylax.features.access.securitydomain.application.usecase.update;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.securitydomain.SecurityDomain;
import net.civeira.phylax.features.access.securitydomain.SecurityDomainFacade;
import net.civeira.phylax.features.access.securitydomain.SecurityDomainRef;
import net.civeira.phylax.features.access.securitydomain.application.projection.SecurityDomainStateProyection;
import net.civeira.phylax.features.access.securitydomain.application.request.SecurityDomainStateChange;
import net.civeira.phylax.features.access.securitydomain.application.service.visibility.SecurityDomainsVisibility;
import net.civeira.phylax.features.access.securitydomain.gateway.SecurityDomainCacheGateway;
import net.civeira.phylax.features.access.securitydomain.gateway.SecurityDomainWriteRepositoryGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class SecurityDomainUpdateUsecase {

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final SecurityDomainCacheGateway cache;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final SecurityDomainFacade facade;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final SecurityDomainWriteRepositoryGateway gateway;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<SecurityDomainAllowUpdateProposal> updateAllowEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<SecurityDomainUpdateProposal> updateProposalEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<SecurityDomainUpdateEvent> updatedEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final SecurityDomainsVisibility visibility;

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  public Allow allow(final Interaction query, final SecurityDomainRef reference) {
    SecurityDomainAllowUpdateProposal base = SecurityDomainAllowUpdateProposal.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).reference(reference).build();
    updateAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    SecurityDomainAllowUpdateProposal base = SecurityDomainAllowUpdateProposal.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).build();
    updateAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * The slide with some values
   *
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @param reference
   * @param input
   * @return The slide with some values
   */
  public SecurityDomainStateProyection update(final Interaction query,
      final SecurityDomainRef reference, final SecurityDomainStateChange input) {
    Allow detail = allow(query, reference);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    SecurityDomain original = visibility.retrieveVisibleForUpdate(query, reference.getUidValue())
        .orElseThrow(() -> new NotFoundException(""));
    SecurityDomainStateChange filled = visibility.copyWithFixed(query, input);
    SecurityDomainUpdateProposal proposal = SecurityDomainUpdateProposal.builder().dto(filled)
        .interaction(query).reference(reference).build();
    updateProposalEmitter.fire(proposal);
    SecurityDomainStateChange dto = proposal.getDto();;
    SecurityDomain saved = gateway.update(original, facade.update(original, dto));
    cache.update(saved);
    SecurityDomainUpdateEvent event =
        SecurityDomainUpdateEvent.builder().payload(saved).interaction(query).build();
    updatedEmitter.fire(event);
    return visibility.copyWithHidden(query, event.getPayload());
  }
}
