package net.civeira.phylax.features.access.securitydomain.domain.valueobject;

import lombok.EqualsAndHashCode;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.ConstraintException;
import net.civeira.phylax.common.value.validation.AbstractFailList;
import net.civeira.phylax.common.value.validation.ConstraintFail;
import net.civeira.phylax.common.value.validation.ConstraintFailList;

/**
 * Value Object representing the user inherit level, and can view only domain with his level or
 * lower of securityDomain.
 * <p>
 * This class ensures that the level is a non-null {@code String} and encapsulates its validation
 * and formatting logic. It is immutable and compliant with DDD (Domain-Driven Design) principles.
 * </p>
 * <p>
 * Use {@link #from(String)} or {@link #tryFrom(Object)} to safely create instances. Validation
 * errors are reported via {@link ConstraintFailList} or thrown as {@link ConstraintException}.
 * </p>
 */
@RequiredArgsConstructor
@EqualsAndHashCode
public class LevelVO {

  /**
   * Crear an empty instance with no value
   *
   * @autogenerated ValueObjectGenerator
   * @param level
   * @return An empty instance
   */
  public static LevelVO from(final Integer level) {
    return tryFrom(level);
  }

  /**
   * Crear an empty instance with no value
   *
   * @autogenerated ValueObjectGenerator
   * @param level
   * @return An empty instance
   */
  public static LevelVO tryFrom(final Object level) {
    ConstraintFailList list = new ConstraintFailList();
    LevelVO result = tryFrom(level, list);
    if (list.hasErrors()) {
      throw new ConstraintException(list);
    }
    return result;
  }

  /**
   * Crear an empty instance with no value
   *
   * @autogenerated ValueObjectGenerator
   * @param level temptative value
   * @param fails Error list
   * @return An empty instance
   */
  public static <T extends AbstractFailList> LevelVO tryFrom(final Object level, final T fails) {
    if (null == level) {
      fails.add(new ConstraintFail("not-null", "level", null, "Cant be null"));
      return null;
    } else if (level instanceof Integer castedLevel) {
      return new LevelVO(castedLevel);
    } else {
      fails.add(new ConstraintFail("wrong-type", "level", level.getClass(),
          "A Integer type is expected for level"));
      return null;
    }
  }

  /**
   * The actual value of the name. * It is guaranteed to be a non-null String after successful
   * value.validation.
   *
   * @autogenerated ValueObjectGenerator
   */
  private final Integer value;

  /**
   * Get the vo value
   *
   * @autogenerated ValueObjectGenerator
   * @return
   */
  public Integer getLevel() {
    return value;
  }

  /**
   * Returns a formatted string representation of the name.
   *
   * @autogenerated ValueObjectGenerator
   * @return the string value wrapped in square brackets.
   */
  @Override
  public String toString() {
    return "[" + value + "]";
  }
}
