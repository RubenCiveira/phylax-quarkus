package net.civeira.phylax.features.access.useracceptedtermnsofuse.domain.valueobject;

import java.util.Optional;

import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.value.validation.ConstraintFail;
import net.civeira.phylax.common.value.validation.ConstraintFailList;
import net.civeira.phylax.features.access.tenanttermsofuse.domain.TenantTermsOfUseChangeSet;
import net.civeira.phylax.features.access.tenanttermsofuse.domain.TenantTermsOfUseRef;
import net.civeira.phylax.features.access.tenanttermsofuse.domain.TenantTermsOfUseReference;
import net.civeira.phylax.features.access.useracceptedtermnsofuse.domain.UserAcceptedTermnsOfUse;

@RequiredArgsConstructor
public class ConditionsValueHolder<T> {

  /**
   * @autogenerated ValueHolderGenerator
   */
  private final T holder;

  /**
   * @autogenerated ValueHolderGenerator
   */
  private Optional<ConditionsVO> value = Optional.empty();

  /**
   * @autogenerated ValueHolderGenerator
   * @param conditions
   * @return
   */
  public T conditions(ConditionsVO conditions) {
    value = Optional.of(conditions);
    return holder;
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @param conditions
   * @return
   */
  public T conditions(Optional<ConditionsVO> conditions) {
    value = conditions;
    return holder;
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @param conditions
   * @return
   */
  public T conditions(TenantTermsOfUseRef conditions) {
    value = Optional.of(ConditionsVO.from(conditions));
    return holder;
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @param conditions
   * @return
   */
  public T conditions(TenantTermsOfUseChangeSet conditions) {
    value = Optional
        .of(ConditionsVO.from(TenantTermsOfUseReference.of(conditions.getUid().orElseThrow())));
    return holder;
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @return
   */
  public Optional<TenantTermsOfUseRef> getConditions() {
    return value.map(ConditionsVO::getConditions);
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @return
   */
  public Optional<ConditionsVO> getConditionsValue() {
    return value;
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @param original
   * @param fails
   * @return
   */
  public ConditionsVO readConditions(final Optional<UserAcceptedTermnsOfUse> original,
      final ConstraintFailList fails) {
    ConditionsVO result =
        value.orElse(original.map(UserAcceptedTermnsOfUse::getConditionsValue).orElse(null));
    if (null == result) {
      fails.add(new ConstraintFail("REQUIRED", "conditions", null));
    }
    return result;
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @return
   */
  public T unsetConditions() {
    value = Optional.empty();
    return holder;
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @param field
   * @return
   */
  public T unsetConditionsIfMatches(final String field) {
    if ("conditions".equals(field)) {
      value = Optional.empty();
    }
    return holder;
  }
}
