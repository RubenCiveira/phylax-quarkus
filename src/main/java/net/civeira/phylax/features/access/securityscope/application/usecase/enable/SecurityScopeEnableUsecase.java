package net.civeira.phylax.features.access.securityscope.application.usecase.enable;

import java.time.Duration;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.BatchIdentificator;
import net.civeira.phylax.common.batch.BatchProgress;
import net.civeira.phylax.common.batch.BatchService;
import net.civeira.phylax.common.batch.ExecutorByDeferSteps;
import net.civeira.phylax.common.batch.ExecutorPlan;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.securityscope.application.visibility.SecurityScopeVisibilityFilter;
import net.civeira.phylax.features.access.securityscope.application.visibility.SecurityScopesVisibility;
import net.civeira.phylax.features.access.securityscope.domain.SecurityScope;
import net.civeira.phylax.features.access.securityscope.domain.SecurityScopeRef;
import net.civeira.phylax.features.access.securityscope.domain.gateway.SecurityScopeCacheGateway;
import net.civeira.phylax.features.access.securityscope.domain.gateway.SecurityScopeWriteRepositoryGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class SecurityScopeEnableUsecase {

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final BatchService batch;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final SecurityScopeCacheGateway cache;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final Event<SecurityScopeEnableAllowDecision> execAllowEmitter;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final Event<SecurityScopeEnableCheck> execProposalEmitter;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final SecurityScopeWriteRepositoryGateway gateway;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final SecurityScopesVisibility visibility;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  public Allow allow(final Interaction query, final SecurityScopeRef reference) {
    SecurityScopeEnableAllowDecision base = SecurityScopeEnableAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).reference(reference).build();
    execAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    SecurityScopeEnableAllowDecision base = SecurityScopeEnableAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).build();
    execAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query a filter to retrieve only matching values
   * @return The slide with some values
   */
  public BatchProgress checkProgress(final SecurityScopeEnableStatus query) {
    return query.getActor().getName()
        .flatMap(name -> batch.retrieve(query.getTaskId(), query.getConnection().getLocale(), name))
        .orElseThrow(() -> new NotFoundException());
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param reference
   * @return The slide with some values
   */
  public SecurityScopeEnableProjection enable(final Interaction query,
      final SecurityScopeRef reference) {
    Allow detail = allow(query, reference);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    SecurityScope original = visibility.retrieveVisibleForUpdate(query, reference.getUid())
        .orElseThrow(() -> new NotFoundException(""));
    SecurityScope saved = enable(query, original);
    flush();
    return SecurityScopeEnableProjection.from(visibility.copyWithHidden(query, saved));
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param filter
   * @return The slide with some values
   */
  public BatchIdentificator enable(final Interaction query,
      final SecurityScopeEnableFilter filter) {
    Allow detail = allow(query);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    SecurityScopeVisibilityFilter filterWithVisibility = SecurityScopeVisibilityFilter.builder()
        .uid(filter.getUid().orElse(null)).uids(filter.getUids().stream().toList())
        .search(filter.getSearch().orElse(null)).resource(filter.getResource().orElse(null))
        .trustedClient(filter.getTrustedClient().orElse(null))
        .trustedClients(filter.getTrustedClients())
        .relyingParty(filter.getRelyingParty().orElse(null))
        .relyingPartys(filter.getRelyingPartys()).build();
    SecurityScopeEnableAllInBatchCommand command = SecurityScopeEnableAllInBatchCommand.builder()
        .interaction(query).filter(filterWithVisibility).build();
    return batch.start(command.getInteraction().getActor().getName().orElse("-"),
        Duration.ofHours(6),
        ExecutorPlan.<SecurityScopeEnableAllInBatchCommand>builder().params(command)
            .name("enable-security-scope")
            .executor(
                ExecutorByDeferSteps.<SecurityScope, SecurityScope, SecurityScopeEnableAllInBatchCommand, SecurityScopeEnablesInBatchExecutor.SecurityScopePaginableBatch>builder()
                    .initializer(SecurityScopeEnablesInBatchExecutor.class)
                    .counter(SecurityScopeEnablesInBatchExecutor.class)
                    .descriptor(SecurityScopeEnablesInBatchExecutor.class)
                    .reader(SecurityScopeEnablesInBatchExecutor.class)
                    .processor(SecurityScopeEnablesInBatchExecutor.class)
                    .writer(SecurityScopeEnablesInBatchExecutor.class)
                    .finalizer(SecurityScopeEnablesInBatchExecutor.class).build())
            .build());
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param interaction
   * @param original
   * @return
   */
  SecurityScope enable(final Interaction interaction, final SecurityScope original) {
    SecurityScopeEnableCheck proposal =
        SecurityScopeEnableCheck.builder().reference(original).interaction(interaction).build();
    execProposalEmitter.fire(proposal);
    SecurityScope result = original.enable();
    SecurityScope saved = gateway.update(original, result);
    return saved;
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  void flush() {
    cache.evict();
  }
}
