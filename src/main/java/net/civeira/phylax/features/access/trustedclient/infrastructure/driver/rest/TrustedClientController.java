  /**
   * @autogenerated ApiControllerGenerator
   * @param trustedClient
   * @return
   */
  @Override
  @Transactional
  public Response trustedClientApiCreate(TrustedClientApiDto trustedClient) {
    TrustedClientStateProyection created =
        create.create(currentRequest.interaction(), toDomainModel(trustedClient));
    return Response.ok(toApiModel(created)).build();
  }

  /**
   * @autogenerated ApiControllerGenerator
   * @param uid
   * @return
   */
  @Override
  @Transactional
  public Response trustedClientApiDelete(final String uid) {
    delete.delete(currentRequest.interaction(), TrustedClientReference.of(uid));
    return Response.noContent().build();
  }

  /**
   * @autogenerated ApiControllerGenerator
   * @param uid
   * @return
   */
  @Override
  @Transactional
  public Response trustedClientApiDisable(final String uid) {
    TrustedClientStateProyection updated =
        disable.disable(currentRequest.interaction(), TrustedClientReference.of(uid));
    return Response.ok(toApiModel(updated)).build();
  }

  /**
   * @autogenerated ApiControllerGenerator
   * @param uid
   * @return
   */
  @Override
  @Transactional
  public Response trustedClientApiEnable(final String uid) {
    TrustedClientStateProyection updated =
        enable.enable(currentRequest.interaction(), TrustedClientReference.of(uid));
    return Response.ok(toApiModel(updated)).build();
  }

  /**
   * @autogenerated ApiControllerGenerator
   * @param uids
   * @param search
   * @param code
   * @param limit
   * @param sinceUid
   * @param sinceCode
   * @param order
   * @return
   */
  @Override
  public Response trustedClientApiList(final List<String> uids, final String search,
      final String code, final Integer limit, final String sinceUid, final String sinceCode,
      final String order) {
    List<TrustedClientOrder> orderSteps = null == order ? List.of()
        : Arrays.asList(order.split(",")).stream().map(this::mapOrder).filter(Objects::nonNull)
            .toList();
    TrustedClientFilter.TrustedClientFilterBuilder filterBuilder = TrustedClientFilter.builder();
    TrustedClientCursor.TrustedClientCursorBuilder cursorBuilder = TrustedClientCursor.builder();
    filterBuilder =
        filterBuilder.uids(uids.stream().flatMap(part -> Stream.of(part.split(","))).toList());
    filterBuilder = filterBuilder.search(search);
    filterBuilder = filterBuilder.code(code);
    cursorBuilder = cursorBuilder.limit(limit);
    cursorBuilder = cursorBuilder.sinceUid(sinceUid);
    cursorBuilder = cursorBuilder.sinceCode(sinceCode);
    cursorBuilder = cursorBuilder.order(orderSteps);
    TrustedClientFilter filter = filterBuilder.build();
    TrustedClientCursor cursor = cursorBuilder.build();
    List<TrustedClientStateProyection> listed =
        list.list(currentRequest.interaction(), filter, cursor);
    return currentRequest.cacheableResponse(listed, toListApiModel(listed, filter, cursor),
        "trusted-client-" + ("" + filter + cursor).hashCode());
  }

  /**
   * @autogenerated ApiControllerGenerator
   * @param uid
   * @return
   */
  @Override
  public Response trustedClientApiRetrieve(final String uid) {
    TrustedClientStateProyection retrieved =
        retrieve.retrieve(currentRequest.interaction(), TrustedClientReference.of(uid));
    /* .header("Last-Modified", value.getSince().format(DateTimeFormatter.RFC_1123_DATE_TIME))) */
    return Response.ok(toApiModel(retrieved)).build();
  }

  /**
   * @autogenerated ApiControllerGenerator
   * @param uid
   * @param trustedClient
   * @return
   */
  @Override
  @Transactional
  public Response trustedClientApiUpdate(final String uid,
      final TrustedClientApiDto trustedClient) {
    TrustedClientStateProyection updated = update.update(currentRequest.interaction(),
        TrustedClientReference.of(uid), toDomainModel(trustedClient));
    return Response.ok(toApiModel(updated)).build();
  }

  /**
   * @autogenerated ApiControllerGenerator
   * @param label
   * @return
   */
  private TrustedClientOrder mapOrder(final String label) {
    if (null == label) {
      return null;
    } else if (label.trim().equals("code-asc")) {
      return TrustedClientOrder.CODE_ASC;
    } else if (label.trim().equals("code-desc")) {
      return TrustedClientOrder.CODE_DESC;
    } else {
      return null;
    }
  }

  /**
   * @autogenerated ApiControllerGenerator
   * @param dto
   * @return
   */
  private TrustedClientApiDto toApiModel(TrustedClientStateProyection dto) {
    TrustedClientApiDto trustedClientApiDto = new TrustedClientApiDto();
    trustedClientApiDto.setUid(dto.getUid().orElse(null));
    trustedClientApiDto.setCode(dto.getCode().orElse(null));
    trustedClientApiDto.setPublicAllow(dto.getPublicAllow().orElse(null));
    trustedClientApiDto.setSecretOauth("*****");
    trustedClientApiDto.setAllowedRedirects(dto.getAllowedRedirects().orElse(null));
    trustedClientApiDto.setEnabled(dto.getEnabled().orElse(null));
    trustedClientApiDto.setVersion(dto.getVersion().orElse(null));
    return trustedClientApiDto;
  }

  /**
   * @autogenerated ApiControllerGenerator
   * @param trustedClientApiDto
   * @return
   */
  private TrustedClientStateChange toDomainModel(TrustedClientApiDto trustedClientApiDto) {
    TrustedClientStateChange dto = new TrustedClientStateChange();
    if (null != trustedClientApiDto.getUid()) {
      dto.setUid(trustedClientApiDto.getUid());
    }
    if (null != trustedClientApiDto.getCode()) {
      dto.setCode(trustedClientApiDto.getCode());
    }
    if (null != trustedClientApiDto.getPublicAllow()) {
      dto.setPublicAllow(trustedClientApiDto.getPublicAllow());
    }
    if (null != trustedClientApiDto.getSecretOauth()
        && !"*****".equals(trustedClientApiDto.getSecretOauth())) {
      dto.setSecretOauth(trustedClientApiDto.getSecretOauth());
    }
    if (null != trustedClientApiDto.getAllowedRedirects()) {
      dto.setAllowedRedirects(trustedClientApiDto.getAllowedRedirects());
    }
    if (null != trustedClientApiDto.getEnabled()) {
      dto.setEnabled(trustedClientApiDto.getEnabled());
    }
    if (null != trustedClientApiDto.getVersion()) {
      dto.setVersion(trustedClientApiDto.getVersion());
    }
    return dto;
  }

  /**
   * @autogenerated ApiControllerGenerator
   * @param trustedClients
   * @param filter
   * @param cursor
   * @return
   */
  private TrustedClientApiDtoList toListApiModel(List<TrustedClientStateProyection> trustedClients,
      TrustedClientFilter filter, TrustedClientCursor cursor) {
    Optional<TrustedClientStateProyection> last = trustedClients.isEmpty() ? Optional.empty()
        : Optional.of(trustedClients.get(trustedClients.size() - 1));
    StringBuilder self = new StringBuilder();
    StringBuilder next = new StringBuilder();
    StringBuilder first = new StringBuilder();
    if (!filter.getUids().isEmpty()) {
      String uidsValue =
          URLEncoder.encode(String.join(",", filter.getUids()), StandardCharsets.UTF_8);
      self.append(UID_APPEND + uidsValue);
      next.append(UID_APPEND + uidsValue);
      first.append(UID_APPEND + uidsValue);
    }
    filter.getSearch().ifPresent(search -> {
      String searchValue = URLEncoder.encode(search, StandardCharsets.UTF_8);
      self.append(SEARCH_APPEND + searchValue);
      next.append(SEARCH_APPEND + searchValue);
      first.append(SEARCH_APPEND + searchValue);
    });
    filter.getCode().ifPresent(filterCode -> {
      String codeValue = URLEncoder.encode(String.valueOf(filterCode), StandardCharsets.UTF_8);
      self.append(CODE_A_P_P_E_N_D + codeValue);
      next.append(CODE_A_P_P_E_N_D + codeValue);
      first.append(CODE_A_P_P_E_N_D + codeValue);
    });
    cursor.getLimit().ifPresent(limit -> {
      self.append(LIMIT_APPEND + limit);
      first.append(LIMIT_APPEND + limit);
      next.append(LIMIT_APPEND + limit);
    });
    cursor.getSinceUid().ifPresent(
        since -> self.append(LIMIT_APPEND + URLEncoder.encode(since, StandardCharsets.UTF_8)));
    cursor.getSinceCode().ifPresent(sinceCode -> self
        .append("&since-code=" + URLEncoder.encode(sinceCode, StandardCharsets.UTF_8)));
    if (!cursor.getOrder().isEmpty()) {
      String urlOrder = URLEncoder.encode(
          String.join(",",
              cursor.getOrder().stream().map(this::writeOrder).filter(Objects::nonNull).toList()),
          StandardCharsets.UTF_8);
      self.append("&order=" + urlOrder);
      next.append("&order=" + urlOrder);
      first.append("&order=" + urlOrder);
    }
    last.ifPresent(lastDto -> {
      lastDto.getUid().ifPresent(
          uid -> next.append(LIMIT_APPEND + URLEncoder.encode(uid, StandardCharsets.UTF_8)));
      cursor.getSinceCode().ifPresent(sinceCode -> next
          .append("&since-code=" + URLEncoder.encode(sinceCode, StandardCharsets.UTF_8)));
    });
    return new TrustedClientApiDtoList()
        .items(trustedClients.stream().map(this::toApiModel).toList())
        .next(next.length() > 1 ? "?" + next.substring(1) : "")
        .self(self.length() > 1 ? "?" + self.substring(1) : "")
        .first(first.length() > 1 ? "?" + first.substring(1) : "");
  }

  /**
   * @autogenerated ApiControllerGenerator
   * @param order
   * @return
   */
  private String writeOrder(final TrustedClientOrder order) {
    if (null == order) {
      return null;
    } else if (order == TrustedClientOrder.CODE_ASC) {
      return "code-asc";
    } else if (order == TrustedClientOrder.CODE_DESC) {
      return "code-desc";
    } else {
      return null;
    }
  }
