package net.civeira.phylax.features.access.apikeyclient.application.usecase.enable;

import java.time.Duration;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.BatchIdentificator;
import net.civeira.phylax.common.batch.BatchProgress;
import net.civeira.phylax.common.batch.BatchService;
import net.civeira.phylax.common.batch.ExecutorByDeferSteps;
import net.civeira.phylax.common.batch.ExecutorPlan;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.apikeyclient.application.visibility.ApiKeyClientVisibilityFilter;
import net.civeira.phylax.features.access.apikeyclient.application.visibility.ApiKeyClientsVisibility;
import net.civeira.phylax.features.access.apikeyclient.domain.ApiKeyClient;
import net.civeira.phylax.features.access.apikeyclient.domain.ApiKeyClientRef;
import net.civeira.phylax.features.access.apikeyclient.domain.gateway.ApiKeyClientCacheGateway;
import net.civeira.phylax.features.access.apikeyclient.domain.gateway.ApiKeyClientWriteRepositoryGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class ApiKeyClientEnableUsecase {

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final BatchService batch;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final ApiKeyClientCacheGateway cache;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final Event<ApiKeyClientEnableAllowDecision> execAllowEmitter;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final Event<ApiKeyClientEnableCheck> execProposalEmitter;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final ApiKeyClientWriteRepositoryGateway gateway;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final ApiKeyClientsVisibility visibility;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  public Allow allow(final Interaction query, final ApiKeyClientRef reference) {
    ApiKeyClientEnableAllowDecision base = ApiKeyClientEnableAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).reference(reference).build();
    execAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    ApiKeyClientEnableAllowDecision base = ApiKeyClientEnableAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).build();
    execAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query a filter to retrieve only matching values
   * @return The slide with some values
   */
  public BatchProgress checkProgress(final ApiKeyClientEnableStatus query) {
    return query.getActor().getName()
        .flatMap(name -> batch.retrieve(query.getTaskId(), query.getConnection().getLocale(), name))
        .orElseThrow(() -> new NotFoundException());
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param reference
   * @return The slide with some values
   */
  public ApiKeyClientEnableProjection enable(final Interaction query,
      final ApiKeyClientRef reference) {
    Allow detail = allow(query, reference);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    ApiKeyClient original = visibility.retrieveVisibleForUpdate(query, reference.getUid())
        .orElseThrow(() -> new NotFoundException(""));
    ApiKeyClient saved = enable(query, original);
    flush();
    return ApiKeyClientEnableProjection.from(visibility.copyWithHidden(query, saved));
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param filter
   * @return The slide with some values
   */
  public BatchIdentificator enable(final Interaction query, final ApiKeyClientEnableFilter filter) {
    Allow detail = allow(query);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    ApiKeyClientVisibilityFilter filterWithVisibility = ApiKeyClientVisibilityFilter.builder()
        .uid(filter.getUid().orElse(null)).uids(filter.getUids().stream().toList())
        .search(filter.getSearch().orElse(null)).key(filter.getKey().orElse(null)).build();
    ApiKeyClientEnableAllInBatchCommand command = ApiKeyClientEnableAllInBatchCommand.builder()
        .interaction(query).filter(filterWithVisibility).build();
    return batch.start(command.getInteraction().getActor().getName().orElse("-"),
        Duration.ofHours(6),
        ExecutorPlan.<ApiKeyClientEnableAllInBatchCommand>builder().params(command)
            .name("enable-api-key-client")
            .executor(
                ExecutorByDeferSteps.<ApiKeyClient, ApiKeyClient, ApiKeyClientEnableAllInBatchCommand, ApiKeyClientEnablesInBatchExecutor.ApiKeyClientPaginableBatch>builder()
                    .initializer(ApiKeyClientEnablesInBatchExecutor.class)
                    .counter(ApiKeyClientEnablesInBatchExecutor.class)
                    .descriptor(ApiKeyClientEnablesInBatchExecutor.class)
                    .reader(ApiKeyClientEnablesInBatchExecutor.class)
                    .processor(ApiKeyClientEnablesInBatchExecutor.class)
                    .writer(ApiKeyClientEnablesInBatchExecutor.class)
                    .finalizer(ApiKeyClientEnablesInBatchExecutor.class).build())
            .build());
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param interaction
   * @param original
   * @return
   */
  ApiKeyClient enable(final Interaction interaction, final ApiKeyClient original) {
    ApiKeyClientEnableCheck proposal =
        ApiKeyClientEnableCheck.builder().reference(original).interaction(interaction).build();
    execProposalEmitter.fire(proposal);
    ApiKeyClient result = original.enable();
    ApiKeyClient saved = gateway.update(original, result);
    return saved;
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  void flush() {
    cache.evict();
  }
}
