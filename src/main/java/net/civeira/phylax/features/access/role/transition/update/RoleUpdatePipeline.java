package net.civeira.phylax.features.access.role.transition.update;

import java.util.ArrayList;
import java.util.List;
import java.util.function.Function;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.inject.Instance;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.features.access.role.Role;
import net.civeira.phylax.features.access.role.command.RoleWriteAttributes;
import net.civeira.phylax.features.access.role.transition.RoleChangeProposal;

@ApplicationScoped
@RequiredArgsConstructor
public class RoleUpdatePipeline {

  /**
   * @autogenerated UpdateEntityRuleGenerator
   */
  private final Instance<RoleUpdateStep> steps;

  /**
   * @autogenerated UpdateEntityRuleGenerator
   * @param attributes
   * @param original
   * @param last
   * @return
   */
  public Role update(final RoleWriteAttributes attributes, final Role original,
      final Function<RoleChangeProposal, Role> last) {
    Function<RoleUpdateTransition, Role> finalHandler = t -> {
      if (t.hasErrors()) {
        throw t.asConstraintException();
      }
      return last.apply(t.getData());
    };
    List<RoleUpdateStep> reversed = new ArrayList<>(steps.stream().toList()).reversed();
    Function<RoleUpdateTransition, Role> pipeline = reversed.stream().reduce(finalHandler,
        (next, step) -> transition -> step.update(transition, next), (f1, f2) -> f1);
    return pipeline.apply(RoleUpdateTransition.builder().data(new RoleChangeProposal(attributes))
        .original(original).build());
  }
}
