package net.civeira.phylax.features.access.tenanttermsofuse.application.usecase.disable;

import java.time.Duration;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.BatchIdentificator;
import net.civeira.phylax.common.batch.BatchProgress;
import net.civeira.phylax.common.batch.BatchService;
import net.civeira.phylax.common.batch.ExecutorByDeferSteps;
import net.civeira.phylax.common.batch.ExecutorPlan;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.common.telemetry.Observed;
import net.civeira.phylax.common.telemetry.Trace;
import net.civeira.phylax.features.access.tenanttermsofuse.application.visibility.TenantTermsOfUseVisibilityFilter;
import net.civeira.phylax.features.access.tenanttermsofuse.application.visibility.TenantTermsOfUsesVisibility;
import net.civeira.phylax.features.access.tenanttermsofuse.domain.TenantTermsOfUse;
import net.civeira.phylax.features.access.tenanttermsofuse.domain.TenantTermsOfUseRef;
import net.civeira.phylax.features.access.tenanttermsofuse.domain.gateway.TenantTermsOfUseAuditGateway;
import net.civeira.phylax.features.access.tenanttermsofuse.domain.gateway.TenantTermsOfUseCacheGateway;
import net.civeira.phylax.features.access.tenanttermsofuse.domain.gateway.TenantTermsOfUseWriteRepositoryGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class TenantTermsOfUseDisableUsecase {

  /**
   * Service to register audit events
   *
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final TenantTermsOfUseAuditGateway audit;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final BatchService batch;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final TenantTermsOfUseCacheGateway cache;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final Event<TenantTermsOfUseDisableAllowDecision> execAllowEmitter;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final Event<TenantTermsOfUseDisableCheck> execProposalEmitter;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final TenantTermsOfUseWriteRepositoryGateway gateway;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final TenantTermsOfUsesVisibility visibility;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  @Observed("disable an entity of tenant terms of use")
  public Allow allow(final Interaction query, final TenantTermsOfUseRef reference) {
    Trace.setAttribute("ref", reference.getUid());
    Allow response = checkAllow(query, reference);
    Trace.setAttribute("allowed", response.isAllowed());
    return response;
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @return
   */
  @Observed("disable an entity of tenant terms of use")
  public Allow allow(final Interaction query) {
    Allow response = checkAllow(query);
    Trace.setAttribute("allowed", response.isAllowed());
    return response;
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param filter
   * @return The slide with some values
   */
  @Observed("disable a set of entity of tenant terms of use")
  public BatchIdentificator batchDisable(final Interaction query,
      final TenantTermsOfUseDisableFilter filter) {
    Allow detail = checkAllow(query);
    if (!detail.isAllowed()) {
      Trace.addEvent(
          "The policies for tenant terms of use dont allow to disable: " + detail.getDescription());
      throw new NotAllowedException(detail.getDescription());
    }
    TenantTermsOfUseVisibilityFilter filterWithVisibility =
        TenantTermsOfUseVisibilityFilter.builder().uid(filter.getUid().orElse(null))
            .uids(filter.getUids().stream().toList()).search(filter.getSearch().orElse(null))
            .tenant(filter.getTenant().orElse(null)).tenants(filter.getTenants())
            .tenantTenantAccesible(filter.getTenantTenantAccesible().orElse(null)).build();
    TenantTermsOfUseDisableAllInBatchCommand command = TenantTermsOfUseDisableAllInBatchCommand
        .builder().interaction(query).filter(filterWithVisibility).build();
    return batch.start(command.getInteraction().getActor().getName().orElse("-"),
        Duration.ofHours(6),
        ExecutorPlan.<TenantTermsOfUseDisableAllInBatchCommand>builder().params(command)
            .name("disable-tenant-terms-of-use")
            .executor(
                ExecutorByDeferSteps.<TenantTermsOfUse, TenantTermsOfUse, TenantTermsOfUseDisableAllInBatchCommand, TenantTermsOfUseDisablesInBatchExecutor.TenantTermsOfUsePaginableBatch>builder()
                    .initializer(TenantTermsOfUseDisablesInBatchExecutor.class)
                    .counter(TenantTermsOfUseDisablesInBatchExecutor.class)
                    .descriptor(TenantTermsOfUseDisablesInBatchExecutor.class)
                    .reader(TenantTermsOfUseDisablesInBatchExecutor.class)
                    .processor(TenantTermsOfUseDisablesInBatchExecutor.class)
                    .writer(TenantTermsOfUseDisablesInBatchExecutor.class)
                    .finalizer(TenantTermsOfUseDisablesInBatchExecutor.class).build())
            .build());
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query a filter to retrieve only matching values
   * @return The slide with some values
   */
  public BatchProgress checkProgress(final TenantTermsOfUseDisableStatus query) {
    return query.getActor().getName()
        .flatMap(name -> batch.retrieve(query.getTaskId(), query.getConnection().getLocale(), name))
        .orElseThrow(() -> new NotFoundException());
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param reference
   * @return The slide with some values
   */
  @Observed("disable an entity of tenant terms of use")
  public TenantTermsOfUseDisableProjection disable(final Interaction query,
      final TenantTermsOfUseRef reference) {
    Trace.setAttribute("ref", reference.getUid());
    Allow detail = checkAllow(query, reference);
    if (!detail.isAllowed()) {
      Trace.addEvent(
          "The policies for tenant terms of use dont allow to disable: " + detail.getDescription());
      throw new NotAllowedException(detail.getDescription());
    }
    TenantTermsOfUse original = visibility.retrieveVisibleForUpdate(query, reference.getUid())
        .orElseThrow(() -> new NotFoundException(""));
    TenantTermsOfUse saved = doDisable(query, original);
    cacheUpdate(saved);
    // Register audit operation
    audit.updated("disable", saved, original, query);
    return TenantTermsOfUseDisableProjection.from(visibility.copyWithHidden(query, saved));
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param saved
   */
  void cacheUpdate(final TenantTermsOfUse saved) {
    cache.update(saved);
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  Allow checkAllow(final Interaction query, final TenantTermsOfUseRef reference) {
    TenantTermsOfUseDisableAllowDecision base = TenantTermsOfUseDisableAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).reference(reference).build();
    execAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @return
   */
  Allow checkAllow(final Interaction query) {
    TenantTermsOfUseDisableAllowDecision base = TenantTermsOfUseDisableAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).build();
    execAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param interaction
   * @param original
   * @return
   */
  TenantTermsOfUse doDisable(final Interaction interaction, final TenantTermsOfUse original) {
    TenantTermsOfUseDisableCheck proposal =
        TenantTermsOfUseDisableCheck.builder().reference(original).interaction(interaction).build();
    execProposalEmitter.fire(proposal);
    TenantTermsOfUse result = original.disable();
    TenantTermsOfUse saved = gateway.update(original, result);
    return saved;
  }
}
