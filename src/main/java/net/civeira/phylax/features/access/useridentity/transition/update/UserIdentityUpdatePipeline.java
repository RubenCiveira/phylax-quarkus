package net.civeira.phylax.features.access.useridentity.transition.update;

import java.util.ArrayList;
import java.util.List;
import java.util.function.Function;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.inject.Instance;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.features.access.useridentity.UserIdentity;
import net.civeira.phylax.features.access.useridentity.command.UserIdentityWriteAttributes;
import net.civeira.phylax.features.access.useridentity.transition.UserIdentityChangeProposal;

@ApplicationScoped
@RequiredArgsConstructor
public class UserIdentityUpdatePipeline {

  /**
   * @autogenerated UpdateEntityRuleGenerator
   */
  private final Instance<UserIdentityUpdateStep> steps;

  /**
   * @autogenerated UpdateEntityRuleGenerator
   * @param attributes
   * @param original
   * @param last
   * @return
   */
  public UserIdentity update(final UserIdentityWriteAttributes attributes,
      final UserIdentity original, final Function<UserIdentityChangeProposal, UserIdentity> last) {
    Function<UserIdentityUpdateTransition, UserIdentity> finalHandler = t -> {
      if (t.hasErrors()) {
        throw t.asConstraintException();
      }
      return last.apply(t.getData());
    };
    List<UserIdentityUpdateStep> reversed = new ArrayList<>(steps.stream().toList()).reversed();
    Function<UserIdentityUpdateTransition, UserIdentity> pipeline = reversed.stream().reduce(
        finalHandler, (next, step) -> transition -> step.update(transition, next), (f1, f2) -> f1);
    return pipeline.apply(UserIdentityUpdateTransition.builder()
        .data(new UserIdentityChangeProposal(attributes)).original(original).build());
  }
}
