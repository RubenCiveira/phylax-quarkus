package net.civeira.phylax.features.access.tenantloginprovider.infrastructure.driver.rest;

import java.util.List;
import java.util.stream.Stream;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.SpanKind;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Context;
import jakarta.enterprise.context.ApplicationScoped;
import jakarta.transaction.Transactional;
import jakarta.ws.rs.core.Response;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.BatchIdentificator;
import net.civeira.phylax.common.batch.BatchProgress;
import net.civeira.phylax.common.infrastructure.CurrentRequest;
import net.civeira.phylax.features.access.tenant.domain.TenantReference;
import net.civeira.phylax.features.access.tenantloginprovider.application.usecase.disable.TenantLoginProviderDisableFilter;
import net.civeira.phylax.features.access.tenantloginprovider.application.usecase.disable.TenantLoginProviderDisableProjection;
import net.civeira.phylax.features.access.tenantloginprovider.application.usecase.disable.TenantLoginProviderDisableStatus;
import net.civeira.phylax.features.access.tenantloginprovider.application.usecase.disable.TenantLoginProviderDisableUsecase;
import net.civeira.phylax.features.access.tenantloginprovider.domain.TenantLoginProviderReference;
import net.civeira.phylax.features.access.tenantloginprovider.domain.TenantLoginProviderSourceOptions;
import net.civeira.phylax.generated.openapi.model.TenantApiRef;
import net.civeira.phylax.generated.openapi.model.TenantLoginProviderApiDto;
import net.civeira.phylax.generated.openapi.model.TenantLoginProviderApiDto.SourceEnum;

@RequiredArgsConstructor
@ApplicationScoped
public class TenantLoginProviderDisableController {

  /**
   * @autogenerated ActionControllerGenerator
   */
  private final CurrentRequest currentRequest;

  /**
   * @autogenerated ActionControllerGenerator
   */
  private final TenantLoginProviderDisableUsecase disable;

  /**
   * @autogenerated ActionControllerGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated ActionControllerGenerator
   * @param uids
   * @param search
   * @param name
   * @param tenant
   * @param tenants
   * @return
   */
  public Response tenantLoginProviderApiBatchDisable(final List<String> uids, final String search,
      final String name, final String tenant, final List<String> tenants) {
    Span span = tracer.spanBuilder("Api to disable on massive entitites of tenant login provider")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      TenantLoginProviderDisableFilter.TenantLoginProviderDisableFilterBuilder filterBuilder =
          TenantLoginProviderDisableFilter.builder();
      filterBuilder =
          filterBuilder.uids(uids.stream().flatMap(part -> Stream.of(part.split(","))).toList());
      filterBuilder = filterBuilder.search(search);
      filterBuilder = filterBuilder.uids(
          null == uids ? null : uids.stream().flatMap(part -> Stream.of(part.split(","))).toList());
      filterBuilder = filterBuilder.search(search);
      filterBuilder = filterBuilder.name(name);
      if (null != tenant) {
        filterBuilder = filterBuilder.tenant(TenantReference.of(tenant));
      }
      filterBuilder = filterBuilder.tenants(null == tenants ? null
          : tenants.stream().flatMap(part -> Stream.of(part.split(","))).toList());
      TenantLoginProviderDisableFilter filter = filterBuilder.build();
      BatchIdentificator task = disable.disable(currentRequest.interaction(), filter);
      /* .header("Last-Modified", value.getSince().format(DateTimeFormatter.RFC_1123_DATE_TIME)) */
      return Response.ok(task).build();
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated ActionControllerGenerator
   * @param batchId
   * @return
   */
  public Response tenantLoginProviderApiBatchDisableQuery(final String batchId) {
    Span span = tracer.spanBuilder("Api to check masive disable on entity of tenant login provider")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      BatchProgress task = disable.checkProgress(TenantLoginProviderDisableStatus.builder()
          .taskId(batchId).build(currentRequest.interaction()));
      return Response.ok(task).build();
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated ActionControllerGenerator
   * @param uid
   * @return
   */
  @Transactional
  public Response tenantLoginProviderApiDisable(final String uid) {
    Span span = tracer.spanBuilder("Api to disable an entity of tenant login provider")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      span.setAttribute("ref", uid);
      TenantLoginProviderDisableProjection updated =
          disable.disable(currentRequest.interaction(), TenantLoginProviderReference.of(uid));
      return Response.ok(toApiModel(updated)).build();
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated ActionControllerGenerator
   * @param domainEnum
   * @return
   */
  private SourceEnum sourceEnumToApi(TenantLoginProviderSourceOptions domainEnum) {
    SourceEnum result;
    if (domainEnum == TenantLoginProviderSourceOptions.GOOGLE) {
      result = SourceEnum.GOOGLE;
    } else if (domainEnum == TenantLoginProviderSourceOptions.GITHUB) {
      result = SourceEnum.GITHUB;
    } else if (domainEnum == TenantLoginProviderSourceOptions.SAML) {
      result = SourceEnum.SAML;
    } else {
      result = null;
    }
    return result;
  }

  /**
   * @autogenerated ActionControllerGenerator
   * @param dto
   * @return
   */
  private TenantLoginProviderApiDto toApiModel(TenantLoginProviderDisableProjection dto) {
    Span span = tracer.spanBuilder(
        "Mapper to build api content to response from model for entity of tenant login provider")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      TenantLoginProviderApiDto tenantLoginProviderApiDto = new TenantLoginProviderApiDto();
      tenantLoginProviderApiDto.setUid(dto.getUid());
      tenantLoginProviderApiDto.setTenant(new TenantApiRef().$ref(dto.getTenantReference()));
      tenantLoginProviderApiDto.setName(dto.getName());
      tenantLoginProviderApiDto.setSource(sourceEnumToApi(dto.getSource()));
      tenantLoginProviderApiDto.setDisabled(dto.getDisabled());
      tenantLoginProviderApiDto.setDirectAccess(dto.getDirectAccess());
      tenantLoginProviderApiDto.setPublicKey(dto.getPublicKey());
      tenantLoginProviderApiDto.setPrivateKey(dto.getPrivateKey());
      tenantLoginProviderApiDto.setCertificate(dto.getCertificate());
      String metadata = dto.getMetadata();
      if (null != metadata) {
        tenantLoginProviderApiDto.setMetadata(currentRequest.getPublicHost()
            + "/api/access/login-providers/" + dto.getUidOrDefault("-") + "/metadata");
      }
      tenantLoginProviderApiDto.setUsersEnabledByDefault(dto.getUsersEnabledByDefault());
      tenantLoginProviderApiDto.setVersion(dto.getVersion());
      return tenantLoginProviderApiDto;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }
}
