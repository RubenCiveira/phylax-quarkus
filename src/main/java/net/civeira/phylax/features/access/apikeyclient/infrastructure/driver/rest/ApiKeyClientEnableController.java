package net.civeira.phylax.features.access.apikeyclient.infrastructure.driver.rest;

import java.time.Instant;
import java.time.OffsetDateTime;
import java.time.ZoneId;
import java.util.ArrayList;
import java.util.List;
import java.util.Map;
import java.util.stream.Stream;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.SpanKind;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Context;
import jakarta.enterprise.context.ApplicationScoped;
import jakarta.transaction.Transactional;
import jakarta.ws.rs.core.Response;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.BatchIdentificator;
import net.civeira.phylax.common.batch.BatchProgress;
import net.civeira.phylax.common.batch.BatchProgress.GlobalStatus;
import net.civeira.phylax.common.batch.BatchStepProgress.ErrorInfo;
import net.civeira.phylax.common.batch.BatchStepProgress.Status;
import net.civeira.phylax.common.infrastructure.CurrentRequest;
import net.civeira.phylax.features.access.apikeyclient.application.usecase.enable.ApiKeyClientEnableFilter;
import net.civeira.phylax.features.access.apikeyclient.application.usecase.enable.ApiKeyClientEnableProjection;
import net.civeira.phylax.features.access.apikeyclient.application.usecase.enable.ApiKeyClientEnableStatus;
import net.civeira.phylax.features.access.apikeyclient.application.usecase.enable.ApiKeyClientEnableUsecase;
import net.civeira.phylax.features.access.apikeyclient.domain.ApiKeyClientReference;
import net.civeira.phylax.generated.openapi.model.ApiKeyClientApiDto;
import net.civeira.phylax.generated.openapi.model.BatchResultErrorItem;
import net.civeira.phylax.generated.openapi.model.BatchStepProgress;
import net.civeira.phylax.generated.openapi.model.BatchStepProgress.StepStatusEnum;
import net.civeira.phylax.generated.openapi.model.BatchTaskDetail;
import net.civeira.phylax.generated.openapi.model.BatchTaskDetail.DetailStatusEnum;
import net.civeira.phylax.generated.openapi.model.BatchTaskLocalizator;
import net.civeira.phylax.generated.openapi.model.ConstraintFail;
import net.civeira.phylax.generated.openapi.model.MultipleErrorCodes;

@RequiredArgsConstructor
@ApplicationScoped
public class ApiKeyClientEnableController {

  /**
   * @autogenerated ActionControllerGenerator
   */
  private final CurrentRequest currentRequest;

  /**
   * @autogenerated ActionControllerGenerator
   */
  private final ApiKeyClientEnableUsecase enable;

  /**
   * @autogenerated ActionControllerGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated ActionControllerGenerator
   * @param uids
   * @param search
   * @param key
   * @return
   */
  public Response apiKeyClientApiBatchEnable(final List<String> uids, final String search,
      final String key) {
    Span span = tracer.spanBuilder("Api to enable on massive entitites of api key client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      ApiKeyClientEnableFilter.ApiKeyClientEnableFilterBuilder filterBuilder =
          ApiKeyClientEnableFilter.builder();
      filterBuilder =
          filterBuilder.uids(uids.stream().flatMap(part -> Stream.of(part.split(","))).toList());
      filterBuilder = filterBuilder.search(search);
      filterBuilder = filterBuilder.uids(
          null == uids ? null : uids.stream().flatMap(part -> Stream.of(part.split(","))).toList());
      filterBuilder = filterBuilder.search(search);
      filterBuilder = filterBuilder.key(key);
      ApiKeyClientEnableFilter filter = filterBuilder.build();
      BatchIdentificator task = enable.batchEnable(currentRequest.interaction(), filter);
      /* .header("Last-Modified", value.getSince().format(DateTimeFormatter.RFC_1123_DATE_TIME)) */
      BatchTaskLocalizator response = new BatchTaskLocalizator();
      response.setUid(task.getUid());
      response.setSteps(task.getSteps());
      return Response.ok(response).build();
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated ActionControllerGenerator
   * @param batchId
   * @return
   */
  public Response apiKeyClientApiBatchEnableQuery(final String batchId) {
    Span span = tracer.spanBuilder("Api to check masive enable on entity of api key client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      BatchProgress task = enable.checkProgress(
          ApiKeyClientEnableStatus.builder().taskId(batchId).build(currentRequest.interaction()));
      BatchTaskDetail response = new BatchTaskDetail();
      response.setUid(task.getUid());
      GlobalStatus taskStatus = task.getStatus();
      if (null != taskStatus) {
        response.setDetailStatus(DetailStatusEnum.valueOf(taskStatus.name()));
      }
      Instant taskStart = task.getStartTime();
      if (null != taskStart) {
        response.setStartTime(OffsetDateTime.ofInstant(taskStart, ZoneId.systemDefault()));
      }
      Instant taskEnd = task.getEndTime();
      if (null != taskEnd) {
        response.setEndTime(OffsetDateTime.ofInstant(taskEnd, ZoneId.systemDefault()));
      }
      response.setSteps(task.getSteps().stream().map(step -> {
        BatchStepProgress bsp = new BatchStepProgress();
        Status stepStatus = step.getStatus();
        if (null != stepStatus) {
          bsp.setStepStatus(StepStatusEnum.valueOf(stepStatus.name()));
        }
        Instant stepStart = step.getStartTime();
        if (null != stepStart) {
          bsp.setStartTime(OffsetDateTime.ofInstant(stepStart, ZoneId.systemDefault()));
        }
        Instant stepEnd = step.getEndTime();
        if (null != stepEnd) {
          bsp.setEndTime(OffsetDateTime.ofInstant(stepEnd, ZoneId.systemDefault()));
        }
        bsp.setError(step.getError());
        bsp.setName(step.getName());
        bsp.setOks(step.getOks());
        bsp.setProcessedItems(step.getProcessedItems());
        bsp.setTotalItems(step.getTotalItems());
        bsp.setErrors(new ArrayList<>());
        bsp.setWarns(new ArrayList<>());
        Map<String, ErrorInfo> stepErrors = step.getErrors();
        if (null != stepErrors) {
          bsp.getErrors().addAll(stepErrors.entrySet().stream()
              .map(entry -> new BatchResultErrorItem().item(entry.getKey())
                  .fails(entry.getValue().getFails().stream()
                      .map(fail -> new MultipleErrorCodes().code(fail.getCode())
                          .violation(fail.getViolation())
                          .wrongValues(fail.getWrongValues().stream()
                              .map(wv -> new ConstraintFail().error(wv.getErrorMessage())
                                  .field(wv.getField()).wrong(String.valueOf(wv.getWrongValue())))
                              .toList()))
                      .toList()))
              .toList());
        }
        Map<String, ErrorInfo> stepWarns = step.getWarns();
        if (null != stepWarns) {
          bsp.getWarns().addAll(stepErrors.entrySet().stream()
              .map(entry -> new BatchResultErrorItem().item(entry.getKey())
                  .fails(entry.getValue().getFails().stream()
                      .map(fail -> new MultipleErrorCodes().code(fail.getCode())
                          .violation(fail.getViolation())
                          .wrongValues(fail.getWrongValues().stream()
                              .map(wv -> new ConstraintFail().error(wv.getErrorMessage())
                                  .field(wv.getField()).wrong(String.valueOf(wv.getWrongValue())))
                              .toList()))
                      .toList()))
              .toList());
        }
        return bsp;
      }).toList());
      return Response.ok(response).build();
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated ActionControllerGenerator
   * @param uid
   * @return
   */
  @Transactional
  public Response apiKeyClientApiEnable(final String uid) {
    Span span = tracer.spanBuilder("Api to enable an entity of api key client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      span.setAttribute("ref", uid);
      ApiKeyClientEnableProjection updated =
          enable.enable(currentRequest.interaction(), ApiKeyClientReference.of(uid));
      return Response.ok(toApiModel(updated)).build();
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated ActionControllerGenerator
   * @param dto
   * @return
   */
  private ApiKeyClientApiDto toApiModel(ApiKeyClientEnableProjection dto) {
    Span span = tracer
        .spanBuilder(
            "Mapper to build api content to response from model for entity of api key client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      ApiKeyClientApiDto apiKeyClientApiDto = new ApiKeyClientApiDto();
      apiKeyClientApiDto.setUid(dto.getUid());
      apiKeyClientApiDto.setCode(dto.getCode());
      apiKeyClientApiDto.setKey(dto.getKey());
      apiKeyClientApiDto.setEnabled(dto.getEnabled());
      apiKeyClientApiDto.setScopes(dto.getScopes());
      apiKeyClientApiDto.setVersion(dto.getVersion());
      return apiKeyClientApiDto;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }
}
