package net.civeira.phylax.features.access.useraccesstemporalcode.transition.update;

import java.util.ArrayList;
import java.util.List;
import java.util.function.Function;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.inject.Instance;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.features.access.useraccesstemporalcode.UserAccessTemporalCode;
import net.civeira.phylax.features.access.useraccesstemporalcode.command.UserAccessTemporalCodeWriteAttributes;
import net.civeira.phylax.features.access.useraccesstemporalcode.transition.UserAccessTemporalCodeChangeProposal;

@ApplicationScoped
@RequiredArgsConstructor
public class UserAccessTemporalCodeUpdatePipeline {

  /**
   * @autogenerated UpdateEntityRuleGenerator
   */
  private final Instance<UserAccessTemporalCodeUpdateStep> steps;

  /**
   * @autogenerated UpdateEntityRuleGenerator
   * @param attributes
   * @param original
   * @param last
   * @return
   */
  public UserAccessTemporalCode update(final UserAccessTemporalCodeWriteAttributes attributes,
      final UserAccessTemporalCode original,
      final Function<UserAccessTemporalCodeChangeProposal, UserAccessTemporalCode> last) {
    Function<UserAccessTemporalCodeUpdateTransition, UserAccessTemporalCode> finalHandler = t -> {
      if (t.hasErrors()) {
        throw t.asConstraintException();
      }
      return last.apply(t.getData());
    };
    List<UserAccessTemporalCodeUpdateStep> reversed =
        new ArrayList<>(steps.stream().toList()).reversed();
    Function<UserAccessTemporalCodeUpdateTransition, UserAccessTemporalCode> pipeline =
        reversed.stream().reduce(finalHandler,
            (next, step) -> transition -> step.update(transition, next), (f1, f2) -> f1);
    return pipeline.apply(UserAccessTemporalCodeUpdateTransition.builder()
        .data(new UserAccessTemporalCodeChangeProposal(attributes)).original(original).build());
  }
}
