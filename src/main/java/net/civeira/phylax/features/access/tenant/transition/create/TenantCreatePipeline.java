package net.civeira.phylax.features.access.tenant.transition.create;

import java.util.ArrayList;
import java.util.List;
import java.util.function.Function;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.inject.Instance;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.features.access.tenant.Tenant;
import net.civeira.phylax.features.access.tenant.command.TenantWriteAttributes;
import net.civeira.phylax.features.access.tenant.transition.TenantChangeProposal;

@ApplicationScoped
@RequiredArgsConstructor
public class TenantCreatePipeline {

  /**
   * @autogenerated UpdateEntityRuleGenerator
   */
  private final Instance<TenantCreateStep> steps;

  /**
   * @autogenerated UpdateEntityRuleGenerator
   * @param attributes
   * @param last
   * @return
   */
  public Tenant create(final TenantWriteAttributes attributes,
      final Function<TenantChangeProposal, Tenant> last) {
    Function<TenantCreateTransition, Tenant> finalHandler = t -> {
      if (t.hasErrors()) {
        throw t.asConstraintException();
      }
      return last.apply(t.getData());
    };
    List<TenantCreateStep> reversed = new ArrayList<>(steps.stream().toList()).reversed();
    Function<TenantCreateTransition, Tenant> pipeline = reversed.stream().reduce(finalHandler,
        (next, step) -> transition -> step.create(transition, next), (f1, f2) -> f1);
    return pipeline
        .apply(TenantCreateTransition.builder().data(new TenantChangeProposal(attributes)).build());
  }
}
