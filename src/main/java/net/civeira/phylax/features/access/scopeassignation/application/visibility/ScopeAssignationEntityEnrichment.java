package net.civeira.phylax.features.access.scopeassignation.application.visibility;

import java.util.Optional;
import java.util.function.Consumer;

import lombok.Builder;
import lombok.Data;
import lombok.EqualsAndHashCode;
import lombok.NonNull;
import lombok.With;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.scopeassignation.domain.ScopeAssignation;
import net.civeira.phylax.features.access.scopeassignation.domain.ScopeAssignationChangeSet;

@Data
@Builder(toBuilder = true)
@With
@EqualsAndHashCode(onlyExplicitlyIncluded = true)
public class ScopeAssignationEntityEnrichment {

  /**
   * @autogenerated EntityEnrichmentGenerator
   */
  @NonNull
  private final Interaction interaction;

  /**
   * @autogenerated EntityEnrichmentGenerator
   */
  @NonNull
  private ScopeAssignationChangeSet dto;

  /**
   * @autogenerated EntityEnrichmentGenerator
   */
  private ScopeAssignation original;

  /**
   * @autogenerated EntityEnrichmentGenerator
   * @return
   */
  public Optional<ScopeAssignation> getOriginal() {
    return Optional.ofNullable(original);
  }

  /**
   * @autogenerated EntityEnrichmentGenerator
   * @param mapper
   */
  public void peek(Consumer<ScopeAssignationChangeSet> mapper) {
    mapper.accept(dto);
  }
}
