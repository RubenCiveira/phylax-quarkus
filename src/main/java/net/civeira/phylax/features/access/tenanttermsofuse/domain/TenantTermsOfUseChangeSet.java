/**
 * A dto transfer to hold tenant terms of use attribute values
 */
@NoArgsConstructor
public class TenantTermsOfUseChangeSet {

  /**
   * El activation date de tenant terms of use
   *
   * @autogenerated EntityChangeSetGenerator
   */
  @Delegate
  private ActivationDateValueHolder<TenantTermsOfUseChangeSet> activationDateValue =
      new ActivationDateValueHolder<>(this);

  /**
   * El attached de tenant terms of use
   *
   * @autogenerated EntityChangeSetGenerator
   */
  @Delegate
  private AttachedValueHolder<TenantTermsOfUseChangeSet> attachedValue =
      new AttachedValueHolder<>(this);

  /**
   * The indicator to allow the account be used without deleting it
   *
   * @autogenerated EntityChangeSetGenerator
   */
  @Delegate
  private EnabledValueHolder<TenantTermsOfUseChangeSet> enabledValue =
      new EnabledValueHolder<>(this);

  /**
   * La cuenta a la que pertenece
   *
   * @autogenerated EntityChangeSetGenerator
   */
  @Delegate
  private TenantValueHolder<TenantTermsOfUseChangeSet> tenantValue = new TenantValueHolder<>(this);

  /**
   * El text de tenant terms of use
   *
   * @autogenerated EntityChangeSetGenerator
   */
  @Delegate
  private TextValueHolder<TenantTermsOfUseChangeSet> textValue = new TextValueHolder<>(this);

  /**
   * A number to identify the db record
   *
   * @autogenerated EntityChangeSetGenerator
   */
  @Delegate
  private UidValueHolder<TenantTermsOfUseChangeSet> uidValue = new UidValueHolder<>(this);

  /**
   * Campo con el n√∫mero de version de tenant terms of use para controlar bloqueos optimistas
   *
   * @autogenerated EntityChangeSetGenerator
   */
  @Delegate
  private VersionValueHolder<TenantTermsOfUseChangeSet> versionValue =
      new VersionValueHolder<>(this);

  /**
   * @autogenerated EntityChangeSetGenerator
   * @return
   */
  public TenantTermsOfUseChangeSet newUid() {
    return version(0).uid(Uuid.comb().toString());
  }

  /**
   * Unset a value by the property name. Unset values will no provide command change neither provide
   * information to the output context (hidden or readonly fields, or policy restrictions).
   *
   * @autogenerated EntityChangeSetGenerator
   * @param field The field name to unset.
   */
  public void unset(final String field) {
    uidValue.unsetUidIfMatches(field);
    tenantValue.unsetTenantIfMatches(field);
    textValue.unsetTextIfMatches(field);
    enabledValue.unsetEnabledIfMatches(field);
    attachedValue.unsetAttachedIfMatches(field);
    activationDateValue.unsetActivationDateIfMatches(field);
    versionValue.unsetVersionIfMatches(field);
  }
}
