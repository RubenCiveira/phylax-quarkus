package net.civeira.phylax.features.access.useridentity.transition.create;

import java.util.ArrayList;
import java.util.List;
import java.util.function.Function;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.inject.Instance;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.features.access.useridentity.UserIdentity;
import net.civeira.phylax.features.access.useridentity.command.UserIdentityWriteAttributes;
import net.civeira.phylax.features.access.useridentity.transition.UserIdentityChangeProposal;

@ApplicationScoped
@RequiredArgsConstructor
public class UserIdentityCreatePipeline {

  /**
   * @autogenerated UpdateEntityRuleGenerator
   */
  private final Instance<UserIdentityCreateStep> steps;

  /**
   * @autogenerated UpdateEntityRuleGenerator
   * @param attributes
   * @param last
   * @return
   */
  public UserIdentity create(final UserIdentityWriteAttributes attributes,
      final Function<UserIdentityChangeProposal, UserIdentity> last) {
    Function<UserIdentityCreateTransition, UserIdentity> finalHandler = t -> {
      if (t.hasErrors()) {
        throw t.asConstraintException();
      }
      return last.apply(t.getData());
    };
    List<UserIdentityCreateStep> reversed = new ArrayList<>(steps.stream().toList()).reversed();
    Function<UserIdentityCreateTransition, UserIdentity> pipeline = reversed.stream().reduce(
        finalHandler, (next, step) -> transition -> step.create(transition, next), (f1, f2) -> f1);
    return pipeline.apply(UserIdentityCreateTransition.builder()
        .data(new UserIdentityChangeProposal(attributes)).build());
  }
}
