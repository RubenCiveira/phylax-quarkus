package net.civeira.phylax.features.access.securitydomain.transition.create;

import java.util.ArrayList;
import java.util.List;
import java.util.function.Function;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.inject.Instance;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.features.access.securitydomain.SecurityDomain;
import net.civeira.phylax.features.access.securitydomain.command.SecurityDomainWriteAttributes;
import net.civeira.phylax.features.access.securitydomain.transition.SecurityDomainChangeProposal;

@ApplicationScoped
@RequiredArgsConstructor
public class SecurityDomainCreatePipeline {

  /**
   * @autogenerated UpdateEntityRuleGenerator
   */
  private final Instance<SecurityDomainCreateStep> steps;

  /**
   * @autogenerated UpdateEntityRuleGenerator
   * @param attributes
   * @param last
   * @return
   */
  public SecurityDomain create(final SecurityDomainWriteAttributes attributes,
      final Function<SecurityDomainChangeProposal, SecurityDomain> last) {
    Function<SecurityDomainCreateTransition, SecurityDomain> finalHandler = t -> {
      if (t.hasErrors()) {
        throw t.asConstraintException();
      }
      return last.apply(t.getData());
    };
    List<SecurityDomainCreateStep> reversed = new ArrayList<>(steps.stream().toList()).reversed();
    Function<SecurityDomainCreateTransition, SecurityDomain> pipeline = reversed.stream().reduce(
        finalHandler, (next, step) -> transition -> step.create(transition, next), (f1, f2) -> f1);
    return pipeline.apply(SecurityDomainCreateTransition.builder()
        .data(new SecurityDomainChangeProposal(attributes)).build());
  }
}
