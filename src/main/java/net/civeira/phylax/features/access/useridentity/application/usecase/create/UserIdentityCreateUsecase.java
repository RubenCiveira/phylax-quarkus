package net.civeira.phylax.features.access.useridentity.application.usecase.create;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.useridentity.application.visibility.UserIdentitysVisibility;
import net.civeira.phylax.features.access.useridentity.domain.UserIdentity;
import net.civeira.phylax.features.access.useridentity.domain.UserIdentityChangeSet;
import net.civeira.phylax.features.access.useridentity.domain.gateway.UserIdentityCacheGateway;
import net.civeira.phylax.features.access.useridentity.domain.gateway.UserIdentityWriteRepositoryGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class UserIdentityCreateUsecase {

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final UserIdentityCacheGateway cache;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Event<UserIdentityCreateAllowDecision> createAllowEmitter;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Event<UserIdentityCreateCheck> createCheckEmitter;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Event<UserIdentityCreateEnrich> createEnrichEmitter;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final UserIdentityWriteRepositoryGateway gateway;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final UserIdentitysVisibility visibility;

  /**
   * @autogenerated CreateUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    UserIdentityCreateAllowDecision proposal = UserIdentityCreateAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allow by default").build()).query(query)
        .build();
    createAllowEmitter.fire(proposal);
    return proposal.getDetail();
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated CreateUsecaseGenerator
   * @param query
   * @param input
   * @return The slide with some values
   */
  public UserIdentityCreateProjection create(final Interaction query,
      final UserIdentityCreateInput input) {
    Allow detail = allow(query);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    UserIdentityCreateInput filled =
        UserIdentityCreateInput.fromChangeSet(visibility.copyWithFixed(query, input.toChangeSet()));
    UserIdentityCreateCheck check =
        UserIdentityCreateCheck.builder().interaction(query).input(filled).build();
    createCheckEmitter.fire(check);
    UserIdentityCreateEnrich proposal =
        UserIdentityCreateEnrich.builder().interaction(query).input(filled).build();
    createEnrichEmitter.fire(proposal);
    UserIdentityChangeSet dto = proposal.getInput().toChangeSet();
    UserIdentity entity = UserIdentity.create(dto);
    UserIdentity created =
        gateway.create(entity, attempt -> visibility.checkVisibility(query, attempt.getUid()));
    cache.update(created);
    return UserIdentityCreateProjection.from(visibility.copyWithHidden(query, created));
  }
}
