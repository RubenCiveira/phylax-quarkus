package net.civeira.phylax.features.access.useridentity.application.usecase.create;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.useridentity.UserIdentity;
import net.civeira.phylax.features.access.useridentity.UserIdentityFacade;
import net.civeira.phylax.features.access.useridentity.application.projection.UserIdentityStateProyection;
import net.civeira.phylax.features.access.useridentity.application.request.UserIdentityStateChange;
import net.civeira.phylax.features.access.useridentity.application.service.visibility.UserIdentitysVisibility;
import net.civeira.phylax.features.access.useridentity.gateway.UserIdentityCacheGateway;
import net.civeira.phylax.features.access.useridentity.gateway.UserIdentityWriteRepositoryGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class UserIdentityCreateUsecase {

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final UserIdentityCacheGateway cache;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Event<UserIdentityAllowCreateProposal> createAllowEmitter;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Event<UserIdentityCreateProposal> createProposalEmitter;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Event<UserIdentityCreateEvent> createdEmitter;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final UserIdentityFacade facade;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final UserIdentityWriteRepositoryGateway gateway;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final UserIdentitysVisibility visibility;

  /**
   * @autogenerated CreateUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    UserIdentityAllowCreateProposal proposal = UserIdentityAllowCreateProposal.builder()
        .detail(Allow.builder().allowed(true).description("Allow by default").build()).query(query)
        .build();
    createAllowEmitter.fire(proposal);
    return proposal.getDetail();
  }

  /**
   * The slide with some values
   *
   * @autogenerated CreateUsecaseGenerator
   * @param query
   * @param input
   * @return The slide with some values
   */
  public UserIdentityStateProyection create(final Interaction query,
      final UserIdentityStateChange input) {
    Allow detail = allow(query);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    UserIdentityStateChange filled = visibility.copyWithFixed(query, input);
    UserIdentityCreateProposal proposal =
        UserIdentityCreateProposal.builder().interaction(query).dto(filled).build();
    createProposalEmitter.fire(proposal);
    UserIdentityStateChange dto = proposal.getDto();
    UserIdentity entity = facade.create(dto);
    UserIdentity created =
        gateway.create(entity, attempt -> visibility.checkVisibility(query, attempt.getUidValue()));
    cache.update(created);
    UserIdentityCreateEvent event =
        UserIdentityCreateEvent.builder().payload(created).interaction(query).build();
    createdEmitter.fire(event);
    return visibility.copyWithHidden(query, event.getPayload());
  }
}
