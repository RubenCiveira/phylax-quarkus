package net.civeira.phylax.features.access.securitydomain.application.visibility;

import java.util.Optional;
import java.util.function.Consumer;

import lombok.Builder;
import lombok.Data;
import lombok.EqualsAndHashCode;
import lombok.NonNull;
import lombok.With;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.securitydomain.domain.SecurityDomain;
import net.civeira.phylax.features.access.securitydomain.domain.SecurityDomainChangeSet;

@Data
@Builder(toBuilder = true)
@With
@EqualsAndHashCode(onlyExplicitlyIncluded = true)
public class SecurityDomainEntityEnrichment {

  /**
   * @autogenerated EntityEnrichmentGenerator
   */
  @NonNull
  private final Interaction interaction;

  /**
   * @autogenerated EntityEnrichmentGenerator
   */
  @NonNull
  private SecurityDomainChangeSet dto;

  /**
   * @autogenerated EntityEnrichmentGenerator
   */
  private SecurityDomain original;

  /**
   * @autogenerated EntityEnrichmentGenerator
   * @return
   */
  public Optional<SecurityDomain> getOriginal() {
    return Optional.ofNullable(original);
  }

  /**
   * @autogenerated EntityEnrichmentGenerator
   * @param mapper
   */
  public void peek(Consumer<SecurityDomainChangeSet> mapper) {
    mapper.accept(dto);
  }
}
