package net.civeira.phylax.features.access.securityscope.application.visibility;

import java.util.ArrayList;
import java.util.HashSet;
import java.util.Iterator;
import java.util.List;
import java.util.Optional;
import java.util.Set;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.relyingparty.application.visibility.RelyingPartysVisibility;
import net.civeira.phylax.features.access.securityscope.domain.SecurityScope;
import net.civeira.phylax.features.access.securityscope.domain.SecurityScopeChangeSet;
import net.civeira.phylax.features.access.securityscope.domain.SecurityScopeRef;
import net.civeira.phylax.features.access.securityscope.domain.gateway.SecurityScopeCacheGateway;
import net.civeira.phylax.features.access.securityscope.domain.gateway.SecurityScopeCached;
import net.civeira.phylax.features.access.securityscope.domain.gateway.SecurityScopeCursor;
import net.civeira.phylax.features.access.securityscope.domain.gateway.SecurityScopeFilter;
import net.civeira.phylax.features.access.securityscope.domain.gateway.SecurityScopeReadRepositoryGateway;
import net.civeira.phylax.features.access.securityscope.domain.gateway.SecurityScopeWriteRepositoryGateway;
import net.civeira.phylax.features.access.securityscope.domain.valueobject.RelyingPartyVO;
import net.civeira.phylax.features.access.securityscope.domain.valueobject.TrustedClientVO;
import net.civeira.phylax.features.access.trustedclient.application.visibility.TrustedClientsVisibility;

@ApplicationScoped
@RequiredArgsConstructor
public class SecurityScopesVisibility {

  /**
   * @autogenerated VisibilityServiceGenerator
   */
  private final SecurityScopeCacheGateway cache;

  /**
   * @autogenerated VisibilityServiceGenerator
   */
  private final Event<SecurityScopeVisibilityCheck> filterProposalEmitter;

  /**
   * @autogenerated VisibilityServiceGenerator
   */
  private final Event<SecurityScopeFixedFieldsCheck> fixedFieldsEmitter;

  /**
   * @autogenerated VisibilityServiceGenerator
   */
  private final Event<SecurityScopeAccesibleCheck> guardEmitter;

  /**
   * @autogenerated VisibilityServiceGenerator
   */
  private final Event<SecurityScopeHiddenFieldsCheck> hiddenFieldsEmitter;

  /**
   * @autogenerated VisibilityServiceGenerator
   */
  private final Event<SecurityScopeEntityEnrichment> presetEmitter;

  /**
   * @autogenerated VisibilityServiceGenerator
   */
  private final RelyingPartysVisibility relyingPartysVisibility;

  /**
   * @autogenerated VisibilityServiceGenerator
   */
  private final SecurityScopeReadRepositoryGateway securityScopeReadRepositoryGateway;

  /**
   * @autogenerated VisibilityServiceGenerator
   */
  private final SecurityScopeWriteRepositoryGateway securityScopeWriteRepositoryGateway;

  /**
   * @autogenerated VisibilityServiceGenerator
   */
  private final TrustedClientsVisibility trustedClientsVisibility;

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param uid
   * @return The input entity with the copy values without hidden
   */
  public boolean checkVisibility(Interaction prev, String uid) {
    return retrieveVisible(prev, uid).isPresent();
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param uids
   * @return The input entity with the copy values without hidden
   */
  public boolean checkVisibility(Interaction prev, List<String> uids) {
    return uids
        .size() == listVisibles(prev, SecurityScopeVisibilityFilter.builder().uids(uids).build(),
            SecurityScopeCursor.builder().build()).size();
  }

  /**
   * Retrieve the list of fields
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param source The source interaction
   * @param original The source interaction
   * @return The input entity with the copy values without hidden
   */
  public SecurityScopeChangeSet copyWithFixed(Interaction prev, SecurityScopeChangeSet source,
      SecurityScope original) {
    SecurityScopeChangeSet withReferences = visiblesReferences(prev, source);
    fieldsToFix(prev, original).forEach(withReferences::unset);
    SecurityScopeEntityEnrichment proposal = SecurityScopeEntityEnrichment.builder()
        .dto(withReferences).interaction(prev).original(original).build();
    presetEmitter.fire(proposal);
    return proposal.getDto();
  }

  /**
   * Retrieve the list of fields
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param source The source interaction
   * @return The input entity with the copy values without hidden
   */
  public SecurityScopeChangeSet copyWithFixed(Interaction prev, SecurityScopeChangeSet source) {
    SecurityScopeChangeSet withReferences = visiblesReferences(prev, source);
    fieldsToFix(prev).forEach(withReferences::unset);
    SecurityScopeEntityEnrichment proposal =
        SecurityScopeEntityEnrichment.builder().dto(withReferences).interaction(prev).build();
    presetEmitter.fire(proposal);
    return proposal.getDto();
  }

  /**
   * Retrieve the list of fields
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param securityScope
   * @return The input dto with hidden values
   */
  public SecurityScopeVisibleProjection copyWithHidden(Interaction prev,
      SecurityScope securityScope) {
    SecurityScopeVisibleProjection target = new SecurityScopeVisibleProjection(securityScope);
    fieldsToHide(prev, securityScope).forEach(target::unset);
    return target;
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param filter The filter to retrieve values
   * @return The input entity with the copy values without hidden
   */
  public long countVisibles(Interaction prev, SecurityScopeVisibilityFilter filter) {
    return securityScopeReadRepositoryGateway.count(applyPreVisibilityFilter(prev, filter));
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @return initialsFixFields
   */
  public Set<String> fieldsToFix(Interaction prev) {
    Set<String> fields = new HashSet<>(fieldsToHide(prev));
    SecurityScopeFixedFieldsCheck proposal =
        SecurityScopeFixedFieldsCheck.builder().fields(fields).query(prev).build();
    fixedFieldsEmitter.fire(proposal);
    return proposal.getFields();
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param securityScope
   * @return initialsFixFields
   */
  public Set<String> fieldsToFix(Interaction prev, SecurityScopeRef securityScope) {
    Set<String> fields = new HashSet<>(fieldsToHide(prev, securityScope));
    SecurityScopeFixedFieldsCheck proposal = SecurityScopeFixedFieldsCheck.builder().fields(fields)
        .securityScope(securityScope).query(prev).build();
    fixedFieldsEmitter.fire(proposal);
    return proposal.getFields();
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @return initialsHideFields
   */
  public Set<String> fieldsToHide(Interaction prev) {
    Set<String> fields = new HashSet<>();
    SecurityScopeHiddenFieldsCheck proposal =
        SecurityScopeHiddenFieldsCheck.builder().fields(fields).query(prev).build();
    hiddenFieldsEmitter.fire(proposal);
    return proposal.getFields();
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param securityScope
   * @return initialsHideFields
   */
  public Set<String> fieldsToHide(Interaction prev, SecurityScopeRef securityScope) {
    Set<String> fields = new HashSet<>(fieldsToHide(prev));
    SecurityScopeHiddenFieldsCheck proposal = SecurityScopeHiddenFieldsCheck.builder()
        .fields(fields).securityScope(securityScope).query(prev).build();
    hiddenFieldsEmitter.fire(proposal);
    return proposal.getFields();
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param filter The filter to retrieve values
   * @param cursor The filter to retrieve values
   * @return The input entity with the copy values without hidden
   */
  public SecurityScopeCached listCachedVisibles(Interaction prev,
      SecurityScopeVisibilityFilter filter, SecurityScopeCursor cursor) {
    SecurityScopeFilter visibleFilter = applyPreVisibilityFilter(prev, filter);
    return cache.retrieve(visibleFilter, cursor)
        .orElseGet(() -> cache.store(visibleFilter, cursor, listVisibles(prev, filter, cursor)));
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param filter The filter to retrieve values
   * @param cursor The filter to retrieve values
   * @return The input entity with the copy values without hidden
   */
  public List<SecurityScope> listVisibles(Interaction prev, SecurityScopeVisibilityFilter filter,
      SecurityScopeCursor cursor) {
    return queryItems(prev, applyPreVisibilityFilter(prev, filter), cursor);
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param filter The filter to retrieve values
   * @param cursor The filter to retrieve values
   * @return The input entity with the copy values without hidden
   */
  public List<SecurityScope> listVisiblesForUpdate(Interaction prev,
      SecurityScopeVisibilityFilter filter, SecurityScopeCursor cursor) {
    return queryItemsForUpdate(prev, applyPreVisibilityFilter(prev, filter), cursor);
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param uid
   * @return The input entity with the copy values without hidden
   */
  public SecurityScopeCached retrieveCachedVisible(Interaction prev, String uid) {
    SecurityScopeCursor cursor = SecurityScopeCursor.builder().limit(1).build();
    SecurityScopeFilter visibleFilter =
        applyPreVisibilityFilter(prev, SecurityScopeVisibilityFilter.builder().uid(uid).build());
    return cache.retrieve(visibleFilter, cursor).orElseGet(() -> {
      List<SecurityScope> list =
          retrieveVisible(prev, uid).<List<SecurityScope>>map(List::of).orElseGet(List::of);
      return cache.store(visibleFilter, cursor, list);
    });
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param uid
   * @return The input entity with the copy values without hidden
   */
  public Optional<SecurityScope> retrieveVisible(Interaction prev, String uid) {
    SecurityScopeFilter filter =
        applyPreVisibilityFilter(prev, SecurityScopeVisibilityFilter.builder().uid(uid).build());
    return queryItem(prev, uid, filter);
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param uid
   * @return The input entity with the copy values without hidden
   */
  public Optional<SecurityScope> retrieveVisibleForUpdate(Interaction prev, String uid) {
    SecurityScopeFilter filter =
        applyPreVisibilityFilter(prev, SecurityScopeVisibilityFilter.builder().uid(uid).build());
    return queryItemForUpdate(prev, uid, filter);
  }

  /**
   * Retrieve the list of fields
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param filter The filter to retrieve values
   * @return The self filter modified with the prepared values.
   */
  private SecurityScopeFilter applyPreVisibilityFilter(Interaction prev,
      SecurityScopeVisibilityFilter filter) {
    SecurityScopeVisibilityCheck proposal =
        SecurityScopeVisibilityCheck.builder().filter(filter).interaction(prev).build();
    filterProposalEmitter.fire(proposal);
    SecurityScopeVisibilityFilter modified = proposal.getFilter();
    return SecurityScopeFilter.builder().uid(modified.getUid().orElse(null))
        .uids(modified.getUids().stream().toList()).search(modified.getSearch().orElse(null))
        .resource(modified.getResource().orElse(null))
        .trustedClient(modified.getTrustedClient().orElse(null))
        .trustedClients(modified.getTrustedClients())
        .relyingParty(modified.getRelyingParty().orElse(null))
        .relyingPartys(modified.getRelyingPartys()).build();
  }

  /**
   * Retrieve the list of fields
   *
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param securityScopeRef The source interaction
   * @return The input dto with hidden values
   */
  private boolean evaluatePostVisibility(Interaction prev, SecurityScope securityScopeRef) {
    SecurityScopeAccesibleCheck proposal = SecurityScopeAccesibleCheck.builder().visible(true)
        .entity(securityScopeRef).interaction(prev).build();
    guardEmitter.fire(proposal);
    return Boolean.TRUE.equals(proposal.getVisible());
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param uid
   * @param filter The filter to retrieve values
   * @return The input entity with the copy values without hidden
   */
  private Optional<SecurityScope> queryItem(Interaction prev, String uid,
      SecurityScopeFilter filter) {
    return securityScopeReadRepositoryGateway.retrieve(uid, Optional.of(filter))
        .filter(values -> evaluatePostVisibility(prev, values));
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param uid
   * @param filter The filter to retrieve values
   * @return The input entity with the copy values without hidden
   */
  private Optional<SecurityScope> queryItemForUpdate(Interaction prev, String uid,
      SecurityScopeFilter filter) {
    return securityScopeWriteRepositoryGateway.retrieveForUpdate(uid, Optional.of(filter))
        .filter(values -> evaluatePostVisibility(prev, values));
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param filter The filter to retrieve values
   * @param cursor The filter to retrieve values
   * @return The input entity with the copy values without hidden
   */
  private List<SecurityScope> queryItems(Interaction prev, SecurityScopeFilter filter,
      SecurityScopeCursor cursor) {
    List<SecurityScope> list = new ArrayList<>();
    Iterator<SecurityScope> slide = securityScopeReadRepositoryGateway.slide(filter, cursor)
        .slide(values -> evaluatePostVisibility(prev, values));
    while (slide.hasNext()) {
      list.add(slide.next());
    }
    return list;
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param filter The filter to retrieve values
   * @param cursor The filter to retrieve values
   * @return The input entity with the copy values without hidden
   */
  private List<SecurityScope> queryItemsForUpdate(Interaction prev, SecurityScopeFilter filter,
      SecurityScopeCursor cursor) {
    List<SecurityScope> list = new ArrayList<>();
    Iterator<SecurityScope> slide = securityScopeWriteRepositoryGateway
        .slideForUpdate(filter, cursor).slide(values -> evaluatePostVisibility(prev, values));
    while (slide.hasNext()) {
      list.add(slide.next());
    }
    return list;
  }

  /**
   * @autogenerated VisibilityServiceGenerator
   * @param prev The source interaction
   * @param source The source interaction
   * @return The input entity with the copy values without hidden
   */
  private SecurityScopeChangeSet visiblesReferences(Interaction prev,
      SecurityScopeChangeSet source) {
    source.getTrustedClient().flatMap(TrustedClientVO::getTrustedClientUid).ifPresent(ref -> {
      boolean visible = trustedClientsVisibility.checkVisibility(prev, ref);
      if (!visible) {
        throw new NotFoundException("No trusted client - " + ref);
      }
    });
    source.getRelyingParty().flatMap(RelyingPartyVO::getRelyingPartyUid).ifPresent(ref -> {
      boolean visible = relyingPartysVisibility.checkVisibility(prev, ref);
      if (!visible) {
        throw new NotFoundException("No relying party - " + ref);
      }
    });
    return source;
  }
}
