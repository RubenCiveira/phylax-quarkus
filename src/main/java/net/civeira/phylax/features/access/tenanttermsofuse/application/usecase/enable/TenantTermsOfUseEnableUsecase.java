package net.civeira.phylax.features.access.tenanttermsofuse.application.usecase.enable;

import java.time.Duration;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.BatchIdentificator;
import net.civeira.phylax.common.batch.BatchProgress;
import net.civeira.phylax.common.batch.BatchService;
import net.civeira.phylax.common.batch.ExecutorByDeferSteps;
import net.civeira.phylax.common.batch.ExecutorPlan;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.tenanttermsofuse.application.visibility.TenantTermsOfUseVisibilityFilter;
import net.civeira.phylax.features.access.tenanttermsofuse.application.visibility.TenantTermsOfUsesVisibility;
import net.civeira.phylax.features.access.tenanttermsofuse.domain.TenantTermsOfUse;
import net.civeira.phylax.features.access.tenanttermsofuse.domain.TenantTermsOfUseRef;
import net.civeira.phylax.features.access.tenanttermsofuse.domain.gateway.TenantTermsOfUseCacheGateway;
import net.civeira.phylax.features.access.tenanttermsofuse.domain.gateway.TenantTermsOfUseWriteRepositoryGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class TenantTermsOfUseEnableUsecase {

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final BatchService batch;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final TenantTermsOfUseCacheGateway cache;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final Event<TenantTermsOfUseEnableAllowDecision> execAllowEmitter;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final Event<TenantTermsOfUseEnableCheck> execProposalEmitter;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final TenantTermsOfUseWriteRepositoryGateway gateway;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final TenantTermsOfUsesVisibility visibility;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  public Allow allow(final Interaction query, final TenantTermsOfUseRef reference) {
    TenantTermsOfUseEnableAllowDecision base = TenantTermsOfUseEnableAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).reference(reference).build();
    execAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    TenantTermsOfUseEnableAllowDecision base = TenantTermsOfUseEnableAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).build();
    execAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query a filter to retrieve only matching values
   * @return The slide with some values
   */
  public BatchProgress checkProgress(final TenantTermsOfUseEnableStatus query) {
    return query.getActor().getName()
        .flatMap(name -> batch.retrieve(query.getTaskId(), query.getConnection().getLocale(), name))
        .orElseThrow(() -> new NotFoundException());
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param reference
   * @return The slide with some values
   */
  public TenantTermsOfUseEnableProjection enable(final Interaction query,
      final TenantTermsOfUseRef reference) {
    Allow detail = allow(query, reference);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    TenantTermsOfUse original = visibility.retrieveVisibleForUpdate(query, reference.getUid())
        .orElseThrow(() -> new NotFoundException(""));
    TenantTermsOfUse saved = enable(query, original);
    flush();
    return TenantTermsOfUseEnableProjection.from(visibility.copyWithHidden(query, saved));
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param filter
   * @return The slide with some values
   */
  public BatchIdentificator enable(final Interaction query,
      final TenantTermsOfUseEnableFilter filter) {
    Allow detail = allow(query);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    TenantTermsOfUseVisibilityFilter filterWithVisibility =
        TenantTermsOfUseVisibilityFilter.builder().uid(filter.getUid().orElse(null))
            .uids(filter.getUids().stream().toList()).search(filter.getSearch().orElse(null))
            .tenant(filter.getTenant().orElse(null)).tenants(filter.getTenants())
            .tenantTenantAccesible(filter.getTenantTenantAccesible().orElse(null)).build();
    TenantTermsOfUseEnableAllInBatchCommand command = TenantTermsOfUseEnableAllInBatchCommand
        .builder().interaction(query).filter(filterWithVisibility).build();
    return batch.start(command.getInteraction().getActor().getName().orElse("-"),
        Duration.ofHours(6),
        ExecutorPlan.<TenantTermsOfUseEnableAllInBatchCommand>builder().params(command)
            .name("enable-tenant-terms-of-use")
            .executor(
                ExecutorByDeferSteps.<TenantTermsOfUse, TenantTermsOfUse, TenantTermsOfUseEnableAllInBatchCommand, TenantTermsOfUseEnablesInBatchExecutor.TenantTermsOfUsePaginableBatch>builder()
                    .initializer(TenantTermsOfUseEnablesInBatchExecutor.class)
                    .counter(TenantTermsOfUseEnablesInBatchExecutor.class)
                    .descriptor(TenantTermsOfUseEnablesInBatchExecutor.class)
                    .reader(TenantTermsOfUseEnablesInBatchExecutor.class)
                    .processor(TenantTermsOfUseEnablesInBatchExecutor.class)
                    .writer(TenantTermsOfUseEnablesInBatchExecutor.class)
                    .finalizer(TenantTermsOfUseEnablesInBatchExecutor.class).build())
            .build());
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param interaction
   * @param original
   * @return
   */
  TenantTermsOfUse enable(final Interaction interaction, final TenantTermsOfUse original) {
    TenantTermsOfUseEnableCheck proposal =
        TenantTermsOfUseEnableCheck.builder().reference(original).interaction(interaction).build();
    execProposalEmitter.fire(proposal);
    TenantTermsOfUse result = original.enable();
    TenantTermsOfUse saved = gateway.update(original, result);
    return saved;
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  void flush() {
    cache.evict();
  }
}
