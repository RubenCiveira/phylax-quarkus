package net.civeira.phylax.features.access.useridentity.application.usecase.update;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.useridentity.UserIdentity;
import net.civeira.phylax.features.access.useridentity.UserIdentityFacade;
import net.civeira.phylax.features.access.useridentity.UserIdentityRef;
import net.civeira.phylax.features.access.useridentity.application.projection.UserIdentityStateProyection;
import net.civeira.phylax.features.access.useridentity.application.request.UserIdentityStateChange;
import net.civeira.phylax.features.access.useridentity.application.service.visibility.UserIdentitysVisibility;
import net.civeira.phylax.features.access.useridentity.gateway.UserIdentityCacheGateway;
import net.civeira.phylax.features.access.useridentity.gateway.UserIdentityWriteRepositoryGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class UserIdentityUpdateUsecase {

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final UserIdentityCacheGateway cache;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final UserIdentityFacade facade;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final UserIdentityWriteRepositoryGateway gateway;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<UserIdentityAllowUpdateProposal> updateAllowEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<UserIdentityUpdateProposal> updateProposalEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<UserIdentityUpdateEvent> updatedEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final UserIdentitysVisibility visibility;

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  public Allow allow(final Interaction query, final UserIdentityRef reference) {
    UserIdentityAllowUpdateProposal base = UserIdentityAllowUpdateProposal.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).reference(reference).build();
    updateAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    UserIdentityAllowUpdateProposal base = UserIdentityAllowUpdateProposal.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).build();
    updateAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * The slide with some values
   *
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @param reference
   * @param input
   * @return The slide with some values
   */
  public UserIdentityStateProyection update(final Interaction query,
      final UserIdentityRef reference, final UserIdentityStateChange input) {
    Allow detail = allow(query, reference);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    UserIdentity original = visibility.retrieveVisibleForUpdate(query, reference.getUidValue())
        .orElseThrow(() -> new NotFoundException(""));
    UserIdentityStateChange filled = visibility.copyWithFixed(query, input);
    UserIdentityUpdateProposal proposal = UserIdentityUpdateProposal.builder().dto(filled)
        .interaction(query).reference(reference).build();
    updateProposalEmitter.fire(proposal);
    UserIdentityStateChange dto = proposal.getDto();;
    UserIdentity saved = gateway.update(original, facade.update(original, dto));
    cache.update(saved);
    UserIdentityUpdateEvent event =
        UserIdentityUpdateEvent.builder().payload(saved).interaction(query).build();
    updatedEmitter.fire(event);
    return visibility.copyWithHidden(query, event.getPayload());
  }
}
