package net.civeira.phylax.features.access.useridentity.application.usecase.update;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.SpanKind;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Context;
import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.useridentity.application.visibility.UserIdentitysVisibility;
import net.civeira.phylax.features.access.useridentity.domain.UserIdentity;
import net.civeira.phylax.features.access.useridentity.domain.UserIdentityChangeSet;
import net.civeira.phylax.features.access.useridentity.domain.UserIdentityRef;
import net.civeira.phylax.features.access.useridentity.domain.gateway.UserIdentityCacheGateway;
import net.civeira.phylax.features.access.useridentity.domain.gateway.UserIdentityWriteRepositoryGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class UserIdentityUpdateUsecase {

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final UserIdentityCacheGateway cache;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final UserIdentityWriteRepositoryGateway gateway;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<UserIdentityUpdateAllowDecision> updateAllowEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<UserIdentityUpdateCheck> updateCheckEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<UserIdentityUpdateEnrich> updateEnrichEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final UserIdentitysVisibility visibility;

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  public Allow allow(final Interaction query, final UserIdentityRef reference) {
    Span span = tracer.spanBuilder("Checking if is allowed to update an entity of user identity")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      span.setAttribute("ref", reference.getUid());
      Allow response = checkAllow(query, reference);
      span.setAttribute("allowed", response.isAllowed());
      return response;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    Span span = tracer.spanBuilder("Checking if is allowed to update an entity of user identity")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      Allow response = checkAllow(query);
      span.setAttribute("allowed", response.isAllowed());
      return response;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @param reference
   * @param input
   * @return The slide with some values
   */
  public UserIdentityUpdateProjection update(final Interaction query,
      final UserIdentityRef reference, final UserIdentityUpdateInput input) {
    Span span = tracer.spanBuilder("Updating an entity of user identity")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      span.setAttribute("ref", reference.getUid());
      Allow detail = checkAllow(query, reference);
      if (!detail.isAllowed()) {
        span.addEvent(
            "The policies for user identity dont allow to update: " + detail.getDescription());
        throw new NotAllowedException(detail.getDescription());
      }
      UserIdentity original = visibility.retrieveVisibleForUpdate(query, reference.getUid())
          .orElseThrow(() -> new NotFoundException(""));
      UserIdentityUpdateInput filled = UserIdentityUpdateInput
          .fromChangeSet(visibility.copyWithFixed(query, input.toChangeSet()));
      span.addEvent(
          "The input value is filled with the fixed values for the creation of user identity");
      UserIdentityUpdateCheck check = UserIdentityUpdateCheck.builder().input(filled)
          .interaction(query).reference(reference).build();
      updateCheckEmitter.fire(check);
      span.addEvent("The input value is checked with the policies for the update of user identity");
      UserIdentityUpdateEnrich proposal = UserIdentityUpdateEnrich.builder().input(filled)
          .interaction(query).reference(reference).build();
      updateEnrichEmitter.fire(proposal);
      UserIdentityChangeSet dto = proposal.getInput().toChangeSet();
      span.addEvent("The update of entity for user identity is ready");
      UserIdentity saved = gateway.update(original, original.update(dto));
      span.addEvent("The updated entity of user identity was saved");
      cache.update(saved);
      return UserIdentityUpdateProjection.from(visibility.copyWithHidden(query, saved));
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  private Allow checkAllow(final Interaction query, final UserIdentityRef reference) {
    UserIdentityUpdateAllowDecision base = UserIdentityUpdateAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).reference(reference).build();
    updateAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @return
   */
  private Allow checkAllow(final Interaction query) {
    UserIdentityUpdateAllowDecision base = UserIdentityUpdateAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).build();
    updateAllowEmitter.fire(base);
    return base.getDetail();
  }
}
