package net.civeira.phylax.features.access.useridentity.domain.valueobject;

import java.util.Optional;

import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.value.validation.ConstraintFailList;
import net.civeira.phylax.features.access.trustedclient.domain.TrustedClientChangeSet;
import net.civeira.phylax.features.access.trustedclient.domain.TrustedClientRef;
import net.civeira.phylax.features.access.trustedclient.domain.TrustedClientReference;
import net.civeira.phylax.features.access.useridentity.domain.UserIdentity;

@RequiredArgsConstructor
public class TrustedClientValueHolder<T> {

  /**
   * @autogenerated ValueHolderGenerator
   */
  private final T holder;

  /**
   * @autogenerated ValueHolderGenerator
   */
  private Optional<TrustedClientVO> value = Optional.empty();

  /**
   * @autogenerated ValueHolderGenerator
   * @return
   */
  public Optional<TrustedClientRef> getTrustedClient() {
    return value.flatMap(TrustedClientVO::getTrustedClient);
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @return
   */
  public Optional<TrustedClientVO> getTrustedClientValue() {
    return value;
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @param original
   * @param fails
   * @return
   */
  public TrustedClientVO readTrustedClient(final Optional<UserIdentity> original,
      final ConstraintFailList fails) {
    return value.orElse(
        original.map(UserIdentity::getTrustedClientValue).orElseGet(TrustedClientVO::nullValue));
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @param trustedClient
   * @return
   */
  public T trustedClient(TrustedClientVO trustedClient) {
    value = Optional.of(trustedClient);
    return holder;
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @param trustedClient
   * @return
   */
  public T trustedClient(Optional<TrustedClientVO> trustedClient) {
    value = trustedClient;
    return holder;
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @param trustedClient
   * @return
   */
  public T trustedClient(TrustedClientRef trustedClient) {
    value = Optional.of(TrustedClientVO.from(trustedClient));
    return holder;
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @param trustedClient
   * @return
   */
  public T trustedClient(TrustedClientChangeSet trustedClient) {
    value = Optional
        .of(TrustedClientVO.from(TrustedClientReference.of(trustedClient.getUid().orElseThrow())));
    return holder;
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @return
   */
  public T trustedClientNull() {
    value = Optional.empty();
    return holder;
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @return
   */
  public T unsetTrustedClient() {
    value = Optional.empty();
    return holder;
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @param field
   * @return
   */
  public T unsetTrustedClientIfMatches(final String field) {
    if ("trustedClient".equals(field)) {
      value = Optional.empty();
    }
    return holder;
  }
}
