package net.civeira.phylax.features.access.tenantloginprovider.infrastructure.repository;

import java.util.Iterator;
import java.util.List;
import java.util.function.BiFunction;

import net.civeira.phylax.common.algorithms.Slider;
import net.civeira.phylax.features.access.tenantloginprovider.domain.TenantLoginProvider;
import net.civeira.phylax.features.access.tenantloginprovider.domain.gateway.TenantLoginProviderCursor;
import net.civeira.phylax.features.access.tenantloginprovider.domain.gateway.TenantLoginProviderFilter;

class TenantLoginProviderSlider extends Slider<TenantLoginProvider> {

  /**
   * @autogenerated SlideGenerator
   */
  private final TenantLoginProviderCursor cursor;

  /**
   * @autogenerated SlideGenerator
   */
  private final TenantLoginProviderFilter filter;

  /**
   * @autogenerated SlideGenerator
   */
  private final BiFunction<TenantLoginProviderFilter, TenantLoginProviderCursor, Iterator<TenantLoginProvider>> gateway;

  /**
   * @autogenerated SlideGenerator
   * @param multi
   * @param limit
   * @param gateway
   * @param filter
   * @param cursor
   */
  TenantLoginProviderSlider(final Iterator<TenantLoginProvider> multi, final int limit,
      final BiFunction<TenantLoginProviderFilter, TenantLoginProviderCursor, Iterator<TenantLoginProvider>> gateway,
      final TenantLoginProviderFilter filter, final TenantLoginProviderCursor cursor) {
    super(multi, limit);
    this.gateway = gateway;
    this.filter = filter;
    this.cursor = cursor;
  }

  /**
   * @autogenerated SlideGenerator
   * @param tenantLoginProviders
   * @param limit
   * @return
   */
  @Override
  public Iterator<TenantLoginProvider> next(List<TenantLoginProvider> tenantLoginProviders,
      int limit) {
    TenantLoginProvider last = tenantLoginProviders.get(tenantLoginProviders.size() - 1);
    TenantLoginProviderCursor cr = this.cursor.withSinceUid(last.getUid()).withLimit(limit);
    return gateway.apply(this.filter, cr);
  }
}
