package net.civeira.phylax.features.access.scopeassignation.application.usecase.list;

import java.util.List;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.algorithms.metadata.TimestampedList;
import net.civeira.phylax.common.algorithms.metadata.WrapMetadata;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.scopeassignation.application.visibility.ScopeAssignationVisibilityFilter;
import net.civeira.phylax.features.access.scopeassignation.application.visibility.ScopeAssignationsVisibility;
import net.civeira.phylax.features.access.scopeassignation.domain.gateway.ScopeAssignationCached;
import net.civeira.phylax.features.access.scopeassignation.domain.gateway.ScopeAssignationCursor;

@ApplicationScoped
@RequiredArgsConstructor
public class ScopeAssignationListUsecase {

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final Event<ScopeAssignationListAllowDecision> listAllowEmitter;

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final ScopeAssignationsVisibility visibility;

  /**
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    ScopeAssignationListAllowDecision proposal = ScopeAssignationListAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allow by default").build()).query(query)
        .build();
    listAllowEmitter.fire(proposal);
    return proposal.getDetail();
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @param filter
   * @param cursor
   * @return The slide with some values
   */
  public List<ScopeAssignationListProjection> list(final Interaction query,
      final ScopeAssignationListFilter filter, final ScopeAssignationListCursor cursor) {
    Allow detail = allow(query);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    ScopeAssignationVisibilityFilter visibleFilter =
        ScopeAssignationVisibilityFilter.builder().uid(filter.getUid().orElse(null))
            .uids(filter.getUids().stream().toList()).search(filter.getSearch().orElse(null))
            .securityDomain(filter.getSecurityDomain().orElse(null))
            .securityDomains(filter.getSecurityDomains())
            .securityScope(filter.getSecurityScope().orElse(null))
            .securityScopes(filter.getSecurityScopes()).build();
    ScopeAssignationCursor gatewayCursor = ScopeAssignationCursor.builder()
        .limit(cursor.getLimit().orElse(null)).sinceUid(cursor.getSinceUid().orElse(null)).build();
    ScopeAssignationCached values =
        visibility.listCachedVisibles(query, visibleFilter, gatewayCursor);
    List<ScopeAssignationListProjection> list =
        values.getValue().stream().map(value -> visibility.copyWithHidden(query, value))
            .map(ScopeAssignationListProjection::from).toList();
    return new TimestampedList<>(WrapMetadata.<List<ScopeAssignationListProjection>>builder()
        .data(list).since(values.getSince().toInstant()).build());
  }
}
