package net.civeira.phylax.features.access.scopeassignation.application.usecase.list;

import java.util.List;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.algorithms.metadata.TimestampedList;
import net.civeira.phylax.common.algorithms.metadata.WrapMetadata;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.scopeassignation.application.projection.ScopeAssignationStateProyection;
import net.civeira.phylax.features.access.scopeassignation.application.service.visibility.ScopeAssignationsVisibility;
import net.civeira.phylax.features.access.scopeassignation.gateway.ScopeAssignationCached;
import net.civeira.phylax.features.access.scopeassignation.query.ScopeAssignationCursor;
import net.civeira.phylax.features.access.scopeassignation.query.ScopeAssignationFilter;

@ApplicationScoped
@RequiredArgsConstructor
public class ScopeAssignationListUsecase {

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final Event<ScopeAssignationAllowListProposal> listAllowEmitter;

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final ScopeAssignationsVisibility visibility;

  /**
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    ScopeAssignationAllowListProposal proposal = ScopeAssignationAllowListProposal.builder()
        .detail(Allow.builder().allowed(true).description("Allow by default").build()).query(query)
        .build();
    listAllowEmitter.fire(proposal);
    return proposal.getDetail();
  }

  /**
   * The slide with some values
   *
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @param filter
   * @param cursor
   * @return The slide with some values
   */
  public List<ScopeAssignationStateProyection> list(final Interaction query,
      final ScopeAssignationFilter filter, final ScopeAssignationCursor cursor) {
    Allow detail = allow(query);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    ScopeAssignationCached values = visibility.listCachedVisibles(query, filter, cursor);
    List<ScopeAssignationStateProyection> list =
        values.getValue().stream().map(value -> visibility.copyWithHidden(query, value)).toList();
    return new TimestampedList<>(WrapMetadata.<List<ScopeAssignationStateProyection>>builder()
        .data(list).since(values.getSince().toInstant()).build());
  }
}
