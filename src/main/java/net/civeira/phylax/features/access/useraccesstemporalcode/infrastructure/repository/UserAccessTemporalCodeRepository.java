  /**
   * @autogenerated RepositoryJdbcGenerator
   * @param entity
   * @return
   */
  public UserAccessTemporalCode update(UserAccessTemporalCode entity) {
    try (SqlTemplate template = new SqlTemplate(datasource, tracer)) {
      SqlCommand sq = template.createSqlCommand(
          "update \"access_user_access_temporal_code\" set  \"user\" = :user, \"temp_second_factor_seed\" = :tempSecondFactorSeed, \"temp_second_factor_seed_expiration\" = :tempSecondFactorSeedExpiration, \"failed_login_attempts\" = :failedLoginAttempts, \"register_code\" = :registerCode, \"register_code_url\" = :registerCodeUrl, \"register_code_expiration\" = :registerCodeExpiration, \"recovery_code\" = :recoveryCode, \"recovery_code_expiration\" = :recoveryCodeExpiration, \"version\" = \"version\" + 1 where \"uid\" = :uid and \"version\" = :version");
      sq.with(UID, SqlParameterValue.of(entity.getUid()));
      sq.with(USER, SqlParameterValue.of(entity.getUserUid()));
      sq.with(TEMP_SECOND_FACTOR_SEED, entity.getTempSecondFactorSeedCyphered(cypher)
          .map(SqlParameterValue::of).orElseGet(SqlParameterValue::ofNullString));
      sq.with(TEMP_SECOND_FACTOR_SEED_EXPIRATION, entity.getTempSecondFactorSeedExpiration()
          .map(SqlParameterValue::of).orElseGet(SqlParameterValue::ofNullOffsetDateTime));
      sq.with(FAILED_LOGIN_ATTEMPTS, entity.getFailedLoginAttempts().map(SqlParameterValue::of)
          .orElseGet(SqlParameterValue::ofNullInteger));
      sq.with(REGISTER_CODE, entity.getRegisterCode().map(SqlParameterValue::of)
          .orElseGet(SqlParameterValue::ofNullString));
      sq.with(REGISTER_CODE_URL, entity.getRegisterCodeUrl().map(SqlParameterValue::of)
          .orElseGet(SqlParameterValue::ofNullString));
      sq.with(REGISTER_CODE_EXPIRATION, entity.getRegisterCodeExpiration()
          .map(SqlParameterValue::of).orElseGet(SqlParameterValue::ofNullOffsetDateTime));
      sq.with(RECOVERY_CODE, entity.getRecoveryCode().map(SqlParameterValue::of)
          .orElseGet(SqlParameterValue::ofNullString));
      sq.with(RECOVERY_CODE_EXPIRATION, entity.getRecoveryCodeExpiration()
          .map(SqlParameterValue::of).orElseGet(SqlParameterValue::ofNullOffsetDateTime));
      sq.with(VERSION, entity.getVersion().map(SqlParameterValue::of)
          .orElseGet(SqlParameterValue::ofNullInteger));
      int num = sq.execute();
      if (0 == num) {
        throw new OptimistLockException("No delete from");
      }
      return entity.withNextVersion();
    }
  }

  /**
   * @autogenerated RepositoryJdbcGenerator
   * @return
   */
  private SqlConverter<UserAccessTemporalCode> converter() {
    return row -> {
      try {
        return Optional.of(UserAccessTemporalCode.builder().uidValue(UidVO.from(row.getString(UID)))
            .userValue(UserVO.fromReference(row.getString(USER)))
            .tempSecondFactorSeedValue(
                TempSecondFactorSeedVO.fromCyphered(row.getString(TEMP_SECOND_FACTOR_SEED_SNAKE)))
            .tempSecondFactorSeedExpirationValue(TempSecondFactorSeedExpirationVO
                .from(null == row.getTimestamp(TEMP_SECOND_FACTOR_SEED_EXPIRATION_SNAKE) ? null
                    : OffsetDateTime.ofInstant(
                        Instant.ofEpochMilli(
                            row.getTimestamp(TEMP_SECOND_FACTOR_SEED_EXPIRATION_SNAKE).getTime()),
                        ZoneId.systemDefault())))
            .failedLoginAttemptsValue(
                FailedLoginAttemptsVO.from(row.getInt(FAILED_LOGIN_ATTEMPTS_SNAKE)))
            .registerCodeValue(RegisterCodeVO.from(row.getString(REGISTER_CODE_SNAKE)))
            .registerCodeUrlValue(RegisterCodeUrlVO.from(row.getString(REGISTER_CODE_URL_SNAKE)))
            .registerCodeExpirationValue(RegisterCodeExpirationVO
                .from(null == row.getTimestamp(REGISTER_CODE_EXPIRATION_SNAKE) ? null
                    : OffsetDateTime.ofInstant(
                        Instant.ofEpochMilli(
                            row.getTimestamp(REGISTER_CODE_EXPIRATION_SNAKE).getTime()),
                        ZoneId.systemDefault())))
            .recoveryCodeValue(RecoveryCodeVO.from(row.getString(RECOVERY_CODE_SNAKE)))
            .recoveryCodeExpirationValue(RecoveryCodeExpirationVO
                .from(null == row.getTimestamp(RECOVERY_CODE_EXPIRATION_SNAKE) ? null
                    : OffsetDateTime.ofInstant(
                        Instant.ofEpochMilli(
                            row.getTimestamp(RECOVERY_CODE_EXPIRATION_SNAKE).getTime()),
                        ZoneId.systemDefault())))
            .versionValue(VersionVO.from(row.getInt(VERSION))).build());
      } catch (ConstraintException ce) {
        log.error("Unable to map data for {}", row.getString(1), ce);
        return Optional.empty();
      }
    };
  }

  /**
   * @autogenerated RepositoryJdbcGenerator
   * @param template
   * @param filter
   * @return
   */
  private <T> SqlSchematicQuery<T> filteredQuery(SqlTemplate template,
      UserAccessTemporalCodeFilter filter) {
    SqlSchematicQuery<T> sq =
        template.createSqlSchematicQuery(ACCESS_USER_ACCESS_TEMPORAL_CODE_SNAKE);
    sq.selectFields(ACCESS_USER_ACCESS_TEMPORAL_CODE_UID, ACCESS_USER_ACCESS_TEMPORAL_CODE_USER,
        ACCESS_USER_ACCESS_TEMPORAL_CODE_TEMP_SECOND_FACTOR_SEED,
        ACCESS_USER_ACCESS_TEMPORAL_CODE_TEMP_SECOND_FACTOR_SEED_EXPIRATION,
        ACCESS_USER_ACCESS_TEMPORAL_CODE_FAILED_LOGIN_ATTEMPTS,
        ACCESS_USER_ACCESS_TEMPORAL_CODE_REGISTER_CODE,
        ACCESS_USER_ACCESS_TEMPORAL_CODE_REGISTER_CODE_URL,
        ACCESS_USER_ACCESS_TEMPORAL_CODE_REGISTER_CODE_EXPIRATION,
        ACCESS_USER_ACCESS_TEMPORAL_CODE_RECOVERY_CODE,
        ACCESS_USER_ACCESS_TEMPORAL_CODE_RECOVERY_CODE_EXPIRATION,
        ACCESS_USER_ACCESS_TEMPORAL_CODE_VERSION);
    filter.getUid().ifPresent(uid -> sq.where(UID, SqlOperator.EQ, SqlParameterValue.of(uid)));
    if (!filter.getUids().isEmpty()) {
      sq.where(UID, SqlOperator.IN, SqlListParameterValue.strings(filter.getUids()));
    }
    filter.getSearch().ifPresent(
        search -> sq.where("uid", SqlOperator.LIKE, SqlParameterValue.of("%" + search + "%")));
    filter.getUser()
        .ifPresent(user -> sq.where(USER, SqlOperator.EQ, SqlParameterValue.of(user.getUid())));
    if (!filter.getUsers().isEmpty()) {
      sq.where(USER, SqlOperator.IN, SqlListParameterValue.strings(filter.getUsers()));
    }
    filter.getUserTenantTenantAccesible().ifPresent(userTenantTenantAccesible -> {
      sq.join("access_user", "access_user_access_temporal_code_user",
          "access_user_access_temporal_code.user", "access_user_access_temporal_code_user.uid");
      sq.where(ACCESS_USER_ACCESS_TEMPORAL_CODE_USER_TENANT, SqlOperator.EQ,
          SqlParameterValue.of(userTenantTenantAccesible));
    });
    return sq;
  }

