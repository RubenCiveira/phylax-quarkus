package net.civeira.phylax.features.access.securityscope.infrastructure.driven;

import java.time.OffsetDateTime;
import java.util.List;
import java.util.Optional;

import io.quarkus.cache.Cache;
import io.quarkus.cache.CacheName;
import jakarta.enterprise.context.ApplicationScoped;
import lombok.RequiredArgsConstructor;
import lombok.extern.slf4j.Slf4j;
import net.civeira.phylax.features.access.securityscope.SecurityScope;
import net.civeira.phylax.features.access.securityscope.gateway.SecurityScopeCacheGateway;
import net.civeira.phylax.features.access.securityscope.gateway.SecurityScopeCached;
import net.civeira.phylax.features.access.securityscope.query.SecurityScopeCursor;
import net.civeira.phylax.features.access.securityscope.query.SecurityScopeFilter;

@ApplicationScoped
@Slf4j
@RequiredArgsConstructor
public class SecurityScopeCacheGatewayAdapter implements SecurityScopeCacheGateway {

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   */
  @CacheName("security-scope")
  private final Cache cache;

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   */
  @Override
  public void evict() {
    log.trace("Clearing cache after big operation");
    invalidate();
  }

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   * @param securityScope
   */
  @Override
  public void remove(final SecurityScope securityScope) {
    log.trace("Invalidating securityScope cache due to the removal of {}", securityScope);
    invalidate();
  }

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   * @param filter
   * @param cursor
   * @return
   */
  public Optional<SecurityScopeCached> retrieve(final SecurityScopeFilter filter,
      final SecurityScopeCursor cursor) {
    String key = key(filter, cursor);
    log.trace("Lookup at securityScope cache for the key {}", key);
    return cache.<String, SecurityScopeCached>get(key, k -> null)
        .map(cached -> Optional.ofNullable(cached)).await().indefinitely();
  }

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   * @param filter
   * @param cursor
   * @param securityScopes
   * @return
   */
  public SecurityScopeCached store(final SecurityScopeFilter filter,
      final SecurityScopeCursor cursor, final List<SecurityScope> securityScopes) {
    String key = key(filter, cursor);
    cache.invalidate(key).await().indefinitely();
    return cache.<String, SecurityScopeCached>get(key, k -> SecurityScopeCached.builder()
        .since(OffsetDateTime.now()).value(securityScopes).build()).await().indefinitely();
  }

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   * @param securityScope
   */
  @Override
  public void update(final SecurityScope securityScope) {
    log.trace("Invalidating securityScope cache due to the update of {}", securityScope);
    invalidate();
  }

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   */
  private void invalidate() {
    cache.invalidateAll().await().indefinitely();
  }

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   * @param filter
   * @param cursor
   * @return
   */
  private String key(final SecurityScopeFilter filter, final SecurityScopeCursor cursor) {
    return filter.toString() + "-" + cursor.toString();
  }
}
