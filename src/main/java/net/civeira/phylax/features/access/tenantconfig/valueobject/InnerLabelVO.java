package net.civeira.phylax.features.access.tenantconfig.valueobject;

import java.util.Optional;

import lombok.EqualsAndHashCode;
import lombok.Getter;
import lombok.RequiredArgsConstructor;
import lombok.With;
import net.civeira.phylax.common.exception.ConstraintException;
import net.civeira.phylax.common.value.validation.AbstractFailList;
import net.civeira.phylax.common.value.validation.ConstraintFail;
import net.civeira.phylax.common.value.validation.ConstraintFailList;

/**
 * Value Object representing a customizable label that tenants can use to personalize the
 * application UI. of tenantConfig.
 * <p>
 * This class ensures that the inner label is a {@code String} and encapsulates its validation and
 * formatting logic. It is immutable and compliant with DDD (Domain-Driven Design) principles.
 * </p>
 * <p>
 * Use {@link #from(String)} or {@link #tryFrom(Object)} to safely create instances. Validation
 * errors are reported via {@link ConstraintFailList} or thrown as {@link ConstraintException}.
 * </p>
 */
@Getter
@RequiredArgsConstructor
@With
@EqualsAndHashCode(onlyExplicitlyIncluded = true)
public class InnerLabelVO {

  /**
   * An empty instance
   *
   * @autogenerated ValueObjectGenerator
   * @param innerLabel
   * @return An empty instance
   */
  public static InnerLabelVO from(final String innerLabel) {
    return tryFrom(innerLabel);
  }

  /**
   * An empty instance
   *
   * @autogenerated ValueObjectGenerator
   * @return An empty instance
   */
  public static InnerLabelVO nullValue() {
    return new InnerLabelVO(null);
  }

  /**
   * An empty instance
   *
   * @autogenerated ValueObjectGenerator
   * @param innerLabel
   * @return An empty instance
   */
  public static InnerLabelVO tryFrom(final Object innerLabel) {
    ConstraintFailList list = new ConstraintFailList();
    InnerLabelVO result = tryFrom(innerLabel, list);
    if (list.hasErrors()) {
      throw new ConstraintException(list);
    }
    return result;
  }

  /**
   * An empty instance
   *
   * @autogenerated ValueObjectGenerator
   * @param innerLabel temptative value
   * @param fails Error list
   * @return An empty instance
   */
  public static <T extends AbstractFailList> InnerLabelVO tryFrom(final Object innerLabel,
      final T fails) {
    if (null == innerLabel) {
      return new InnerLabelVO(null);
    } else if (innerLabel instanceof String castedInnerLabel) {
      return new InnerLabelVO(castedInnerLabel);
    } else {
      fails.add(new ConstraintFail("wrong-type", "innerLabel", innerLabel.getClass(),
          "A String type is expected for innerLabel"));
      return null;
    }
  }

  /**
   * The actual value of the name.
   *
   * @autogenerated ValueObjectGenerator
   */
  private final String value;

  /**
   * @autogenerated ValueObjectGenerator
   * @return
   */
  public Optional<String> getValue() {
    return Optional.ofNullable(value);
  }

  /**
   * the string value wrapped in square brackets.
   *
   * @autogenerated ValueObjectGenerator
   * @return the string value wrapped in square brackets.
   */
  @Override
  public String toString() {
    return "[" + value + "]";
  }
}
