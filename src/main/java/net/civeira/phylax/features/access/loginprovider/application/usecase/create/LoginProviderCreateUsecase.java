package net.civeira.phylax.features.access.loginprovider.application.usecase.create;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.loginprovider.application.visibility.LoginProvidersVisibility;
import net.civeira.phylax.features.access.loginprovider.domain.LoginProvider;
import net.civeira.phylax.features.access.loginprovider.domain.LoginProviderChangeSet;
import net.civeira.phylax.features.access.loginprovider.domain.gateway.LoginProviderCacheGateway;
import net.civeira.phylax.features.access.loginprovider.domain.gateway.LoginProviderWriteRepositoryGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class LoginProviderCreateUsecase {

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final LoginProviderCacheGateway cache;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Event<LoginProviderCreateAllowDecision> createAllowEmitter;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Event<LoginProviderCreateCheck> createCheckEmitter;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Event<LoginProviderCreateEnrich> createEnrichEmitter;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final LoginProviderWriteRepositoryGateway gateway;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final LoginProvidersVisibility visibility;

  /**
   * @autogenerated CreateUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    LoginProviderCreateAllowDecision proposal = LoginProviderCreateAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allow by default").build()).query(query)
        .build();
    createAllowEmitter.fire(proposal);
    return proposal.getDetail();
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated CreateUsecaseGenerator
   * @param query
   * @param input
   * @return The slide with some values
   */
  public LoginProviderCreateProjection create(final Interaction query,
      final LoginProviderCreateInput input) {
    Allow detail = allow(query);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    LoginProviderCreateInput filled = LoginProviderCreateInput
        .fromChangeSet(visibility.copyWithFixed(query, input.toChangeSet()));
    LoginProviderCreateCheck check =
        LoginProviderCreateCheck.builder().interaction(query).input(filled).build();
    createCheckEmitter.fire(check);
    LoginProviderCreateEnrich proposal =
        LoginProviderCreateEnrich.builder().interaction(query).input(filled).build();
    createEnrichEmitter.fire(proposal);
    LoginProviderChangeSet dto = proposal.getInput().toChangeSet();
    LoginProvider entity = LoginProvider.create(dto);
    LoginProvider created =
        gateway.create(entity, attempt -> visibility.checkVisibility(query, attempt.getUid()));
    cache.update(created);
    return LoginProviderCreateProjection.from(visibility.copyWithHidden(query, created));
  }
}
