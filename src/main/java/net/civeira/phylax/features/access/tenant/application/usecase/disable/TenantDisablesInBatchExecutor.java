package net.civeira.phylax.features.access.tenant.application.usecase.disable;

import java.util.List;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.SpanKind;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Context;
import io.quarkus.arc.Unremovable;
import jakarta.enterprise.context.ApplicationScoped;
import lombok.Data;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.stepper.ItemDescriptor;
import net.civeira.phylax.common.batch.stepper.ItemProcessor;
import net.civeira.phylax.common.batch.stepper.ItemReader;
import net.civeira.phylax.common.batch.stepper.ItemWriter;
import net.civeira.phylax.common.batch.stepper.StepContext;
import net.civeira.phylax.common.batch.stepper.StepCounter;
import net.civeira.phylax.common.batch.stepper.StepFinalizer;
import net.civeira.phylax.common.batch.stepper.StepInitializer;
import net.civeira.phylax.common.exception.ExecutionException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.features.access.tenant.application.visibility.TenantsVisibility;
import net.civeira.phylax.features.access.tenant.domain.Tenant;
import net.civeira.phylax.features.access.tenant.domain.gateway.TenantCursor;

@Unremovable
@ApplicationScoped
@RequiredArgsConstructor
class TenantDisablesInBatchExecutor implements
    StepCounter<TenantDisableAllInBatchCommand, TenantDisablesInBatchExecutor.TenantPaginableBatch>,
    StepFinalizer<TenantDisableAllInBatchCommand, TenantDisablesInBatchExecutor.TenantPaginableBatch>,
    StepInitializer<TenantDisableAllInBatchCommand, TenantDisablesInBatchExecutor.TenantPaginableBatch>,
    ItemReader<Tenant, TenantDisableAllInBatchCommand, TenantDisablesInBatchExecutor.TenantPaginableBatch>,
    ItemWriter<Tenant, TenantDisableAllInBatchCommand, TenantDisablesInBatchExecutor.TenantPaginableBatch>,
    ItemProcessor<Tenant, Tenant, TenantDisableAllInBatchCommand, TenantDisablesInBatchExecutor.TenantPaginableBatch>,
    ItemDescriptor<Tenant, TenantDisableAllInBatchCommand, TenantDisablesInBatchExecutor.TenantPaginableBatch> {

  /**
   * @autogenerated EntityGenerator
   */
  @Data
  public static class TenantPaginableBatch {

    /**
     * @autogenerated EntityGenerator
     */
    private String since;
  }

  /**
   * @autogenerated EntityGenerator
   */
  private final int size = 10;

  /**
   * @autogenerated EntityGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated EntityGenerator
   */
  private final TenantDisableUsecase usecase;

  /**
   * @autogenerated EntityGenerator
   */
  private final TenantsVisibility visibility;

  /**
   * @autogenerated EntityGenerator
   * @param context
   * @return
   */
  @Override
  public long approximatedItems(
      final StepContext<TenantDisableAllInBatchCommand, TenantPaginableBatch> context) {
    Span span = tracer
        .spanBuilder("Count selected items to the batch disable for a set of entities of tenant")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      return visibility.countVisibles(context.getParams().getInteraction(),
          context.getParams().getFilter());
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   */
  @Override
  public void finish(
      final StepContext<TenantDisableAllInBatchCommand, TenantPaginableBatch> context) {
    usecase.flush();
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   */
  @Override
  public void init(
      final StepContext<TenantDisableAllInBatchCommand, TenantPaginableBatch> context) {
    context.setState(new TenantPaginableBatch());
  }

  /**
   * @autogenerated EntityGenerator
   * @param item
   * @param context
   * @return
   */
  @Override
  public String itemDescription(final Tenant item,
      final StepContext<TenantDisableAllInBatchCommand, TenantPaginableBatch> context) {
    return item.getUid();
  }

  /**
   * @autogenerated EntityGenerator
   * @param item
   * @param context
   * @return
   */
  @Override
  public Tenant process(final Tenant item,
      final StepContext<TenantDisableAllInBatchCommand, TenantPaginableBatch> context) {
    Span span = tracer
        .spanBuilder("Prepare individual data for the batch disable on a set of entities of tenant")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      Allow allowed = usecase.checkAllow(context.getParams().getInteraction(), item);
      if (!allowed.isAllowed()) {
        span.addEvent("The policies for tenant dont allow to delete: " + allowed.getDescription());
        throw new ExecutionException("not-allowed", null);
      }
      return item;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   * @return
   */
  @Override
  public List<Tenant> read(
      final StepContext<TenantDisableAllInBatchCommand, TenantPaginableBatch> context) {
    Span span = tracer
        .spanBuilder("Read selected entitites for a batch disable for a set of entities of tenant")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      List<Tenant> page = visibility.listVisiblesForUpdate(context.getParams().getInteraction(),
          context.getParams().getFilter(),
          TenantCursor.builder().limit(size).sinceUid(context.getState().getSince()).build());
      context.getState().setSince(page.isEmpty() ? null : page.get(page.size() - 1).getUid());
      return page;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated EntityGenerator
   * @param items
   * @param context
   */
  @Override
  public void write(final List<Tenant> items,
      final StepContext<TenantDisableAllInBatchCommand, TenantPaginableBatch> context) {
    Span span = tracer.spanBuilder("Perform the batch disable for a set of entities of tenant")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      span.setAttribute("size", items.size());
      items.forEach(item -> usecase.disable(context.getParams().getInteraction(), item));
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }
}
