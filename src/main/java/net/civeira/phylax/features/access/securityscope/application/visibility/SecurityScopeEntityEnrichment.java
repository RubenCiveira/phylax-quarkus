package net.civeira.phylax.features.access.securityscope.application.visibility;

import java.util.Optional;
import java.util.function.Consumer;

import lombok.Builder;
import lombok.Data;
import lombok.EqualsAndHashCode;
import lombok.NonNull;
import lombok.With;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.securityscope.domain.SecurityScope;
import net.civeira.phylax.features.access.securityscope.domain.SecurityScopeChangeSet;

@Data
@Builder(toBuilder = true)
@With
@EqualsAndHashCode(onlyExplicitlyIncluded = true)
public class SecurityScopeEntityEnrichment {

  /**
   * @autogenerated EntityEnrichmentGenerator
   */
  @NonNull
  private final Interaction interaction;

  /**
   * @autogenerated EntityEnrichmentGenerator
   */
  @NonNull
  private SecurityScopeChangeSet dto;

  /**
   * @autogenerated EntityEnrichmentGenerator
   */
  private SecurityScope original;

  /**
   * @autogenerated EntityEnrichmentGenerator
   * @return
   */
  public Optional<SecurityScope> getOriginal() {
    return Optional.ofNullable(original);
  }

  /**
   * @autogenerated EntityEnrichmentGenerator
   * @param mapper
   */
  public void peek(Consumer<SecurityScopeChangeSet> mapper) {
    mapper.accept(dto);
  }
}
