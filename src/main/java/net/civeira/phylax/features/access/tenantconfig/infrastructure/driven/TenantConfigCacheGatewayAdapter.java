package net.civeira.phylax.features.access.tenantconfig.infrastructure.driven;

import java.time.OffsetDateTime;
import java.util.List;
import java.util.Optional;

import io.quarkus.cache.Cache;
import io.quarkus.cache.CacheName;
import jakarta.enterprise.context.ApplicationScoped;
import lombok.RequiredArgsConstructor;
import lombok.extern.slf4j.Slf4j;
import net.civeira.phylax.features.access.tenantconfig.TenantConfig;
import net.civeira.phylax.features.access.tenantconfig.gateway.TenantConfigCacheGateway;
import net.civeira.phylax.features.access.tenantconfig.gateway.TenantConfigCached;
import net.civeira.phylax.features.access.tenantconfig.query.TenantConfigCursor;
import net.civeira.phylax.features.access.tenantconfig.query.TenantConfigFilter;

@ApplicationScoped
@Slf4j
@RequiredArgsConstructor
public class TenantConfigCacheGatewayAdapter implements TenantConfigCacheGateway {

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   */
  @CacheName("tenant-config")
  private final Cache cache;

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   */
  @Override
  public void evict() {
    log.trace("Clearing cache after big operation");
    invalidate();
  }

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   * @param tenantConfig
   */
  @Override
  public void remove(final TenantConfig tenantConfig) {
    log.trace("Invalidating tenantConfig cache due to the removal of {}", tenantConfig);
    invalidate();
  }

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   * @param filter
   * @param cursor
   * @return
   */
  public Optional<TenantConfigCached> retrieve(final TenantConfigFilter filter,
      final TenantConfigCursor cursor) {
    String key = key(filter, cursor);
    log.trace("Lookup at tenantConfig cache for the key {}", key);
    return cache.<String, TenantConfigCached>get(key, k -> null)
        .map(cached -> Optional.ofNullable(cached)).await().indefinitely();
  }

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   * @param filter
   * @param cursor
   * @param tenantConfigs
   * @return
   */
  public TenantConfigCached store(final TenantConfigFilter filter, final TenantConfigCursor cursor,
      final List<TenantConfig> tenantConfigs) {
    String key = key(filter, cursor);
    cache.invalidate(key).await().indefinitely();
    return cache.<String, TenantConfigCached>get(key,
        k -> TenantConfigCached.builder().since(OffsetDateTime.now()).value(tenantConfigs).build())
        .await().indefinitely();
  }

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   * @param tenantConfig
   */
  @Override
  public void update(final TenantConfig tenantConfig) {
    log.trace("Invalidating tenantConfig cache due to the update of {}", tenantConfig);
    invalidate();
  }

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   */
  private void invalidate() {
    cache.invalidateAll().await().indefinitely();
  }

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   * @param filter
   * @param cursor
   * @return
   */
  private String key(final TenantConfigFilter filter, final TenantConfigCursor cursor) {
    return filter.toString() + "-" + cursor.toString();
  }
}
