package net.civeira.phylax.features.access.tenantloginprovider.infrastructure.driven;

import java.time.OffsetDateTime;
import java.util.List;
import java.util.Optional;

import io.quarkus.cache.Cache;
import io.quarkus.cache.CacheName;
import jakarta.enterprise.context.ApplicationScoped;
import lombok.RequiredArgsConstructor;
import lombok.extern.slf4j.Slf4j;
import net.civeira.phylax.features.access.tenantloginprovider.domain.TenantLoginProvider;
import net.civeira.phylax.features.access.tenantloginprovider.domain.gateway.TenantLoginProviderCacheGateway;
import net.civeira.phylax.features.access.tenantloginprovider.domain.gateway.TenantLoginProviderCached;
import net.civeira.phylax.features.access.tenantloginprovider.domain.gateway.TenantLoginProviderCursor;
import net.civeira.phylax.features.access.tenantloginprovider.domain.gateway.TenantLoginProviderFilter;

@ApplicationScoped
@Slf4j
@RequiredArgsConstructor
public class TenantLoginProviderCacheGatewayAdapter implements TenantLoginProviderCacheGateway {

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   */
  @CacheName("tenant-login-provider")
  private final Cache cache;

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   */
  @Override
  public void evict() {
    log.trace("Clearing cache after big operation");
    invalidate();
  }

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   * @param tenantLoginProvider
   */
  @Override
  public void remove(final TenantLoginProvider tenantLoginProvider) {
    log.trace("Invalidating tenantLoginProvider cache due to the removal of {}",
        tenantLoginProvider);
    invalidate();
  }

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   * @param filter
   * @param cursor
   * @return
   */
  public Optional<TenantLoginProviderCached> retrieve(final TenantLoginProviderFilter filter,
      final TenantLoginProviderCursor cursor) {
    String key = key(filter, cursor);
    log.trace("Lookup at tenantLoginProvider cache for the key {}", key);
    return cache.<String, TenantLoginProviderCached>get(key, k -> null)
        .map(cached -> Optional.ofNullable(cached)).await().indefinitely();
  }

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   * @param filter
   * @param cursor
   * @param tenantLoginProviders
   * @return
   */
  public TenantLoginProviderCached store(final TenantLoginProviderFilter filter,
      final TenantLoginProviderCursor cursor,
      final List<TenantLoginProvider> tenantLoginProviders) {
    String key = key(filter, cursor);
    cache.invalidate(key).await().indefinitely();
    return cache
        .<String, TenantLoginProviderCached>get(key, k -> TenantLoginProviderCached.builder()
            .since(OffsetDateTime.now()).value(tenantLoginProviders).build())
        .await().indefinitely();
  }

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   * @param tenantLoginProvider
   */
  @Override
  public void update(final TenantLoginProvider tenantLoginProvider) {
    log.trace("Invalidating tenantLoginProvider cache due to the update of {}",
        tenantLoginProvider);
    invalidate();
  }

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   */
  private void invalidate() {
    cache.invalidateAll().await().indefinitely();
  }

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   * @param filter
   * @param cursor
   * @return
   */
  private String key(final TenantLoginProviderFilter filter,
      final TenantLoginProviderCursor cursor) {
    return filter.toString() + "-" + cursor.toString();
  }
}
