package net.civeira.phylax.features.access.apikeyclient.application.usecase.update;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.common.telemetry.Observed;
import net.civeira.phylax.common.telemetry.Trace;
import net.civeira.phylax.features.access.apikeyclient.application.visibility.ApiKeyClientsVisibility;
import net.civeira.phylax.features.access.apikeyclient.domain.ApiKeyClient;
import net.civeira.phylax.features.access.apikeyclient.domain.ApiKeyClientChangeSet;
import net.civeira.phylax.features.access.apikeyclient.domain.ApiKeyClientRef;
import net.civeira.phylax.features.access.apikeyclient.domain.gateway.ApiKeyClientAuditGateway;
import net.civeira.phylax.features.access.apikeyclient.domain.gateway.ApiKeyClientCacheGateway;
import net.civeira.phylax.features.access.apikeyclient.domain.gateway.ApiKeyClientWriteRepositoryGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class ApiKeyClientUpdateUsecase {

  /**
   * Service to register audit events
   *
   * @autogenerated UpdateUsecaseGenerator
   */
  private final ApiKeyClientAuditGateway audit;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final ApiKeyClientCacheGateway cache;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final ApiKeyClientWriteRepositoryGateway gateway;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<ApiKeyClientUpdateAllowDecision> updateAllowEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<ApiKeyClientUpdateCheck> updateCheckEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<ApiKeyClientUpdateEnrich> updateEnrichEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final ApiKeyClientsVisibility visibility;

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  @Observed("Checking if is allowed to update an entity of api key client")
  public Allow allow(final Interaction query, final ApiKeyClientRef reference) {
    Trace.setAttribute("ref", reference.getUid());
    Allow response = checkAllow(query, reference);
    Trace.setAttribute("allowed", response.isAllowed());
    return response;
  }

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @return
   */
  @Observed("Checking if is allowed to update an entity of api key client")
  public Allow allow(final Interaction query) {
    Allow response = checkAllow(query);
    Trace.setAttribute("allowed", response.isAllowed());
    return response;
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @param reference
   * @param input
   * @return The slide with some values
   */
  @Observed("Updating an entity of api key client")
  public ApiKeyClientUpdateProjection update(final Interaction query,
      final ApiKeyClientRef reference, final ApiKeyClientUpdateInput input) {
    Trace.setAttribute("ref", reference.getUid());
    Allow detail = checkAllow(query, reference);
    if (!detail.isAllowed()) {
      Trace.addEvent(
          "The policies for api key client dont allow to update: " + detail.getDescription());
      throw new NotAllowedException(detail.getDescription());
    }
    ApiKeyClient original = visibility.retrieveVisibleForUpdate(query, reference.getUid())
        .orElseThrow(() -> new NotFoundException(""));
    ApiKeyClientUpdateInput filled =
        ApiKeyClientUpdateInput.fromChangeSet(visibility.copyWithFixed(query, input.toChangeSet()));
    Trace.addEvent(
        "The input value is filled with the fixed values for the creation of api key client");
    ApiKeyClientUpdateCheck check = ApiKeyClientUpdateCheck.builder().input(filled)
        .interaction(query).reference(reference).build();
    updateCheckEmitter.fire(check);
    Trace.addEvent("The input value is checked with the policies for the update of api key client");
    ApiKeyClientUpdateEnrich proposal = ApiKeyClientUpdateEnrich.builder().input(filled)
        .interaction(query).reference(reference).build();
    updateEnrichEmitter.fire(proposal);
    ApiKeyClientChangeSet dto = proposal.getInput().toChangeSet();
    Trace.addEvent("The update of entity for api key client is ready");
    ApiKeyClient saved = gateway.update(original, original.update(dto));
    Trace.addEvent("The updated entity of api key client was saved");
    cache.update(saved);
    // Register audit operation
    audit.updated("update", saved, original, query);
    return ApiKeyClientUpdateProjection.from(visibility.copyWithHidden(query, saved));
  }

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  private Allow checkAllow(final Interaction query, final ApiKeyClientRef reference) {
    ApiKeyClientUpdateAllowDecision base = ApiKeyClientUpdateAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).reference(reference).build();
    updateAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @return
   */
  private Allow checkAllow(final Interaction query) {
    ApiKeyClientUpdateAllowDecision base = ApiKeyClientUpdateAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).build();
    updateAllowEmitter.fire(base);
    return base.getDetail();
  }
}
