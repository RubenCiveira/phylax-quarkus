package net.civeira.phylax.features.access.securityscope.application.usecase.update;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.securityscope.SecurityScope;
import net.civeira.phylax.features.access.securityscope.SecurityScopeFacade;
import net.civeira.phylax.features.access.securityscope.SecurityScopeRef;
import net.civeira.phylax.features.access.securityscope.application.projection.SecurityScopeStateProyection;
import net.civeira.phylax.features.access.securityscope.application.request.SecurityScopeStateChange;
import net.civeira.phylax.features.access.securityscope.application.service.visibility.SecurityScopesVisibility;
import net.civeira.phylax.features.access.securityscope.gateway.SecurityScopeCacheGateway;
import net.civeira.phylax.features.access.securityscope.gateway.SecurityScopeWriteRepositoryGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class SecurityScopeUpdateUsecase {

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final SecurityScopeCacheGateway cache;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final SecurityScopeFacade facade;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final SecurityScopeWriteRepositoryGateway gateway;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<SecurityScopeAllowUpdateProposal> updateAllowEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<SecurityScopeUpdateProposal> updateProposalEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<SecurityScopeUpdateEvent> updatedEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final SecurityScopesVisibility visibility;

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  public Allow allow(final Interaction query, final SecurityScopeRef reference) {
    SecurityScopeAllowUpdateProposal base = SecurityScopeAllowUpdateProposal.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).reference(reference).build();
    updateAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    SecurityScopeAllowUpdateProposal base = SecurityScopeAllowUpdateProposal.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).build();
    updateAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * The slide with some values
   *
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @param reference
   * @param input
   * @return The slide with some values
   */
  public SecurityScopeStateProyection update(final Interaction query,
      final SecurityScopeRef reference, final SecurityScopeStateChange input) {
    Allow detail = allow(query, reference);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    SecurityScope original = visibility.retrieveVisibleForUpdate(query, reference.getUidValue())
        .orElseThrow(() -> new NotFoundException(""));
    SecurityScopeStateChange filled = visibility.copyWithFixed(query, input);
    SecurityScopeUpdateProposal proposal = SecurityScopeUpdateProposal.builder().dto(filled)
        .interaction(query).reference(reference).build();
    updateProposalEmitter.fire(proposal);
    SecurityScopeStateChange dto = proposal.getDto();;
    SecurityScope saved = gateway.update(original, facade.update(original, dto));
    cache.update(saved);
    SecurityScopeUpdateEvent event =
        SecurityScopeUpdateEvent.builder().payload(saved).interaction(query).build();
    updatedEmitter.fire(event);
    return visibility.copyWithHidden(query, event.getPayload());
  }
}
