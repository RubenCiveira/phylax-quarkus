package net.civeira.phylax.features.access.tenantloginprovider.application.usecase.update;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.common.telemetry.Observed;
import net.civeira.phylax.common.telemetry.Trace;
import net.civeira.phylax.features.access.tenantloginprovider.application.visibility.TenantLoginProvidersVisibility;
import net.civeira.phylax.features.access.tenantloginprovider.domain.TenantLoginProvider;
import net.civeira.phylax.features.access.tenantloginprovider.domain.TenantLoginProviderChangeSet;
import net.civeira.phylax.features.access.tenantloginprovider.domain.TenantLoginProviderRef;
import net.civeira.phylax.features.access.tenantloginprovider.domain.gateway.TenantLoginProviderAuditGateway;
import net.civeira.phylax.features.access.tenantloginprovider.domain.gateway.TenantLoginProviderCacheGateway;
import net.civeira.phylax.features.access.tenantloginprovider.domain.gateway.TenantLoginProviderWriteRepositoryGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class TenantLoginProviderUpdateUsecase {

  /**
   * Service to register audit events
   *
   * @autogenerated UpdateUsecaseGenerator
   */
  private final TenantLoginProviderAuditGateway audit;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final TenantLoginProviderCacheGateway cache;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final TenantLoginProviderWriteRepositoryGateway gateway;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<TenantLoginProviderUpdateAllowDecision> updateAllowEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<TenantLoginProviderUpdateCheck> updateCheckEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<TenantLoginProviderUpdateEnrich> updateEnrichEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final TenantLoginProvidersVisibility visibility;

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  @Observed("Checking if is allowed to update an entity of tenant login provider")
  public Allow allow(final Interaction query, final TenantLoginProviderRef reference) {
    Trace.setAttribute("ref", reference.getUid());
    Allow response = checkAllow(query, reference);
    Trace.setAttribute("allowed", response.isAllowed());
    return response;
  }

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @return
   */
  @Observed("Checking if is allowed to update an entity of tenant login provider")
  public Allow allow(final Interaction query) {
    Allow response = checkAllow(query);
    Trace.setAttribute("allowed", response.isAllowed());
    return response;
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @param reference
   * @param input
   * @return The slide with some values
   */
  @Observed("Updating an entity of tenant login provider")
  public TenantLoginProviderUpdateProjection update(final Interaction query,
      final TenantLoginProviderRef reference, final TenantLoginProviderUpdateInput input) {
    Trace.setAttribute("ref", reference.getUid());
    Allow detail = checkAllow(query, reference);
    if (!detail.isAllowed()) {
      Trace.addEvent("The policies for tenant login provider dont allow to update: "
          + detail.getDescription());
      throw new NotAllowedException(detail.getDescription());
    }
    TenantLoginProvider original = visibility.retrieveVisibleForUpdate(query, reference.getUid())
        .orElseThrow(() -> new NotFoundException(""));
    TenantLoginProviderUpdateInput filled = TenantLoginProviderUpdateInput
        .fromChangeSet(visibility.copyWithFixed(query, input.toChangeSet()));
    Trace.addEvent(
        "The input value is filled with the fixed values for the creation of tenant login provider");
    TenantLoginProviderUpdateCheck check = TenantLoginProviderUpdateCheck.builder().input(filled)
        .interaction(query).reference(reference).build();
    updateCheckEmitter.fire(check);
    Trace.addEvent(
        "The input value is checked with the policies for the update of tenant login provider");
    TenantLoginProviderUpdateEnrich proposal = TenantLoginProviderUpdateEnrich.builder()
        .input(filled).interaction(query).reference(reference).build();
    updateEnrichEmitter.fire(proposal);
    TenantLoginProviderChangeSet dto = proposal.getInput().toChangeSet();
    Trace.addEvent("The update of entity for tenant login provider is ready");
    TenantLoginProvider saved = gateway.update(original, original.update(dto));
    Trace.addEvent("The updated entity of tenant login provider was saved");
    cache.update(saved);
    // Register audit operation
    audit.updated("update", saved, original, query);
    return TenantLoginProviderUpdateProjection.from(visibility.copyWithHidden(query, saved));
  }

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  private Allow checkAllow(final Interaction query, final TenantLoginProviderRef reference) {
    TenantLoginProviderUpdateAllowDecision base = TenantLoginProviderUpdateAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).reference(reference).build();
    updateAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @return
   */
  private Allow checkAllow(final Interaction query) {
    TenantLoginProviderUpdateAllowDecision base = TenantLoginProviderUpdateAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).build();
    updateAllowEmitter.fire(base);
    return base.getDetail();
  }
}
