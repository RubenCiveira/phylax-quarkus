package net.civeira.phylax.features.access.relyingparty.transition.update;

import java.util.ArrayList;
import java.util.List;
import java.util.function.Function;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.inject.Instance;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.features.access.relyingparty.RelyingParty;
import net.civeira.phylax.features.access.relyingparty.command.RelyingPartyWriteAttributes;
import net.civeira.phylax.features.access.relyingparty.transition.RelyingPartyChangeProposal;

@ApplicationScoped
@RequiredArgsConstructor
public class RelyingPartyUpdatePipeline {

  /**
   * @autogenerated UpdateEntityRuleGenerator
   */
  private final Instance<RelyingPartyUpdateStep> steps;

  /**
   * @autogenerated UpdateEntityRuleGenerator
   * @param attributes
   * @param original
   * @param last
   * @return
   */
  public RelyingParty update(final RelyingPartyWriteAttributes attributes,
      final RelyingParty original, final Function<RelyingPartyChangeProposal, RelyingParty> last) {
    Function<RelyingPartyUpdateTransition, RelyingParty> finalHandler = t -> {
      if (t.hasErrors()) {
        throw t.asConstraintException();
      }
      return last.apply(t.getData());
    };
    List<RelyingPartyUpdateStep> reversed = new ArrayList<>(steps.stream().toList()).reversed();
    Function<RelyingPartyUpdateTransition, RelyingParty> pipeline = reversed.stream().reduce(
        finalHandler, (next, step) -> transition -> step.update(transition, next), (f1, f2) -> f1);
    return pipeline.apply(RelyingPartyUpdateTransition.builder()
        .data(new RelyingPartyChangeProposal(attributes)).original(original).build());
  }
}
