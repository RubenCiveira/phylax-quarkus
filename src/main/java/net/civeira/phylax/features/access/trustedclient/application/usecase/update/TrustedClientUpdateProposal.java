package net.civeira.phylax.features.access.trustedclient.application.usecase.update;

import java.util.Optional;
import java.util.function.Consumer;
import java.util.function.UnaryOperator;

import lombok.Builder;
import lombok.Data;
import lombok.EqualsAndHashCode;
import lombok.NonNull;
import lombok.With;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.trustedclient.TrustedClientRef;
import net.civeira.phylax.features.access.trustedclient.application.request.TrustedClientStateChange;

@Data
@Builder(toBuilder = true)
@With
@EqualsAndHashCode(onlyExplicitlyIncluded = true)
public class TrustedClientUpdateProposal {

  /**
   * @autogenerated UpdateProposalGenerator
   */
  @NonNull
  private final Interaction interaction;

  /**
   * @autogenerated UpdateProposalGenerator
   */
  private final TrustedClientRef reference;

  /**
   * @autogenerated UpdateProposalGenerator
   */
  @NonNull
  private TrustedClientStateChange dto;

  /**
   * @autogenerated UpdateProposalGenerator
   * @return
   */
  public Optional<TrustedClientRef> getReference() {
    return Optional.ofNullable(reference);
  }

  /**
   * @autogenerated UpdateProposalGenerator
   * @param mapper
   */
  public void map(UnaryOperator<TrustedClientStateChange> mapper) {
    dto = mapper.apply(dto);
  }

  /**
   * @autogenerated UpdateProposalGenerator
   * @param mapper
   */
  public void peek(Consumer<TrustedClientStateChange> mapper) {
    mapper.accept(dto);
  }
}
