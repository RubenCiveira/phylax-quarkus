package net.civeira.phylax.features.access.tenantconfig.infrastructure.driven;

import java.util.List;
import java.util.Optional;
import java.util.function.Predicate;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.SpanKind;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Context;
import jakarta.enterprise.context.ApplicationScoped;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.algorithms.Slider;
import net.civeira.phylax.features.access.tenantconfig.domain.TenantConfig;
import net.civeira.phylax.features.access.tenantconfig.domain.TenantConfigRef;
import net.civeira.phylax.features.access.tenantconfig.domain.gateway.TenantConfigCursor;
import net.civeira.phylax.features.access.tenantconfig.domain.gateway.TenantConfigFilter;
import net.civeira.phylax.features.access.tenantconfig.domain.gateway.TenantConfigWriteRepositoryGateway;
import net.civeira.phylax.features.access.tenantconfig.infrastructure.event.TenantConfigEventDispatcher;
import net.civeira.phylax.features.access.tenantconfig.infrastructure.repository.TenantConfigRepository;

@ApplicationScoped
@RequiredArgsConstructor
public class TenantConfigWriteGatewayAdapter implements TenantConfigWriteRepositoryGateway {

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   */
  private final TenantConfigEventDispatcher eventDispatcher;

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   */
  private final TenantConfigRepository repository;

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param filter
   * @return
   */
  @Override
  public long countForUpdate(TenantConfigFilter filter) {
    Span span = tracer
        .spanBuilder("Count on the repository for update the matches entities of tenant config")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.count(filter);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param entity
   * @return
   */
  @Override
  public TenantConfig create(TenantConfig entity) {
    Span span = tracer.spanBuilder("Create on the repository an entity of tenant config")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      TenantConfig result = repository.create(entity);
      eventDispatcher.dispatch(entity);
      return result;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param entity
   * @param verifier
   * @return
   */
  @Override
  public TenantConfig create(TenantConfig entity, Predicate<TenantConfig> verifier) {
    Span span = tracer
        .spanBuilder(
            "Create on the repository an entity of tenant config and verify its visibility")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      TenantConfig result = repository.create(entity, verifier);
      eventDispatcher.dispatch(entity);
      return result;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param entity
   */
  @Override
  public void delete(TenantConfig entity) {
    Span span = tracer
        .spanBuilder("Delete from the repository a slide for entities of tenant config")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      repository.delete(entity);
      eventDispatcher.dispatch(entity);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param uid
   * @param filter
   * @return
   */
  @Override
  public boolean existsForUpdate(String uid, Optional<TenantConfigFilter> filter) {
    Span span = tracer
        .spanBuilder("Check if exists on the repository for update a entity of tenant config")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.existsForUpdate(uid, filter);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param filter
   * @return
   */
  @Override
  public Optional<TenantConfig> findForUpdate(TenantConfigFilter filter) {
    Span span = tracer.spanBuilder("Find on the repository for update one entity of tenant config")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.findForUpdate(filter);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param filter
   * @return
   */
  @Override
  public List<TenantConfig> listForUpdate(TenantConfigFilter filter) {
    Span span = tracer
        .spanBuilder("Retrieve from repository for update a list for entities of tenant config")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.listForUpdate(filter);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param reference
   * @return
   */
  @Override
  public TenantConfig resolveForUpdate(TenantConfigRef reference) {
    Span span = tracer
        .spanBuilder("Enrich from the repository for update a entity of tenant config")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.resolveForUpdate(reference);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param uid
   * @param filter
   * @return
   */
  @Override
  public Optional<TenantConfig> retrieveForUpdate(String uid, Optional<TenantConfigFilter> filter) {
    Span span = tracer.spanBuilder("Retrieve from repository a slide for entities of tenant config")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.retrieveForUpdate(uid, filter);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param filter
   * @param cursor
   * @return
   */
  @Override
  public Slider<TenantConfig> slideForUpdate(TenantConfigFilter filter, TenantConfigCursor cursor) {
    Span span = tracer
        .spanBuilder("Retrieve from repository for update a slide for entities of tenant config")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.slideForUpdate(filter, cursor);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param reference
   * @param entity
   * @return
   */
  @Override
  public TenantConfig update(TenantConfigRef reference, TenantConfig entity) {
    TenantConfig result = repository.update(entity);
    eventDispatcher.dispatch(entity);
    return result;
  }
}
