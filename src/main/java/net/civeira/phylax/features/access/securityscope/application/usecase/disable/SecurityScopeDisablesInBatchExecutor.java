package net.civeira.phylax.features.access.securityscope.application.usecase.disable;

import java.util.List;

import io.quarkus.arc.Unremovable;
import jakarta.enterprise.context.ApplicationScoped;
import lombok.Data;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.stepper.ItemDescriptor;
import net.civeira.phylax.common.batch.stepper.ItemProcessor;
import net.civeira.phylax.common.batch.stepper.ItemReader;
import net.civeira.phylax.common.batch.stepper.ItemWriter;
import net.civeira.phylax.common.batch.stepper.StepContext;
import net.civeira.phylax.common.batch.stepper.StepCounter;
import net.civeira.phylax.common.batch.stepper.StepFinalizer;
import net.civeira.phylax.common.batch.stepper.StepInitializer;
import net.civeira.phylax.common.exception.ExecutionException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.features.access.securityscope.application.visibility.SecurityScopesVisibility;
import net.civeira.phylax.features.access.securityscope.domain.SecurityScope;
import net.civeira.phylax.features.access.securityscope.domain.gateway.SecurityScopeCursor;

@Unremovable
@ApplicationScoped
@RequiredArgsConstructor
class SecurityScopeDisablesInBatchExecutor implements
    StepCounter<SecurityScopeDisableAllInBatchCommand, SecurityScopeDisablesInBatchExecutor.SecurityScopePaginableBatch>,
    StepFinalizer<SecurityScopeDisableAllInBatchCommand, SecurityScopeDisablesInBatchExecutor.SecurityScopePaginableBatch>,
    StepInitializer<SecurityScopeDisableAllInBatchCommand, SecurityScopeDisablesInBatchExecutor.SecurityScopePaginableBatch>,
    ItemReader<SecurityScope, SecurityScopeDisableAllInBatchCommand, SecurityScopeDisablesInBatchExecutor.SecurityScopePaginableBatch>,
    ItemWriter<SecurityScope, SecurityScopeDisableAllInBatchCommand, SecurityScopeDisablesInBatchExecutor.SecurityScopePaginableBatch>,
    ItemProcessor<SecurityScope, SecurityScope, SecurityScopeDisableAllInBatchCommand, SecurityScopeDisablesInBatchExecutor.SecurityScopePaginableBatch>,
    ItemDescriptor<SecurityScope, SecurityScopeDisableAllInBatchCommand, SecurityScopeDisablesInBatchExecutor.SecurityScopePaginableBatch> {

  /**
   * @autogenerated EntityGenerator
   */
  @Data
  public static class SecurityScopePaginableBatch {

    /**
     * @autogenerated EntityGenerator
     */
    private String since;
  }

  /**
   * @autogenerated EntityGenerator
   */
  private final int size = 10;

  /**
   * @autogenerated EntityGenerator
   */
  private final SecurityScopeDisableUsecase usecase;

  /**
   * @autogenerated EntityGenerator
   */
  private final SecurityScopesVisibility visibility;

  /**
   * @autogenerated EntityGenerator
   * @param context
   * @return
   */
  @Override
  public long approximatedItems(
      final StepContext<SecurityScopeDisableAllInBatchCommand, SecurityScopePaginableBatch> context) {
    return visibility.countVisibles(context.getParams().getInteraction(),
        context.getParams().getFilter());
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   */
  @Override
  public void finish(
      final StepContext<SecurityScopeDisableAllInBatchCommand, SecurityScopePaginableBatch> context) {
    usecase.flush();
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   */
  @Override
  public void init(
      final StepContext<SecurityScopeDisableAllInBatchCommand, SecurityScopePaginableBatch> context) {
    context.setState(new SecurityScopePaginableBatch());
  }

  /**
   * @autogenerated EntityGenerator
   * @param item
   * @param context
   * @return
   */
  @Override
  public String itemDescription(final SecurityScope item,
      final StepContext<SecurityScopeDisableAllInBatchCommand, SecurityScopePaginableBatch> context) {
    return item.getUid();
  }

  /**
   * @autogenerated EntityGenerator
   * @param item
   * @param context
   * @return
   */
  @Override
  public SecurityScope process(final SecurityScope item,
      final StepContext<SecurityScopeDisableAllInBatchCommand, SecurityScopePaginableBatch> context) {
    Allow allowed = usecase.allow(context.getParams().getInteraction(), item);
    if (!allowed.isAllowed()) {
      throw new ExecutionException("not-allowed", null);
    }
    return item;
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   * @return
   */
  @Override
  public List<SecurityScope> read(
      final StepContext<SecurityScopeDisableAllInBatchCommand, SecurityScopePaginableBatch> context) {
    List<SecurityScope> page = visibility.listVisiblesForUpdate(
        context.getParams().getInteraction(), context.getParams().getFilter(),
        SecurityScopeCursor.builder().limit(size).sinceUid(context.getState().getSince()).build());
    context.getState().setSince(page.isEmpty() ? null : page.get(page.size() - 1).getUid());
    return page;
  }

  /**
   * @autogenerated EntityGenerator
   * @param items
   * @param context
   */
  @Override
  public void write(final List<SecurityScope> items,
      final StepContext<SecurityScopeDisableAllInBatchCommand, SecurityScopePaginableBatch> context) {
    items.forEach(item -> usecase.disable(context.getParams().getInteraction(), item));
  }
}
