package net.civeira.phylax.features.access.trustedclient.application.usecase.list;

import java.util.List;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.algorithms.metadata.TimestampedList;
import net.civeira.phylax.common.algorithms.metadata.WrapMetadata;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.trustedclient.application.projection.TrustedClientStateProyection;
import net.civeira.phylax.features.access.trustedclient.application.service.visibility.TrustedClientsVisibility;
import net.civeira.phylax.features.access.trustedclient.gateway.TrustedClientCached;
import net.civeira.phylax.features.access.trustedclient.query.TrustedClientCursor;
import net.civeira.phylax.features.access.trustedclient.query.TrustedClientFilter;

@ApplicationScoped
@RequiredArgsConstructor
public class TrustedClientListUsecase {

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final Event<TrustedClientAllowListProposal> listAllowEmitter;

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final TrustedClientsVisibility visibility;

  /**
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    TrustedClientAllowListProposal proposal = TrustedClientAllowListProposal.builder()
        .detail(Allow.builder().allowed(true).description("Allow by default").build()).query(query)
        .build();
    listAllowEmitter.fire(proposal);
    return proposal.getDetail();
  }

  /**
   * The slide with some values
   *
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @param filter
   * @param cursor
   * @return The slide with some values
   */
  public List<TrustedClientStateProyection> list(final Interaction query,
      final TrustedClientFilter filter, final TrustedClientCursor cursor) {
    Allow detail = allow(query);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    TrustedClientCached values = visibility.listCachedVisibles(query, filter, cursor);
    List<TrustedClientStateProyection> list =
        values.getValue().stream().map(value -> visibility.copyWithHidden(query, value)).toList();
    return new TimestampedList<>(WrapMetadata.<List<TrustedClientStateProyection>>builder()
        .data(list).since(values.getSince().toInstant()).build());
  }
}
