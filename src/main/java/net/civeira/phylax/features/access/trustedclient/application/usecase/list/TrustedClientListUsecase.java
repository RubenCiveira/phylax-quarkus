package net.civeira.phylax.features.access.trustedclient.application.usecase.list;

import java.util.List;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.algorithms.metadata.TimestampedList;
import net.civeira.phylax.common.algorithms.metadata.WrapMetadata;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.trustedclient.application.visibility.TrustedClientVisibilityFilter;
import net.civeira.phylax.features.access.trustedclient.application.visibility.TrustedClientsVisibility;
import net.civeira.phylax.features.access.trustedclient.domain.gateway.TrustedClientCached;
import net.civeira.phylax.features.access.trustedclient.domain.gateway.TrustedClientCursor;
import net.civeira.phylax.features.access.trustedclient.domain.gateway.TrustedClientOrder;

@ApplicationScoped
@RequiredArgsConstructor
public class TrustedClientListUsecase {

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final Event<TrustedClientListAllowDecision> listAllowEmitter;

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final TrustedClientsVisibility visibility;

  /**
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    TrustedClientListAllowDecision proposal = TrustedClientListAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allow by default").build()).query(query)
        .build();
    listAllowEmitter.fire(proposal);
    return proposal.getDetail();
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @param filter
   * @param cursor
   * @return The slide with some values
   */
  public List<TrustedClientListProjection> list(final Interaction query,
      final TrustedClientListFilter filter, final TrustedClientListCursor cursor) {
    Allow detail = allow(query);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    TrustedClientVisibilityFilter visibleFilter = TrustedClientVisibilityFilter.builder()
        .uid(filter.getUid().orElse(null)).uids(filter.getUids().stream().toList())
        .search(filter.getSearch().orElse(null)).code(filter.getCode().orElse(null)).build();
    TrustedClientCursor gatewayCursor = TrustedClientCursor.builder()
        .limit(cursor.getLimit().orElse(null)).sinceUid(cursor.getSinceUid().orElse(null))
        .order(cursor.getOrder().stream().map(Object::toString).map(TrustedClientOrder::valueOf)
            .toList())
        .sinceCode(cursor.getSinceCode().orElse(null)).sinceCode(cursor.getSinceCode().orElse(null))
        .build();
    TrustedClientCached values = visibility.listCachedVisibles(query, visibleFilter, gatewayCursor);
    List<TrustedClientListProjection> list =
        values.getValue().stream().map(value -> visibility.copyWithHidden(query, value))
            .map(TrustedClientListProjection::from).toList();
    return new TimestampedList<>(WrapMetadata.<List<TrustedClientListProjection>>builder()
        .data(list).since(values.getSince().toInstant()).build());
  }
}
