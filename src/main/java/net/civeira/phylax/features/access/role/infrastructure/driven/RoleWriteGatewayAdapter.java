package net.civeira.phylax.features.access.role.infrastructure.driven;

import java.util.List;
import java.util.Optional;
import java.util.function.Predicate;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.SpanKind;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Context;
import jakarta.enterprise.context.ApplicationScoped;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.algorithms.Slider;
import net.civeira.phylax.features.access.role.domain.Role;
import net.civeira.phylax.features.access.role.domain.RoleRef;
import net.civeira.phylax.features.access.role.domain.gateway.RoleCursor;
import net.civeira.phylax.features.access.role.domain.gateway.RoleFilter;
import net.civeira.phylax.features.access.role.domain.gateway.RoleWriteRepositoryGateway;
import net.civeira.phylax.features.access.role.infrastructure.event.RoleEventDispacher;
import net.civeira.phylax.features.access.role.infrastructure.repository.RoleRepository;

@ApplicationScoped
@RequiredArgsConstructor
public class RoleWriteGatewayAdapter implements RoleWriteRepositoryGateway {

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   */
  private final RoleEventDispacher eventDispacher;

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   */
  private final RoleRepository repository;

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param filter
   * @return
   */
  @Override
  public long countForUpdate(RoleFilter filter) {
    Span span = tracer
        .spanBuilder("Count on the repository for update the matches entities of role")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.count(filter);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param entity
   * @return
   */
  @Override
  public Role create(Role entity) {
    Span span = tracer.spanBuilder("Create on the repository an entity of role")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      Role result = repository.create(entity);
      eventDispacher.dispatch(entity);
      return result;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param entity
   * @param verifier
   * @return
   */
  @Override
  public Role create(Role entity, Predicate<Role> verifier) {
    Span span = tracer
        .spanBuilder("Create on the repository an entity of role and verify its visibility")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      Role result = repository.create(entity, verifier);
      eventDispacher.dispatch(entity);
      return result;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param entity
   */
  @Override
  public void delete(Role entity) {
    Span span = tracer.spanBuilder("Delete from the repository a slide for entities of role")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      repository.delete(entity);
      eventDispacher.dispatch(entity);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param uid
   * @param filter
   * @return
   */
  @Override
  public boolean existsForUpdate(String uid, Optional<RoleFilter> filter) {
    Span span = tracer.spanBuilder("Check if exists on the repository for update a entity of role")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.existsForUpdate(uid, filter);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param filter
   * @return
   */
  @Override
  public Optional<Role> findForUpdate(RoleFilter filter) {
    Span span = tracer.spanBuilder("Find on the repository for update one entity of role")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.findForUpdate(filter);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param filter
   * @return
   */
  @Override
  public List<Role> listForUpdate(RoleFilter filter) {
    Span span = tracer
        .spanBuilder("Retrieve from repository for update a list for entities of role")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.listForUpdate(filter);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param reference
   * @return
   */
  @Override
  public Role resolveForUpdate(RoleRef reference) {
    Span span = tracer.spanBuilder("Enrich from the repository for update a entity of role")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.resolveForUpdate(reference);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param uid
   * @param filter
   * @return
   */
  @Override
  public Optional<Role> retrieveForUpdate(String uid, Optional<RoleFilter> filter) {
    Span span = tracer.spanBuilder("Retrieve from repository a slide for entities of role")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.retrieveForUpdate(uid, filter);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param filter
   * @param cursor
   * @return
   */
  @Override
  public Slider<Role> slideForUpdate(RoleFilter filter, RoleCursor cursor) {
    Span span = tracer
        .spanBuilder("Retrieve from repository for update a slide for entities of role")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.slideForUpdate(filter, cursor);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param reference
   * @param entity
   * @return
   */
  @Override
  public Role update(RoleRef reference, Role entity) {
    Role result = repository.update(entity);
    eventDispacher.dispatch(entity);
    return result;
  }
}
