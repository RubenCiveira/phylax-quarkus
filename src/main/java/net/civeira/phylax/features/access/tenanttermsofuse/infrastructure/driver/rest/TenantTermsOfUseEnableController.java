package net.civeira.phylax.features.access.tenanttermsofuse.infrastructure.driver.rest;

import java.util.List;
import java.util.stream.Stream;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.SpanKind;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Context;
import jakarta.enterprise.context.ApplicationScoped;
import jakarta.transaction.Transactional;
import jakarta.ws.rs.core.Response;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.BatchIdentificator;
import net.civeira.phylax.common.batch.BatchProgress;
import net.civeira.phylax.common.infrastructure.CurrentRequest;
import net.civeira.phylax.features.access.tenant.domain.TenantReference;
import net.civeira.phylax.features.access.tenanttermsofuse.application.usecase.enable.TenantTermsOfUseEnableFilter;
import net.civeira.phylax.features.access.tenanttermsofuse.application.usecase.enable.TenantTermsOfUseEnableProjection;
import net.civeira.phylax.features.access.tenanttermsofuse.application.usecase.enable.TenantTermsOfUseEnableStatus;
import net.civeira.phylax.features.access.tenanttermsofuse.application.usecase.enable.TenantTermsOfUseEnableUsecase;
import net.civeira.phylax.features.access.tenanttermsofuse.domain.TenantTermsOfUseReference;
import net.civeira.phylax.generated.openapi.model.TenantApiRef;
import net.civeira.phylax.generated.openapi.model.TenantTermsOfUseApiDto;

@RequiredArgsConstructor
@ApplicationScoped
public class TenantTermsOfUseEnableController {

  /**
   * @autogenerated ActionControllerGenerator
   */
  private final CurrentRequest currentRequest;

  /**
   * @autogenerated ActionControllerGenerator
   */
  private final TenantTermsOfUseEnableUsecase enable;

  /**
   * @autogenerated ActionControllerGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated ActionControllerGenerator
   * @param uids
   * @param search
   * @param tenant
   * @param tenants
   * @return
   */
  public Response tenantTermsOfUseApiBatchEnable(final List<String> uids, final String search,
      final String tenant, final List<String> tenants) {
    Span span = tracer.spanBuilder("Api to enable on massive entitites of tenant terms of use")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      TenantTermsOfUseEnableFilter.TenantTermsOfUseEnableFilterBuilder filterBuilder =
          TenantTermsOfUseEnableFilter.builder();
      filterBuilder =
          filterBuilder.uids(uids.stream().flatMap(part -> Stream.of(part.split(","))).toList());
      filterBuilder = filterBuilder.search(search);
      filterBuilder = filterBuilder.uids(
          null == uids ? null : uids.stream().flatMap(part -> Stream.of(part.split(","))).toList());
      filterBuilder = filterBuilder.search(search);
      if (null != tenant) {
        filterBuilder = filterBuilder.tenant(TenantReference.of(tenant));
      }
      filterBuilder = filterBuilder.tenants(null == tenants ? null
          : tenants.stream().flatMap(part -> Stream.of(part.split(","))).toList());
      TenantTermsOfUseEnableFilter filter = filterBuilder.build();
      BatchIdentificator task = enable.enable(currentRequest.interaction(), filter);
      /* .header("Last-Modified", value.getSince().format(DateTimeFormatter.RFC_1123_DATE_TIME)) */
      return Response.ok(task).build();
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated ActionControllerGenerator
   * @param batchId
   * @return
   */
  public Response tenantTermsOfUseApiBatchEnableQuery(final String batchId) {
    Span span = tracer.spanBuilder("Api to check masive enable on entity of tenant terms of use")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      BatchProgress task = enable.checkProgress(TenantTermsOfUseEnableStatus.builder()
          .taskId(batchId).build(currentRequest.interaction()));
      return Response.ok(task).build();
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated ActionControllerGenerator
   * @param uid
   * @return
   */
  @Transactional
  public Response tenantTermsOfUseApiEnable(final String uid) {
    Span span = tracer.spanBuilder("Api to enable an entity of tenant terms of use")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      span.setAttribute("ref", uid);
      TenantTermsOfUseEnableProjection updated =
          enable.enable(currentRequest.interaction(), TenantTermsOfUseReference.of(uid));
      return Response.ok(toApiModel(updated)).build();
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated ActionControllerGenerator
   * @param dto
   * @return
   */
  private TenantTermsOfUseApiDto toApiModel(TenantTermsOfUseEnableProjection dto) {
    Span span = tracer
        .spanBuilder(
            "Mapper to build api content to response from model for entity of tenant terms of use")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      TenantTermsOfUseApiDto tenantTermsOfUseApiDto = new TenantTermsOfUseApiDto();
      tenantTermsOfUseApiDto.setUid(dto.getUid());
      tenantTermsOfUseApiDto.setTenant(new TenantApiRef().$ref(dto.getTenantReference()));
      tenantTermsOfUseApiDto.setText(dto.getText());
      tenantTermsOfUseApiDto.setEnabled(dto.getEnabled());
      String attached = dto.getAttached();
      if (null != attached) {
        tenantTermsOfUseApiDto.setAttached(currentRequest.getPublicHost()
            + "/api/access/tenants-terms-of-use/" + dto.getUidOrDefault("-") + "/attached");
      }
      tenantTermsOfUseApiDto.setActivationDate(dto.getActivationDate());
      tenantTermsOfUseApiDto.setVersion(dto.getVersion());
      return tenantTermsOfUseApiDto;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }
}
