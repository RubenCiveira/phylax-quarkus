import net.civeira.phylax.features.access.tenant.domain.TenantReference;
import net.civeira.phylax.features.access.tenanttermsofuse.application.usecase.enable.TenantTermsOfUseEnableFilter;
import net.civeira.phylax.features.access.tenanttermsofuse.application.usecase.enable.TenantTermsOfUseEnableProjection;
import net.civeira.phylax.features.access.tenanttermsofuse.application.usecase.enable.TenantTermsOfUseEnableStatus;
import net.civeira.phylax.features.access.tenanttermsofuse.application.usecase.enable.TenantTermsOfUseEnableUsecase;
