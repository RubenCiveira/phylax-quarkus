package net.civeira.phylax.features.access.apikeyclient.infrastructure.driven;

import java.time.OffsetDateTime;
import java.util.List;
import java.util.Optional;

import io.quarkus.cache.Cache;
import io.quarkus.cache.CacheName;
import jakarta.enterprise.context.ApplicationScoped;
import lombok.RequiredArgsConstructor;
import lombok.extern.slf4j.Slf4j;
import net.civeira.phylax.features.access.apikeyclient.domain.ApiKeyClient;
import net.civeira.phylax.features.access.apikeyclient.domain.gateway.ApiKeyClientCacheGateway;
import net.civeira.phylax.features.access.apikeyclient.domain.gateway.ApiKeyClientCached;
import net.civeira.phylax.features.access.apikeyclient.domain.gateway.ApiKeyClientCursor;
import net.civeira.phylax.features.access.apikeyclient.domain.gateway.ApiKeyClientFilter;

@ApplicationScoped
@Slf4j
@RequiredArgsConstructor
public class ApiKeyClientCacheGatewayAdapter implements ApiKeyClientCacheGateway {

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   */
  @CacheName("api-key-client")
  private final Cache cache;

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   */
  @Override
  public void evict() {
    log.trace("Clearing cache after big operation");
    invalidate();
  }

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   * @param apiKeyClient
   */
  @Override
  public void remove(final ApiKeyClient apiKeyClient) {
    log.trace("Invalidating apiKeyClient cache due to the removal of {}", apiKeyClient);
    invalidate();
  }

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   * @param filter
   * @param cursor
   * @return
   */
  public Optional<ApiKeyClientCached> retrieve(final ApiKeyClientFilter filter,
      final ApiKeyClientCursor cursor) {
    String key = key(filter, cursor);
    log.trace("Lookup at apiKeyClient cache for the key {}", key);
    return cache.<String, ApiKeyClientCached>get(key, k -> null)
        .map(cached -> Optional.ofNullable(cached)).await().indefinitely();
  }

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   * @param filter
   * @param cursor
   * @param apiKeyClients
   * @return
   */
  public ApiKeyClientCached store(final ApiKeyClientFilter filter, final ApiKeyClientCursor cursor,
      final List<ApiKeyClient> apiKeyClients) {
    String key = key(filter, cursor);
    cache.invalidate(key).await().indefinitely();
    return cache.<String, ApiKeyClientCached>get(key,
        k -> ApiKeyClientCached.builder().since(OffsetDateTime.now()).value(apiKeyClients).build())
        .await().indefinitely();
  }

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   * @param apiKeyClient
   */
  @Override
  public void update(final ApiKeyClient apiKeyClient) {
    log.trace("Invalidating apiKeyClient cache due to the update of {}", apiKeyClient);
    invalidate();
  }

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   */
  private void invalidate() {
    cache.invalidateAll().await().indefinitely();
  }

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   * @param filter
   * @param cursor
   * @return
   */
  private String key(final ApiKeyClientFilter filter, final ApiKeyClientCursor cursor) {
    return filter.toString() + "-" + cursor.toString();
  }
}
