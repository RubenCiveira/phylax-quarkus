package net.civeira.phylax.features.access.useridentity.application.usecase.delete;

import java.time.Duration;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.BatchIdentificator;
import net.civeira.phylax.common.batch.BatchProgress;
import net.civeira.phylax.common.batch.BatchService;
import net.civeira.phylax.common.batch.ExecutorByDeferSteps;
import net.civeira.phylax.common.batch.ExecutorPlan;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.useridentity.application.visibility.UserIdentityVisibilityFilter;
import net.civeira.phylax.features.access.useridentity.application.visibility.UserIdentitysVisibility;
import net.civeira.phylax.features.access.useridentity.domain.UserIdentity;
import net.civeira.phylax.features.access.useridentity.domain.UserIdentityRef;
import net.civeira.phylax.features.access.useridentity.domain.gateway.UserIdentityCacheGateway;
import net.civeira.phylax.features.access.useridentity.domain.gateway.UserIdentityWriteRepositoryGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class UserIdentityDeleteUsecase {

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  private final BatchService batch;

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  private final UserIdentityCacheGateway cache;

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  private final Event<UserIdentityDeleteAllowDecision> deleteAllowEmitter;

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  private final Event<UserIdentityDeleteCheck> deleteCheckEmitter;

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  private final UserIdentityWriteRepositoryGateway gateway;

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  private final UserIdentitysVisibility visibility;

  /**
   * @autogenerated DeleteUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  public Allow allow(final Interaction query, final UserIdentityRef reference) {
    UserIdentityDeleteAllowDecision base = UserIdentityDeleteAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).reference(reference).build();
    deleteAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated DeleteUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    UserIdentityDeleteAllowDecision base = UserIdentityDeleteAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).build();
    deleteAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated DeleteUsecaseGenerator
   * @param query a filter to retrieve only matching values
   * @return The slide with some values
   */
  public BatchProgress checkProgress(final UserIdentityCheckBatchDeleteStatus query) {
    return query.getActor().getName()
        .flatMap(name -> batch.retrieve(query.getTaskId(), query.getConnection().getLocale(), name))
        .orElseThrow(() -> new NotFoundException());
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated DeleteUsecaseGenerator
   * @param query
   * @param reference
   */
  public void delete(final Interaction query, final UserIdentityRef reference) {
    Allow detail = allow(query, reference);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    UserIdentity original = visibility.retrieveVisibleForUpdate(query, reference.getUid())
        .orElseThrow(() -> new NotFoundException(""));
    delete(query, original);
    flush();
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated DeleteUsecaseGenerator
   * @param query
   * @param filter
   * @return The slide with some values
   */
  public BatchIdentificator delete(final Interaction query, final UserIdentityDeleteFilter filter) {
    Allow detail = allow(query);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    UserIdentityVisibilityFilter filterOnVisibles = UserIdentityVisibilityFilter.builder()
        .uid(filter.getUid().orElse(null)).uids(filter.getUids().stream().toList())
        .search(filter.getSearch().orElse(null)).user(filter.getUser().orElse(null))
        .users(filter.getUsers()).relyingParty(filter.getRelyingParty().orElse(null))
        .relyingPartys(filter.getRelyingPartys())
        .trustedClient(filter.getTrustedClient().orElse(null))
        .trustedClients(filter.getTrustedClients())
        .userTenantTenantAccesible(filter.getUserTenantTenantAccesible().orElse(null)).build();
    UserIdentityDeleteAllInBatchCommand command = UserIdentityDeleteAllInBatchCommand.builder()
        .interaction(query).filter(filterOnVisibles).build();
    return batch.start(query.getActor().getName().orElse("-"), Duration.ofHours(6), ExecutorPlan
        .<UserIdentityDeleteAllInBatchCommand>builder().params(command).name("delete-color")
        .executor(
            ExecutorByDeferSteps.<UserIdentity, UserIdentity, UserIdentityDeleteAllInBatchCommand, UserIdentitysInBatchExecutor.UserIdentityPaginableBatch>builder()
                .initializer(UserIdentitysInBatchExecutor.class)
                .counter(UserIdentitysInBatchExecutor.class)
                .descriptor(UserIdentitysInBatchExecutor.class)
                .reader(UserIdentitysInBatchExecutor.class)
                .processor(UserIdentitysInBatchExecutor.class)
                .writer(UserIdentitysInBatchExecutor.class)
                .finalizer(UserIdentitysInBatchExecutor.class).build())
        .build());
  }

  /**
   * @autogenerated DeleteUsecaseGenerator
   * @param interaction
   * @param original
   */
  void delete(final Interaction interaction, final UserIdentity original) {
    UserIdentityDeleteCheck proposal =
        UserIdentityDeleteCheck.builder().reference(original).interaction(interaction).build();
    deleteCheckEmitter.fire(proposal);
    UserIdentity clean = original.delete();
    gateway.delete(clean);
  }

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  void flush() {
    cache.evict();
  }
}
