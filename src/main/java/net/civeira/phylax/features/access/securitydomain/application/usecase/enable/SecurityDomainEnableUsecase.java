package net.civeira.phylax.features.access.securitydomain.application.usecase.enable;

import java.time.Duration;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.BatchIdentificator;
import net.civeira.phylax.common.batch.BatchProgress;
import net.civeira.phylax.common.batch.BatchService;
import net.civeira.phylax.common.batch.ExecutorByDeferSteps;
import net.civeira.phylax.common.batch.ExecutorPlan;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.securitydomain.application.visibility.SecurityDomainVisibilityFilter;
import net.civeira.phylax.features.access.securitydomain.application.visibility.SecurityDomainsVisibility;
import net.civeira.phylax.features.access.securitydomain.domain.SecurityDomain;
import net.civeira.phylax.features.access.securitydomain.domain.SecurityDomainRef;
import net.civeira.phylax.features.access.securitydomain.domain.gateway.SecurityDomainCacheGateway;
import net.civeira.phylax.features.access.securitydomain.domain.gateway.SecurityDomainWriteRepositoryGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class SecurityDomainEnableUsecase {

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final BatchService batch;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final SecurityDomainCacheGateway cache;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final Event<SecurityDomainEnableAllowDecision> execAllowEmitter;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final Event<SecurityDomainEnableCheck> execProposalEmitter;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final SecurityDomainWriteRepositoryGateway gateway;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final SecurityDomainsVisibility visibility;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  public Allow allow(final Interaction query, final SecurityDomainRef reference) {
    SecurityDomainEnableAllowDecision base = SecurityDomainEnableAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).reference(reference).build();
    execAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    SecurityDomainEnableAllowDecision base = SecurityDomainEnableAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).build();
    execAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query a filter to retrieve only matching values
   * @return The slide with some values
   */
  public BatchProgress checkProgress(final SecurityDomainEnableStatus query) {
    return query.getActor().getName()
        .flatMap(name -> batch.retrieve(query.getTaskId(), query.getConnection().getLocale(), name))
        .orElseThrow(() -> new NotFoundException());
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param reference
   * @return The slide with some values
   */
  public SecurityDomainEnableProjection enable(final Interaction query,
      final SecurityDomainRef reference) {
    Allow detail = allow(query, reference);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    SecurityDomain original = visibility.retrieveVisibleForUpdate(query, reference.getUid())
        .orElseThrow(() -> new NotFoundException(""));
    SecurityDomain saved = enable(query, original);
    flush();
    return SecurityDomainEnableProjection.from(visibility.copyWithHidden(query, saved));
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param filter
   * @return The slide with some values
   */
  public BatchIdentificator enable(final Interaction query,
      final SecurityDomainEnableFilter filter) {
    Allow detail = allow(query);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    SecurityDomainVisibilityFilter filterWithVisibility =
        SecurityDomainVisibilityFilter.builder().uid(filter.getUid().orElse(null))
            .uids(filter.getUids().stream().toList()).search(filter.getSearch().orElse(null))
            .enabled(filter.getEnabled().orElse(null)).name(filter.getName().orElse(null)).build();
    SecurityDomainEnableAllInBatchCommand command = SecurityDomainEnableAllInBatchCommand.builder()
        .interaction(query).filter(filterWithVisibility).build();
    return batch.start(command.getInteraction().getActor().getName().orElse("-"),
        Duration.ofHours(6),
        ExecutorPlan.<SecurityDomainEnableAllInBatchCommand>builder().params(command)
            .name("enable-security-domain")
            .executor(
                ExecutorByDeferSteps.<SecurityDomain, SecurityDomain, SecurityDomainEnableAllInBatchCommand, SecurityDomainEnablesInBatchExecutor.SecurityDomainPaginableBatch>builder()
                    .initializer(SecurityDomainEnablesInBatchExecutor.class)
                    .counter(SecurityDomainEnablesInBatchExecutor.class)
                    .descriptor(SecurityDomainEnablesInBatchExecutor.class)
                    .reader(SecurityDomainEnablesInBatchExecutor.class)
                    .processor(SecurityDomainEnablesInBatchExecutor.class)
                    .writer(SecurityDomainEnablesInBatchExecutor.class)
                    .finalizer(SecurityDomainEnablesInBatchExecutor.class).build())
            .build());
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param interaction
   * @param original
   * @return
   */
  SecurityDomain enable(final Interaction interaction, final SecurityDomain original) {
    SecurityDomainEnableCheck proposal =
        SecurityDomainEnableCheck.builder().reference(original).interaction(interaction).build();
    execProposalEmitter.fire(proposal);
    SecurityDomain result = original.enable();
    SecurityDomain saved = gateway.update(original, result);
    return saved;
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  void flush() {
    cache.evict();
  }
}
