package net.civeira.phylax.features.access.relyingparty.infrastructure.driven;

import java.util.List;
import java.util.Optional;
import java.util.function.Predicate;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.SpanKind;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Context;
import jakarta.enterprise.context.ApplicationScoped;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.algorithms.Slider;
import net.civeira.phylax.features.access.relyingparty.domain.RelyingParty;
import net.civeira.phylax.features.access.relyingparty.domain.RelyingPartyRef;
import net.civeira.phylax.features.access.relyingparty.domain.gateway.RelyingPartyCursor;
import net.civeira.phylax.features.access.relyingparty.domain.gateway.RelyingPartyFilter;
import net.civeira.phylax.features.access.relyingparty.domain.gateway.RelyingPartyWriteRepositoryGateway;
import net.civeira.phylax.features.access.relyingparty.infrastructure.event.RelyingPartyEventDispatcher;
import net.civeira.phylax.features.access.relyingparty.infrastructure.repository.RelyingPartyRepository;

@ApplicationScoped
@RequiredArgsConstructor
public class RelyingPartyWriteGatewayAdapter implements RelyingPartyWriteRepositoryGateway {

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   */
  private final RelyingPartyEventDispatcher eventDispatcher;

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   */
  private final RelyingPartyRepository repository;

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param filter
   * @return
   */
  @Override
  public long countForUpdate(RelyingPartyFilter filter) {
    Span span = tracer
        .spanBuilder("Count on the repository for update the matches entities of relying party")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.count(filter);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param entity
   * @return
   */
  @Override
  public RelyingParty create(RelyingParty entity) {
    Span span = tracer.spanBuilder("Create on the repository an entity of relying party")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      RelyingParty result = repository.create(entity);
      eventDispatcher.dispatch(entity);
      return result;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param entity
   * @param verifier
   * @return
   */
  @Override
  public RelyingParty create(RelyingParty entity, Predicate<RelyingParty> verifier) {
    Span span = tracer
        .spanBuilder(
            "Create on the repository an entity of relying party and verify its visibility")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      RelyingParty result = repository.create(entity, verifier);
      eventDispatcher.dispatch(entity);
      return result;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param entity
   */
  @Override
  public void delete(RelyingParty entity) {
    Span span = tracer
        .spanBuilder("Delete from the repository a slide for entities of relying party")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      repository.delete(entity);
      eventDispatcher.dispatch(entity);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param uid
   * @param filter
   * @return
   */
  @Override
  public boolean existsForUpdate(String uid, Optional<RelyingPartyFilter> filter) {
    Span span = tracer
        .spanBuilder("Check if exists on the repository for update a entity of relying party")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.existsForUpdate(uid, filter);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param filter
   * @return
   */
  @Override
  public Optional<RelyingParty> findForUpdate(RelyingPartyFilter filter) {
    Span span = tracer.spanBuilder("Find on the repository for update one entity of relying party")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.findForUpdate(filter);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param filter
   * @return
   */
  @Override
  public List<RelyingParty> listForUpdate(RelyingPartyFilter filter) {
    Span span = tracer
        .spanBuilder("Retrieve from repository for update a list for entities of relying party")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.listForUpdate(filter);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param reference
   * @return
   */
  @Override
  public RelyingParty resolveForUpdate(RelyingPartyRef reference) {
    Span span = tracer
        .spanBuilder("Enrich from the repository for update a entity of relying party")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.resolveForUpdate(reference);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param uid
   * @param filter
   * @return
   */
  @Override
  public Optional<RelyingParty> retrieveForUpdate(String uid, Optional<RelyingPartyFilter> filter) {
    Span span = tracer.spanBuilder("Retrieve from repository a slide for entities of relying party")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.retrieveForUpdate(uid, filter);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param filter
   * @param cursor
   * @return
   */
  @Override
  public Slider<RelyingParty> slideForUpdate(RelyingPartyFilter filter, RelyingPartyCursor cursor) {
    Span span = tracer
        .spanBuilder("Retrieve from repository for update a slide for entities of relying party")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      return repository.slideForUpdate(filter, cursor);
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated WriteAdaterGatewayGenerator
   * @param reference
   * @param entity
   * @return
   */
  @Override
  public RelyingParty update(RelyingPartyRef reference, RelyingParty entity) {
    RelyingParty result = repository.update(entity);
    eventDispatcher.dispatch(entity);
    return result;
  }
}
