package net.civeira.phylax.features.access.loginprovider.transition.update;

import java.util.ArrayList;
import java.util.List;
import java.util.function.Function;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.inject.Instance;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.features.access.loginprovider.LoginProvider;
import net.civeira.phylax.features.access.loginprovider.command.LoginProviderWriteAttributes;
import net.civeira.phylax.features.access.loginprovider.transition.LoginProviderChangeProposal;

@ApplicationScoped
@RequiredArgsConstructor
public class LoginProviderUpdatePipeline {

  /**
   * @autogenerated UpdateEntityRuleGenerator
   */
  private final Instance<LoginProviderUpdateStep> steps;

  /**
   * @autogenerated UpdateEntityRuleGenerator
   * @param attributes
   * @param original
   * @param last
   * @return
   */
  public LoginProvider update(final LoginProviderWriteAttributes attributes,
      final LoginProvider original,
      final Function<LoginProviderChangeProposal, LoginProvider> last) {
    Function<LoginProviderUpdateTransition, LoginProvider> finalHandler = t -> {
      if (t.hasErrors()) {
        throw t.asConstraintException();
      }
      return last.apply(t.getData());
    };
    List<LoginProviderUpdateStep> reversed = new ArrayList<>(steps.stream().toList()).reversed();
    Function<LoginProviderUpdateTransition, LoginProvider> pipeline = reversed.stream().reduce(
        finalHandler, (next, step) -> transition -> step.update(transition, next), (f1, f2) -> f1);
    return pipeline.apply(LoginProviderUpdateTransition.builder()
        .data(new LoginProviderChangeProposal(attributes)).original(original).build());
  }
}
