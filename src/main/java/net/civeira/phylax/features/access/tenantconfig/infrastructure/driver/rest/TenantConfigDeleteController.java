package net.civeira.phylax.features.access.tenantconfig.infrastructure.driver.rest;

import java.util.List;
import java.util.stream.Stream;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.SpanKind;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Context;
import jakarta.enterprise.context.ApplicationScoped;
import jakarta.transaction.Transactional;
import jakarta.ws.rs.core.Response;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.BatchIdentificator;
import net.civeira.phylax.common.batch.BatchProgress;
import net.civeira.phylax.common.infrastructure.CurrentRequest;
import net.civeira.phylax.features.access.tenant.domain.TenantReference;
import net.civeira.phylax.features.access.tenantconfig.application.usecase.delete.TenantConfigCheckBatchDeleteStatus;
import net.civeira.phylax.features.access.tenantconfig.application.usecase.delete.TenantConfigDeleteFilter;
import net.civeira.phylax.features.access.tenantconfig.application.usecase.delete.TenantConfigDeleteUsecase;
import net.civeira.phylax.features.access.tenantconfig.domain.TenantConfigReference;

@RequiredArgsConstructor
@ApplicationScoped
public class TenantConfigDeleteController {

  /**
   * @autogenerated DeleteControllerGenerator
   */
  private final CurrentRequest currentRequest;

  /**
   * @autogenerated DeleteControllerGenerator
   */
  private final TenantConfigDeleteUsecase delete;

  /**
   * @autogenerated DeleteControllerGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated DeleteControllerGenerator
   * @param uids
   * @param search
   * @param tenant
   * @param tenants
   * @return
   */
  public Response tenantConfigApiBatchDelete(final List<String> uids, final String search,
      final String tenant, final List<String> tenants) {
    Span span = tracer.spanBuilder("Api to delete masive entities of tenant config")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      TenantConfigDeleteFilter.TenantConfigDeleteFilterBuilder filterBuilder =
          TenantConfigDeleteFilter.builder();
      filterBuilder =
          filterBuilder.uids(uids.stream().flatMap(part -> Stream.of(part.split(","))).toList());
      filterBuilder = filterBuilder.search(search);
      filterBuilder = filterBuilder.uids(
          null == uids ? null : uids.stream().flatMap(part -> Stream.of(part.split(","))).toList());
      filterBuilder = filterBuilder.search(search);
      if (null != tenant) {
        filterBuilder = filterBuilder.tenant(TenantReference.of(tenant));
      }
      filterBuilder = filterBuilder.tenants(null == tenants ? null
          : tenants.stream().flatMap(part -> Stream.of(part.split(","))).toList());
      TenantConfigDeleteFilter filter = filterBuilder.build();
      BatchIdentificator task = delete.delete(currentRequest.interaction(), filter);
      /* .header("Last-Modified", value.getSince().format(DateTimeFormatter.RFC_1123_DATE_TIME)) */
      return Response.ok(task).build();
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated DeleteControllerGenerator
   * @param batchId
   * @return
   */
  public Response tenantConfigApiBatchDeleteQuery(final String batchId) {
    Span span = tracer
        .spanBuilder("Api to check the status of massive delete for entities of tenant config")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      BatchProgress task = delete.checkProgress(TenantConfigCheckBatchDeleteStatus.builder()
          .taskId(batchId).build(currentRequest.interaction()));
      return Response.ok(task).build();
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated DeleteControllerGenerator
   * @param uid
   * @return
   */
  @Transactional
  public Response tenantConfigApiDelete(final String uid) {
    Span span = tracer.spanBuilder("Api to delete an entity of tenant config")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      span.setAttribute("ref", uid);
      delete.delete(currentRequest.interaction(), TenantConfigReference.of(uid));
      return Response.noContent().build();
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }
}
