package net.civeira.phylax.features.access.useracceptedtermnsofuse.domain.valueobject;

import java.util.Optional;

import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.value.validation.ConstraintFail;
import net.civeira.phylax.common.value.validation.ConstraintFailList;
import net.civeira.phylax.features.access.user.domain.UserChangeSet;
import net.civeira.phylax.features.access.user.domain.UserRef;
import net.civeira.phylax.features.access.user.domain.UserReference;
import net.civeira.phylax.features.access.useracceptedtermnsofuse.domain.UserAcceptedTermnsOfUse;

@RequiredArgsConstructor
public class UserValueHolder<T> {

  /**
   * @autogenerated ValueHolderGenerator
   */
  private final T holder;

  /**
   * @autogenerated ValueHolderGenerator
   */
  private Optional<UserVO> value = Optional.empty();

  /**
   * @autogenerated ValueHolderGenerator
   * @return
   */
  public Optional<UserRef> getUser() {
    return value.map(UserVO::getUser);
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @return
   */
  public Optional<UserVO> getUserValue() {
    return value;
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @param original
   * @param fails
   * @return
   */
  public UserVO readUser(final Optional<UserAcceptedTermnsOfUse> original,
      final ConstraintFailList fails) {
    UserVO result = value.orElse(original.map(UserAcceptedTermnsOfUse::getUserValue).orElse(null));
    if (null == result) {
      fails.add(new ConstraintFail("REQUIRED", "user", null));
    }
    return result;
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @return
   */
  public T unsetUser() {
    value = Optional.empty();
    return holder;
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @param field
   * @return
   */
  public T unsetUserIfMatches(final String field) {
    if ("user".equals(field)) {
      value = Optional.empty();
    }
    return holder;
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @param user
   * @return
   */
  public T user(UserVO user) {
    value = Optional.of(user);
    return holder;
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @param user
   * @return
   */
  public T user(Optional<UserVO> user) {
    value = user;
    return holder;
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @param user
   * @return
   */
  public T user(UserRef user) {
    value = Optional.of(UserVO.from(user));
    return holder;
  }

  /**
   * @autogenerated ValueHolderGenerator
   * @param user
   * @return
   */
  public T user(UserChangeSet user) {
    value = Optional.of(UserVO.from(UserReference.of(user.getUid().orElseThrow())));
    return holder;
  }
}
