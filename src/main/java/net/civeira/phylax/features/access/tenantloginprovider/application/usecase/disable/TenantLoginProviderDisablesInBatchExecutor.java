package net.civeira.phylax.features.access.tenantloginprovider.application.usecase.disable;

import java.util.List;

import io.quarkus.arc.Unremovable;
import jakarta.enterprise.context.ApplicationScoped;
import lombok.Data;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.stepper.ItemDescriptor;
import net.civeira.phylax.common.batch.stepper.ItemProcessor;
import net.civeira.phylax.common.batch.stepper.ItemReader;
import net.civeira.phylax.common.batch.stepper.ItemWriter;
import net.civeira.phylax.common.batch.stepper.StepContext;
import net.civeira.phylax.common.batch.stepper.StepCounter;
import net.civeira.phylax.common.batch.stepper.StepFinalizer;
import net.civeira.phylax.common.batch.stepper.StepInitializer;
import net.civeira.phylax.common.exception.ExecutionException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.features.access.tenantloginprovider.application.visibility.TenantLoginProvidersVisibility;
import net.civeira.phylax.features.access.tenantloginprovider.domain.TenantLoginProvider;
import net.civeira.phylax.features.access.tenantloginprovider.domain.gateway.TenantLoginProviderCursor;

@Unremovable
@ApplicationScoped
@RequiredArgsConstructor
class TenantLoginProviderDisablesInBatchExecutor implements
    StepCounter<TenantLoginProviderDisableAllInBatchCommand, TenantLoginProviderDisablesInBatchExecutor.TenantLoginProviderPaginableBatch>,
    StepFinalizer<TenantLoginProviderDisableAllInBatchCommand, TenantLoginProviderDisablesInBatchExecutor.TenantLoginProviderPaginableBatch>,
    StepInitializer<TenantLoginProviderDisableAllInBatchCommand, TenantLoginProviderDisablesInBatchExecutor.TenantLoginProviderPaginableBatch>,
    ItemReader<TenantLoginProvider, TenantLoginProviderDisableAllInBatchCommand, TenantLoginProviderDisablesInBatchExecutor.TenantLoginProviderPaginableBatch>,
    ItemWriter<TenantLoginProvider, TenantLoginProviderDisableAllInBatchCommand, TenantLoginProviderDisablesInBatchExecutor.TenantLoginProviderPaginableBatch>,
    ItemProcessor<TenantLoginProvider, TenantLoginProvider, TenantLoginProviderDisableAllInBatchCommand, TenantLoginProviderDisablesInBatchExecutor.TenantLoginProviderPaginableBatch>,
    ItemDescriptor<TenantLoginProvider, TenantLoginProviderDisableAllInBatchCommand, TenantLoginProviderDisablesInBatchExecutor.TenantLoginProviderPaginableBatch> {

  /**
   * @autogenerated EntityGenerator
   */
  @Data
  public static class TenantLoginProviderPaginableBatch {

    /**
     * @autogenerated EntityGenerator
     */
    private String since;
  }

  /**
   * @autogenerated EntityGenerator
   */
  private final int size = 10;

  /**
   * @autogenerated EntityGenerator
   */
  private final TenantLoginProviderDisableUsecase usecase;

  /**
   * @autogenerated EntityGenerator
   */
  private final TenantLoginProvidersVisibility visibility;

  /**
   * @autogenerated EntityGenerator
   * @param context
   * @return
   */
  @Override
  public long approximatedItems(
      final StepContext<TenantLoginProviderDisableAllInBatchCommand, TenantLoginProviderPaginableBatch> context) {
    return visibility.countVisibles(context.getParams().getInteraction(),
        context.getParams().getFilter());
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   */
  @Override
  public void finish(
      final StepContext<TenantLoginProviderDisableAllInBatchCommand, TenantLoginProviderPaginableBatch> context) {
    usecase.flush();
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   */
  @Override
  public void init(
      final StepContext<TenantLoginProviderDisableAllInBatchCommand, TenantLoginProviderPaginableBatch> context) {
    context.setState(new TenantLoginProviderPaginableBatch());
  }

  /**
   * @autogenerated EntityGenerator
   * @param item
   * @param context
   * @return
   */
  @Override
  public String itemDescription(final TenantLoginProvider item,
      final StepContext<TenantLoginProviderDisableAllInBatchCommand, TenantLoginProviderPaginableBatch> context) {
    return item.getUid();
  }

  /**
   * @autogenerated EntityGenerator
   * @param item
   * @param context
   * @return
   */
  @Override
  public TenantLoginProvider process(final TenantLoginProvider item,
      final StepContext<TenantLoginProviderDisableAllInBatchCommand, TenantLoginProviderPaginableBatch> context) {
    Allow allowed = usecase.allow(context.getParams().getInteraction(), item);
    if (!allowed.isAllowed()) {
      throw new ExecutionException("not-allowed", null);
    }
    return item;
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   * @return
   */
  @Override
  public List<TenantLoginProvider> read(
      final StepContext<TenantLoginProviderDisableAllInBatchCommand, TenantLoginProviderPaginableBatch> context) {
    List<TenantLoginProvider> page =
        visibility.listVisiblesForUpdate(context.getParams().getInteraction(),
            context.getParams().getFilter(), TenantLoginProviderCursor.builder().limit(size)
                .sinceUid(context.getState().getSince()).build());
    context.getState().setSince(page.isEmpty() ? null : page.get(page.size() - 1).getUid());
    return page;
  }

  /**
   * @autogenerated EntityGenerator
   * @param items
   * @param context
   */
  @Override
  public void write(final List<TenantLoginProvider> items,
      final StepContext<TenantLoginProviderDisableAllInBatchCommand, TenantLoginProviderPaginableBatch> context) {
    items.forEach(item -> usecase.disable(context.getParams().getInteraction(), item));
  }
}
