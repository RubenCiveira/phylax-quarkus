package net.civeira.phylax.features.access.tenantconfig.transition.create;

import java.util.ArrayList;
import java.util.List;
import java.util.function.Function;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.inject.Instance;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.features.access.tenantconfig.TenantConfig;
import net.civeira.phylax.features.access.tenantconfig.command.TenantConfigWriteAttributes;
import net.civeira.phylax.features.access.tenantconfig.transition.TenantConfigChangeProposal;

@ApplicationScoped
@RequiredArgsConstructor
public class TenantConfigCreatePipeline {

  /**
   * @autogenerated UpdateEntityRuleGenerator
   */
  private final Instance<TenantConfigCreateStep> steps;

  /**
   * @autogenerated UpdateEntityRuleGenerator
   * @param attributes
   * @param last
   * @return
   */
  public TenantConfig create(final TenantConfigWriteAttributes attributes,
      final Function<TenantConfigChangeProposal, TenantConfig> last) {
    Function<TenantConfigCreateTransition, TenantConfig> finalHandler = t -> {
      if (t.hasErrors()) {
        throw t.asConstraintException();
      }
      return last.apply(t.getData());
    };
    List<TenantConfigCreateStep> reversed = new ArrayList<>(steps.stream().toList()).reversed();
    Function<TenantConfigCreateTransition, TenantConfig> pipeline = reversed.stream().reduce(
        finalHandler, (next, step) -> transition -> step.create(transition, next), (f1, f2) -> f1);
    return pipeline.apply(TenantConfigCreateTransition.builder()
        .data(new TenantConfigChangeProposal(attributes)).build());
  }
}
