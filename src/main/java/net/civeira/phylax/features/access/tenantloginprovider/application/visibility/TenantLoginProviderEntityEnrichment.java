package net.civeira.phylax.features.access.tenantloginprovider.application.visibility;

import java.util.Optional;
import java.util.function.Consumer;

import lombok.Builder;
import lombok.Data;
import lombok.EqualsAndHashCode;
import lombok.NonNull;
import lombok.With;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.tenantloginprovider.domain.TenantLoginProvider;
import net.civeira.phylax.features.access.tenantloginprovider.domain.TenantLoginProviderChangeSet;

/**
 * Entity Enrichment events happen when a dto params is read throught the visibility service from
 * some input parameters to store on the repository.The programmers could add policies to fill
 * calculated values to store at the application level.
 */
@Data
@Builder(toBuilder = true)
@With
@EqualsAndHashCode(onlyExplicitlyIncluded = true)
public class TenantLoginProviderEntityEnrichment {

  /**
   * @autogenerated EntityEnrichmentGenerator
   */
  @NonNull
  private final Interaction interaction;

  /**
   * @autogenerated EntityEnrichmentGenerator
   */
  @NonNull
  private TenantLoginProviderChangeSet dto;

  /**
   * @autogenerated EntityEnrichmentGenerator
   */
  private TenantLoginProvider original;

  /**
   * @autogenerated EntityEnrichmentGenerator
   * @return
   */
  public Optional<TenantLoginProvider> getOriginal() {
    return Optional.ofNullable(original);
  }

  /**
   * @autogenerated EntityEnrichmentGenerator
   * @param mapper
   */
  public void peek(Consumer<TenantLoginProviderChangeSet> mapper) {
    mapper.accept(dto);
  }
}
