package net.civeira.phylax.features.access.scopeassignation.application.usecase.create;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.scopeassignation.ScopeAssignation;
import net.civeira.phylax.features.access.scopeassignation.ScopeAssignationFacade;
import net.civeira.phylax.features.access.scopeassignation.application.projection.ScopeAssignationStateProyection;
import net.civeira.phylax.features.access.scopeassignation.application.request.ScopeAssignationStateChange;
import net.civeira.phylax.features.access.scopeassignation.application.service.visibility.ScopeAssignationsVisibility;
import net.civeira.phylax.features.access.scopeassignation.gateway.ScopeAssignationCacheGateway;
import net.civeira.phylax.features.access.scopeassignation.gateway.ScopeAssignationWriteRepositoryGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class ScopeAssignationCreateUsecase {

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final ScopeAssignationCacheGateway cache;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Event<ScopeAssignationAllowCreateProposal> createAllowEmitter;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Event<ScopeAssignationCreateProposal> createProposalEmitter;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Event<ScopeAssignationCreateEvent> createdEmitter;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final ScopeAssignationFacade facade;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final ScopeAssignationWriteRepositoryGateway gateway;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final ScopeAssignationsVisibility visibility;

  /**
   * @autogenerated CreateUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    ScopeAssignationAllowCreateProposal proposal = ScopeAssignationAllowCreateProposal.builder()
        .detail(Allow.builder().allowed(true).description("Allow by default").build()).query(query)
        .build();
    createAllowEmitter.fire(proposal);
    return proposal.getDetail();
  }

  /**
   * The slide with some values
   *
   * @autogenerated CreateUsecaseGenerator
   * @param query
   * @param input
   * @return The slide with some values
   */
  public ScopeAssignationStateProyection create(final Interaction query,
      final ScopeAssignationStateChange input) {
    Allow detail = allow(query);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    ScopeAssignationStateChange filled = visibility.copyWithFixed(query, input);
    ScopeAssignationCreateProposal proposal =
        ScopeAssignationCreateProposal.builder().interaction(query).dto(filled).build();
    createProposalEmitter.fire(proposal);
    ScopeAssignationStateChange dto = proposal.getDto();
    ScopeAssignation entity = facade.create(dto);
    ScopeAssignation created =
        gateway.create(entity, attempt -> visibility.checkVisibility(query, attempt.getUidValue()));
    cache.update(created);
    ScopeAssignationCreateEvent event =
        ScopeAssignationCreateEvent.builder().payload(created).interaction(query).build();
    createdEmitter.fire(event);
    return visibility.copyWithHidden(query, event.getPayload());
  }
}
