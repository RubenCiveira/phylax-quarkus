package net.civeira.phylax.features.access.scopeassignation.application.usecase.create;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.scopeassignation.application.visibility.ScopeAssignationsVisibility;
import net.civeira.phylax.features.access.scopeassignation.domain.ScopeAssignation;
import net.civeira.phylax.features.access.scopeassignation.domain.ScopeAssignationChangeSet;
import net.civeira.phylax.features.access.scopeassignation.domain.gateway.ScopeAssignationCacheGateway;
import net.civeira.phylax.features.access.scopeassignation.domain.gateway.ScopeAssignationWriteRepositoryGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class ScopeAssignationCreateUsecase {

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final ScopeAssignationCacheGateway cache;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Event<ScopeAssignationCreateAllowDecision> createAllowEmitter;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Event<ScopeAssignationCreateCheck> createCheckEmitter;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Event<ScopeAssignationCreateEnrich> createEnrichEmitter;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final ScopeAssignationWriteRepositoryGateway gateway;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final ScopeAssignationsVisibility visibility;

  /**
   * @autogenerated CreateUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    ScopeAssignationCreateAllowDecision proposal = ScopeAssignationCreateAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allow by default").build()).query(query)
        .build();
    createAllowEmitter.fire(proposal);
    return proposal.getDetail();
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated CreateUsecaseGenerator
   * @param query
   * @param input
   * @return The slide with some values
   */
  public ScopeAssignationCreateProjection create(final Interaction query,
      final ScopeAssignationCreateInput input) {
    Allow detail = allow(query);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    ScopeAssignationCreateInput filled = ScopeAssignationCreateInput
        .fromChangeSet(visibility.copyWithFixed(query, input.toChangeSet()));
    ScopeAssignationCreateCheck check =
        ScopeAssignationCreateCheck.builder().interaction(query).input(filled).build();
    createCheckEmitter.fire(check);
    ScopeAssignationCreateEnrich proposal =
        ScopeAssignationCreateEnrich.builder().interaction(query).input(filled).build();
    createEnrichEmitter.fire(proposal);
    ScopeAssignationChangeSet dto = proposal.getInput().toChangeSet();
    ScopeAssignation entity = ScopeAssignation.create(dto);
    ScopeAssignation created =
        gateway.create(entity, attempt -> visibility.checkVisibility(query, attempt.getUid()));
    cache.update(created);
    return ScopeAssignationCreateProjection.from(visibility.copyWithHidden(query, created));
  }
}
