package net.civeira.phylax.features.access.role.infrastructure.driven;

import jakarta.enterprise.context.ApplicationScoped;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.infrastructure.audit.AuditEvent;
import net.civeira.phylax.common.infrastructure.audit.AuditWriteService;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.role.domain.Role;
import net.civeira.phylax.features.access.role.domain.gateway.RoleAuditGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class RoleAuditAdapter implements RoleAuditGateway {

  /**
   * @autogenerated AuditAdapterGenerator
   */
  private final AuditWriteService writer;

  /**
   * @autogenerated AuditAdapterGenerator
   * @param usecase The usecase responsible to the action
   * @param role
   * @param interaction The interaction
   */
  @Override
  public void created(final String usecase, final Role role, final Interaction interaction) {
    writer.record(AuditEvent.builder().operation("create").usecase(usecase).entityType("role")
        .entityId(role.getUid()).newValue(role.toMap())
        .performedBy(interaction.getActor().getName().orElse("<<no-user>>"))
        .tenant(interaction.getActor().getTenant().orElse("<<no-tenant>>"))
        .timestamp(interaction.getConnection().getStartTime())
        .sourceRequest(interaction.getConnection().getRequest())
        .remoteAddress(interaction.getConnection().getRemote().orElse("<<no-device>>"))
        .remoteApplication(
            interaction.getConnection().getRemoteApplication().orElse("<<no-application>>"))
        .remoteDevice(interaction.getConnection().getRemoteDevice().orElse("<<no-device>>"))
        .claims(interaction.getActor().getClaims()).build());
  }

  /**
   * @autogenerated AuditAdapterGenerator
   * @param usecase The usecase responsible to the action
   * @param role
   * @param interaction The interaction
   */
  @Override
  public void deleted(final String usecase, final Role role, final Interaction interaction) {
    writer.record(AuditEvent.builder().operation("delete").usecase(usecase).entityType("role")
        .entityId(role.getUid()).oldValue(role.toMap())
        .performedBy(interaction.getActor().getName().orElse("<<no-user>>"))
        .tenant(interaction.getActor().getTenant().orElse("<<no-tenant>>"))
        .timestamp(interaction.getConnection().getStartTime())
        .sourceRequest(interaction.getConnection().getRequest())
        .remoteAddress(interaction.getConnection().getRemote().orElse("<<no-device>>"))
        .remoteApplication(
            interaction.getConnection().getRemoteApplication().orElse("<<no-application>>"))
        .remoteDevice(interaction.getConnection().getRemoteDevice().orElse("<<no-device>>"))
        .claims(interaction.getActor().getClaims()).build());
  }

  /**
   * @autogenerated AuditAdapterGenerator
   * @param usecase The usecase responsible to the action
   * @param role
   * @param roleOriginal
   * @param interaction The interaction
   */
  @Override
  public void updated(final String usecase, final Role role, final Role roleOriginal,
      final Interaction interaction) {
    writer.record(AuditEvent.builder().operation("update").usecase(usecase).entityType("role")
        .entityId(role.getUid()).newValue(role.toMap()).oldValue(roleOriginal.toMap())
        .performedBy(interaction.getActor().getName().orElse("<<no-user>>"))
        .tenant(interaction.getActor().getTenant().orElse("<<no-tenant>>"))
        .timestamp(interaction.getConnection().getStartTime())
        .sourceRequest(interaction.getConnection().getRequest())
        .remoteAddress(interaction.getConnection().getRemote().orElse("<<no-device>>"))
        .remoteApplication(
            interaction.getConnection().getRemoteApplication().orElse("<<no-application>>"))
        .remoteDevice(interaction.getConnection().getRemoteDevice().orElse("<<no-device>>"))
        .claims(interaction.getActor().getClaims()).build());
  }
}
