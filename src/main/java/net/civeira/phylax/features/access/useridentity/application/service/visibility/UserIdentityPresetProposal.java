package net.civeira.phylax.features.access.useridentity.application.service.visibility;

import java.util.Optional;
import java.util.function.Consumer;

import lombok.Builder;
import lombok.Data;
import lombok.EqualsAndHashCode;
import lombok.NonNull;
import lombok.With;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.useridentity.UserIdentity;
import net.civeira.phylax.features.access.useridentity.application.request.UserIdentityStateChange;

@Data
@Builder(toBuilder = true)
@With
@EqualsAndHashCode(onlyExplicitlyIncluded = true)
public class UserIdentityPresetProposal {

  /**
   * @autogenerated PresetProposalGenerator
   */
  @NonNull
  private final Interaction interaction;

  /**
   * @autogenerated PresetProposalGenerator
   */
  @NonNull
  private UserIdentityStateChange dto;

  /**
   * @autogenerated PresetProposalGenerator
   */
  private UserIdentity original;

  /**
   * @autogenerated PresetProposalGenerator
   * @return
   */
  public Optional<UserIdentity> getOriginal() {
    return Optional.ofNullable(original);
  }

  /**
   * @autogenerated PresetProposalGenerator
   * @param mapper
   */
  public void peek(Consumer<UserIdentityStateChange> mapper) {
    mapper.accept(dto);
  }
}
