package net.civeira.phylax.features.access.securitydomain.application.usecase.disable;

import java.time.Duration;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.BatchIdentificator;
import net.civeira.phylax.common.batch.BatchProgress;
import net.civeira.phylax.common.batch.BatchService;
import net.civeira.phylax.common.batch.ExecutorByDeferSteps;
import net.civeira.phylax.common.batch.ExecutorPlan;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.securitydomain.SecurityDomain;
import net.civeira.phylax.features.access.securitydomain.SecurityDomainFacade;
import net.civeira.phylax.features.access.securitydomain.SecurityDomainRef;
import net.civeira.phylax.features.access.securitydomain.application.projection.SecurityDomainStateProyection;
import net.civeira.phylax.features.access.securitydomain.application.service.visibility.SecurityDomainsVisibility;
import net.civeira.phylax.features.access.securitydomain.gateway.SecurityDomainCacheGateway;
import net.civeira.phylax.features.access.securitydomain.gateway.SecurityDomainWriteRepositoryGateway;
import net.civeira.phylax.features.access.securitydomain.query.SecurityDomainFilter;

@ApplicationScoped
@RequiredArgsConstructor
public class SecurityDomainDisableUsecase {

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final BatchService batch;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final SecurityDomainCacheGateway cache;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final Event<SecurityDomainAllowDisableProposal> execAllowEmitter;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final Event<SecurityDomainDisableProposal> execProposalEmitter;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final Event<SecurityDomainDisableEvent> executedEmitter;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final SecurityDomainFacade facade;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final SecurityDomainWriteRepositoryGateway gateway;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final SecurityDomainsVisibility visibility;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  public Allow allow(final Interaction query, final SecurityDomainRef reference) {
    SecurityDomainAllowDisableProposal base = SecurityDomainAllowDisableProposal.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).reference(reference).build();
    execAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    SecurityDomainAllowDisableProposal base = SecurityDomainAllowDisableProposal.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).build();
    execAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * The slide with some values
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query a filter to retrieve only matching values
   * @return The slide with some values
   */
  public BatchProgress checkProgress(final SecurityDomainDisableStatus query) {
    return query.getActor().getName()
        .flatMap(name -> batch.retrieve(query.getTaskId(), query.getConnection().getLocale(), name))
        .orElseThrow(() -> new NotFoundException());
  }

  /**
   * The slide with some values
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param reference
   * @return The slide with some values
   */
  public SecurityDomainStateProyection disable(final Interaction query,
      final SecurityDomainRef reference) {
    Allow detail = allow(query, reference);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    SecurityDomain original = visibility.retrieveVisibleForUpdate(query, reference.getUidValue())
        .orElseThrow(() -> new NotFoundException(""));
    SecurityDomain saved = disable(query, original);
    flush();
    return visibility.copyWithHidden(query, saved);
  }

  /**
   * The slide with some values
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param filter
   * @return The slide with some values
   */
  public BatchIdentificator disable(final Interaction query, final SecurityDomainFilter filter) {
    Allow detail = allow(query);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    SecurityDomainDisableAllInBatchCommand command =
        SecurityDomainDisableAllInBatchCommand.builder().interaction(query).filter(filter).build();
    return batch.start(command.getInteraction().getActor().getName().orElse("-"),
        Duration.ofHours(6),
        ExecutorPlan.<SecurityDomainDisableAllInBatchCommand>builder().params(command)
            .name("disable-color")
            .executor(
                ExecutorByDeferSteps.<SecurityDomain, SecurityDomain, SecurityDomainDisableAllInBatchCommand, SecurityDomainDisablesInBatchExecutor.SecurityDomainPaginableBatch>builder()
                    .initializer(SecurityDomainDisablesInBatchExecutor.class)
                    .counter(SecurityDomainDisablesInBatchExecutor.class)
                    .descriptor(SecurityDomainDisablesInBatchExecutor.class)
                    .reader(SecurityDomainDisablesInBatchExecutor.class)
                    .processor(SecurityDomainDisablesInBatchExecutor.class)
                    .writer(SecurityDomainDisablesInBatchExecutor.class)
                    .finalizer(SecurityDomainDisablesInBatchExecutor.class).build())
            .build());
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param interaction
   * @param original
   * @return
   */
  SecurityDomain disable(final Interaction interaction, final SecurityDomain original) {
    SecurityDomain filled = facade.disable(original);
    SecurityDomainDisableProposal proposal =
        SecurityDomainDisableProposal.builder().entity(filled).interaction(interaction).build();
    execProposalEmitter.fire(proposal);
    SecurityDomain result = proposal.getEntity();
    SecurityDomain saved = gateway.update(original, result);
    SecurityDomainDisableEvent event =
        SecurityDomainDisableEvent.builder().payload(saved).interaction(interaction).build();
    executedEmitter.fire(event);
    return event.getPayload();
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  void flush() {
    cache.evict();
  }
}
