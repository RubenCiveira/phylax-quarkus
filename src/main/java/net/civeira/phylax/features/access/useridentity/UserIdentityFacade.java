package net.civeira.phylax.features.access.useridentity;

import java.util.HashSet;
import java.util.Set;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.inject.Instance;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.features.access.useridentity.command.UserIdentityWriteAttributes;
import net.civeira.phylax.features.access.useridentity.transition.create.UserIdentityCreatePipeline;
import net.civeira.phylax.features.access.useridentity.transition.delete.UserIdentityDeletePipeline;
import net.civeira.phylax.features.access.useridentity.transition.update.UserIdentityUpdatePipeline;

@ApplicationScoped
@RequiredArgsConstructor
public class UserIdentityFacade {

  /**
   * @autogenerated AggregateFacadeGenerator
   */
  private final UserIdentityCreatePipeline createPipeline;

  /**
   * @autogenerated AggregateFacadeGenerator
   */
  private final UserIdentityDeletePipeline deletePipeline;

  /**
   * @autogenerated AggregateFacadeGenerator
   */
  private final Instance<UserIdentityMetadataProvider> metadataProvider;

  /**
   * @autogenerated AggregateFacadeGenerator
   */
  private final UserIdentityUpdatePipeline updatePipeline;

  /**
   * The entity
   *
   * @autogenerated AggregateFacadeGenerator
   * @param attributes a filter to retrieve only matching values
   * @return The entity
   */
  public UserIdentity create(final UserIdentityWriteAttributes attributes) {
    return UserIdentity.create(attributes, createPipeline);
  }

  /**
   * The entity
   *
   * @autogenerated AggregateFacadeGenerator
   * @param entity a filter to retrieve only matching values
   * @return The entity
   */
  public UserIdentity delete(final UserIdentity entity) {
    return entity.delete(deletePipeline);
  }

  /**
   * @autogenerated AggregateFacadeGenerator
   * @return
   */
  public Set<String> readonlyFields() {
    Set<String> set = new HashSet<>();
    metadataProvider.forEach(meta -> set.addAll(meta.readonlyFields()));
    return set;
  }

  /**
   * The entity
   *
   * @autogenerated AggregateFacadeGenerator
   * @param base a filter to retrieve only matching values
   * @param attributes a filter to retrieve only matching values
   * @return The entity
   */
  public UserIdentity update(final UserIdentity base,
      final UserIdentityWriteAttributes attributes) {
    return base.update(attributes, updatePipeline);
  }
}
