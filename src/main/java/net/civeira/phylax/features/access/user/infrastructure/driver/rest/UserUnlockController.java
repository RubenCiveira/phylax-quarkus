@RequiredArgsConstructor
@ApplicationScoped
public class UserUnlockController {

  /**
   * @autogenerated ActionControllerGenerator
   */
  private final CurrentRequest currentRequest;

  /**
   * @autogenerated ActionControllerGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated ActionControllerGenerator
   */
  private final UserUnlockUsecase unlock;

  /**
   * @autogenerated ActionControllerGenerator
   * @param uids
   * @param search
   * @param name
   * @param tenant
   * @param tenants
   * @return
   */
  public Response userApiBatchUnlock(final List<String> uids, final String search,
      final String name, final String tenant, final List<String> tenants) {
    Span span = tracer.spanBuilder("Api to unlock on massive entitites of user")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      UserUnlockFilter.UserUnlockFilterBuilder filterBuilder = UserUnlockFilter.builder();
      filterBuilder =
          filterBuilder.uids(uids.stream().flatMap(part -> Stream.of(part.split(","))).toList());
      filterBuilder = filterBuilder.search(search);
      filterBuilder = filterBuilder.uids(
          null == uids ? null : uids.stream().flatMap(part -> Stream.of(part.split(","))).toList());
      filterBuilder = filterBuilder.search(search);
      filterBuilder = filterBuilder.name(name);
      if (null != tenant) {
        filterBuilder = filterBuilder.tenant(TenantReference.of(tenant));
      }
      filterBuilder = filterBuilder.tenants(null == tenants ? null
          : tenants.stream().flatMap(part -> Stream.of(part.split(","))).toList());
      UserUnlockFilter filter = filterBuilder.build();
      BatchIdentificator task = unlock.batchUnlock(currentRequest.interaction(), filter);
      /* .header("Last-Modified", value.getSince().format(DateTimeFormatter.RFC_1123_DATE_TIME)) */
      BatchTaskLocalizator response = new BatchTaskLocalizator();
      response.setUid(task.getUid());
      response.setSteps(task.getSteps());
      return Response.ok(response).build();
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated ActionControllerGenerator
   * @param batchId
   * @return
   */
  public Response userApiBatchUnlockQuery(final String batchId) {
    Span span = tracer.spanBuilder("Api to check masive unlock on entity of user")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      BatchProgress task = unlock.checkProgress(
          UserUnlockStatus.builder().taskId(batchId).build(currentRequest.interaction()));
      BatchTaskDetail response = new BatchTaskDetail();
      response.setUid(task.getUid());
      GlobalStatus taskStatus = task.getStatus();
      if (null != taskStatus) {
        response.setDetailStatus(DetailStatusEnum.valueOf(taskStatus.name()));
      }
      Instant taskStart = task.getStartTime();
      if (null != taskStart) {
        response.setStartTime(OffsetDateTime.ofInstant(taskStart, ZoneId.systemDefault()));
      }
      Instant taskEnd = task.getEndTime();
      if (null != taskEnd) {
        response.setEndTime(OffsetDateTime.ofInstant(taskEnd, ZoneId.systemDefault()));
      }
      response.setSteps(task.getSteps().stream().map(step -> {
        BatchStepProgress bsp = new BatchStepProgress();
        Status stepStatus = step.getStatus();
        if (null != stepStatus) {
          bsp.setStepStatus(StepStatusEnum.valueOf(stepStatus.name()));
        }
        Instant stepStart = step.getStartTime();
        if (null != stepStart) {
          bsp.setStartTime(OffsetDateTime.ofInstant(stepStart, ZoneId.systemDefault()));
        }
        Instant stepEnd = step.getEndTime();
        if (null != stepEnd) {
          bsp.setEndTime(OffsetDateTime.ofInstant(stepEnd, ZoneId.systemDefault()));
        }
        bsp.setError(step.getError());
        bsp.setName(step.getName());
        bsp.setOks(step.getOks());
        bsp.setProcessedItems(step.getProcessedItems());
        bsp.setTotalItems(step.getTotalItems());
        bsp.setErrors(new ArrayList<>());
        bsp.setWarns(new ArrayList<>());
        Map<String, ErrorInfo> stepErrors = step.getErrors();
        if (null != stepErrors) {
          bsp.getErrors().addAll(stepErrors.entrySet().stream()
              .map(entry -> new BatchResultErrorItem().item(entry.getKey())
                  .fails(entry.getValue().getFails().stream()
                      .map(fail -> new MultipleErrorCodes().code(fail.getCode())
                          .violation(fail.getViolation())
                          .wrongValues(fail.getWrongValues().stream()
                              .map(wv -> new ConstraintFail().error(wv.getErrorMessage())
                                  .field(wv.getField()).wrong(String.valueOf(wv.getWrongValue())))
                              .toList()))
                      .toList()))
              .toList());
        }
        Map<String, ErrorInfo> stepWarns = step.getWarns();
        if (null != stepWarns) {
          bsp.getWarns().addAll(stepErrors.entrySet().stream()
              .map(entry -> new BatchResultErrorItem().item(entry.getKey())
                  .fails(entry.getValue().getFails().stream()
                      .map(fail -> new MultipleErrorCodes().code(fail.getCode())
                          .violation(fail.getViolation())
                          .wrongValues(fail.getWrongValues().stream()
                              .map(wv -> new ConstraintFail().error(wv.getErrorMessage())
                                  .field(wv.getField()).wrong(String.valueOf(wv.getWrongValue())))
                              .toList()))
                      .toList()))
              .toList());
        }
        return bsp;
      }).toList());
      return Response.ok(response).build();
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated ActionControllerGenerator
   * @param uid
   * @return
   */
  @Transactional
  public Response userApiUnlock(final String uid) {
    Span span = tracer.spanBuilder("Api to unlock an entity of user")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      span.setAttribute("ref", uid);
      UserUnlockProjection updated =
          unlock.unlock(currentRequest.interaction(), UserReference.of(uid));
      return Response.ok(toApiModel(updated)).build();
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated ActionControllerGenerator
   * @param domainEnum
   * @return
   */
  private ApproveEnum approveEnumToApi(UserApproveOptions domainEnum) {
    ApproveEnum result;
    if (domainEnum == UserApproveOptions.UNVERIFIED) {
      result = ApproveEnum.UNVERIFIED;
    } else if (domainEnum == UserApproveOptions.PENDING) {
      result = ApproveEnum.PENDING;
    } else if (domainEnum == UserApproveOptions.ACCEPTED) {
      result = ApproveEnum.ACCEPTED;
    } else if (domainEnum == UserApproveOptions.REJECTED) {
      result = ApproveEnum.REJECTED;
    } else {
      result = null;
    }
    return result;
  }

  /**
   * @autogenerated ActionControllerGenerator
   * @param dto
   * @return
   */
  private UserApiDto toApiModel(UserUnlockProjection dto) {
    Span span =
        tracer.spanBuilder("Mapper to build api content to response from model for entity of user")
            .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
            .startSpan();
    try {
      UserApiDto userApiDto = new UserApiDto();
      userApiDto.setUid(dto.getUid());
      userApiDto.setTenant(new TenantApiRef().$ref(dto.getTenantReference()));
      userApiDto.setName(dto.getName());
      userApiDto.setPassword("*****");
      userApiDto.setEmail(dto.getEmail());
      userApiDto.setWellcomeAt(dto.getWellcomeAt());
      userApiDto.setEnabled(dto.getEnabled());
      userApiDto.setApprove(approveEnumToApi(dto.getApprove()));
      userApiDto.setTemporalPassword(dto.getTemporalPassword());
      userApiDto.setUseSecondFactors(dto.getUseSecondFactors());
      userApiDto.setSecondFactorSeed("*****");
      userApiDto.setBlockedUntil(dto.getBlockedUntil());
      userApiDto.setProvider(dto.getProvider());
      userApiDto.setVersion(dto.getVersion());
      return userApiDto;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }
}
