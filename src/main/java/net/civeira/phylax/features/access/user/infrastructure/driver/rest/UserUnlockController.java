package net.civeira.phylax.features.access.user.infrastructure.driver.rest;

import java.util.List;
import java.util.stream.Stream;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.transaction.Transactional;
import jakarta.ws.rs.core.Response;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.BatchIdentificator;
import net.civeira.phylax.common.batch.BatchProgress;
import net.civeira.phylax.common.infrastructure.CurrentRequest;
import net.civeira.phylax.features.access.tenant.domain.TenantReference;
import net.civeira.phylax.features.access.user.application.usecase.unlock.UserUnlockFilter;
import net.civeira.phylax.features.access.user.application.usecase.unlock.UserUnlockProjection;
import net.civeira.phylax.features.access.user.application.usecase.unlock.UserUnlockStatus;
import net.civeira.phylax.features.access.user.application.usecase.unlock.UserUnlockUsecase;
import net.civeira.phylax.features.access.user.domain.UserApproveOptions;
import net.civeira.phylax.features.access.user.domain.UserReference;
import net.civeira.phylax.generated.openapi.model.TenantApiRef;
import net.civeira.phylax.generated.openapi.model.UserApiDto;
import net.civeira.phylax.generated.openapi.model.UserApiDto.ApproveEnum;

@RequiredArgsConstructor
@ApplicationScoped
public class UserUnlockController {

  /**
   * @autogenerated ActionControllerGenerator
   */
  private final CurrentRequest currentRequest;

  /**
   * @autogenerated ActionControllerGenerator
   */
  private final UserUnlockUsecase unlock;

  /**
   * @autogenerated ActionControllerGenerator
   * @param uids
   * @param search
   * @param name
   * @param tenant
   * @param tenants
   * @return
   */
  public Response userApiBatchUnlock(final List<String> uids, final String search,
      final String name, final String tenant, final List<String> tenants) {
    UserUnlockFilter.UserUnlockFilterBuilder filterBuilder = UserUnlockFilter.builder();
    filterBuilder =
        filterBuilder.uids(uids.stream().flatMap(part -> Stream.of(part.split(","))).toList());
    filterBuilder = filterBuilder.search(search);
    filterBuilder = filterBuilder.uids(
        null == uids ? null : uids.stream().flatMap(part -> Stream.of(part.split(","))).toList());
    filterBuilder = filterBuilder.search(search);
    filterBuilder = filterBuilder.name(name);
    if (null != tenant) {
      filterBuilder = filterBuilder.tenant(TenantReference.of(tenant));
    }
    filterBuilder = filterBuilder.tenants(null == tenants ? null
        : tenants.stream().flatMap(part -> Stream.of(part.split(","))).toList());
    UserUnlockFilter filter = filterBuilder.build();
    BatchIdentificator task = unlock.unlock(currentRequest.interaction(), filter);
    /* .header("Last-Modified", value.getSince().format(DateTimeFormatter.RFC_1123_DATE_TIME)) */
    return Response.ok(task).build();
  }

  /**
   * @autogenerated ActionControllerGenerator
   * @param batchId
   * @return
   */
  public Response userApiBatchUnlockQuery(final String batchId) {
    BatchProgress task = unlock.checkProgress(
        UserUnlockStatus.builder().taskId(batchId).build(currentRequest.interaction()));
    return Response.ok(task).build();
  }

  /**
   * @autogenerated ActionControllerGenerator
   * @param uid
   * @return
   */
  @Transactional
  public Response userApiUnlock(final String uid) {
    UserUnlockProjection updated =
        unlock.unlock(currentRequest.interaction(), UserReference.of(uid));
    return Response.ok(toApiModel(updated)).build();
  }

  /**
   * @autogenerated ActionControllerGenerator
   * @param domainEnum
   * @return
   */
  private ApproveEnum approveEnumToApi(UserApproveOptions domainEnum) {
    ApproveEnum result;
    if (domainEnum == UserApproveOptions.UNVERIFIED) {
      result = ApproveEnum.UNVERIFIED;
    } else if (domainEnum == UserApproveOptions.PENDING) {
      result = ApproveEnum.PENDING;
    } else if (domainEnum == UserApproveOptions.ACCEPTED) {
      result = ApproveEnum.ACCEPTED;
    } else if (domainEnum == UserApproveOptions.REJECTED) {
      result = ApproveEnum.REJECTED;
    } else {
      result = null;
    }
    return result;
  }

  /**
   * @autogenerated ActionControllerGenerator
   * @param dto
   * @return
   */
  private UserApiDto toApiModel(UserUnlockProjection dto) {
    UserApiDto userApiDto = new UserApiDto();
    userApiDto.setUid(dto.getUid());
    userApiDto.setTenant(new TenantApiRef().$ref(dto.getTenantReference()));
    userApiDto.setName(dto.getName());
    userApiDto.setPassword("*****");
    userApiDto.setEmail(dto.getEmail());
    userApiDto.setWellcomeAt(dto.getWellcomeAt());
    userApiDto.setEnabled(dto.getEnabled());
    userApiDto.setApprove(approveEnumToApi(dto.getApprove()));
    userApiDto.setTemporalPassword(dto.getTemporalPassword());
    userApiDto.setUseSecondFactors(dto.getUseSecondFactors());
    userApiDto.setSecondFactorSeed("*****");
    userApiDto.setBlockedUntil(dto.getBlockedUntil());
    userApiDto.setProvider(dto.getProvider());
    userApiDto.setVersion(dto.getVersion());
    return userApiDto;
  }
}
