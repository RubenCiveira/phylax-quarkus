package net.civeira.phylax.features.access.trustedclient.infrastructure.bootstrap;

import java.util.ArrayList;
import java.util.List;
import java.util.Map;
import java.util.function.UnaryOperator;

import jakarta.enterprise.context.ApplicationScoped;
import net.civeira.phylax.common.infrastructure.projection.ExecutionAggregation;
import net.civeira.phylax.common.infrastructure.projection.ExecutionNode;
import net.civeira.phylax.common.infrastructure.projection.ParamKind;
import net.civeira.phylax.common.infrastructure.projection.ProjectionDescriptor;
import net.civeira.phylax.common.infrastructure.projection.RelationshipDefinition;
import net.civeira.phylax.features.access.securityscope.infrastructure.bootstrap.SecurityScopeProjectionDescriptor.SecurityScopeExecutionPlanner;
import net.civeira.phylax.features.access.useridentity.infrastructure.bootstrap.UserIdentityProjectionDescriptor.UserIdentityExecutionPlanner;

@ApplicationScoped
public class TrustedClientProjectionDescriptor implements ProjectionDescriptor {

  /**
   * @autogenerated ProjectionDescriptorGeneratorGenerator
   */
  public static class TrustedClientExecutionPlanner {

    /**
     * @autogenerated ProjectionDescriptorGeneratorGenerator
     */
    private final List<ExecutionAggregation> aggregation = new ArrayList<>();

    /**
     * @autogenerated ProjectionDescriptorGeneratorGenerator
     */
    String prefix;

    /**
     * @autogenerated ProjectionDescriptorGeneratorGenerator
     */
    public TrustedClientExecutionPlanner() {
      this.prefix = "";
    }

    /**
     * @autogenerated ProjectionDescriptorGeneratorGenerator
     * @param prefix
     */
    public TrustedClientExecutionPlanner(final String prefix) {
      this.prefix = prefix + ".";
    }

    /**
     * @autogenerated ProjectionDescriptorGeneratorGenerator
     * @return
     */
    public List<ExecutionAggregation> build() {
      return aggregation;
    }

    /**
     * @autogenerated ProjectionDescriptorGeneratorGenerator
     * @return
     */
    public TrustedClientExecutionPlanner withAllowedRedirects() {
      return withAllowedRedirects(prefix + "allowedRedirects");
    }

    /**
     * @autogenerated ProjectionDescriptorGeneratorGenerator
     * @param alias
     * @return
     */
    public TrustedClientExecutionPlanner withAllowedRedirects(final String alias) {
      aggregation.add(ExecutionAggregation.builder().name("allowedRedirects").alias(alias)
          .selection(List.of()).build());
      return this;
    }

    /**
     * @autogenerated ProjectionDescriptorGeneratorGenerator
     * @return
     */
    public TrustedClientExecutionPlanner withCode() {
      return withCode(prefix + "code");
    }

    /**
     * @autogenerated ProjectionDescriptorGeneratorGenerator
     * @param alias
     * @return
     */
    public TrustedClientExecutionPlanner withCode(final String alias) {
      aggregation.add(
          ExecutionAggregation.builder().name("code").alias(alias).selection(List.of()).build());
      return this;
    }

    /**
     * @autogenerated ProjectionDescriptorGeneratorGenerator
     * @return
     */
    public TrustedClientExecutionPlanner withEnabled() {
      return withEnabled(prefix + "enabled");
    }

    /**
     * @autogenerated ProjectionDescriptorGeneratorGenerator
     * @param alias
     * @return
     */
    public TrustedClientExecutionPlanner withEnabled(final String alias) {
      aggregation.add(
          ExecutionAggregation.builder().name("enabled").alias(alias).selection(List.of()).build());
      return this;
    }

    /**
     * @autogenerated ProjectionDescriptorGeneratorGenerator
     * @return
     */
    public TrustedClientExecutionPlanner withPublicAllow() {
      return withPublicAllow(prefix + "publicAllow");
    }

    /**
     * @autogenerated ProjectionDescriptorGeneratorGenerator
     * @param alias
     * @return
     */
    public TrustedClientExecutionPlanner withPublicAllow(final String alias) {
      aggregation.add(ExecutionAggregation.builder().name("publicAllow").alias(alias)
          .selection(List.of()).build());
      return this;
    }

    /**
     * @autogenerated ProjectionDescriptorGeneratorGenerator
     * @return
     */
    public TrustedClientExecutionPlanner withSecretOauth() {
      return withSecretOauth(prefix + "secretOauth");
    }

    /**
     * @autogenerated ProjectionDescriptorGeneratorGenerator
     * @param alias
     * @return
     */
    public TrustedClientExecutionPlanner withSecretOauth(final String alias) {
      aggregation.add(ExecutionAggregation.builder().name("secretOauth").alias(alias)
          .selection(List.of()).build());
      return this;
    }

    /**
     * @autogenerated ProjectionDescriptorGeneratorGenerator
     * @return
     */
    public TrustedClientExecutionPlanner withSecurityScope() {
      return withSecurityScope(prefix + "securityScope", null);
    }

    /**
     * @autogenerated ProjectionDescriptorGeneratorGenerator
     * @param alias
     * @return
     */
    public TrustedClientExecutionPlanner withSecurityScope(final String alias) {
      return withSecurityScope(alias, null);
    }

    /**
     * @autogenerated ProjectionDescriptorGeneratorGenerator
     * @param callback
     * @return
     */
    public TrustedClientExecutionPlanner withSecurityScope(
        final UnaryOperator<SecurityScopeExecutionPlanner> callback) {
      return withSecurityScope(prefix + "securityScope", callback);
    }

    /**
     * @autogenerated ProjectionDescriptorGeneratorGenerator
     * @param alias
     * @param callback
     * @return
     */
    public TrustedClientExecutionPlanner withSecurityScope(final String alias,
        final UnaryOperator<SecurityScopeExecutionPlanner> callback) {
      aggregation.add(ExecutionAggregation.builder().name("securityScope").alias(alias)
          .selection(null != callback
              ? callback.apply(new SecurityScopeExecutionPlanner(prefix + alias)).build()
              : List.of())
          .build());
      return this;
    }

    /**
     * @autogenerated ProjectionDescriptorGeneratorGenerator
     * @return
     */
    public TrustedClientExecutionPlanner withUid() {
      return withUid(prefix + "uid");
    }

    /**
     * @autogenerated ProjectionDescriptorGeneratorGenerator
     * @param alias
     * @return
     */
    public TrustedClientExecutionPlanner withUid(final String alias) {
      aggregation.add(
          ExecutionAggregation.builder().name("uid").alias(alias).selection(List.of()).build());
      return this;
    }

    /**
     * @autogenerated ProjectionDescriptorGeneratorGenerator
     * @return
     */
    public TrustedClientExecutionPlanner withUserIdentity() {
      return withUserIdentity(prefix + "userIdentity", null);
    }

    /**
     * @autogenerated ProjectionDescriptorGeneratorGenerator
     * @param alias
     * @return
     */
    public TrustedClientExecutionPlanner withUserIdentity(final String alias) {
      return withUserIdentity(alias, null);
    }

    /**
     * @autogenerated ProjectionDescriptorGeneratorGenerator
     * @param callback
     * @return
     */
    public TrustedClientExecutionPlanner withUserIdentity(
        final UnaryOperator<UserIdentityExecutionPlanner> callback) {
      return withUserIdentity(prefix + "userIdentity", callback);
    }

    /**
     * @autogenerated ProjectionDescriptorGeneratorGenerator
     * @param alias
     * @param callback
     * @return
     */
    public TrustedClientExecutionPlanner withUserIdentity(final String alias,
        final UnaryOperator<UserIdentityExecutionPlanner> callback) {
      aggregation.add(ExecutionAggregation.builder().name("userIdentity").alias(alias)
          .selection(null != callback
              ? callback.apply(new UserIdentityExecutionPlanner(prefix + alias)).build()
              : List.of())
          .build());
      return this;
    }

    /**
     * @autogenerated ProjectionDescriptorGeneratorGenerator
     * @return
     */
    public TrustedClientExecutionPlanner withVersion() {
      return withVersion(prefix + "version");
    }

    /**
     * @autogenerated ProjectionDescriptorGeneratorGenerator
     * @param alias
     * @return
     */
    public TrustedClientExecutionPlanner withVersion(final String alias) {
      aggregation.add(
          ExecutionAggregation.builder().name("version").alias(alias).selection(List.of()).build());
      return this;
    }
  }

  /**
   * @autogenerated ProjectionDescriptorGeneratorGenerator
   * @param baseServer
   * @return
   */
  @Override
  public List<ExecutionNode> baseNodes(final String baseServer) {
    Map<String, RelationshipDefinition> relations = relations(baseServer);
    return List.of(
        ExecutionNode.builder().server(baseServer).endpoint("/api/access/trusted-clients")
            .method("GET").list(true).params(Map.of()).relations(relations).build(),
        ExecutionNode.builder().server(baseServer).endpoint("/api/access/trusted-clients/{uid}")
            .method("GET").list(false).params(Map.of("uid", ParamKind.PATH)).relations(relations)
            .build());
  }

  /**
   * @autogenerated ProjectionDescriptorGeneratorGenerator
   * @param baseServer
   * @return
   */
  private Map<String, RelationshipDefinition> relations(final String baseServer) {
    return Map.of("userIdentity",
        RelationshipDefinition.builder().list(true).id("/api/access/user-identities")
            .url(baseServer + "/api/access/user-identities").method("GET")
            .batchParam("trusted-clients").referenceField("trusted-client").build(),
        "securityScope",
        RelationshipDefinition.builder().list(true).id("/api/access/security-scopes")
            .url(baseServer + "/api/access/security-scopes").method("GET")
            .batchParam("trusted-clients").referenceField("trusted-client").build());
  }
}
