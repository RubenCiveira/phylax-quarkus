package net.civeira.phylax.features.access.trustedclient.application.usecase.create;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.SpanKind;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Context;
import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.trustedclient.application.visibility.TrustedClientsVisibility;
import net.civeira.phylax.features.access.trustedclient.domain.TrustedClient;
import net.civeira.phylax.features.access.trustedclient.domain.TrustedClientChangeSet;
import net.civeira.phylax.features.access.trustedclient.domain.gateway.TrustedClientAuditGateway;
import net.civeira.phylax.features.access.trustedclient.domain.gateway.TrustedClientCacheGateway;
import net.civeira.phylax.features.access.trustedclient.domain.gateway.TrustedClientWriteRepositoryGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class TrustedClientCreateUsecase {

  /**
   * Service to register audit events
   *
   * @autogenerated CreateUsecaseGenerator
   */
  private final TrustedClientAuditGateway audit;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final TrustedClientCacheGateway cache;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Event<TrustedClientCreateAllowDecision> createAllowEmitter;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Event<TrustedClientCreateCheck> createCheckEmitter;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Event<TrustedClientCreateEnrich> createEnrichEmitter;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final TrustedClientWriteRepositoryGateway gateway;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final TrustedClientsVisibility visibility;

  /**
   * @autogenerated CreateUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    Span span = tracer.spanBuilder("Checking if is allowed to create an entity of trusted client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      Allow response = checkAllow(query);
      span.setAttribute("allowed", response.isAllowed());
      return response;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated CreateUsecaseGenerator
   * @param query
   * @param input
   * @return The slide with some values
   */
  public TrustedClientCreateProjection create(final Interaction query,
      final TrustedClientCreateInput input) {
    Span span = tracer.spanBuilder("Creating an entity of trusted client")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      // Check if the user can perform the action
      Allow detail = checkAllow(query);
      if (!detail.isAllowed()) {
        span.addEvent(
            "The policies for trusted client dont allow to create: " + detail.getDescription());
        throw new NotAllowedException(detail.getDescription());
      }
      // Append the fixed fields to the requested fields
      TrustedClientCreateInput filled = TrustedClientCreateInput
          .fromChangeSet(visibility.copyWithFixed(query, input.toChangeSet()));
      span.addEvent(
          "The input value is filled with the fixed values for the creation of trusted client");
      // Pass bussines rules to validate the the requested data filled
      TrustedClientCreateCheck check =
          TrustedClientCreateCheck.builder().interaction(query).input(filled).build();
      createCheckEmitter.fire(check);
      span.addEvent(
          "The input value is checked with the policies for the creation of trusted client");
      // Pass bussines rules to calculate derivated fields for the requested data filled
      TrustedClientCreateEnrich proposal =
          TrustedClientCreateEnrich.builder().interaction(query).input(filled).build();
      createEnrichEmitter.fire(proposal);
      // Convert the result input request data to a entity change set
      TrustedClientChangeSet dto = proposal.getInput().toChangeSet();
      // Run the creation from the domain model
      TrustedClient entity = TrustedClient.create(dto);
      span.addEvent("A created entity for trusted client is ready");
      // Use the write gateway to store the entity on a persistent way
      TrustedClient created =
          gateway.create(entity, attempt -> visibility.checkVisibility(query, attempt.getUid()));
      span.addEvent("The created entity of trusted client was saved");
      // Update the cache
      cache.update(created);
      // Register audit operation
      audit.created("create", created, query);
      // Convert the entity into a proyection and filter non visible fields
      return TrustedClientCreateProjection.from(visibility.copyWithHidden(query, created));
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated CreateUsecaseGenerator
   * @param query
   * @return
   */
  private Allow checkAllow(final Interaction query) {
    TrustedClientCreateAllowDecision proposal = TrustedClientCreateAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allow by default").build()).query(query)
        .build();
    createAllowEmitter.fire(proposal);
    return proposal.getDetail();
  }
}
