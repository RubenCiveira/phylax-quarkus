package net.civeira.phylax.features.access.tenant.application.usecase.update;

import java.util.Optional;
import java.util.function.Consumer;
import java.util.function.UnaryOperator;

import lombok.Builder;
import lombok.Data;
import lombok.EqualsAndHashCode;
import lombok.NonNull;
import lombok.With;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.tenant.TenantRef;
import net.civeira.phylax.features.access.tenant.application.request.TenantStateChange;

@Data
@Builder(toBuilder = true)
@With
@EqualsAndHashCode(onlyExplicitlyIncluded = true)
public class TenantUpdateProposal {

  /**
   * @autogenerated UpdateProposalGenerator
   */
  @NonNull
  private final Interaction interaction;

  /**
   * @autogenerated UpdateProposalGenerator
   */
  private final TenantRef reference;

  /**
   * @autogenerated UpdateProposalGenerator
   */
  @NonNull
  private TenantStateChange dto;

  /**
   * @autogenerated UpdateProposalGenerator
   * @return
   */
  public Optional<TenantRef> getReference() {
    return Optional.ofNullable(reference);
  }

  /**
   * @autogenerated UpdateProposalGenerator
   * @param mapper
   */
  public void map(UnaryOperator<TenantStateChange> mapper) {
    dto = mapper.apply(dto);
  }

  /**
   * @autogenerated UpdateProposalGenerator
   * @param mapper
   */
  public void peek(Consumer<TenantStateChange> mapper) {
    mapper.accept(dto);
  }
}
