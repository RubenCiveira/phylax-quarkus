package net.civeira.phylax.features.access.tenantloginprovider.application.usecase.list;

import java.util.List;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.SpanKind;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Context;
import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.algorithms.metadata.TimestampedList;
import net.civeira.phylax.common.algorithms.metadata.WrapMetadata;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.tenantloginprovider.application.visibility.TenantLoginProviderVisibilityFilter;
import net.civeira.phylax.features.access.tenantloginprovider.application.visibility.TenantLoginProvidersVisibility;
import net.civeira.phylax.features.access.tenantloginprovider.domain.gateway.TenantLoginProviderCached;
import net.civeira.phylax.features.access.tenantloginprovider.domain.gateway.TenantLoginProviderCursor;
import net.civeira.phylax.features.access.tenantloginprovider.domain.gateway.TenantLoginProviderOrder;

@ApplicationScoped
@RequiredArgsConstructor
public class TenantLoginProviderListUsecase {

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final Event<TenantLoginProviderListAllowDecision> listAllowEmitter;

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final TenantLoginProvidersVisibility visibility;

  /**
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    Span span =
        tracer.spanBuilder("Checking if is allowed to list entities of tenant login provider")
            .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
            .startSpan();
    try {
      Allow response = checkAllow(query);
      span.setAttribute("allowed", response.isAllowed());
      return response;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @param filter
   * @param cursor
   * @return The slide with some values
   */
  public List<TenantLoginProviderListProjection> list(final Interaction query,
      final TenantLoginProviderListFilter filter, final TenantLoginProviderListCursor cursor) {
    Span span = tracer.spanBuilder("Listing an entity of tenant login provider")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      Allow detail = checkAllow(query);
      if (!detail.isAllowed()) {
        span.addEvent("The policies for tenant login provider dont allow to list: "
            + detail.getDescription());
        throw new NotAllowedException(detail.getDescription());
      }
      TenantLoginProviderVisibilityFilter visibleFilter = TenantLoginProviderVisibilityFilter
          .builder().uid(filter.getUid().orElse(null)).uids(filter.getUids().stream().toList())
          .search(filter.getSearch().orElse(null)).name(filter.getName().orElse(null))
          .tenant(filter.getTenant().orElse(null)).tenants(filter.getTenants())
          .tenantTenantAccesible(filter.getTenantTenantAccesible().orElse(null)).build();
      TenantLoginProviderCursor gatewayCursor = TenantLoginProviderCursor.builder()
          .limit(cursor.getLimit().orElse(null)).sinceUid(cursor.getSinceUid().orElse(null))
          .order(cursor.getOrder().stream().map(Object::toString)
              .map(TenantLoginProviderOrder::valueOf).toList())
          .sinceName(cursor.getSinceName().orElse(null))
          .sinceName(cursor.getSinceName().orElse(null)).build();
      TenantLoginProviderCached values =
          visibility.listCachedVisibles(query, visibleFilter, gatewayCursor);
      List<TenantLoginProviderListProjection> list =
          values.getValue().stream().map(value -> visibility.copyWithHidden(query, value))
              .map(TenantLoginProviderListProjection::from).toList();
      return new TimestampedList<>(WrapMetadata.<List<TenantLoginProviderListProjection>>builder()
          .data(list).since(values.getSince().toInstant()).build());
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @return
   */
  private Allow checkAllow(final Interaction query) {
    TenantLoginProviderListAllowDecision proposal = TenantLoginProviderListAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allow by default").build()).query(query)
        .build();
    listAllowEmitter.fire(proposal);
    return proposal.getDetail();
  }
}
