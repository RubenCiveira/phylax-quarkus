package net.civeira.phylax.features.access.tenanttermsofuse.transition.create;

import java.util.ArrayList;
import java.util.List;
import java.util.function.Function;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.inject.Instance;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.features.access.tenanttermsofuse.TenantTermsOfUse;
import net.civeira.phylax.features.access.tenanttermsofuse.command.TenantTermsOfUseWriteAttributes;
import net.civeira.phylax.features.access.tenanttermsofuse.transition.TenantTermsOfUseChangeProposal;

@ApplicationScoped
@RequiredArgsConstructor
public class TenantTermsOfUseCreatePipeline {

  /**
   * @autogenerated UpdateEntityRuleGenerator
   */
  private final Instance<TenantTermsOfUseCreateStep> steps;

  /**
   * @autogenerated UpdateEntityRuleGenerator
   * @param attributes
   * @param last
   * @return
   */
  public TenantTermsOfUse create(final TenantTermsOfUseWriteAttributes attributes,
      final Function<TenantTermsOfUseChangeProposal, TenantTermsOfUse> last) {
    Function<TenantTermsOfUseCreateTransition, TenantTermsOfUse> finalHandler = t -> {
      if (t.hasErrors()) {
        throw t.asConstraintException();
      }
      return last.apply(t.getData());
    };
    List<TenantTermsOfUseCreateStep> reversed = new ArrayList<>(steps.stream().toList()).reversed();
    Function<TenantTermsOfUseCreateTransition, TenantTermsOfUse> pipeline =
        reversed.stream().reduce(finalHandler,
            (next, step) -> transition -> step.create(transition, next), (f1, f2) -> f1);
    return pipeline.apply(TenantTermsOfUseCreateTransition.builder()
        .data(new TenantTermsOfUseChangeProposal(attributes)).build());
  }
}
