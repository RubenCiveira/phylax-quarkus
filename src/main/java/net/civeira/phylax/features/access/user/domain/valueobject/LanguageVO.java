package net.civeira.phylax.features.access.user.domain.valueobject;

import java.util.Optional;

import lombok.EqualsAndHashCode;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.ConstraintException;
import net.civeira.phylax.common.value.validation.AbstractFailList;
import net.civeira.phylax.common.value.validation.ConstraintFail;
import net.civeira.phylax.common.value.validation.ConstraintFailList;

/**
 * Value Object representing el language de user of user.
 * <p>
 * This class ensures that the language is a {@code String} and encapsulates its validation and
 * formatting logic. It is immutable and compliant with DDD (Domain-Driven Design) principles.
 * </p>
 * <p>
 * Use {@link #from(String)} or {@link #tryFrom(Object)} to safely create instances. Validation
 * errors are reported via {@link ConstraintFailList} or thrown as {@link ConstraintException}.
 * </p>
 */
@RequiredArgsConstructor
@EqualsAndHashCode
public class LanguageVO {

  /**
   * Crear an empty instance with no value
   *
   * @autogenerated ValueObjectGenerator
   * @param language
   * @return An empty instance
   */
  public static LanguageVO from(final String language) {
    return tryFrom(language);
  }

  /**
   * Crear an empty instance with no value
   *
   * @autogenerated ValueObjectGenerator
   * @return An empty instance
   */
  public static LanguageVO nullValue() {
    return new LanguageVO(null);
  }

  /**
   * Crear an empty instance with no value
   *
   * @autogenerated ValueObjectGenerator
   * @param language
   * @return An empty instance
   */
  public static LanguageVO tryFrom(final Object language) {
    ConstraintFailList list = new ConstraintFailList();
    LanguageVO result = tryFrom(language, list);
    if (list.hasErrors()) {
      throw new ConstraintException(list);
    }
    return result;
  }

  /**
   * Crear an empty instance with no value
   *
   * @autogenerated ValueObjectGenerator
   * @param language temptative value
   * @param fails Error list
   * @return An empty instance
   */
  public static <T extends AbstractFailList> LanguageVO tryFrom(final Object language,
      final T fails) {
    if (null == language) {
      return new LanguageVO(null);
    } else if (language instanceof String castedLanguage) {
      return new LanguageVO(castedLanguage);
    } else {
      fails.add(new ConstraintFail("wrong-type", "language", language.getClass(),
          "A String type is expected for language"));
      return null;
    }
  }

  /**
   * The actual value of the name.
   *
   * @autogenerated ValueObjectGenerator
   */
  private final String value;

  /**
   * Get the vo value
   *
   * @autogenerated ValueObjectGenerator
   * @return
   */
  public Optional<String> getLanguage() {
    return Optional.ofNullable(value);
  }

  /**
   * Returns a formatted string representation of the name.
   *
   * @autogenerated ValueObjectGenerator
   * @return the string value wrapped in square brackets.
   */
  @Override
  public String toString() {
    return "[" + value + "]";
  }
}
