package net.civeira.phylax.features.access.useridentity.domain;

import lombok.NoArgsConstructor;
import lombok.experimental.Delegate;
import net.civeira.phylax.common.value.Uuid;
import net.civeira.phylax.features.access.useridentity.domain.valueobject.RelyingPartyValueHolder;
import net.civeira.phylax.features.access.useridentity.domain.valueobject.RolesValueHolder;
import net.civeira.phylax.features.access.useridentity.domain.valueobject.TrustedClientValueHolder;
import net.civeira.phylax.features.access.useridentity.domain.valueobject.UidValueHolder;
import net.civeira.phylax.features.access.useridentity.domain.valueobject.UserValueHolder;
import net.civeira.phylax.features.access.useridentity.domain.valueobject.VersionValueHolder;

/**
 * A dto transfer to hold user identity attribute values
 */
@NoArgsConstructor
public class UserIdentityChangeSet {

  /**
   * El relying party de user identity
   *
   * @autogenerated EntityChangeSetGenerator
   */
  @Delegate
  private RelyingPartyValueHolder<UserIdentityChangeSet> relyingPartyValue =
      new RelyingPartyValueHolder<>(this);

  /**
   * El roles de user identity
   *
   * @autogenerated EntityChangeSetGenerator
   */
  @Delegate
  private RolesValueHolder<UserIdentityChangeSet> rolesValue = new RolesValueHolder<>(this);

  /**
   * El trusted client de user identity
   *
   * @autogenerated EntityChangeSetGenerator
   */
  @Delegate
  private TrustedClientValueHolder<UserIdentityChangeSet> trustedClientValue =
      new TrustedClientValueHolder<>(this);

  /**
   * El identificador de la aplicacion
   *
   * @autogenerated EntityChangeSetGenerator
   */
  @Delegate
  private UidValueHolder<UserIdentityChangeSet> uidValue = new UidValueHolder<>(this);

  /**
   * El user de user identity
   *
   * @autogenerated EntityChangeSetGenerator
   */
  @Delegate
  private UserValueHolder<UserIdentityChangeSet> userValue = new UserValueHolder<>(this);

  /**
   * Campo con el n√∫mero de version de user identity para controlar bloqueos optimistas
   *
   * @autogenerated EntityChangeSetGenerator
   */
  @Delegate
  private VersionValueHolder<UserIdentityChangeSet> versionValue = new VersionValueHolder<>(this);

  /**
   * @autogenerated EntityChangeSetGenerator
   * @return
   */
  public UserIdentityChangeSet newUid() {
    return version(0).uid(Uuid.comb().toString());
  }

  /**
   * Unset a value by the property name. Unset values will no provide command change neither provide
   * information to the output context (hidden or readonly fields, or policy restrictions).
   *
   * @autogenerated EntityChangeSetGenerator
   * @param field The field name to unset.
   */
  public void unset(final String field) {
    uidValue.unsetUidIfMatches(field);
    userValue.unsetUserIfMatches(field);
    relyingPartyValue.unsetRelyingPartyIfMatches(field);
    trustedClientValue.unsetTrustedClientIfMatches(field);
    rolesValue.unsetRolesIfMatches(field);
    versionValue.unsetVersionIfMatches(field);
  }
}
