package net.civeira.phylax.features.access.user.application.usecase.accept;

import java.time.Duration;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.SpanKind;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Context;
import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.BatchIdentificator;
import net.civeira.phylax.common.batch.BatchProgress;
import net.civeira.phylax.common.batch.BatchService;
import net.civeira.phylax.common.batch.ExecutorByDeferSteps;
import net.civeira.phylax.common.batch.ExecutorPlan;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.user.application.visibility.UserVisibilityFilter;
import net.civeira.phylax.features.access.user.application.visibility.UsersVisibility;
import net.civeira.phylax.features.access.user.domain.User;
import net.civeira.phylax.features.access.user.domain.UserRef;
import net.civeira.phylax.features.access.user.domain.gateway.UserAuditGateway;
import net.civeira.phylax.features.access.user.domain.gateway.UserCacheGateway;
import net.civeira.phylax.features.access.user.domain.gateway.UserWriteRepositoryGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class UserAcceptUsecase {

  /**
   * Service to register audit events
   *
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final UserAuditGateway audit;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final BatchService batch;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final UserCacheGateway cache;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final Event<UserAcceptAllowDecision> execAllowEmitter;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final Event<UserAcceptCheck> execProposalEmitter;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final UserWriteRepositoryGateway gateway;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  private final UsersVisibility visibility;

  /**
   * Recover a slide of data.
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param reference
   * @return The slide with some values
   */
  public UserAcceptProjection accept(final Interaction query, final UserRef reference) {
    Span span = tracer.spanBuilder("accept an entity of user")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      span.setAttribute("ref", reference.getUid());
      Allow detail = checkAllow(query, reference);
      if (!detail.isAllowed()) {
        span.addEvent("The policies for user dont allow to accept: " + detail.getDescription());
        throw new NotAllowedException(detail.getDescription());
      }
      User original = visibility.retrieveVisibleForUpdate(query, reference.getUid())
          .orElseThrow(() -> new NotFoundException(""));
      User saved = accept(query, original);
      flush();
      // Register audit operation
      audit.updated("accept", saved, original, query);
      return UserAcceptProjection.from(visibility.copyWithHidden(query, saved));
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param filter
   * @return The slide with some values
   */
  public BatchIdentificator accept(final Interaction query, final UserAcceptFilter filter) {
    Span span = tracer.spanBuilder("accept a set of entities of user")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      Allow detail = checkAllow(query);
      if (!detail.isAllowed()) {
        span.addEvent("The policies for user dont allow to accept: " + detail.getDescription());
        throw new NotAllowedException(detail.getDescription());
      }
      UserVisibilityFilter filterWithVisibility =
          UserVisibilityFilter.builder().uid(filter.getUid().orElse(null))
              .uids(filter.getUids().stream().toList()).search(filter.getSearch().orElse(null))
              .nameOrEmail(filter.getNameOrEmail().orElse(null)).name(filter.getName().orElse(null))
              .tenant(filter.getTenant().orElse(null)).tenants(filter.getTenants())
              .tenantTenantAccesible(filter.getTenantTenantAccesible().orElse(null)).build();
      UserAcceptAllInBatchCommand command = UserAcceptAllInBatchCommand.builder().interaction(query)
          .filter(filterWithVisibility).build();
      return batch.start(command.getInteraction().getActor().getName().orElse("-"),
          Duration.ofHours(6),
          ExecutorPlan.<UserAcceptAllInBatchCommand>builder().params(command).name("accept-user")
              .executor(
                  ExecutorByDeferSteps.<User, User, UserAcceptAllInBatchCommand, UserAcceptsInBatchExecutor.UserPaginableBatch>builder()
                      .initializer(UserAcceptsInBatchExecutor.class)
                      .counter(UserAcceptsInBatchExecutor.class)
                      .descriptor(UserAcceptsInBatchExecutor.class)
                      .reader(UserAcceptsInBatchExecutor.class)
                      .processor(UserAcceptsInBatchExecutor.class)
                      .writer(UserAcceptsInBatchExecutor.class)
                      .finalizer(UserAcceptsInBatchExecutor.class).build())
              .build());
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  public Allow allow(final Interaction query, final UserRef reference) {
    Span span = tracer.spanBuilder("Checking if is allowed to update an entity of user")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      span.setAttribute("ref", reference.getUid());
      Allow response = checkAllow(query, reference);
      span.setAttribute("allowed", response.isAllowed());
      return response;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    Span span = tracer.spanBuilder("Checking if is allowed to update an entity of user")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      Allow response = checkAllow(query);
      span.setAttribute("allowed", response.isAllowed());
      return response;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query a filter to retrieve only matching values
   * @return The slide with some values
   */
  public BatchProgress checkProgress(final UserAcceptStatus query) {
    return query.getActor().getName()
        .flatMap(name -> batch.retrieve(query.getTaskId(), query.getConnection().getLocale(), name))
        .orElseThrow(() -> new NotFoundException());
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param interaction
   * @param original
   * @return
   */
  User accept(final Interaction interaction, final User original) {
    UserAcceptCheck proposal =
        UserAcceptCheck.builder().reference(original).interaction(interaction).build();
    execProposalEmitter.fire(proposal);
    User result = original.accept();
    User saved = gateway.update(original, result);
    return saved;
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  Allow checkAllow(final Interaction query, final UserRef reference) {
    UserAcceptAllowDecision base = UserAcceptAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).reference(reference).build();
    execAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   * @param query
   * @return
   */
  Allow checkAllow(final Interaction query) {
    UserAcceptAllowDecision base = UserAcceptAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).build();
    execAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated ActionChangeUsecaseGenerator
   */
  void flush() {
    cache.evict();
  }
}
