package net.civeira.phylax.features.access.relyingparty.application.usecase.enable;

import java.util.List;

import io.quarkus.arc.Unremovable;
import jakarta.enterprise.context.ApplicationScoped;
import lombok.Data;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.batch.stepper.ItemDescriptor;
import net.civeira.phylax.common.batch.stepper.ItemProcessor;
import net.civeira.phylax.common.batch.stepper.ItemReader;
import net.civeira.phylax.common.batch.stepper.ItemWriter;
import net.civeira.phylax.common.batch.stepper.StepContext;
import net.civeira.phylax.common.batch.stepper.StepCounter;
import net.civeira.phylax.common.batch.stepper.StepFinalizer;
import net.civeira.phylax.common.batch.stepper.StepInitializer;
import net.civeira.phylax.common.exception.ExecutionException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.features.access.relyingparty.application.visibility.RelyingPartysVisibility;
import net.civeira.phylax.features.access.relyingparty.domain.RelyingParty;
import net.civeira.phylax.features.access.relyingparty.domain.gateway.RelyingPartyCursor;

@Unremovable
@ApplicationScoped
@RequiredArgsConstructor
class RelyingPartyEnablesInBatchExecutor implements
    StepCounter<RelyingPartyEnableAllInBatchCommand, RelyingPartyEnablesInBatchExecutor.RelyingPartyPaginableBatch>,
    StepFinalizer<RelyingPartyEnableAllInBatchCommand, RelyingPartyEnablesInBatchExecutor.RelyingPartyPaginableBatch>,
    StepInitializer<RelyingPartyEnableAllInBatchCommand, RelyingPartyEnablesInBatchExecutor.RelyingPartyPaginableBatch>,
    ItemReader<RelyingParty, RelyingPartyEnableAllInBatchCommand, RelyingPartyEnablesInBatchExecutor.RelyingPartyPaginableBatch>,
    ItemWriter<RelyingParty, RelyingPartyEnableAllInBatchCommand, RelyingPartyEnablesInBatchExecutor.RelyingPartyPaginableBatch>,
    ItemProcessor<RelyingParty, RelyingParty, RelyingPartyEnableAllInBatchCommand, RelyingPartyEnablesInBatchExecutor.RelyingPartyPaginableBatch>,
    ItemDescriptor<RelyingParty, RelyingPartyEnableAllInBatchCommand, RelyingPartyEnablesInBatchExecutor.RelyingPartyPaginableBatch> {

  /**
   * @autogenerated EntityGenerator
   */
  @Data
  public static class RelyingPartyPaginableBatch {

    /**
     * @autogenerated EntityGenerator
     */
    private String since;
  }

  /**
   * @autogenerated EntityGenerator
   */
  private final int size = 10;

  /**
   * @autogenerated EntityGenerator
   */
  private final RelyingPartyEnableUsecase usecase;

  /**
   * @autogenerated EntityGenerator
   */
  private final RelyingPartysVisibility visibility;

  /**
   * @autogenerated EntityGenerator
   * @param context
   * @return
   */
  @Override
  public long approximatedItems(
      final StepContext<RelyingPartyEnableAllInBatchCommand, RelyingPartyPaginableBatch> context) {
    return visibility.countVisibles(context.getParams().getInteraction(),
        context.getParams().getFilter());
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   */
  @Override
  public void finish(
      final StepContext<RelyingPartyEnableAllInBatchCommand, RelyingPartyPaginableBatch> context) {
    usecase.flush();
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   */
  @Override
  public void init(
      final StepContext<RelyingPartyEnableAllInBatchCommand, RelyingPartyPaginableBatch> context) {
    context.setState(new RelyingPartyPaginableBatch());
  }

  /**
   * @autogenerated EntityGenerator
   * @param item
   * @param context
   * @return
   */
  @Override
  public String itemDescription(final RelyingParty item,
      final StepContext<RelyingPartyEnableAllInBatchCommand, RelyingPartyPaginableBatch> context) {
    return item.getUid();
  }

  /**
   * @autogenerated EntityGenerator
   * @param item
   * @param context
   * @return
   */
  @Override
  public RelyingParty process(final RelyingParty item,
      final StepContext<RelyingPartyEnableAllInBatchCommand, RelyingPartyPaginableBatch> context) {
    Allow allowed = usecase.allow(context.getParams().getInteraction(), item);
    if (!allowed.isAllowed()) {
      throw new ExecutionException("not-allowed", null);
    }
    return item;
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   * @return
   */
  @Override
  public List<RelyingParty> read(
      final StepContext<RelyingPartyEnableAllInBatchCommand, RelyingPartyPaginableBatch> context) {
    List<RelyingParty> page = visibility.listVisiblesForUpdate(context.getParams().getInteraction(),
        context.getParams().getFilter(),
        RelyingPartyCursor.builder().limit(size).sinceUid(context.getState().getSince()).build());
    context.getState().setSince(page.isEmpty() ? null : page.get(page.size() - 1).getUid());
    return page;
  }

  /**
   * @autogenerated EntityGenerator
   * @param items
   * @param context
   */
  @Override
  public void write(final List<RelyingParty> items,
      final StepContext<RelyingPartyEnableAllInBatchCommand, RelyingPartyPaginableBatch> context) {
    items.forEach(item -> usecase.enable(context.getParams().getInteraction(), item));
  }
}
