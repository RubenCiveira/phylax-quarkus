package net.civeira.phylax.features.access.role.infrastructure.driver.rest;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.SpanKind;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Context;
import jakarta.enterprise.context.ApplicationScoped;
import jakarta.transaction.Transactional;
import jakarta.ws.rs.core.Response;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.infrastructure.CurrentRequest;
import net.civeira.phylax.features.access.relyingparty.domain.RelyingPartyReference;
import net.civeira.phylax.features.access.role.application.usecase.create.RoleCreateInput;
import net.civeira.phylax.features.access.role.application.usecase.create.RoleCreateProjection;
import net.civeira.phylax.features.access.role.application.usecase.create.RoleCreateUsecase;
import net.civeira.phylax.generated.openapi.model.RelyingPartyApiRef;
import net.civeira.phylax.generated.openapi.model.RoleApiDto;

@RequiredArgsConstructor
@ApplicationScoped
public class RoleCreateController {

  /**
   * @autogenerated CreateControllerGenerator
   */
  private final RoleCreateUsecase create;

  /**
   * @autogenerated CreateControllerGenerator
   */
  private final CurrentRequest currentRequest;

  /**
   * @autogenerated CreateControllerGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated CreateControllerGenerator
   * @param role
   * @return
   */
  @Transactional
  public Response roleApiCreate(RoleApiDto role) {
    Span span = tracer.spanBuilder("Api to create an entity of role")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.SERVER).startSpan();
    try {
      RoleCreateProjection created =
          create.create(currentRequest.interaction(), toDomainModel(role));
      return Response.ok(toApiModel(created)).build();
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated CreateControllerGenerator
   * @param dto
   * @return
   */
  private RoleApiDto toApiModel(RoleCreateProjection dto) {
    Span span =
        tracer.spanBuilder("Mapper to build api content to response from model for entity of role")
            .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
            .startSpan();
    try {
      RoleApiDto roleApiDto = new RoleApiDto();
      roleApiDto.setUid(dto.getUid());
      roleApiDto.setName(dto.getName());
      roleApiDto.setRelyingParty(new RelyingPartyApiRef().$ref(dto.getRelyingPartyReference()));
      roleApiDto.setVersion(dto.getVersion());
      return roleApiDto;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated CreateControllerGenerator
   * @param roleApiDto
   * @return
   */
  private RoleCreateInput toDomainModel(RoleApiDto roleApiDto) {
    Span span =
        tracer.spanBuilder("Mapper to build model content to request from api for entity of role")
            .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
            .startSpan();
    try {
      RoleCreateInput dto = new RoleCreateInput();
      if (null != roleApiDto.getUid()) {
        dto.setUid(roleApiDto.getUid());
      }
      if (null != roleApiDto.getName()) {
        dto.setName(roleApiDto.getName());
      }
      if (null != roleApiDto.getRelyingParty()) {
        dto.setRelyingParty(RelyingPartyReference.of(roleApiDto.getRelyingParty().get$Ref()));
      }
      if (null != roleApiDto.getVersion()) {
        dto.setVersion(roleApiDto.getVersion());
      }
      return dto;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }
}
