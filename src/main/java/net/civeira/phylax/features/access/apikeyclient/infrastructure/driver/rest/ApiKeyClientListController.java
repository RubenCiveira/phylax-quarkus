package net.civeira.phylax.features.access.apikeyclient.infrastructure.driver.rest;

import java.net.URLEncoder;
import java.nio.charset.StandardCharsets;
import java.util.List;
import java.util.Optional;
import java.util.stream.Stream;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.ws.rs.core.Response;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.infrastructure.CurrentRequest;
import net.civeira.phylax.common.telemetry.ApiObserved;
import net.civeira.phylax.features.access.apikeyclient.application.usecase.list.ApiKeyClientListCursor;
import net.civeira.phylax.features.access.apikeyclient.application.usecase.list.ApiKeyClientListFilter;
import net.civeira.phylax.features.access.apikeyclient.application.usecase.list.ApiKeyClientListProjection;
import net.civeira.phylax.features.access.apikeyclient.application.usecase.list.ApiKeyClientListUsecase;
import net.civeira.phylax.generated.openapi.model.ApiKeyClientApiDto;
import net.civeira.phylax.generated.openapi.model.ApiKeyClientApiDtoList;

@RequiredArgsConstructor
@ApplicationScoped
public class ApiKeyClientListController {

  /**
   * @autogenerated ListControllerGenerator
   */
  private static final String KEY_APPEND = "&key=";

  /**
   * @autogenerated ListControllerGenerator
   */
  private static final String LIMIT_APPEND = "&limit=";

  /**
   * @autogenerated ListControllerGenerator
   */
  private static final String SEARCH_APPEND = "&search=";

  /**
   * @autogenerated ListControllerGenerator
   */
  private static final String SINCE_APPEND = "&since-uid=";

  /**
   * @autogenerated ListControllerGenerator
   */
  private static final String UID_APPEND = "&uid=";

  /**
   * ApiKeyClient
   *
   * @autogenerated ListControllerGenerator
   */
  private final CurrentRequest currentRequest;

  /**
   * @autogenerated ListControllerGenerator
   */
  private final ApiKeyClientListUsecase list;

  /**
   * @autogenerated ListControllerGenerator
   * @param uids
   * @param search
   * @param key
   * @param limit
   * @param sinceUid
   * @return
   */
  @ApiObserved("Api to list an entity of api key client")
  public Response apiKeyClientApiList(final List<String> uids, final String search,
      final String key, final Integer limit, final String sinceUid) {
    ApiKeyClientListFilter.ApiKeyClientListFilterBuilder filterBuilder =
        ApiKeyClientListFilter.builder();
    ApiKeyClientListCursor.ApiKeyClientListCursorBuilder cursorBuilder =
        ApiKeyClientListCursor.builder();
    filterBuilder = filterBuilder.uids(
        null == uids ? null : uids.stream().flatMap(part -> Stream.of(part.split(","))).toList());
    filterBuilder = filterBuilder.search(search);
    filterBuilder = filterBuilder.key(key);
    cursorBuilder = cursorBuilder.limit(limit);
    cursorBuilder = cursorBuilder.sinceUid(sinceUid);
    ApiKeyClientListFilter filter = filterBuilder.build();
    ApiKeyClientListCursor cursor = cursorBuilder.build();
    List<ApiKeyClientListProjection> listed =
        list.list(currentRequest.interaction(), filter, cursor);
    return currentRequest.cacheableResponse(listed, toListApiModel(listed, filter, cursor),
        "api-key-client-" + ("" + filter + cursor).hashCode());
  }

  /**
   * @autogenerated ListControllerGenerator
   * @param dto
   * @return
   */
  private ApiKeyClientApiDto toApiModel(ApiKeyClientListProjection dto) {
    ApiKeyClientApiDto apiKeyClientApiDto = new ApiKeyClientApiDto();
    apiKeyClientApiDto.setUid(dto.getUid());
    apiKeyClientApiDto.setCode(dto.getCode());
    apiKeyClientApiDto.setKey(dto.getKey());
    apiKeyClientApiDto.setEnabled(dto.getEnabled());
    apiKeyClientApiDto.setScopes(dto.getScopes());
    apiKeyClientApiDto.setVersion(dto.getVersion());
    return apiKeyClientApiDto;
  }

  /**
   * @autogenerated ListControllerGenerator
   * @param apiKeyClients
   * @param filter
   * @param cursor
   * @return
   */
  private ApiKeyClientApiDtoList toListApiModel(List<ApiKeyClientListProjection> apiKeyClients,
      ApiKeyClientListFilter filter, ApiKeyClientListCursor cursor) {
    Optional<ApiKeyClientListProjection> last = apiKeyClients.isEmpty() ? Optional.empty()
        : Optional.of(apiKeyClients.get(apiKeyClients.size() - 1));
    StringBuilder self = new StringBuilder();
    StringBuilder next = new StringBuilder();
    StringBuilder first = new StringBuilder();
    if (!filter.getUids().isEmpty()) {
      String uidsValue =
          URLEncoder.encode(String.join(",", filter.getUids()), StandardCharsets.UTF_8);
      self.append(UID_APPEND + uidsValue);
      next.append(UID_APPEND + uidsValue);
      first.append(UID_APPEND + uidsValue);
    }
    filter.getSearch().ifPresent(search -> {
      String searchValue = URLEncoder.encode(search, StandardCharsets.UTF_8);
      self.append(SEARCH_APPEND + searchValue);
      next.append(SEARCH_APPEND + searchValue);
      first.append(SEARCH_APPEND + searchValue);
    });
    filter.getKey().ifPresent(filterKey -> {
      String keyValue = URLEncoder.encode(String.valueOf(filterKey), StandardCharsets.UTF_8);
      self.append(KEY_APPEND + keyValue);
      next.append(KEY_APPEND + keyValue);
      first.append(KEY_APPEND + keyValue);
    });
    cursor.getLimit().ifPresent(limit -> {
      self.append(LIMIT_APPEND + limit);
      first.append(LIMIT_APPEND + limit);
      next.append(LIMIT_APPEND + limit);
    });
    cursor.getSinceUid().ifPresent(
        since -> self.append(SINCE_APPEND + URLEncoder.encode(since, StandardCharsets.UTF_8)));
    last.ifPresent(lastDto -> {
      next.append(SINCE_APPEND + URLEncoder.encode(lastDto.getUid(), StandardCharsets.UTF_8));
    });
    return new ApiKeyClientApiDtoList().items(apiKeyClients.stream().map(this::toApiModel).toList())
        .next(next.length() > 1 ? "?" + next.substring(1) : "")
        .self(self.length() > 1 ? "?" + self.substring(1) : "")
        .first(first.length() > 1 ? "?" + first.substring(1) : "");
  }
}
