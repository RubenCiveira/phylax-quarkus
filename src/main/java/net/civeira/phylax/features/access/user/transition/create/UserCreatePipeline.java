package net.civeira.phylax.features.access.user.transition.create;

import java.util.ArrayList;
import java.util.List;
import java.util.function.Function;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.inject.Instance;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.features.access.user.User;
import net.civeira.phylax.features.access.user.command.UserWriteAttributes;
import net.civeira.phylax.features.access.user.transition.UserChangeProposal;

@ApplicationScoped
@RequiredArgsConstructor
public class UserCreatePipeline {

  /**
   * @autogenerated UpdateEntityRuleGenerator
   */
  private final Instance<UserCreateStep> steps;

  /**
   * @autogenerated UpdateEntityRuleGenerator
   * @param attributes
   * @param last
   * @return
   */
  public User create(final UserWriteAttributes attributes,
      final Function<UserChangeProposal, User> last) {
    Function<UserCreateTransition, User> finalHandler = t -> {
      if (t.hasErrors()) {
        throw t.asConstraintException();
      }
      return last.apply(t.getData());
    };
    List<UserCreateStep> reversed = new ArrayList<>(steps.stream().toList()).reversed();
    Function<UserCreateTransition, User> pipeline = reversed.stream().reduce(finalHandler,
        (next, step) -> transition -> step.create(transition, next), (f1, f2) -> f1);
    return pipeline
        .apply(UserCreateTransition.builder().data(new UserChangeProposal(attributes)).build());
  }
}
