package net.civeira.phylax.features.access.tenantconfig.application.usecase.update;

import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.tenantconfig.TenantConfig;
import net.civeira.phylax.features.access.tenantconfig.TenantConfigFacade;
import net.civeira.phylax.features.access.tenantconfig.TenantConfigRef;
import net.civeira.phylax.features.access.tenantconfig.application.projection.TenantConfigStateProyection;
import net.civeira.phylax.features.access.tenantconfig.application.request.TenantConfigStateChange;
import net.civeira.phylax.features.access.tenantconfig.application.service.visibility.TenantConfigsVisibility;
import net.civeira.phylax.features.access.tenantconfig.gateway.TenantConfigCacheGateway;
import net.civeira.phylax.features.access.tenantconfig.gateway.TenantConfigWriteRepositoryGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class TenantConfigUpdateUsecase {

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final TenantConfigCacheGateway cache;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final TenantConfigFacade facade;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final TenantConfigWriteRepositoryGateway gateway;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<TenantConfigAllowUpdateProposal> updateAllowEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<TenantConfigUpdateProposal> updateProposalEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<TenantConfigUpdateEvent> updatedEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final TenantConfigsVisibility visibility;

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  public Allow allow(final Interaction query, final TenantConfigRef reference) {
    TenantConfigAllowUpdateProposal base = TenantConfigAllowUpdateProposal.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).reference(reference).build();
    updateAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    TenantConfigAllowUpdateProposal base = TenantConfigAllowUpdateProposal.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).build();
    updateAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * The slide with some values
   *
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @param reference
   * @param input
   * @return The slide with some values
   */
  public TenantConfigStateProyection update(final Interaction query,
      final TenantConfigRef reference, final TenantConfigStateChange input) {
    Allow detail = allow(query, reference);
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    TenantConfig original = visibility.retrieveVisibleForUpdate(query, reference.getUidValue())
        .orElseThrow(() -> new NotFoundException(""));
    TenantConfigStateChange filled = visibility.copyWithFixed(query, input);
    TenantConfigUpdateProposal proposal = TenantConfigUpdateProposal.builder().dto(filled)
        .interaction(query).reference(reference).build();
    updateProposalEmitter.fire(proposal);
    TenantConfigStateChange dto = proposal.getDto();;
    TenantConfig saved = gateway.update(original, facade.update(original, dto));
    cache.update(saved);
    TenantConfigUpdateEvent event =
        TenantConfigUpdateEvent.builder().payload(saved).interaction(query).build();
    updatedEmitter.fire(event);
    return visibility.copyWithHidden(query, event.getPayload());
  }
}
