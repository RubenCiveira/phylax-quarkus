package net.civeira.phylax.features.access.tenantconfig.application.usecase.update;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.SpanKind;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Context;
import jakarta.enterprise.context.ApplicationScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;
import net.civeira.phylax.common.exception.NotAllowedException;
import net.civeira.phylax.common.exception.NotFoundException;
import net.civeira.phylax.common.security.Allow;
import net.civeira.phylax.common.security.Interaction;
import net.civeira.phylax.features.access.tenantconfig.application.visibility.TenantConfigsVisibility;
import net.civeira.phylax.features.access.tenantconfig.domain.TenantConfig;
import net.civeira.phylax.features.access.tenantconfig.domain.TenantConfigChangeSet;
import net.civeira.phylax.features.access.tenantconfig.domain.TenantConfigRef;
import net.civeira.phylax.features.access.tenantconfig.domain.gateway.TenantConfigAuditGateway;
import net.civeira.phylax.features.access.tenantconfig.domain.gateway.TenantConfigCacheGateway;
import net.civeira.phylax.features.access.tenantconfig.domain.gateway.TenantConfigWriteRepositoryGateway;

@ApplicationScoped
@RequiredArgsConstructor
public class TenantConfigUpdateUsecase {

  /**
   * Service to register audit events
   *
   * @autogenerated UpdateUsecaseGenerator
   */
  private final TenantConfigAuditGateway audit;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final TenantConfigCacheGateway cache;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final TenantConfigWriteRepositoryGateway gateway;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<TenantConfigUpdateAllowDecision> updateAllowEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<TenantConfigUpdateCheck> updateCheckEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<TenantConfigUpdateEnrich> updateEnrichEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final TenantConfigsVisibility visibility;

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  public Allow allow(final Interaction query, final TenantConfigRef reference) {
    Span span = tracer.spanBuilder("Checking if is allowed to update an entity of tenant config")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      span.setAttribute("ref", reference.getUid());
      Allow response = checkAllow(query, reference);
      span.setAttribute("allowed", response.isAllowed());
      return response;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    Span span = tracer.spanBuilder("Checking if is allowed to update an entity of tenant config")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      Allow response = checkAllow(query);
      span.setAttribute("allowed", response.isAllowed());
      return response;
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * Recover a slide of data.
   *
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @param reference
   * @param input
   * @return The slide with some values
   */
  public TenantConfigUpdateProjection update(final Interaction query,
      final TenantConfigRef reference, final TenantConfigUpdateInput input) {
    Span span = tracer.spanBuilder("Updating an entity of tenant config")
        .setParent(Context.current().with(Span.current())).setSpanKind(SpanKind.INTERNAL)
        .startSpan();
    try {
      span.setAttribute("ref", reference.getUid());
      Allow detail = checkAllow(query, reference);
      if (!detail.isAllowed()) {
        span.addEvent(
            "The policies for tenant config dont allow to update: " + detail.getDescription());
        throw new NotAllowedException(detail.getDescription());
      }
      TenantConfig original = visibility.retrieveVisibleForUpdate(query, reference.getUid())
          .orElseThrow(() -> new NotFoundException(""));
      TenantConfigUpdateInput filled = TenantConfigUpdateInput
          .fromChangeSet(visibility.copyWithFixed(query, input.toChangeSet()));
      span.addEvent(
          "The input value is filled with the fixed values for the creation of tenant config");
      TenantConfigUpdateCheck check = TenantConfigUpdateCheck.builder().input(filled)
          .interaction(query).reference(reference).build();
      updateCheckEmitter.fire(check);
      span.addEvent("The input value is checked with the policies for the update of tenant config");
      TenantConfigUpdateEnrich proposal = TenantConfigUpdateEnrich.builder().input(filled)
          .interaction(query).reference(reference).build();
      updateEnrichEmitter.fire(proposal);
      TenantConfigChangeSet dto = proposal.getInput().toChangeSet();
      span.addEvent("The update of entity for tenant config is ready");
      TenantConfig saved = gateway.update(original, original.update(dto));
      span.addEvent("The updated entity of tenant config was saved");
      cache.update(saved);
      // Register audit operation
      audit.updated("update", saved, original, query);
      return TenantConfigUpdateProjection.from(visibility.copyWithHidden(query, saved));
    } catch (RuntimeException ex) {
      span.setAttribute("error", true);
      span.recordException(ex);
      throw ex;
    } finally {
      span.end();
    }
  }

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  private Allow checkAllow(final Interaction query, final TenantConfigRef reference) {
    TenantConfigUpdateAllowDecision base = TenantConfigUpdateAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).reference(reference).build();
    updateAllowEmitter.fire(base);
    return base.getDetail();
  }

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @return
   */
  private Allow checkAllow(final Interaction query) {
    TenantConfigUpdateAllowDecision base = TenantConfigUpdateAllowDecision.builder()
        .detail(Allow.builder().allowed(true).description("Allowed by default").build())
        .query(query).build();
    updateAllowEmitter.fire(base);
    return base.getDetail();
  }
}
